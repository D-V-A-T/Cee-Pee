/*
Cho mảng a gồm n phần tử. Giá trị của một dãy con liên tiếp a[l…r]
là min(al,ar) * (r−l−1). 
Hãy tìm giá trị lớn nhất của một dãy con liên tiếp của mảng a.
*/

void sol(){
    int n, ans=0;
    cin >> n;
    int a[n];
    for(int& i : a)cin >> i;
    int l=0,r=n-1;
    while(l <= r){
        ans=max(ans, min(a[l], a[r]) * (r-l-1));
        if(a[l] < a[r])l++;
        else r--;
    }cout << ans;
}

