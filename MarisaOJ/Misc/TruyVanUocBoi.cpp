/*
Cho mảng A gồm n phần tử nguyên và q truy vấn dạng (l,r,x), 
đếm số lượng số là bội hoặc ước của x trong đoạn [l,r] của mảng A.
*/
struct query{
    int l,r,x;
};

int ans(int l, int r, int x, vect<vect<int>>& reimu){
    int posl=lwb(bend(reimu[x]), l)-reimu[x].begin(),
        posr=upb(bend(reimu[x]), r)-1-reimu[x].begin();
    return max(0, posr-posl+1);
}

void sol(){
    int n, q;
    cin >> n >> q;
    vect<int> v(n);
    umap<int,bool> seen, xes;
    for(int& i : v){
        cin >> i;
        seen[i]=1;
    }vect<vect<int>> dm(N+1), reimu(N);
    vect<query> qry;
    while(q--){
        int l,r,x;
        cin >> l >> r >> x;
        xes[x]=1;
        l--;r--;
        qry.pb({l,r,x});
    }for(auto& i : xes){
        for(int f=i.fi;N>=f;f+=i.fi){
            if(seen[f]){
                dm[f].eb(i.fi);
            }
        }for(int f=1;f*f<=i.fi;f++){
            if(i.fi % f == 0){
                if(seen[f] && f%i.fi)dm[f].eb(i.fi);
                if(seen[i.fi/f] && (i.fi/f)%i.fi && f*f<i.fi)dm[i.fi/f].eb(i.fi);
            }
        }
    }for(int i=0;n>i;i++){
        for(int f : dm[v[i]]){
            reimu[f].eb(i);
        }
    }nextl;
    for(auto& i : qry)cout << ans(i.l, i.r, i.x, reimu) << el;
}

