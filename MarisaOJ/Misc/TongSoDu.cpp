int funky(int n, int lim){
    // calculate n*n - ([n/1].1 + [n/2].2 + ... + [n/n].n)
    int res=0;
    for(int i=1;min((int)sqrt(n), lim) >= i;i++){
        res = (res+i*(n/i))%MOD;
    }
    for(int i=1;min(n/((int)sqrt(n)+1), lim)>=i;i++){
        int l = n/(i+1)+1, r=min(lim, n/i);
        if(l > lim)continue;
        int p;
        if((l+r)%2 == 0)p=((l+r)/2)%MOD * ((r-l+1)%MOD);
        else p=((r-l+1)/2)%MOD * ((l+r)%MOD);
        p%=MOD;
        res = (res + (i*p)%MOD)%MOD;
    }return posmod((n%MOD * (lim%MOD))-res, MOD);
}

