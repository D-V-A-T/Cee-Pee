int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int lg2(int i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}

vect<vect<int>> spt(N, vect<int>(30, N-5));

void sol(){
    int n, q, len;
    cin >> n >> q >> len;
    vect<pii> a(n);
    for(pii& i : a)cin >> i.fi >> i.se;
    auto comp = [](pii a, pii b){
        return a.fi != b.fi ? a.fi < b.fi : a.se > b.se;
    };
    sort(bend(a), comp);
    int endp = 0;
    for(int i=0;n>i;i++){
        for(int j = max(endp+1, a[i].fi);a[i].se>=j;j++){
            spt[j][0] = a[i].fi-1;
        }endp = max(endp, a[i].se);
    }
    fill(bend(spt[0]), 0);
    for(int bit=1;lg2(len) >= bit;bit++){
        for(int i=1;len>=i;i++){
            spt[i][bit] = min(spt[i][bit-1], spt[spt[i][bit-1]][bit-1]);
        }
    }

    auto cbd = [&](int x, int l, int r){
        int bit = 0;
        while(x){
            if(x&1){
                r = spt[r][bit];
                if(r < l)return true;
            }
            bit++;
            x /= 2;
        }return false;
    };

    while(q--){
        int L, R;
        cin >> L >> R;
        int l=1, r=n, ans=-1;
        while(l <= r){
            int mid = (l+r)/2;
            if(cbd(mid, L, R)){
                ans = mid;
                r = mid-1;
            }else l = mid+1;
        }cout << ans << el;
    }
}   

