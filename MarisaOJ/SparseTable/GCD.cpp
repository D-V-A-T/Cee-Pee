int posmod(int a,int b){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int n, spt[N][20];
vect<int> v;

inline int lg(unsigned long long i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}

inline int rgcd(int l, int r){
    int i=lg(r-l+1);
    return __gcd(spt[l][i], spt[r-(1<<i)+1][i]);
}

void sol(){
    cin >> n;
    vect<int> v(n);
    for(int i=0;n>i;i++){
        cin >> v[i];
        spt[i][0]=v[i];
    }for(int i=1;20>i;i++)
        for(int f=0;n>=f + (1<<i);f++)
            spt[f][i]=__gcd(spt[f][i-1], spt[f+(1<<(i-1))][i-1]);
    int ans=0;
    for(int i=0;n>i;i++){
        int px=i, l=px, r=n, d=v[i];
        for(;;){
            l=px;r=n-1;int p;
            while(l <= r){
                int mid=(l+r)/2;
                if(rgcd(i, mid) == d){
                    p=mid;
                    l=mid+1;
                }else r=mid-1;
            }ans=(ans + d*(p-px+1))%MOD;
            px=p+1;
            if(px < n)d=rgcd(i, px);
            else break;
        }
    }cout << ans;
}

