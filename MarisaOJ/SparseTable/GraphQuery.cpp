int posmod(int a,int b){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int n, q, lg2[N], ln=32, spt[N][33];
vect<int> v;


void prep(){
	lg2[1]=0;
	for(int i=2;N>i;i++)lg2[i]=lg2[i/2]+1;
	for(int i=1;n>=i;i++)spt[i][0]=v[i];
	for(int i=1;ln>=i;i++)
		for(int f=1;n>=f;f++)
			spt[f][i]=spt[spt[f][i-1]][i-1];
}

void sol(){
    cin >> n >> q;
    v.rs(n+1);
    for(int i=1;n>=i;i++)cin >> v[i];
    prep();
    while(q--){
    	int u, k;
    	cin >> u >> k;
    	while(k){
    		int pw=0;
    		while(k >= (1 << pw))pw++;
    		pw--;
    		u=spt[u][pw];
    		k -= (1<<pw);
    	}cout << u << el;
    }
}

