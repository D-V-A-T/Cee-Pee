ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int prcnt[N]; // prcnt[i]: so bo (a,b) ma a+b la so ngto && a,b <= i 
vect<bool> isp(N, 1);

void sol(){
    int n;
    cin >> n;
    int trig = n*(n-1)*(n-2)/6; // tong so tam giac
    trig -= prcnt[n] * (n-2);   // so tam giac co it nhat 1 canh xanh
                                // (so tam giac co 2 canh xanh bi tinh 2 lan)
    int trig2 = 0; // so tam giac co 2 canh xanh
    vect<int> pedge(N); 
    // pedge[i]: so bo (a,b) ma (a=i || b=i) && a+b la so ngto && a,b<=n  
    for(int i=3;N>i;i++){
        if(isp[i] && i-n < n){
            if(i > n){
                pedge[i-n]++;
                pedge[n+1]--;
            }else if(i <= n){
                pedge[1]++;
                pedge[i]--;
            }            
        }
    }
    partial_sum(bend(pedge), pedge.begin());
    for(int i=1;n>=i;i++){
        trig2 += pedge[i] * (pedge[i]-1) / 2;
    }
    trig += trig2;
    cout << trig << el;
}

