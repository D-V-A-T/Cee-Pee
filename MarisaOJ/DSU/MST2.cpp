/*
Given a connected, weighted, undirected graph of n vertices and m edges.

For every edge, determine if it belongs to at least one minimum spanning tree.
*/
class DSU{
public:
    vector<int> par, len;
    DSU(int& n){
        par.rs(n+69);
        len.rs(n+69, 1);
        iota(bend(par), 0);
    }int findpar(int u){
        if(u == par[u])
            return u;
        return u=findpar(par[u]);
    }void join(int u, int v){
        u=findpar(u);
        v=findpar(v);
        if(u != v){
            if(len[u] < len[v])swap(u, v);
            par[v]=u;
            len[u]+=len[v];
        }
    }
};

struct edge{
    int u,v,w,id;
    bool operator < (edge& other){
        return w < other.w;
    }
};

void sol(){
    int n,m;
    cin >> n >> m;
    vect<edge> v(m);
    for(int i=0;m>i;i++){
        cin >> v[i].u >> v[i].v >> v[i].w;
        v[i].id=i;
    }sort(bend(v));
    DSU A(n);
    vect<int> ans(m);
    for(int i=0;m>i;i++){
        if(A.findpar(v[i].u) != A.findpar(v[i].v)){
            for(int f=i;m>f && v[f].w == v[i].w;f++){
                edge e=v[f];
                if(A.findpar(e.u) != A.findpar(e.v)){
                    ans[e.id]=1;
                }
            }A.join(v[i].u, v[i].v);
        }
    }for(int i : ans)cout << i;
}

