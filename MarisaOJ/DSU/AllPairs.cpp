/*
Given a weighted tree of n vertices. Each vertex has weight a[i]

Let’s denote max(u,v)
is the maximum weight on the simple path from u to v. 
Similarly, min(u,v) is the minimum weight on the simple path from u to v

Calculate the sum of max(i,j)−min(i,j)
with 1 <= i < j <= n. In other words, calculate: 
    for(int i=1;n>=i;i++)
        for(int j=i+1;n>=j;j++)
            x += max(a[i], a[j]) - min(a[i], a[j])
*/

class DSU{
public:
    vector<int> par, len;
    DSU(int& n){
        par.rs(n+69);
        len.rs(n+69, 1);
        for(int i=0;n>=i;i++)
            par[i]=i;
    }int findpar(int u){
        if(u == par[u])
            return u;
        return u=findpar(par[u]);
    }void join(int u, int v){
        u=findpar(u);
        v=findpar(v);
        if(u != v){
            if(len[u] < len[v])swap(u, v);
            par[v]=u;
            len[u]+=len[v];
        }
    }
};

struct query{
    int u,v,wmax,wmin;
};

bool cmax(query& a, query& b){
    return a.wmax < b.wmax;
}

bool cmin(query& a, query& b){
    return a.wmin > b.wmin;
}

void sol(){
    int n,m;
    cin >> n;
    m=n-1;
    vect<int> wei(n+1);
    vect<query> qry(m);
    for(int i=1;n>=i;i++)cin >> wei[i];
    for(auto& i : qry){
        cin >> i.u >> i.v;
        i.wmax=max(wei[i.u], wei[i.v]);
        i.wmin=min(wei[i.u], wei[i.v]);
    }sort(bend(qry), cmax);
    DSU A(n), B(n);
    int maxv=0, minv=0;
    // if we add the edges in ascending order, the current weight(w) is the maximum;
    // therefore the path between a node in the u's family and the v's family is w
    for(auto& i : qry){
        int pu = A.findpar(i.u), pv = A.findpar(i.v);
        maxv += i.wmax * (A.len[pu] * A.len[pv]);
        A.join(i.u, i.v);
    }sort(bend(qry), cmin);
    for(auto& i : qry){
        int pu = B.findpar(i.u), pv = B.findpar(i.v);
        minv += i.wmin * (B.len[pu] * B.len[pv]);
        B.join(i.u, i.v);
    }cout << maxv - minv;
}

