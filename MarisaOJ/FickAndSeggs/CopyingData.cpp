/*
You are given 2 arrays A,B of n integers.

There are q queries of either form:
    1 x y k: assign A[x+t] to B[y+t] for 0 <= t < k
    2 i: find the value B[i]
*/
int n, q;

class Seggs{
    vect<int> t, lz;
public:
    Seggs(){t.rs(4*n+1, -1);lz.rs(4*n+1, -1);}
    void push(int id, int l, int r){
        if(lz[id] == -1)return;
        t[id] = lz[id];
        if(l < r){
            lz[id*2] = lz[id*2+1] = lz[id];
        }lz[id]=-1;
    }
    void update(int id, int l, int r, int i, int j, int qid){
        push(id, l, r);
        if(l > j || r < i)return;
        if(l >= i && r <= j){
            lz[id] = qid;
            push(id, l, r);
            return;
        }int mid=(l+r)/2;
        update(id*2, l, mid, i, j ,qid);
        update(id*2+1, mid+1, r, i, j, qid);
    }int get(int id, int l, int r, int pos){
        push(id, l, r);
        if(l > pos || r < pos)return -1;
        if(l == r && l == pos)return t[id];
        int mid=(l+r)/2;
        return max(get(id*2, l, mid, pos),
                    get(id*2+1, mid+1, r, pos));
    }
};

struct query{
    int x,y,k;
};

void sol(){
    cin >> n >> q;
    int a[n], b[n];
    for(int& i : a)cin >> i;
    for(int& i : b)cin >> i;
    // with each query of type 2, we find the nearest query of type 1
    // that modify that position i 
    // -> seggs to update the positions 
    //   (t[id] is the q_id of the type 1 query)
    int qid=0;
    Seggs sgt;
    umap<int, query> qry;
    while(q--){
        int t;cin >> t;
        if(t == 1){
            query qu;
            cin >> qu.x >> qu.y >> qu.k;
            qu.x--;qu.y--;
            qry[qid]=qu;
            sgt.update(1, 0, n-1, qu.y, qu.y+qu.k, qid);
        }else{
            int pos;cin >> pos;pos--;
            int reimu=sgt.get(1, 0, n-1, pos);
            if(reimu == -1)cout << b[pos] << el;
            else{
                query qu = qry[reimu];
                cout << a[qu.x + pos - qu.y] << el;
            }
        }qid++;
    }
}

