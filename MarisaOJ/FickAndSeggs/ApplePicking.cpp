/*
    There're n Nitoris. The i-th Nitori harvest l[i] -> r[i] with the 
    cost of c[i]
    Find the minimum cost to harvest 1 -> n 
*/
int q,n;

class Seggs{
    vect<int> t, lz;
    // t[i] : minimum cost to harvest 1 -> i
public:
    Seggs(){
        t.rs(4*n+1, INF);
        lz.rs(4*n+1, INF);
    }void push(int id, int l, int r){
        if(lz[id] == INF)return;
        t[id]=min(lz[id], t[id]);
        if(l < r){
            lz[id*2]=min(lz[id*2], lz[id]);
            lz[id*2+1]=min(lz[id*2+1], lz[id]);
        }
        lz[id]=INF;
    }void update(int id, int l, int r, int i, int j, int x){
        // set t[i] -> t[j] to x (if x < the current cost)
        push(id, l ,r);
        if(l > j || r < i)return;
        if(l >= i && r <= j){
            lz[id]=min(lz[id], x);
            push(id, l, r);
            return;
        }int mid=(l+r)/2;
        update(id*2, l, mid, i, j, x);
        update(id*2+1, mid+1, r, i, j, x);
        t[id]=min(t[id*2], t[id*2+1]);
    }int get(int id, int l, int r, int i ,int j){
        push(id, l, r);
        if(l > i || r < j)return INF;
        if(l >= i && r <= j)return t[id];
        int mid=(l+r)/2;
        return min(get(id*2, l, mid, i ,j),
                    get(id*2+1, mid+1, r, i, j));
    }
};

struct query{
    int l, r, c;
    bool operator < (query& other){
        return l < other.l;
    }
};

void sol(){
    cin >> n >> q;
    Seggs sgt;
    vect<query> qry(q);
    for(auto& i : qry){
        cin >> i.l >> i.r >> i.c;
        i.l--;i.r--;
    }sort(bend(qry));
    for(auto& i : qry){
        int x=i.c;
        if(i.l)x+=sgt.get(1, 0, n-1, i.l-1, i.l-1);
        // the min cost to harvest 1 -> i.r is equal to
        // (min cost to harvest 1 -> i.l-1) + i.cost
        sgt.update(1, 0, n-1, i.l, i.r, x);
    }if(sgt.get(1, 0, n-1, n-1, n-1) < 1e15)cout << sgt.get(1, 0, n-1, n-1, n-1);
    else cout << -1;
}

