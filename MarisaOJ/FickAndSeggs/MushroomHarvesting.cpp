vect<int> day, ans;
// day[i] : vertex Marisa visits on day i

class FenwickTree{
public:
    vect<int> bit;
    FenwickTree(){bit.rs(N);}
    void upd(int x, int p){
        for(;N>=x; x += x&-x)
            bit[x] += p;
    }int get(int x){
        int res=0;
        for(;x>0; x -= x&-x)
            res+=bit[x];
        return res;
    }
};

FenwickTree fwt;

void DFS(int start){
    fwt.upd(day[start], 1);
    for(int i : t[start]){
        if(!visited[i]){
            visited[i]=1;
            DFS(i);
        }
    }// the answer for the day that Marisa goes to the i-th vertex
    // is the number of vertexes on the path from 1 -> i that
    // Marisa already visited, in other words, the number of vertexes j
    // on the path from 1 -> i that day[j] < day[i]
    ans[day[start]] = fwt.get(day[start]-1);
    // done exploring, remove day[start] from the current path
    fwt.upd(day[start], -1); 
}

