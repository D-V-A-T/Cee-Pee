int n;

struct Seggs{
    int t[4*N+1][2], lz[4*N+1];
    Seggs(){
        build(1, 0, n-1);
    }void build(int id, int l, int r){
        if(l==r){ 
            t[id][0]=1;
            return;
        }int mid=(l+r)/2;
        build(id*2, l, mid);
        build(id*2+1, mid+1, r);
        t[id][0]=t[id*2][0] + t[id*2+1][0];
    }inline void push(int id, int l, int r){
        if(lz[id])swap(t[id][0], t[id][1]);
        if(l < r){
            lz[id*2] ^= lz[id];
            lz[id*2+1] ^= lz[id];
        }lz[id]=0;
    }void update(int id, int l, int r, int i, int j){
        push(id, l, r);
        if(r < i || l > j)return;
        if(l >= i && r <= j){
            lz[id]^=1;
            push(id, l, r);
            return;
        }int mid=(l+r)/2;
        update(id*2, l, mid, i, j);
        update(id*2+1, mid+1, r, i, j);
        t[id][0]=t[id*2][0]+t[id*2+1][0];
        t[id][1]=t[id*2][1]+t[id*2+1][1];
    }int get(int id, int l, int r, int i, int j){
        push(id, l, r);
        if(l > j || r < i)return 0;
        if(l >= i && r <= j)return t[id][1];
        int mid=(l+r)/2;
        return get(id*2, l, mid, i, j)+get(id*2+1, mid+1, r, i, j);
    }
};

