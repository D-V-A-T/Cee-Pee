struct reimu{
    int x=0,y=0,z=0;
};

class Seggs{
    vect<int> t;
    vect<reimu> lz;
public:
    Seggs(){
        t.rs(4*n+1);lz.rs(4*n+1);
    }void push(int id, int l, int r){
        int len=r-l+1, mid=(l+r)/2;
        lz[id].x %= MOD;
        lz[id].y %= MOD;
        lz[id].z %= MOD;
        t[id] += lz[id].x*(len+1)*len/2 + len*(lz[id].y + lz[id].z);
        t[id] %= MOD;
        if(l < r){
            lz[id*2].x += lz[id].x;
            lz[id*2+1].x += lz[id].x;
            
            lz[id*2].y += lz[id].y;
            lz[id*2+1].y += lz[id].y + lz[id].x*(mid-l+1);
            
            lz[id*2].z += lz[id].z;
            lz[id*2+1].z += lz[id].z;
        }lz[id].x = lz[id].y =  lz[id].z = 0;
    }void segupd(int id, int l, int r, int i, int j, int y){
        push(id, l, r);
        if(l > j || r < i)return;
        if(l >= i && r <= j){
            lz[id].z += y;
            push(id, l, r);
            return;
        }int mid=(l+r)/2;
        segupd(id*2, l, mid, i, j, y);
        segupd(id*2+1, mid+1, r, i, j, y);
        t[id] = t[id*2] + t[id*2+1];
    }
    void laddupd(int id, int l, int r, int i, int j, int x){
        push(id, l, r);
        if(l > j || r < i)return;
        if(l >= i && r <= j){
            lz[id].x += x;
            lz[id].y += x*(l-i);
            push(id, l, r);
            return;
        }int mid=(l+r)/2;
        laddupd(id*2, l, mid, i, j, x);
        laddupd(id*2+1, mid+1, r, i, j , x);
        t[id] = t[id*2] + t[id*2+1];
    }int get(int id, int l, int r, int i, int j){
        push(id, l, r);
        if(l > j || r < i)return 0;
        if(l >= i && r <= j)return t[id];
        int mid=(l+r)/2;
        return (get(id*2, l, mid, i, j) + get(id*2+1, mid+1, r, i, j))%MOD;
    }
};

void sol(){
    int q;
    cin >> n >> q;
    Seggs sgt;
    while(q--){
        int t;cin >> t;
        if(t==1){
            int l,r,x,y;
            cin >> l >> r;
            x=1;y=0;
            l--;r--;
            sgt.segupd(1, 0, n-1, l, r, y);
            if(l < r)
                sgt.laddupd(1, 0, n-1, l+1, r, x);
        }else{
            int l,r;
            cin >> l >> r;
            l--;r--;
            cout << sgt.get(1, 0, n-1, l, r) << el;
        }
    }
}

