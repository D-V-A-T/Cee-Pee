int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int n, k;
umap<int, int> new_val;
int pfs[N], sp[N], cpfs[N];



class FenwickTree{
public:
    int bit[N];
    void upd(int x){
        for(;x <= N; x += x&-x)
            bit[x]++;       
    }int get(int x){
        int res=0; 
        for(;x > 0; x -= x&-x)
            res += bit[x];
        return res;
    }inline void reset(){
        memset(bit, 0, sizeof(bit));
    }
};

FenwickTree fwt; 

int count(int sum){
    fwt.reset();
    int res=0;
    for(int i=0;n>=i;i++){
        int p = upb(sp, sp+n, pfs[i] - sum) - sp;
        res += fwt.get(p);
        fwt.upd(cpfs[i]);
    }return res;
}

void sol(){
    cin >> n >> k;
    int ans, l=INF, r=-INF;
    for(int i=1;n>=i;i++){
        cin >> pfs[i];
        l=min(l, pfs[i]);
        r=max(r, pfs[i]);
        pfs[i]+=pfs[i-1];
        cpfs[i]=sp[i]=pfs[i];
    }l*=n;r*=n;
    sort(sp, sp+n);
    for(int i=0;n>=i;i++)new_val[sp[i]]=i+1;
    for(int& i : cpfs)i=new_val[i];
    // binary search the answer
    while(l <= r){
        int mid=(l+r)/2;
        // count the number of subarrays whose sum is >= k
        if(count(mid) >= k){
            ans=mid;
            l=mid+1;
        }else r=mid-1;
    }cout << ans;
}

