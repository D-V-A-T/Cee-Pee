// Given array A. Count the number of subarrays whose sum is non-negative

int n, maxn=2e5+69, ans=0;
vect<int> v, pfs;

class FenwickTree{
public:
    vect<int> bit;
    FenwickTree(){
        bit.rs(maxn);
    }void upd(int x){
        // +1 for every number >= x
        for(;x <= maxn; x += x&-x)
            bit[x] += 1;        
    }int get(int x){
        // count the number of numbers <= x
        int res=0;
        for(;x > 0; x -= x&-x)
            res += bit[x];
        return res;
    }
};

void compress(vect<int>& pfs, vect<int> v){
    sort(bend(v));
    map<int,int> val;
    for(int i=0;n>i;i++)
        val[v[i]] = i+1;
    for(int& i : pfs)
        i = val[i];
}

void sol(){
    cin >> n;
    v.rs(n);pfs.rs(n);
    for(int i=0;n>i;i++){
        cin >> v[i];
        pfs[i]=v[i];
        if(i)pfs[i] += pfs[i-1];
    }pfs.insert(pfs.begin(), 0);
    n=pfs.size();
    // for each i, the number of subarrays whose sum >= 0 end at i
    // is the number of j so that j < i && pfs[j] <= pfs[i]
    // => we can compress the pfs array while keeping the difference between
    //    2 elements
    compress(pfs, pfs);
    FenwickTree fwt;
    for(int i=0;n>i;i++){
        ans += fwt.get(pfs[i]);
        fwt.upd(pfs[i]);
    }cout << ans;
}

