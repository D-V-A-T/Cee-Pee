class FenwickTree{
public:
    int bit[N];
    void upd(int x, int p){
        for(;x<N;x|=(x+1))
            bit[x]+=p;
    }int get(int x){
        int res=0;
        for(;x>=0;x=(x&(x+1))-1)
            res+=bit[x];
        return res;
    }
};

struct query{
    int id, l, r;
    bool operator < (query& o){
        return r < o.r;
    }
};

