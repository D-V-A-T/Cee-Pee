int n, a[N], pfs[N], q;

struct marisa{
    int id, l, r;
};

struct reimu{
    int tm, lm, rm;
};

class Seggs{
public:
    vect<reimu> t;
    Seggs(){t.rs(4*n+1);build(1, 0, n-1);}
    int sum(int l, int r){
        if(r < l)return 0;
        if(l)return pfs[r]-pfs[l-1];
        return pfs[r];
    }void build(int id, int l, int r){
        if(l == r){
            t[id].lm=t[id].rm=t[id].tm=a[l];
            return;
        }int mid=(l+r)/2;
        build(id*2, l, mid);
        build(id*2+1, mid+1, r);
        t[id].lm = max(t[id*2].lm, sum(l, mid) + t[id*2+1].lm);
        t[id].tm = max({t[id*2].rm + t[id*2+1].lm, t[id*2].tm, t[id*2+1].tm});
        t[id].rm = max(t[id*2+1].rm, sum(mid+1, r) + t[id*2].rm);
    }void get(int id, int l, int r, int i, int j, vect<marisa>& v){
        if(l > j || r < i)return;
        if(l >= i && r <= j){
            marisa x = {id, l, r};
            v.pb(x);
            return;
        }int mid=(l+r)/2;
        get(id*2, l, mid, i, j, v);
        get(id*2+1, mid+1, r, i, j, v);
    }int ans(int l, int r){
        int res=-INF;
        vect<marisa> v;
        get(1, 0, n-1, l, r, v);
        for(int i=0;v.size()>i;i++){
            for(int f=i;v.size()>f;f++){
                if(i == f){
                    res = max(res, t[v[f].id].tm);
                }else{
                    marisa a=v[i], b=v[f];
                    if(a.l > b.r)swap(a, b);
                    res = max(res,
                        t[a.id].rm + t[b.id].lm + sum(a.r+1, b.l-1));
                }
            }
        }return res;
    }
};  

