struct FenwickTree{
    vect<int> bit;
    FenwickTree(){bit.rs(N);}
    void upd(int x, int p){
        for(;x<=N;x+=x&-x)
            bit[x]+=p;
    }int get(int x){
        int res=0;
        for(;x>0;x-=x&-x)
            res+=bit[x];
        return res;
    }
};

