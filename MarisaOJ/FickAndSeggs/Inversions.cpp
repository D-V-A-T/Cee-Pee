/*
    You are given a permutation A of length n. In one operation, 
    you can select 2 adjacent elements A[i] and A[i+1] and swap them.
    Find the minimum number of operations required to sort
    the permutation in ascending order.
*/

class FenwickTree{
public:
    vect<int> bit;
    FenwickTree(){
        bit.rs(maxn);
    }void upd(int x){
        // +1 for every number >= x
        for(;x <= maxn; x += x&-x)
            bit[x] += 1;        
    }int get(int x){
        // count the number of numbers < x
        int res=0;
        for(;x > 0; x -= x&-x)
            res += bit[x];
        return res;
    }
};

void sol(){
    int n;
    cin >> n;
    int a[n+1], ans=0;
    FenwickTree fwt;
    for(int i=1;n>=i;i++){
        cin >> a[i];
        int r = fwt.get(a[i])+1;
        // Assuming all the elements from 1 -> i-1 is sorted
        // a[i] current position is i
        // a[i]'s desired postion is r
        // => need i - r moves to get a[i] to its desired position
        ans += i - r;
        fwt.upd(a[i]);
    }cout << ans;
}

