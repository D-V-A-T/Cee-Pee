void sol(){
    int n;
    cin >> n;
    vect<vect<int>> dp((1 << n) +5, vect<int>(n, INF));
    int cost[n+1][n+1];
    for(int i=0;n>i;i++)for(int f=0;n>f;f++)cin >> cost[i][f];
    dp[1][0]=0;
    for(int i=1;1<<n >i;i++){
        for(int f=0;n>f;f++){
            // if we starts travelling from city f
            int mask=1<<f;
            if(i&mask){
                for(int j=0;n>j;j++){
                    int mask1=1<<j;
                    if(i&mask1)continue; // already visit city j
                    dp[i|mask1][j]=min(dp[i|mask1][j], dp[i][f]+cost[f][j]);
                }
            }
        }
    }int ans=INF;
    for(int i=0;n>i;i++)ans=min(ans, dp[(1<<n)-1][i] + cost[i][0]);
    cout << ans;
}

