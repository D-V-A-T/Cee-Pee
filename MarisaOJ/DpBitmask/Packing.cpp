int p(const string& s){
    int res=0;
    for(int i=0;s.size()>i;i++)if(s[i]=='1')res+=1<<i;
    return res;
}

vect<vect<int>> dp(1<<20, vect<int>(2, INF));
// dp[mask][0] : number of boxes
// dp[mask][1] : used weight of last boxes

void sol(){
    int n, W;
    cin >> n >> W;
    vect<int> w(n);
    for(int& i : w)cin >> i;
    dp[0][0]=1;
    dp[0][1]=0;
    for(int mask=0;1<<n >mask;mask++){
        for(int i=0;n>i;i++){
            if(!(mask&(1<<i))){
                int nmask=mask|(1<<i), nw=w[i]+dp[mask][1];
                if(nw > W && dp[nmask][0] > dp[mask][0]+1){
                    dp[nmask][0]=dp[mask][0]+1;
                    dp[nmask][1]=w[i];
                }else if(nw > W && dp[nmask][0] == dp[nmask][0]+1)
                    dp[nmask][1]=min(dp[nmask][1], w[i]);
            
                if(nw <= W && dp[nmask][0] > dp[mask][0]){
                    dp[nmask][0]=dp[mask][0];
                    dp[nmask][1]=nw;
                }else if(nw <= W && dp[nmask][0] == dp[mask][0])
                    dp[mask][1]=min(dp[mask][1], nw);
            }
        }
    }cout << dp[(1<<n)-1][0];
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
