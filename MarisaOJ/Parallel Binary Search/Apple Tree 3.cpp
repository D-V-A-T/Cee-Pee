ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct Fick{
    int bit[N];
    Fick(){
        memset(bit, 0, sizeof bit);
    }

    void upd(int x, int v){
        for(;x < N; x += x & -x) bit[x] += v;
    }

    int get(int x){
        int res = 0;
        for(; x > 0; x -= x & -x) res += bit[x];
        return res;
    }

    int query(int l, int r){
        return get(r) - get(l-1);
    }
}  ft;

struct query{
    int l, r, st, need, id;
    bool operator < (const query& o) const{
        return st < o.st;
    }
};

int n, q, a[N], gay[N], ans[N], ptr=-1;

void pbs(int lo, int hi, vect<query> &curr){

    int mid = (lo + hi) / 2;
    while(ptr < mid) ft.upd(a[++ptr], 1);
    while(ptr > mid) ft.upd(a[ptr--], -1);

    vect<query> left, right;

    for(auto qry  : curr){
        auto [l, r, st, need, id] = qry;
        if(ft.query(l, r) - gay[id] >= need) ans[id] = mid + 1, left.pb(qry);
        else right.pb(qry);
    }

    curr.clear();

    if(lo == hi) return;
    pbs(lo, mid, left);
    pbs(mid+1, hi, right);
}

void sol(){
    cin >> n >> q;
    for(int i=0; n>i; i++) cin >> a[i];


    vect<query> queries(q), pfs[N];
    for(int i=0; q>i; i++) {
        auto &[l, r, st, need, id] = queries[i];
        cin >> l >> r >> st >> need;
        st--;
        id = i;
        pfs[st].pb(queries[i]); 
    }

    for(int i=0; n>i; i++){
        for(auto &qry : pfs[i]){
            gay[qry.id] = ft.query(qry.l, qry.r);
        }
        ft.upd(a[i], 1);
    }


    memset(ft.bit, 0, sizeof ft.bit);
    memset(ans, -1, sizeof ans);

    pbs(0, n-1, queries);
    for(int i=0; q>i; i++) cout << ans[i] << ' ';
}

