//Given array A. Determine the smallest number of elements that need to change
//to make A only consists of consecutive numbers

void sol(){
    int n, ans=INF;
    cin >> n;
    vect<int> v;
    umap<int,int> seen;
    for(int i=0;n>i;i++){
    	int x;cin >> x;
    	if(!seen[x]){
    		v.eb(x);seen[x]++;
    	}
    }sort(bend(v));
    //Assume a number is the smallest number in the consecutive sequence, we
    //can find the largest number appearing in A. Calculate number of numbers
    //between the two, we know the number of elements that need to change
    for(int i=0;v.size()>i;i++){
    	int l=0, r=v.size()-1;
    	int reimu=v[i]+n-1, pos=-INF;
    	while(l <= r){
    		int mid=(l+r)/2;
    		if(v[mid] > reimu)r=mid-1;
    		else{
    			pos=max(pos, mid);
    			l=mid+1;
    		}
    	}//number of elements that need to change
        ans=min(ans, n-(pos-i+1));
    }cout << ans;
}

