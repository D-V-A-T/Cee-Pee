int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
int __lcm(int a,int b){return a*b/__gcd(a,b);}

int n, q, SQRT;

struct query{
    int l, r, id;
    bool operator < (const query& o) const {
        const int S=SQRT;
        if(l/S != o.l/S)return l < o.l;
        if((l/S)&1)return r < o.r;
        return r > o.r;
    }
};

int bits[N], bitg[N], fumo=0;
void upd(int a, int p){
    int x=a;
    for(;N>x;x+=x&-x)bits[x]+=p;
    x=a;
    for(;x>0;x-=x&-x)bitg[x]+=p;
}
int get(int x, bool b){
    int res=0;
    for(;x>0 && !b;x-=x&-x)res += bits[x];
    for(;x<N && b;x+=x&-x)res += bitg[x];
    return res;
}

void ZIP(vect<int>& v){
    vect<int> zip=v;
    sort(bend(zip));
    auto it=unique(bend(zip));
    zip.rs(it-zip.begin());
    for(int& i : v)i=lwb(bend(zip), i)-zip.begin()+2;
}

void sol(){
    cin >> n >> q;
    SQRT=sqrt(n);
    vect<int> a(n);
    for(int& i : a)cin >> i;
    ZIP(a);
    vect<query> que(q);
    int ans[q];
    for(int i=0;q>i;i++){
        cin >> que[i].l >> que[i].r;
        que[i].l--;que[i].r--;
        que[i].id=i;
    }sort(bend(que));
    int l=0, r=-1;
    for(auto& i : que){
        while(l < i.l){
            fumo -= get(a[l]-1, 0);
            upd(a[l], -1);
            l++;
        }while(l > i.l){
            l--;
            fumo += get(a[l]-1, 0);
            upd(a[l], 1);
        }while(r < i.r){
            r++;
            fumo += get(a[r]+1, 1);
            upd(a[r], 1);
        }while(r > i.r){
            fumo -= get(a[r]+1, 1);
            upd(a[r], -1);
            r--;
        }ans[i.id]=fumo;
    }for(int i : ans)cout << i << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
