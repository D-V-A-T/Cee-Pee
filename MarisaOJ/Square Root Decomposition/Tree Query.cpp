int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int lz[N], fumo[N], par[N];
vect<int> graph[N];

void DFS(int u){
    for(int i : graph[u]){
        if(i>1 && !par[i]){
            par[i] = u;
            DFS(i);
        }
    }
}

void sol(){
    int n, q;
    cin >> n >> q;
    const int S = sqrt(n);
    for(int i=1;n>i;i++){
        int u, v;
        cin >> u >> v;
        graph[u].eb(v);
        graph[v].eb(u);
    }
    DFS(1);
    vect<int> que;
    while(q--){
        int t, u;cin >> t >> u;
        if(t == 1){
            int d;cin >> d;
            if(u>1)fumo[par[u]] += d;
            lz[u]+=d;
            que.eb(u);
            if(que.size() > S){
                for(int i=2;n>=i;i++) fumo[i] += lz[par[i]];
                for(int i=1;n>=i;i++) lz[i] = 0;
                que.clear();
            }
        }else{
            cout << fumo[u] + lz[par[u]] << el;
        }
    }
}

