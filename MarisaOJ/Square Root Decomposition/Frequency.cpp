int S, ans=0;
vect<int> a;
int cnt[N], fumo[N];

struct query{
    int l, r, x, id;
    bool operator < (const query& o) const {
        if(l/S != o.l/S)return l < o.l;
        if((l/S)&1)return r < o.r;
        return r > o.r;
    }
};

bool comp(const query& a, const query& b){
    if(a.l / S != b.l / S) 
        return a.l < b.l; 

    if((a.l / S) % 2 == 1)
        return a.r < b.r;
    else
        return a.r > b.r;
}

void upd(int val, int p){
    fumo[cnt[val]]--;
    cnt[val]+=p;
    fumo[cnt[val]]++;
}

void sol(const int n){
    int q;cin >> q;
    a.rs(n);
    vect<query> que(q);
    vect<int> ans(q);
    for(int& i : a)cin >> i;
    S = sqrt(n);
    for(int i=0;q>i;++i){
        cin >> que[i].l >> que[i].r >> que[i].x;
        que[i].l--;que[i].r--;
        que[i].id=i;
    }
    sort(bend(que));
    int l=0, r=-1;
    for(auto& i : que){
        while(l < i.l) upd(a[l++], -1);
        while(l > i.l) upd(a[--l], 1);
        while(r < i.r) upd(a[++r], 1);
        while(r > i.r) upd(a[r--], -1);
        ans[i.id]= r-l+1 >= i.x ? fumo[i.x] : 0;
        //cout << fumo[i.x] << el;
    }for(int i : ans)cout << i << el;
}

