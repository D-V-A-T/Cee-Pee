int sq(int x){return x*x;}
int __lcm(int a,int b){return a*b/__gcd(a,b);}

void sol(){
    int n, ans=0, L, R;
    cin >> n >> L >> R;
    int a[n];
    vect<int> v;
    for(int& i : a)cin >> i;
    sort(a, a+n);
    v.eb(a[0]);
    for(int i=1;n>i;i++)if(a[i] != v.back())v.eb(a[i]);
    n=v.size();
    for(int mask=1;1<<n >mask;mask++){
        int r=0, reimu=1;
        for(int i=0;n>i;i++){
            if(mask&(1<<i)){
                r++;
                if(reimu > INF/a[i]){
                    reimu=0;
                    break;
                }reimu *= a[i];
            }
        }if(reimu){
            int fumo=R/reimu - (L-1)/reimu;
            ans += r%2 ? fumo : -fumo;
        }
    }cout << ans;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
