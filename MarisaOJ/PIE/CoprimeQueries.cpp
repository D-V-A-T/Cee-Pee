const int INF=LLONG_MAX, N=2e5+69, MOD=1e9+7;    

void ffopen(const string& file){
    if(file.empty())return;
    freopen((file + ".inp").c_str(), "r", stdin);
    freopen((file + ".out").c_str(), "w", stdout);
}

int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
int __lcm(int a,int b){return a*b/__gcd(a,b);}

void sol(){
    int n, q;
    cin >> n >> q;
    vect<int> d={n}, fumo;
    for(int i=2;i*i<=n;i++){
        if(n%i==0){
            d.eb(i);
            d.eb(n/i);
        }
    }
    sort(bend(d));
    for(int i : d){
        if(n==1)break;
        if(n%i==0)fumo.eb(i);
        while(n%i==0)n/=i;
    }n=fumo.size();
    vect<pii> reimus;
    for(int mask=1;1<<n >mask;mask++){
        int reimu=1;
        for(int b=mask;b;b^=b&-b){
            int i=ctz(b&-b);
            if(reimu > INF/fumo[i]){reimu=0;break;}
            reimu *= fumo[i];
        }if(reimu){
            reimus.eb(reimu, popcnt(mask));
        }
    }
    while(q--){
        int L, R, ans=0;
        cin >> L >> R;
        ans=R-L+1;
        for(pii& i : reimus){
            int rapi=R/i.fi - (L-1)/i.fi;
            ans -= i.se%2 ? rapi : -rapi;
        }cout << ans << el;
    }
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
