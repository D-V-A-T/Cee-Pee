vect<int> a;
int L, R;

void sol(){
    int n, ans=0;
    cin >> n >> L >> R;
    vect<int> v;
    a.rs(n);
    for(int& i : a)cin >> i;
    for(int mask=1;1<<n >mask;mask++){
        int r=popcnt(mask);
        if(r != n-1)continue;
        int sus;
        for(int i=0;n>i;i++)if(!(mask&(1<<i)))sus=a[i];
        ans += R/sus - (L-1)/sus;
        for(int smask=mask;smask;smask=(smask-1)&mask){
            int cb=0, reimu=sus;
            for(int b=smask;b;b^=b&-b){
                int i=ctz(b&-b);
                if(reimu > R/a[i]){
                    reimu=0;break;
                }reimu *= a[i];
            }if(reimu){
                int fumo=R/reimu - (L-1)/reimu;
                ans -= popcnt(smask)%2 ? fumo : -fumo;
            }
        }
    }
    cout << ans;
}

