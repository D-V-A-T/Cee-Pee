// Given string S, Count the number of palindromome substrings in S

const int base=311;
vector<int> POW, hashS, hashRS;

int getHashS(int i, int j){
    return ((hashS[j] - hashS[i - 1] * POW[j - i + 1]) % MOD + MOD * MOD) % MOD;
}

int getHashRS(int i, int j){
    // reverse gethash
    return ((hashRS[j] - hashRS[i + 1] * POW[i - j + 1]) % MOD + MOD * MOD) % MOD;
}

void Hashing(string& s){
    int lens=s.size();
    s=" " + s + " ";
    POW.rs(lens+2);
    hashS.rs(lens+2);
    hashRS.rs(lens+2);
    POW[0] = 1;
    for (int i = 1; i <= lens; i++)
        POW[i] = POW[i - 1] * base % MOD;
    for (int i = 1; i <= lens; i++)
        hashS[i] = (hashS[i - 1] * base + (s[i] - 'a' + 1)) % MOD;
    // reverse hash
    for (int i = lens; i >= 1; i--)
        hashRS[i] = (hashRS[i + 1] * base + (s[i] - 'a' + 1)) % MOD;
}

void sol(){
    string s;
    cin >> s;
    int n=s.size(), ans=0;
    Hashing(s);
    for(int i=1;n>=i;i++){
        // binary search the maximum length of the palindrome substr 
        // if S[i] is the centre of it
        int l=0, r=min(i-1, n-i), len=0;
        while(l <= r){
            int mid=(l+r)/2;
            if(getHashS(i-mid, i+mid) == getHashRS(i+mid, i-mid)){
                len=max(len, mid);
                l=mid+1;
            }else r=mid-1;
        }ans+=len+1;
        if(s[i] == s[i+1]){
            // if the palindrome substr has an even length
            l=len=0;r=min(i-1, n-i-1);
            while(l <= r){
                int mid=(l+r)/2;
                if(getHashS(i-mid, i+1+mid) == getHashRS(i+1+mid, i-mid)){
                    len=max(len, mid);
                    l=mid+1;
                }else r=mid-1;
            }ans+=len+1;
        }
    }cout << ans;
}

