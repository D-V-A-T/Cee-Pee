/*
Given a binary matrix A of n rows and m columns.

You can do the following operation on the matrix:

Select a row or a column and reverse every numbers on that row or column 
(i.e 1 turn into 0, and 0 turn into 1).

You are given q
queries of form (a,b,c,d). Determine if the submatrix with upper-left corner at (a,b)
 and lower-right corner at (c,d) can be transformed into a matrix
  full of 0 by applying the above operation?
*/

const int base=311;
vector<int> POW;
vect<vect<int>> hashS, hashRS;

char ch(char c){
    if(c=='0')return '1';
    return '0';
}

int getHash(int i, int j, const vect<int>& hashv){
    return ((hashv[j] - hashv[i - 1] * POW[j - i + 1]) % MOD + MOD * MOD) % MOD;
}

void Hashing(string& s, int p){
    int lens=s.size();
    s=" " + s;
    for (int i = 1; i <= lens; i++){
        hashRS[p][i] = (hashRS[p][i - 1] * base + (ch(s[i]) - '0' + 1)) % MOD;
        hashS[p][i] = (hashS[p][i - 1] * base + (s[i] - '0' + 1)) % MOD;
    }
}

void sol(){
    int n,m, q;
    cin >> n >> m >> q;
    hashS.rs(n+1, vect<int>(m+1));
    hashRS.rs(n+1, vect<int>(m+1));// the row if the operation is performed
    POW.rs(m+1);
    POW[0] = 1;
    for (int i = 1; i <= m; i++){
        POW[i] = POW[i - 1] * base % MOD;
    }vect<string> v(n+1);
    for(int i=1;n>=i;i++){
        cin >> v[i];
        Hashing(v[i], i);
    }// The submatrix can only be transformed into a matrix full of 0 if
    // after doing the operation for some times on every row
    // we end up with a submatrix whose rows are the same
    // Example: 
    // 00010     101    111   
    // 11101     010    000
    while(q--){
        int a,b,c,d;
        cin >> a >> b >> c >> d;
        string ans="YES\n";
        int reimu=getHash(b, d, hashS[a]),
            marisa=getHash(b, d, hashRS[a]);
        for(int i=a;c>=i;i++){
            if(getHash(b, d, hashS[i]) != reimu &&
                getHash(b, d, hashS[i]) != marisa)
                ans="NO\n";
        }cout << ans;
    }
}

