const int INF=9023372036954775807;
int MOD=1e9+7;    

int posmod(int a,int b){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

// Given a string S only consists of numbers
// There are q queries (x, y, len),
// each requires you to compare S[x -> x+len] and S[y -> y+len]

const int base=311;
vector<int> POW, hashS;

int getHash(int i, int j){
    return ((hashS[j] - hashS[i - 1] * POW[j - i + 1]) % MOD + MOD * MOD) % MOD;
}

void Hashing(string& s){
    int lens=s.size();
    s=" " + s;
    POW.rs(lens+1);
    hashS.rs(lens+1);
    POW[0] = 1;
    for (int i = 1; i <= lens; i++)
        POW[i] = POW[i - 1] * base % MOD;
    for (int i = 1; i <= lens; i++)
        hashS[i] = (hashS[i - 1] * base + (s[i] - 'a' + 1)) % MOD;
}

void sol(){
    string s;
    cin >> s;
    Hashing(s);
    int q;
    cin >> q;
    while(q--){
        int x, y ,len, pos=INF;
        cin >> x >> y >> len;
        int l=0, r=len;
        while(r >= l){
            int mid=(l+r)/2;
            // if hash s[x+i] = hash s[y+i] thenall of the previous 
            // characters are the same; therefore, check the next half
            if(getHash(x, x+mid) == getHash(y, y+mid)){
                l=mid+1;
            }else{
                pos=min(pos, mid);
                r=mid-1;
            }
        }// compare the first position that differs
        if(pos < INF){
            if(s[x+pos] > s[y+pos])
                cout << '>';
            else cout << '<';
        }else cout << '=';
        nextl;
    }
}

