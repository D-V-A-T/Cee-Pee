/*
Given a string S and n string T1,T2,…,Tm

Find the longest substring of S
that can be created using T1,T2,…Tm. One string can be used multiple times.
*/
const int base=311;
vector<int> POW, hashS;

int getHash(int i, int j){
    return posmod((hashS[j] - hashS[i - 1] * POW[j - i + 1]), MOD) ;
}

void Hashing(string& s){
    int lens=s.size();
    s=" " + s;
    POW.rs(lens+1);
    hashS.rs(lens+1);
    POW[0] = 1;
    for (int i = 1; i <= lens; i++){
        POW[i] = POW[i - 1] * base % MOD;
        hashS[i] = (hashS[i - 1] * base + (s[i] - 'a' + 1)) % MOD;
    }
}

void sol(){
    int m;
    string s;
    cin >> m >> s;
    int n=s.size();
    Hashing(s);
    vect<string> v(m);
    umap<int,int> hashT, lens;
    for(auto& i : v){
        cin >> i;
        int curr,prev=0;
        for(char& f : i){
            curr=(prev * base + f-'a'+1)%MOD;
            prev=curr;
        }hashT[curr]++;
        lens[i.size()]=1;
    }vect<int> dp(n+1);int ans=0;
    // dp[i] : the longest substr can be created end at i

    for(int i=1;n>=i;i++){
        // only check possible lengths of substrs to reduce 
        // running time
        for(auto& gay : lens){
            int k=gay.fi;
            if(hashT[getHash(i, i+k-1)])
                dp[i+k-1]=max(dp[i+k-1], dp[i-1]+k);
            
        }
    }cout << *max_element(bend(dp));
}

