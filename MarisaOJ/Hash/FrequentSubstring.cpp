/*
Given a string S. Find the longest string appearing at least k times in S.
*/
const int base=311;
vector<int> POW, hashS;

int getHash(int i, int j){
    return ((hashS[j] - hashS[i - 1] * POW[j - i + 1]) % MOD + MOD * MOD) % MOD;
}

void Hashing(string& s){
    int lens=s.size();
    s=" " + s;
    POW.rs(lens+1);
    hashS.rs(lens+1);
    POW[0] = 1;
    for (int i = 1; i <= lens; i++)
        POW[i] = POW[i - 1] * base % MOD;
    for (int i = 1; i <= lens; i++)
        hashS[i] = (hashS[i - 1] * base + (s[i] - 'a' + 1)) % MOD;
}

void sol(){
    int k;
    string s;
    cin >> s >> k;
    int l=1, r=s.size(), ans=0, n=s.size();
    Hashing(s);
    // Bs the maximum length
    while(l <= r){
        int mid=(l+r)/2;
        umap<int,int> hashes;
        int cnt=0;
        // find the most frequent substr of length mid
        for(int i=1;n>=i;i++){
            int reimu=getHash(i, i+mid-1);
            hashes[reimu]++;
            cnt=max(cnt, hashes[reimu]);
        }if(cnt >= k){
            ans=max(ans, mid);
            l=mid+1;
        }else r=mid-1;
    }cout << ans;
}

