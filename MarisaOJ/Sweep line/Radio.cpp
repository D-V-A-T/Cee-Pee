int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n;

struct Seggs{
    vect<int> t, lz;
    Seggs(){
        t.rs(5e5, INF);
        lz.rs(5e5, INF);
    }

    void upd(int i, int x, int ty, int l=0, int r=n-1, int id=1){
        if(l > i || r < i)return;
        if(l == r){
            if(ty==-1)t[id]=INF;
            else t[id]=min(t[id], x);
            return;
        }int mid = (l+r)/2;
        upd(i, x, ty, l, mid, id*2);
        upd(i, x, ty, mid+1, r, id*2+1);
        t[id] = min(t[id*2], t[id*2+1]);
    }

    int get(int i, int j, int l=0, int r=n-1, int id=1){
        if(l > j || r < i)return INF;
        if(i <= l && r <= j)return t[id];
        int mid = (l+r)/2;
        return min(get(i, j, l, mid, id*2), get(i, j, mid+1, r, id*2+1));
    }

};

struct seg{
    int p, pp, x, ty;
    bool operator < (const seg& o) const{
        return (p != o.p ? p < o.p : x > o.x);
    }
};

void sol(){
    cin >> n;
    set<seg> segs;
    Seggs sgt;
    for(int i=1;n>=i;i++){
        int a,b,c,d,t;
        cin >> t >> a >> b >> c >> d;
        while(segs.size() && (*segs.begin()).p == i){
            seg s = *segs.begin();
            sgt.upd(s.pp, s.x, s.ty);
            segs.erase(segs.begin());
        }
        int ans = (i==1 ? 0 : sgt.get(a, b) + t + i);
        if(i>1) cout << (ans<INF ? ans : -1) << ' ';
        
        segs.insert({c+(c==i), i, ans + t-i, 1});
        segs.insert({d+1, i, -ans -t+i, -1});
    }
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
