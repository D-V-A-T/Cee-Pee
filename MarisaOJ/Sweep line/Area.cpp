struct Seggs{
    vect<int> t, lz;
    Seggs(){
        lz.rs(N);
        t.rs(N);
    }

    void upd(int i, int j, int x, int l=0, int r=1e5, int id=1){
        if(l > j || r < i)return;
        if(i <= l && r <= j){
            lz[id] += x;    
            if(lz[id]) t[id] = r-l+1;
            else if(l < r)t[id] = t[id*2] + t[id*2+1];
            else t[id] = 0;

            return;
        }int mid = (l+r)/2;
        upd(i, j, x, l, mid, id*2);
        upd(i, j, x, mid+1, r, id*2+1);

        if(lz[id]) t[id] = r-l+1;
        else if(l < r)t[id] = t[id*2] + t[id*2+1];
        else t[id] = 0;
    }
};

struct seg{
    int x, y1, y2, t;
    bool operator < (const seg& o) const{
        return (x != o.x ? x < o.x : t > o.t);
    }
};

void sol(){
    cin >> n;
    deque<seg> segs;
    for(int i=0;n>i;i++){
        int x1, y1, x2, y2;
        cin >> x1 >> y1 >> x2 >> y2;
        // tu y1 -> y2 phu cac o tu y1 -> y2-1
        segs.pb({x1, y1, y2-1, 1});
        segs.pb({x2, y1, y2-1, -1});
    }
    sort(bend(segs));
    int ans = 0;
    Seggs sgt;
    for(int j=0;1e5>=j;j++){
        while(segs.size() && segs.front().x == j){
            seg i = segs.front();
            sgt.upd(i.y1, i.y2, i.t);
            segs.pof();
        }
        ans += sgt.t[1];
    }
    cout << ans;
}

