ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n,k;

struct Seggs{
    vect<int> t, lz;
    Seggs(){
        t.rs(N);
        lz.rs(N);
    }
    void push(int l, int r, int id){
        t[id] += lz[id];
        if(l < r){
            lz[id*2] += lz[id];
            lz[id*2+1] += lz[id];
        }lz[id] = 0;
    }
    void upd(int i, int j, int x, int l=0, int r=N/4-1, int id=1){
        i = max(i, 0ll);
        push(l, r, id);
        if(l > j || r < i)return;
        if(i <= l && r <= j){
            lz[id] += x;
            push(l, r, id);
            return;
        }int mid = (l+r)/2;
        upd(i, j, x, l, mid, id*2);
        upd(i, j, x, mid+1, r, id*2+1);
        t[id] = max(t[id*2], t[id*2+1]);
    }
} sgt;



struct seg{
    int x, y, t;
    bool operator < (const seg& o) const{
        return x != o.x ? x < o.x : t > o.t;
    }
};

void sol(){
    cin >> n >> k;
    k = min(k, (int)1e5+69);
    vect<seg> segs;
    for(int i=0;n>i;i++){
        int x, y;
        cin >> x >> y;
        segs.pb({x, y, 1});
        segs.pb({x+k, y, -1});
    }sort(bend(segs));
    int ans = 1;
    for(seg i : segs){
        sgt.upd(i.y-k, i.y, i.t);
        if(i.t == 1)ans = max(ans, sgt.t[1]);
    }
    cout << ans;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
