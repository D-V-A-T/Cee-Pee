int posmod(int a,int b){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int n,m, dx[4]={1,-1,0,0}, dy[4]={0,0,1,-1}, day[1505][1505];
pii R={-1,-1},T;
vect<vect<char>> v;

inline bool safe(int x, int y){
    return x>=0 && y>=0 && x<n && y<m;
}

struct reimu{
    int x,y,dist;
    bool operator < (const reimu& o) const{
        return dist > o.dist;
    }
};

struct Graph{
    vect<vect<int>> dist;
    queue<reimu> que;
    Graph(){
        dist.rs(n ,vect<int>(m, INF));
    }void BFS(){
        while(que.size()){
            reimu A=que.front();
            int x=A.x, y=A.y, d=A.dist;
            que.pop();
            if(d > dist[x][y])continue;
            for(int i=0;4>i;i++){
                int nx=x+dx[i], ny=y+dy[i];
                if(safe(nx, ny) && dist[nx][ny] > d+1){
                    dist[nx][ny]=d+1;
                    que.push({nx,ny,d+1});
                }
            }
        }for(int i=0;n>i;i++)
                for(int f=0;m>f;f++)
                    day[i][f]=dist[i][f];
    }void dickcha(){
        que.push({R.fi,R.se,0});
        dist.assign(n ,vect<int>(m, INF));
        dist[R.fi][R.se]=0;
        while(que.size()){
            reimu A=que.front();
            int x=A.x, y=A.y, d=A.dist;
            que.pop();
            if(d > dist[x][y])continue;
            for(int i=0;4>i;i++){
                int nx=x+dx[i], ny=y+dy[i];
                if(safe(nx, ny) && dist[nx][ny] > max(d, day[nx][ny])){
                    dist[nx][ny]=max(d, day[nx][ny]);
                    que.push({nx,ny,dist[nx][ny]});
                }
            }
        }cout << dist[T.fi][T.se];
    }
};

void sol() {
    cin >> n >> m ;
    v.rs(n, vect<char>(m));
    Graph g;
    for(int i=0;n>i;i++)
        for(int f=0;m>f;f++){
            cin >> v[i][f];
            if(v[i][f] != 'X'){
                g.dist[i][f]=0;
                g.que.push({i,f,0});
            }if(v[i][f] == 'L'){
                if(R.fi == -1)R={i,f};
                else T={i,f};
            }
        }
    g.BFS();
    g.dickcha();
}

