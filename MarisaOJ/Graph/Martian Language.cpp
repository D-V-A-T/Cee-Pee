ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> G[N], visited(N);

void sol(){
    int n;
    cin >> n;

    string a[n];
    for(auto& i : a)cin >> i;

    vect<int> topo, in(269), seen(269);
    
    for(auto& i : a)for(auto& j : i)seen[j] = 1;

    for(int i=0;n>i;i++){
        for(int j=i+1;n>j;j++){
            int sz = min(a[i].size(), a[j].size());

            for(int k=0;sz>k;k++) if(a[i][k] != a[j][k]){
                in[a[j][k]]++;
                G[a[i][k]].eb(a[j][k]);
                break;
            }
        }
    }


    queue<int> que;
    for(int i='a';'z'>=i;i++)if(!in[i]) que.push(i);
    while(que.size()){
        int u = que.front();
        topo.eb(u);
        que.pop();

        for(int v : G[u]){
            in[v]--;
            if(!in[v]) que.push(v);
        }
    }

    if(topo.size() != 26) cout << -1;
    else{
        for(int i : topo) cout << (char)i;    
    }
}

