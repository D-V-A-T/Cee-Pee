int n,m,k;

struct reimu {
    int u, dist;
    bool operator <(const reimu& o)const{
        return dist > o.dist;
    }
};

struct Graph {
    vect<vect<pii>> graph;
    vect<vect<int>> sources, dist;
    prq<reimu> pq;
    Graph(){
        dist.rs(n+1, vect<int>(k+1, INF));
        graph.rs(n+1);
        sources.rs(k+1);
        for(int i=1;n>=i;i++){
            int x;cin >> x;
            sources[x].eb(i);
        }for(int i=0;m>i;i++){
            int u,v,w;
            cin >> u >> v >> w;
            graph[u].eb(v,w);
            graph[v].eb(u,w);
        }int cost=INF, city;
        for(int i=1;k>=i;i++)dickcha(i);
        for(int i=1;n>=i;i++){
            int nc=0;
            for(int f=1;k>=f;f++)nc+=dist[i][f];
            if(cost > nc){
                cost=nc;
                city=i;
            }
        }cout << city;
    }void dickcha(int source) {
        for(int& i : sources[source]){
            pq.push({i, 0});
            dist[i][source]=0;
        }while(pq.size()){
            reimu A=pq.top();
            pq.pop();
            int start=A.u, d=A.dist;
            if(dist[start][source] < d)continue;
            for(pii& i : graph[start]){
                int v=i.fi, w=i.se;
                if(dist[v][source] > d + w){
                    dist[v][source]=d+w;
                    pq.push({v, d+w});
                }
            }
        }
    }
};

