int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> G[N], visited(N);
int dp[N][26], ch[N], ans = 1;

void DFS(int u){
    visited[u] = 1;

    for(int v : G[u]){
        if(!visited[v]) DFS(v);
        for(int c=0;26>c;c++) dp[u][c] = max(dp[u][c], dp[v][c]);
    }
    dp[u][ch[u]]++;
    for(int c=0;26>c;c++) ans = max(ans, dp[u][c]);
}

void sol(){
    int n, m;
    cin >> n >> m;

    for(int i=1;n>=i;i++){
        char c;cin >> c;
        ch[i] = c-'a';
    }

    for(int i=0;m>i;i++){
        int u,v;
        cin >> u >> v;
        G[u].eb(v);
    }

    for(int i=1;n>=i;i++){
        if(!visited[i]) DFS(i);
    }
    cout << ans;
}

