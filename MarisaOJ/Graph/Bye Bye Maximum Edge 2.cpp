ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<pii> graph[N];
int dist[N][4];

struct fumo{
    int u, d, cbd;
    bool operator < (const fumo& o) const{
        return d > o.d;
    }
};

void sol(){
    int n, m, S=1, X;
    cin >> n >> m >> S >> X;
    for(int i=0;m>i;i++){
        int u,v,w;cin >> u >> v >> w;
        w += X;
        graph[u].eb(v, w);
        graph[v].eb(u, w);
    }

    memset(dist, 5, sizeof dist);

    dist[S][0] = 0;
    prq<fumo> que;
    que.push({S, 0, 0});
    while(que.size()){
        auto [u, d, cbd] = que.top();
        que.pop();
        if(dist[u][cbd] < d)continue;
        for(auto [v, w] : graph[u]){
            int nd1 = min(dist[u][1] + w, dist[u][0]);
            int nd2 = min(dist[u][2] + w, dist[u][0] + w + w);
            int nd3 = min({dist[u][3]+w, dist[u][1] + w + w, dist[u][2], dist[u][0]+w});
            if(dist[v][1] > nd1){
                dist[v][1] = nd1;
                que.push({v, nd1, 1});
            }
            if(dist[v][2] > nd2){
                dist[v][2] = nd2;
                que.push({v, nd2, 2});
            }
            if(dist[v][3] > nd3){
                dist[v][3] = nd3;
                que.push({v, nd3, 3});
            }
            if(dist[v][0] > dist[u][0] + w){
                dist[v][0] = dist[u][0] + w;
                que.push({v, dist[v][0], 0});
            }
        }
    }
    for(int i=1;n>=i;i++)if(i != S)cout << dist[i][3] << ' ';
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
