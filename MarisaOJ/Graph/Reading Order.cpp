int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> G[N], visited(N);

void sol(){
    int n, m;
    cin >> n >> m;
    vect<int> topo, in(n+1);
    for(int i=0;m>i;i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        in[v]++;
    }

    auto TopoSort = [&](){
        queue<int> que;
        for(int i=1;n>=i;i++) if(!in[i])que.push(i);

        while(que.size()){
            int u = que.front();
            topo.eb(u);
            que.pop();
            if(visited[u])continue;
            visited[u] = 1;

            for(int v : G[u]){
                in[v]--;
                if(!in[v]) que.push(v);
            }
        }

    };
    
    TopoSort();
    for(int i : topo)cout << i << ' ' ;
}

