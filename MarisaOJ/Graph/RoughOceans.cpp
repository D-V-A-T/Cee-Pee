int dx[4] = {0, 0, 1, -1}, dy[4] = {1, -1, 0, 0}, n, r;

struct reimu {
    int x, y, dist;
    bool operator <(const reimu& o)const{
        return dist > o.dist;
    }
};

struct Graph {
    vector<vector<int>> dist;
    Graph() : dist(n, vect<int>(n, INF)) {}
    bool safe(int x, int y) {
        return x >= 0 && y >= 0 && x < n && y < n;
    }int BFS() {
        queue<reimu> pq;
        int res=0;
        for(int i=0;n>i;i++){
            for(int f=0;n>f;f++){
                char c;
                cin >> c;
                if(c=='X'){
                    dist[i][f]=0;
                    pq.push({i,f,0});
                    res++;
                }
            }
        }
        while(pq.size()){
            reimu A=pq.front();
            pq.pop();
            int x=A.x, y=A.y, d=A.dist;
            if(d >= r)break;
            if(d > dist[x][y])continue;
            for(int i=0;4>i;i++){
                int nx=x+dx[i], ny=y+dy[i];
                if(safe(nx, ny) && dist[nx][ny] == INF){
                    res++;
                    dist[nx][ny]=d+1;
                    pq.push({nx,ny,dist[nx][ny]});
                }
            }
        }return n*n-res;
    }
};

