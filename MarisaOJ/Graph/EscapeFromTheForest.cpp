int n;
vect<int> dx={0, 0, -1, 1}, dy={1, -1 ,0 ,0};
vect<string> v;

struct reimu{
    int x,y,dst,dir;
};

class Graph{
    vect<vect<pii>> graph;
    vect<vect<int>> dist;
public:
    Graph(){
        graph.rs(n*n+1);
        dist.rs(n, vect<int>(n, INF));
    }bool safe(int x, int y){
        if(x>=0 && y>=0 && x<n && y<n && v[x][y] == '.')return 1;
        return 0;
    }void BFS(){
        dist[0][0]=1;
        deque<reimu> dq;    
        dq.pb({0,0,0, 3});
        while(dq.size()){
            reimu X=dq.front();
            dq.pof();
            int x=X.x, y=X.y, d=X.dst, dir=X.dir;
            if(d > dist[x][y])continue;
            for(int i=0;4>i;i++){
                int nd=d;
                if(safe(x+dx[i], y+dy[i])){
                    if(i != dir)nd++;
                    if(dist[x+dx[i]][y+dy[i]] > nd){
                        dist[x+dx[i]][y+dy[i]]=nd;
                        if(i != dir)dq.pb({x+dx[i],y+dy[i],nd,i});
                        else dq.push_front({x+dx[i],y+dy[i],nd,i});
                    }
                }
            }
        }if(dist[n-1][n-1] >= INF)cout << -1;
        else cout << dist[n-1][n-1];
    }
};

void sol(){
    cin >> n;
    v.rs(n);
    for(auto& i : v)cin >> i;
    Graph g;
    g.BFS();
}

