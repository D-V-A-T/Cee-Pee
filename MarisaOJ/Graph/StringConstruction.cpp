/*
You are given n strings. Your task is to construct a new string S
by following these steps:

    Start with an empty string S

    In each move, choose a string from the given n strings 
    and append it to the end of S

    You can choose a string multiple times.

    From the second move, the chosen string must have its first character 
    equal to the last character of the previously chosen string.
    And that first character is removed. For example, S=marisa
    then you choose ad, the resulting string is S=marisad

You need to find the smallest possible length of S
that contains a target string T as a subsequence.
*/
int n,m;
string T;
vect<string> a;

struct reimu{
    int matched, len;
    char lc;
    bool operator < (const reimu& o) const{
        return len > o.len;
    }
};

class Graph{
public:
    int dickcha(){
        vect<vect<int>> dist(301, vect<int>(269, 1e9));
        // dist[i][j] : the minimum length of the string that
        // has the first i characters of string T as subsequence
        // and ends with 'j'
        prq<reimu> pq;
        pq.push({0, 0, '#'});
        dist[0]['#']=0;
        while(!pq.empty()) {
            reimu x=pq.top();
            pq.pop();
            int matched=x.matched, len=x.len;
            char lc=x.lc;
            if(dist[matched][lc] < len)continue;
            for(const string& i : a) {
                if(i[0]==lc || lc=='#') {
                    int nmatched=matched;
                    for(int f=(lc != '#');f<i.size();f++)
                        if(nmatched < m && T[nmatched] == i[f])
                            nmatched++;
                    int d=len + (int)i.size() - (lc != '#');
                    if (dist[nmatched][i.back()] >= d) {
                        dist[nmatched][i.back()]=d;
                        pq.push({nmatched, d, i.back()});
                    }
                }
            }
        }return *min_element(bend(dist[m]));
    }
};

void sol(){
    cin >> n;
    a.rs(n);
    for(auto& i : a)cin >> i;
    cin >> T;
    m=T.size();
    Graph g;
    cout << g.dickcha();
}

