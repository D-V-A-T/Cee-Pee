ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> G[N], D(N);
map<int, int> un[N];
vect<pii> fumo;
string ans = "YES";


class DSU{
public:
    vector<int> par, len;
    DSU(const int& n){
        par.rs(n+69);
        len.rs(n+69, 1);
        iota(bend(par), 0);
    }int findpar(int u){
        if(u == par[u])
            return u;
        return par[u]=findpar(par[u]);
    }void join(int u, int v){
        u=findpar(u);
        v=findpar(v);
        if(u != v){
            if(len[u] < len[v])swap(u, v);
            par[v]=u;
            len[u]+=len[v];
        }
    }
};


void sol(){
    int n, m;
    cin >> n >> m;

    vect<int> in(n+1);
    DSU A(n);

    for(int i=0;m>i;i++){
        int t, u, v;
        cin >> t >> u >> v;
        if(t == 1){
            G[u].eb(v);
            fumo.eb(u, v);
            A.join(u, v);
            in[v]++;
        }else{
            un[u][v] = un[v][u] = 1;
        }
    }

    queue<int> que;
    for(int i=1;n>=i;i++)if(!in[i]) que.push(i);
    vect<int> topo;
    while(que.size()){
        int u = que.front();
        que.pop();
        topo.eb(u);
        for(int v : G[u])if(--in[v] == 0)que.push(v);
    }

    if(topo.size() != n){
        cout << "NO";
        return;
    }else for(int i=0;n>i;i++)D[topo[i]] = i;


    for(int i=1;n>=i;++i){
        for(auto& j : un[i]){
            if(!j.se) continue;

            un[i][j.fi] = un[j.fi][i] = 0;

            int u = A.findpar(i), v = A.findpar(j.fi);


            if(u == v){

                if(D[i] <= D[j.fi]) fumo.eb(i, j.fi);
                else fumo.eb(j.fi, i);
                continue;
            }

            if(u < v) fumo.eb(j.fi, i);
            else fumo.eb(i, j.fi);
            

        }
    }


    cout << "YES\n";
    for(pii i : fumo)cout << i.fi << ' ' << i.se << el;

}

