struct reimu {
    int u, dist;
    bool operator <(const reimu& o)const{
        return dist > o.dist;
    }
};

struct Graph {
    vect<vect<int>> graph;
    vect<int> a,b,dist;
    umap<int, int> dolls;
    int DTM;
    queue<reimu> pq;
    Graph(){
        dist.rs(n+1,INF);
        graph.rs(n+1);
        a.rs(k);b.rs(p);
        for(int& i : a)cin >> i;
        for(int& i : b){
            cin >> i;
            dolls[i]=INF;
        }for(int i=0;m>i;i++){
            int u,v;
            cin >> u >> v;
            graph[u].eb(v);
            graph[v].eb(u);
        }BFS(1);
        BFS(0);
        for(int i : b){
            if(dolls[i] > dist[i] && dist[i] < INF){
                cout << "YES";
                return;
            }
        }cout << "NO";
    }void BFS(bool t){
        if(t)
            for(int i : a){
                if(dolls[i])dolls[i]=0;
                pq.push({i,0});dist[i]=0;
            }
        else{
            fill(bend(dist), INF);
            pq.push({1,0});dist[1]=0;
        }
        while(pq.size()){
            int start=pq.front().u, d=pq.front().dist;
            pq.pop();
            if(d > dist[start])continue;
            for(int v : graph[start]){
                if(dist[v] > dist[start]+1){
                    dist[v]=dist[start]+1;
                    if(t && dolls[v])dolls[v]=dist[v];
                    pq.push({v, dist[v]});
                }
            }
        }
    }
};

