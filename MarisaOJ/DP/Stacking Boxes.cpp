int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[5005][5005];
// dp[i][j] : i hop dau, chon j hop 

void sol(){
    int n;
    cin >> n;
    pii a[n];
    for(pii& i : a)cin >> i.fi;
    for(pii& i : a)cin >> i.se;
    sort(a, a+n, [](const pii& a, const pii& b){
        return a.fi + a.se > b.fi + b.se;
    });
    
    memset(dp, -1, sizeof dp);
    dp[0][1] = a[0].se;
    dp[0][0] = INF;

    
    for(int i=1;n>i;i++){
        dp[i][0] = INF;
        for(int j=1;n>=j;j++){
            int cap = dp[i-1][j-1];
            if(a[i].fi <= cap){
                cap = min(a[i].se, cap - a[i].fi);
                dp[i][j] = max(dp[i][j], cap);
            }
            dp[i][j] = max(dp[i-1][j], dp[i][j]);
        }
    }

    for(int i=n;i>=1;i--) if(dp[n-1][i] > -1){
        cout << i;
        return;
    }
    
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
