/*
Given two strings A and B, count the number of different ways
to choose k non-overlapping consecutive substrings from A to 
concatenate and form the string B. 
The chosen substrings must maintain their relative order.
*/

int dp[2][201][201][2];
// dp[i][j][k][0] : number of ways to create the first j characters
//                  of B from k groups using the first i characters of A
//                  (A[i] IS NOT present in the groups)
// dp[i][j][k][0] : number of ways to create the first j characters
//                  of B from k groups using the first i characters of A
//                  (A[i] IS present in the groups)

void sol(){
    int n,m,k;
    cin >> n >> m >> k;
    string a,b;
    cin >> a >> b;
    a=" "+a;
    b=" "+b;
    dp[0][0][0][0]=1;
    for(int i=1;n>=i;i++){
        for(int f=0;m>=f;f++){
            for(int p=0;k>=p;p++){
                dp[i%2][f][p][0]=dp[i%2][f][p][1]=0;
                if(a[i] == b[f] && f && p){
                    dp[i%2][f][p][1] = dp[(i-1)%2][f-1][p-1][0] 
                                    + dp[(i-1)%2][f-1][p][1]
                                    + dp[(i-1)%2][f-1][p-1][1] ;
                }
                dp[i%2][f][p][0] += dp[(i-1)%2][f][p][1] 
                                    + dp[(i-1)%2][f][p][0];
                dp[i%2][f][p][0] %= MOD;
                dp[i%2][f][p][1] %= MOD;
            }
        }
    }cout << (dp[n%2][m][k][0] + dp[n%2][m][k][1]) % MOD;
}

