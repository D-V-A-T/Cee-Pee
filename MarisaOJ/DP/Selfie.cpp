int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int visited[5005], ss[5005];
ll dp[5005][5005][2];
vect<pii> graph[5005];

void DFS(int u){
    visited[u] = 1;    
    dp[u][1][0] = dp[u][1][1] = 0;
    ss[u] = 1;
    for(pii i : graph[u]){
        int v = i.fi, w= i.se;
        if(!visited[v]){
            DFS(v);
            int ps = ss[u];
            ss[u] += ss[v];
            for(int j = ss[u];j >= 2; j--){
                for(int k = 1;min(j, ps+1)>k;k++){
                    dp[u][j][0] = min({dp[u][j][0], dp[u][k][1] + dp[v][j-k][0] + w,
                                       dp[u][k][0] + dp[v][j-k][1] + 2*w});
                    dp[u][j][1] = min(dp[u][j][1], dp[u][k][1] + dp[v][j-k][1] + 2*w);
                }
            }
        }
    }
}

void sol(){
    int n;
    cin >> n;
    for(int i=1;n>=i;i++)for(int j=1;n>=j;j++)dp[i][j][0] = dp[i][j][1] = INF;    
    for(int i=1;n>i;i++){
        int u, v, w;
        cin >> u >> v >> w;
        graph[u].eb(v, w);
        graph[v].eb(u, w);
    }
    DFS(1);
    for(int i=1;n>=i;i++){
        cout << dp[1][i][0] << el;
    }
}

