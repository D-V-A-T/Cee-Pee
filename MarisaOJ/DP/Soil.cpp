/*
Marisa has a garden with n plots, and each plot i initially contains 
Ai units of soil. Marisa wants to modify the amount of soil in each 
plot so that they contain Bi units of soil.

Marisa can perform the following actions, each with its associated cost:

    Buy an additional unit of soil at a cost of x
    Dump a unit of soil from a plot at a cost of y
    Move an unit of soil from plot i to plot j at a cost of |iâˆ’j|*z

Your task is to determine the minimum cost for Marisa to renovate her garden by adjusting the soil in each plot.
*/

void sol(){
    int n,x,y,z,na=0,nb=0;
    cin >> n >> x >> y >> z;
    vect<int> a={-1},b={-1};
    // If we store the amount of soil plot A[i], B[i] have,
    // the problem turns into what's the minimum number of 
    // operations to turn a to b
    for(int i=0;n>i;i++){
        int u,v;cin >> u >> v;
        for(int f=0;u>f;f++)a.eb(i);
        for(int f=0;v>f;f++)b.eb(i);
    }na=a.size()-1;nb=b.size()-1;
    vect<vect<int>> dp(na+1, vect<int>(nb+1, INF));
    dp[0][0]=0;
    for(int i=1;max(na,nb)>=i;i++){
        if(i <= na)dp[i][0]=y*i;
        if(i <= nb)dp[0][i]=x*i;
    }for(int i=1;na>=i;i++){
        for(int f=1;nb>=f;f++){
            dp[i][f]=min({dp[i][f-1]+x, dp[i-1][f]+y, 
                        dp[i-1][f-1] + z*abs(b[f]-a[i])});
        }
    }cout << dp[na][nb] ;
}

