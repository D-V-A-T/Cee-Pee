/*
Given A[n][n] (A[i][j] is the number of mushrooms at (i, j))
Marisa starts at (1,1), goes to (n, n) and returns
Find the maxmimum number of mushrooms she can collect
(she can't pick mushrooms from the same cell twice)
*/
int dp[69][69][69][69];

void sol(){
    int n;
    cin >> n;
    vect<vect<int>> v(n+1, vect<int>(n+1));
    for(int i=1;n>=i;i++)
        for(int f=1;n>=f;f++)
            cin >> v[i][f];
    dp[1][1][1][1]=v[1][1];
    // imagine there're 2 marisas: marisa and marisad
    // dp[i][j][k][l] = the maximum number of mushrooms can be collected
    // if marisa goes to (i, j) and marisad goes to (k, l)
    for(int i=1;n>=i;i++){
        for(int j=1;n>=j;j++){
            for(int f=1;n>=f;f++){
                for(int p=1;n>=p;p++){
                    if(dp[i][j][f][p])continue;
                    dp[i][j][f][p]=max({dp[i][j-1][f-1][p], dp[i-1][j][f-1][p],
                                        dp[i][j-1][f][p-1], dp[i-1][j][f][p-1]})
                                    + v[i][j];
                    if(i!=f || j!=p)
                        dp[i][j][f][p]+=v[f][p];
                }
            }
        }
    }cout << dp[n][n][n][n];
}

