ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct DP{
    pii dp[20][10][2];
    vect<int> x, p10;
    DP(int n){
        if(n<0)return;
        int p = 19;
        for(auto& a : dp)for(auto& b : a)for(auto& c : b)c={0, 0};
        x.rs(20);
        p10.rs(20);
        p10[0]=1;
        for(int i=1;20>i;i++)p10[i] = p10[i-1] * 10;
        while(n){
            x[p--] = n%10;
            n/=10;
        }
    }
    int cbd(){
        // dp[pos][digit][0/1]
        // 0 : =   | 1 : <
        int ans = 0;
        for(int d=0;9>=d;d++){
            if(x[0] > d)dp[0][d][1] = {1, d};
            else if(x[0] == d)dp[0][d][0] = {1, d};
        }
        for(int i=1;20>i;i++){
            for(int d=0;9>=d;d++){
                for(int c=0;9>=c;c++){
                    dp[i][d][1].fi += dp[i-1][c][1].fi;
                    dp[i][d][1].se += dp[i-1][c][1].se;
                }
                if(x[i] > d){
                    dp[i][d][1].fi += dp[i-1][x[i-1]][0].fi;
                    dp[i][d][1].se += dp[i-1][x[i-1]][0].se;
                }else if(x[i] == d){
                    dp[i][d][0].fi = dp[i-1][x[i-1]][0].fi;
                    dp[i][d][0].se += dp[i-1][x[i-1]][0].se;
                }
                dp[i][d][1].se += dp[i][d][1].fi * d;
                dp[i][d][0].se += dp[i][d][0].fi * d;
            }
        }
        int res = 0;
        for(int d=0;10>d;d++)res += dp[19][d][0].se + dp[19][d][1].se;
        return res;
    }
};

