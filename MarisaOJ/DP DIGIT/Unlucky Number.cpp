ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct DP{
    int dp[20][10][2];
    vect<int> x;
    DP(int n){
        int p = 19;
        for(auto& a : dp)for(auto& b : a)for(auto& c : b)c=0;
        x.rs(20);
        while(n){
            x[p--] = n%10;
            n/=10;
        }
    }
    int cbd(){
        // dp[pos][digit][0/1] 
        // 0 : =   | 1 : <
        for(char d=0;9>=d;d++){
            if(d==4)continue;
            if(x[0] > d)dp[0][d][1] = 1;
            else if(x[0] == d)dp[0][d][0] = 1;
        }
        for(int i=1;20>i;i++){
            for(char d=0;9>=d;d++){
                if(d == 4)continue;
                for(char c=0;9>=c;c++){
                    if((d == 3 && c==1) || (c==4))continue;
                    dp[i][d][1] += dp[i-1][c][1];
                }
                if(x[i] > d){     
                    if(!(x[i-1] == 1 && d==3)){
                        // if(i==19)cout << dp[i-1][n[i-1]-'0'][0] << el;
                        dp[i][d][1] += dp[i-1][x[i-1]][0];
                    }
                }else if(x[i] == d){   
                    if(!(x[i-1] == 1 && d==3)){
                        //if(i==1)cout << dp[i-1][n[i-1]-'0'][0] << el;
                        dp[i][d][0] = dp[i-1][x[i-1]][0];
                    }
                }
            }
        }
        int res = 0;
        for(int d=0;10>d;d++)res += dp[19][d][0] + dp[19][d][1];
        return res-1;
    }
};

void sol(){
    int l, r;cin >> r;
    DP f1(r);
    cout << r - f1.cbd();
}

