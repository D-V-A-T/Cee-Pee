int pm(int a,const int b=MOD){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

ll ans = 0, a[N], n;    

inline bool on(int x, int b){
    return x & (1<<b);
}
int get(int l, int r, int* pfs){
    return pm(pfs[r] - (l?pfs[l-1]:0));
}

void dnc(int l, int r){
    if(l == r) return;
    int mid = (l+r)/2;
    dnc(l, mid);
    dnc(mid+1, r);
    vect<int> left, right;
    for(int i=l;mid>=i;i++) left.eb(a[i]);
    for(int i=mid+1;r>=i;i++) right.eb(a[i]);    
    
    int lsz = left.size();

    int sfx[lsz], sfn[lsz], c1n[lsz], c1x[lsz];
    ll pfs[lsz];


    for(int i=left.size()-1;i>=0;i--){
        if(i+1<left.size()) sfx[i] = max(left[i], sfx[i+1]), sfn[i] = min(left[i], sfn[i+1]);
        else sfx[i] = sfn[i] = left[i];
    }

for(int bit=0;30>bit;bit++){

    for(int i=0;left.size()>i;i++){
        c1n[i] = on(sfn[i], bit);
        c1x[i] = on(sfx[i], bit);
        pfs[i] = (c1n[i] ^ c1x[i]) * (1ll<<bit);
        if(i) c1n[i] += c1n[i-1], c1x[i] += c1x[i-1], pfs[i] += pfs[i-1];
        pfs[i] %= MOD;
    }

    int mxp = lsz, mnp = lsz;
    int mx = -2e9, mn = 2e9;

    for(int i=0;right.size()>i;i++){
        mx = max(mx, right[i]), mn = min(mn, right[i]);
        while(mxp>0 && sfx[mxp-1] <= mx) mxp--;
        while(mnp>0 && sfn[mnp-1] >= mn) mnp--;

        int unchanged = left.size() - max(mxp, mnp);
        ans += (on(mx, bit) ^ on(mn, bit)) * unchanged * (1ll<<bit);
        ans %= MOD;

        if(mxp < mnp){
            ll onee = get(mxp, mnp-1, c1n), ze = mnp-1-mxp+1 - onee;

            ans += ((!on(mx, bit)) * onee + (on(mx, bit)) * ze) * (1ll<<bit);
            ans %= MOD;

        }else if(mnp < mxp){

            ll onee = get(mnp, mxp-1, c1x), ze = mxp-1-mnp+1 - onee;

            ans += ((!on(mn, bit)) * onee + (on(mn, bit)) * ze) * (1ll<<bit);
            ans %= MOD;
        }

        if(min(mnp, mxp)) ans += pfs[min(mnp, mxp)-1];
        ans %= MOD;
    }

}
}

void sol(){
    cin >> n;

    for(int i=0;n>i;i++) cin >> a[i];

    dnc(0, n-1);

    cout << ans;
}

