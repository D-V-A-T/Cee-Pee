ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct Fick{
    int bit[N];
    Fick(){memset(bit, 0, sizeof bit);}
    void reset(int x){
        for(;x>0;x-=x&-x) bit[x] = 0;
    }
    void upd(int x, int v=1){
        for(;x>0;x-=x&-x) bit[x] += v;
    }
    int get(int x){
        int res = 0;
        for(;x<N;x+=x&-x) res += bit[x];
        return res;
    }
} ft;

struct seg{
    int t, l, r, id;    
    auto get() const{
        return tie(t, l, r, id);
    }
};

int q;  
vect<int> ans(N, -1);
vect<seg> a;

bool comp(seg a, seg b){
    return a.l != b.l ? a.l < b.l : a.t < b.t;  
}

vect<seg> dnc(int l, int r, bool bl){
    vect<seg> v;
    for(int i=l;r>=i;i++){
        if(bl && a[i].t == 2) v.eb(a[i]);
        if(!bl && a[i].t == 1) v.eb(a[i]);
    }
    if(l == r) return v;

    int mid = (l+r)/2;
    vect<seg> add = dnc(l, mid, 0);
    vect<seg> que = dnc(mid+1, r, 1);

    for(const auto& i : add) que.eb(i);
    sort(bend(que), comp);

    for(const auto& i : que){
        int t, L, R, id;
        tie(t, L, R, id) = i.get();

        if(t == 2) ans[id] += ft.get(R);
        else ft.upd(R);
    }

    for(const auto& i : que){
        int t, L, R, id;
        tie(t, L, R, id) = i.get();
        if(t == 1) ft.reset(R);
    }
    
    return v;

}

void sol(){
    cin >> q;
    a.rs(q);
    for(int i=0;q>i;i++){
        int t, l, r;
        cin >> t >> l >> r;
        a[i] = {t, l, r, i};
        if(t == 2) ans[i] = 0;
    }

    dnc(0, q-1, 0);

    for(int i=0;q>i;i++) if(ans[i] != -1) cout << ans[i] << el;
}

