int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, ans = 0;
vect<int> a(n), sufx[2*N], sufn[2*N];

int get_sum(int l, int r, const vect<int>& pfs){
    if(l > r) return 0;

    return pm(pfs[r] - (l ? pfs[l-1] : 0));
}

void dnc(int l, int r, int id){
    if(l == r){
        ans = (ans + a[l] * a[l]) % MOD;
        sufx[id].eb(a[l]);
        sufn[id].eb(a[r]);
        return;
    }

    int M = (l+r)/2;
    dnc(l, M, id*2);
    dnc(M+1, r, id*2+1);

    auto &sln = sufn[id*2], &slx = sufx[id*2];
    int sz = sln.size();
    vect<int> psn(sz), psx(sz), pfs;
    pfs.reserve(sz);
    partial_sum(bend(sln), psn.begin());
    partial_sum(bend(slx), psx.begin());

    for(int i=l;M>=i;i++){
        int fumo = sln[i-l] * slx[i-l] % MOD;
        if(pfs.empty()) pfs.eb(fumo);
        else pfs.eb((pfs.back() + fumo) % MOD);
    }

    int mn = INF, mx = -INF, add = 0;
    for(int i=M+1;r>=i;i++){
        mn = min(mn, a[i]);
        mx = max(mx, a[i]);

        int lo = 0, hi = sz-1, lmn = sz, lmx = sz;
        while(lo <= hi){
            int mid = (lo + hi) / 2;

            if(sln[mid] >= mn) lmn = mid, hi = mid-1;
            else lo = mid + 1;
        }

        lo = 0, hi = slx.size()-1;
        while(lo <= hi){
            int mid = (lo + hi) / 2;

            if(slx[mid] <= mx) lmx = mid, hi = mid-1;
            else lo = mid+1;
        }

        // unchanged part
        add += min((sz-1) - lmn + 1, (sz-1) - lmx + 1) * (mn * mx % MOD);
        add %= MOD;

        // if(id == 1) cout << add << ' ';
        
        // only mx / mn
        if(lmx < lmn)
            add = (add + get_sum(lmx, lmn-1, psn) * mx) % MOD; 
        else if(lmx > lmn)
            add = (add + get_sum(lmn, lmx-1, psx) * mn) % MOD;

        // if(id == 1) cout << add << ' ';

        // skibidi toilet

        if(min(lmn, lmx)) add = (add + pfs[min(lmn, lmx)-1]) % MOD;

        // if(id == 1) cout << lmn << ' ' << lmx << el;
    }

    for(int i=l;r>=i;i++) sufn[id].eb(a[i]);
    sufx[id] = sufn[id];

    
    // cout << l << ' ' << r << ':' << el << sufn[id].back() << ' ';
    for(int i=r-l+1-2;i>=0;i--){
        sufn[id][i] = min(sufn[id][i], sufn[id][i+1]);
        sufx[id][i] = max(sufx[id][i], sufx[id][i+1]);

        // cout << sufn[id][i] << ' ';
    }
    // nextl;
    // for(int i=r-l;i>=0;i--) cout << sufx[id][i] << ' ';

    // cout << el << add << el << el;

    ans = (ans + add) % MOD;
}


void sol(){
    cin >> n;
    a.rs(n);
    for(int& i : a) cin >> i;

    dnc(0, n-1, 1);

    cout << ans;
}

