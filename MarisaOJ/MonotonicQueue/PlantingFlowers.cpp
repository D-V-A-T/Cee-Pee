int funky(int k, vect<int>& v){
    int n=v.size();
    vect<int> dp(n, INF);
    dp[0]=0;
    multiset<int> ms;
    ms.insert(0);int p=0;
    for(int i=1;n>i;i++){
        while(ms.size() > k+1){
            ms.erase(ms.find(dp[p]));
            p++;
        }dp[i]=*ms.begin()+v[i];
        ms.insert(dp[i]);
    }for(int i=n-1;i >=0 && i>=n-k-1;i--)
        dp[n-1]=min(dp[n-1], dp[i]);
    return dp[n-1];
}

