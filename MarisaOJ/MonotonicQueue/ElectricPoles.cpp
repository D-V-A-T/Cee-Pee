/*
A company is responsible for constructing n electric poles for a city. 
The i-th electric pole has a height of h[i]. 
In order to maintain urban aesthetics, the city has imposed a 
regulation that the company will be charged a cost based on the 
height difference between two adjacent poles i and i+1, 
given by c[i] x |h[i] − h[i+1]|. 
Additionally, the height difference between two neighboring poles 
must not exceed d


To meet these requirements, the company can increase the height of certain 
poles. However, increasing the height of the i-th pole by x units will 
incur a cost of x^2. 
Find the minimum cost required to comply with the city’s regulations.
*/
void sol(){
    int n,d;
    cin >> n >> d;
    int h[n], c[n-1];
    vect<vect<int>> dp(n, vect<int>(5069, INF));
    for(int& i : c)cin >> i;
    for(int& i : h)cin >> i;
    for(int i=0;n>i;i++){
        int p_optp=-1;
        // Nhan xet: vi tri optimal cho dp[i][f] va vi tri optimal
        //          cho dp[i][f-1] co chenh lech ko qua 1 
        for(int f=h[i], h[i-1];5000>=f;f++){
            if(!i)dp[i][f]=(f-h[i])*(f-h[i]);
            else{
                int l=max(f-d, h[i-1]), r=min(5000ll, f+d), optp=-1;
                if(p_optp > -1){
                    // neu da tim duoc vi tri optimal cua dp[i][f-1]
                    // thi chinh sua l, r de cai thien thoi gian chay
                    l=max(l, p_optp-1);
                    r=min(r, p_optp+1);
                }
                while(l <= r){
                    int mid=(l+r)/2, reimu, marisa;
                    int p=mid;
                    // p : do cao cua cot thu i-1
                    reimu=dp[i-1][p]+abs(f-p)*c[i-1]+(f-h[i])*(f-h[i]);
                    if(abs(p+1-f) > d || mid>=5000){
                        optp=mid;
                        dp[i][f]=reimu;
                        r=mid-1;
                        continue;
                    }
                    p=mid+1;
                    marisa=dp[i-1][p]+abs(f-p)*c[i-1]+(f-h[i])*(f-h[i]);
                    if(reimu <= marisa && marisa<=INF && reimu<=INF){
                        optp=mid;
                        dp[i][f]=reimu;
                        r=mid-1;
                    }else l=mid+1;
                }p_optp=optp;
            }
        }
    }
    cout << *min_element(bend(dp[n-1]));
}

