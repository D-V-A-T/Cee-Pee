ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int h, w, n;
    cin >> h >> w >> n;

    vect<pii> a(n);
    vect<int> r_coord, c_coord;

    for(pii &i : a) cin >> i.fi >> i.se, r_coord.eb(i.fi), c_coord.eb(i.se);

    sort(bend(r_coord));  
    sort(bend(c_coord));

    pii ans = {INF, INF};

    pii answ = {INF, INF}, ansh = {INF, INF};

    for(int i=0; n>i; i++){
        pii pivot = a[i];

        int ifup = 0, ifdown = 0, ifleft = 0, ifright = 0;
        int p = lwb(bend(r_coord), a[i].fi) - r_coord.begin() - 1;

        int vert = 2e9, horni = 2e9, ifvert = 0, ifhorni = 0;

        if(p >= 0) { // move up to reach the top
            vert = r_coord[p];
            ifvert = (h - (a[i].fi - vert) + 1);
        }else{
            vert = 0;
            ifvert = (r_coord.back() - a[i].fi + 1);
        }

        vert = min(vert, h - a[i].fi + 1);// move down to reach the top


        p = lwb(bend(c_coord), a[i].se) - c_coord.begin() - 1;

        if(p >= 0){ // move left to reach the leftmost
            horni = c_coord[p];
            ifhorni = (w - (a[i].se - horni) + 1);
        }else{
            horni = 0;
            ifhorni = c_coord.back() - a[i].se + 1;
        }

        horni = min(horni, w - a[i].se + 1); // move right to reach the leftmost

        answ = min(answ, make_pair(ifhorni, horni));
        ansh = min(ansh, make_pair(ifvert, vert));


        ans = min(ans, make_pair(ifvert * ifhorni, horni + vert));
    }

    cout << answ.fi * ansh.fi << ' ' << answ.se + ansh.se << el;
}

