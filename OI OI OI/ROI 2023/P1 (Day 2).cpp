ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, k, nig[N], sack[3], sz[N], sus[N];
pii T[N];

void dfsus(int u, int turn_cnt, int prev){

    if(nig[u] == 0) {
        sus[u] = turn_cnt <= k;
        return;
    }

    // prev = 3: start
    // prev = 1: left
    // prev = 2: right

    if(prev == 3) dfsus(T[u].fi, turn_cnt, 1), dfsus(T[u].se, turn_cnt, 2);

    if(prev == 1) dfsus(T[u].fi, turn_cnt, 1), dfsus(T[u].se, turn_cnt+1, 2);

    if(prev == 2) dfsus(T[u].fi, turn_cnt+1, 1), dfsus(T[u].se, turn_cnt, 2);
}

void dfs(int u){

    if(nig[u] == 0) return;

    dfs(T[u].fi);
    dfs(T[u].se);

    sus[u] = sus[T[u].fi] + sus[T[u].se];
}

