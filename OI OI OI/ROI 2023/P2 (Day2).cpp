ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct meeting{
	int l, r, id;
	bool operator < (const meeting &o) const{
		return r < o.r;
	}
};

void sol(){
	int n;
	cin >> n;
	meeting a[n];

	for(int i=0; n>i; i++){
		cin >> a[i].l >> a[i].r;
		a[i].id = i+1;
	}

	sort(a, a+n);

	int ed = -1;
	vect<meeting> lig, m1, m2, ma;


	for(auto i : a){
		if(i.l > ed) lig.pb(i), ed = i.r;
		else ma.pb(i);
	}

	for(int i=0; lig.size()/2 > i; i++) m1.pb(lig[i]);
	for(int i=lig.size()/2; lig.size()>i; i++) m2.pb(lig[i]);

	int susr = m1.back().r;

	// cout << m1.size() << ' ' << m2.size() << el;

	if(m1.size() == n/2){
		for(auto i : m1) cout << i.id << ' ';
		nextl;
		return;
	}

	if(m2.size() == n/2){
		for(auto i : m2) cout << i.id << ' ';
		nextl;
		return;
	}

	for(auto i : ma){
		if(i.l <= susr) m1.pb(i);
		else m2.pb(i);

		if(m1.size() == n/2){
			for(auto i : m1) cout << i.id << ' ';
			nextl;
			return;
		}

		if(m2.size() == n/2){
			for(auto i : m2) cout << i.id << ' ';
			nextl;
			return;
		}

	}
}

