ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

#define pll pair<long long, long long>

const ll LIM = 5e6 + 5;
ll n, k, len[N], in[N], sz[N], ans = 1e9;
pll dep[N];
vect<pll> G[N];
vect<ll> tour;

void prep(int u, int par){
    in[u] = tour.size();
    tour.eb(u);
    sz[u] = 1;

    for(auto [v, w] : G[u]) if(v != par){
        dep[v].fi = dep[u].fi + w;
        dep[v].se = dep[u].se + 1;
        prep(v, u);
        sz[u] += sz[v];
    }
}

map<int, int> fumo;

void sack(int u, int par, bool keep){
    int fat = -1, fw;
    for(auto [v, w] : G[u]) if(v != par){
        if(fat == -1 || sz[fat] < sz[v]) fat = v, fw = w;
    }

    for(auto [v, w] : G[u]) if(v != par && v != fat) sack(v, u, 0);

    if(fat != -1) sack(fat, u, 1);

    if(fumo.count(dep[u].fi)) fumo[dep[u].fi] = min(fumo[dep[u].fi], dep[u].se);
    else fumo[dep[u].fi] = dep[u].se;
    
    for(auto [v, w] : G[u]) if(v != par && v != fat){
        for(int i = in[v]; in[v] + sz[v] > i; i++){
            // dep[?].fi = k + 2 * dep[u].fi - dep[v].fi;


            int y = tour[i], x = k + 2 * dep[u].fi - dep[y].fi;
           
            if(fumo.count(x)) ans = min(ans, dep[y].se + fumo[x] - 2 * dep[u].se);
        }
        for(int i = in[v]; in[v] + sz[v] > i; i++) {
            int y = tour[i];
            if(fumo.count(dep[y].fi)) fumo[dep[y].fi] = min(fumo[dep[y].fi], dep[y].se);
            else fumo[dep[y].fi] = dep[y].se;
        }     
    }


    // dep[?].fi - dep[u].fi = k
    if(fumo.count(dep[u].fi + k)) ans = min(ans, fumo[dep[u].fi + k] - dep[u].se);

    if(!keep){
        fumo.clear();
    }
}


void sol(){
    cin >> n >> k;
    for(int i=0; n-1>i; i++){
        int u, v, w;
        cin >> u >> v >> w;
        G[u].eb(v, w);
        G[v].eb(u, w);
    }

    prep(0, -1);
    sack(0, -1, 0);

    cout <<  (ans > n ? -1 : ans);
}

signed main(){
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}

