int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, S;
    cin >> S >> n;
    vect<int> dp(S+1);
    vect<pii> a, w[S+1];
    for(int i=0;n>i;i++){
        int u, v, z;
        cin >> v >> u >> z;
        w[u].eb(v, z);
    }
    
    for(int i=0;S>=i;i++){
        sort(bend(w[i]), greater<pii>());
        int cnt = 0, p=0;
        while(p<w[i].size() && cnt<S/i){
            a.eb(i, w[i][p].fi);
            w[i][p].se--;
            cnt++;
            if(!w[i][p].se)p++;
        }
    }
    
    for(int i=0;a.size()>i;i++){
        for(int j=S;j>=a[i].fi;j--){
            dp[j] = max(dp[j], dp[j - a[i].fi] + a[i].se);
        }
    }

    cout << dp[S];

}

