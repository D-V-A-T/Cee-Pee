ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int lg2(int i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}

int spt[1000005][21];

void sol(){
    int n;
    cin >> n;
    int a[n*2], pfs[n*2], g[n*2];
    for(int i=0;n>i;i++){
        cin >> a[i];
        a[i+n] = a[i];
    }
    partial_sum(a, a+n*2, pfs);
    
    auto gs = [&](int l, int r){
        if(l > r) return pfs[r] + pfs[n-1] - pfs[l-1];
        return pfs[r] - (l?pfs[l-1]:0);
    };

    int s = (n+1)/2, ans = -INF;

    for(int i=0;n*2>i;i++) g[i] = spt[i][0] = gs(i, (i+s-1)%(2*n));

    for(int b=1;21>b;b++){
        for(int i=0;n*2>i+(1<<(b-1));i++) 
            spt[i][b] = min(spt[i][b-1], spt[i+(1<<(b-1))][b-1]);
    }

    auto get = [&](int l, int r){
        int b = lg2(r-l+1);
        return min(spt[l][b], spt[r-(1<<b)+1][b]);
    };

    for(int j=s-1;n+s-1>j;j++){
        ans = max(ans, get(j-s+1, j));
    }
    cout << ans;
}

