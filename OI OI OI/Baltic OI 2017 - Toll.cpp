int pm(int a,const int b=MOD){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<pii> F[50005], T[50005];
int dt[50005][17][6], n, m, q, k, mask[50005], fumo[50005][17];
// let the block in where mid lies in is B, dt[i][d][p] is the minimum cost from B[p] to i or vice versa
// fumo[i][j] : the mid corresponds to i in dep j

void fillBlock(int B, int l, int r, int dep, int x = 0){
    for(int i=max(l, B*k);min(r+1, (B+1)*k)>i;i++) dt[i][dep][i%k] = x;
}

void dnc(int l, int r, int dep){
    if(l == r) return ;

    
    int mid = (l+r)/2, B = mid/k;
    fillBlock(B, l, r, dep);
    
    for(int i=B*k-1;i>=l;i--){
        int preB = (i/k) + 1;
        for(auto [j, w] : F[i]){
            if(preB == B) dt[i][dep][j%k] = w;
            else{
                for(int f=0;k>f;f++){
                    dt[i][dep][f] = min(dt[i][dep][f], dt[j][dep][f] + w);
                }
            }
        }
    }

    for(int i=(B+1)*k;r>=i;i++){
        int preB = (i/k) - 1;
        for(auto [j, w] : T[i]){
            if(preB == B) dt[i][dep][j%k] = w;
            else{
                for(int f=0;k>f;f++){
                    dt[i][dep][f] = min(dt[i][dep][f], dt[j][dep][f] + w);
                }
            }            
        }
    }

    for(int i=l;r>=i;i++) fumo[i][dep] = mid;
    for(int i=mid+1;r>=i;i++) mask[i] ^= 1<<dep;

    dnc(l, mid, dep+1);
    dnc(mid+1, r, dep+1);

}

void sol(){
    cin >> k >> n >> m >> q;
    for(int i=0;m>i;i++){
        int u, v, w;
        cin >> u >> v >> w;
        F[u].eb(v, w);
        T[v].eb(u, w);
    }

    for(auto& i : dt)for(auto& j : i) for(auto& f : j) f = INF;
    dnc(0, n-1, 0);

    while(q--){
        int u, v;cin >> u >> v;
        if(u == v) {
            cout << "0\n";
            continue;
        }
        if(u/k >= v/k){
            cout << "-1\n";
            continue;
        }

        int d = __builtin_ctz(mask[u] ^ mask[v]);
        int ans = INF;

        if(u/k == fumo[u][d]/k) ans = dt[u][d][u%k] + dt[v][d][u%k];
        else if(v/k == fumo[v][d]/k) ans = dt[v][d][v%k] + dt[u][d][v%k];
        else{            
            for(int i=0;k>i;i++)
                ans = min(ans, dt[u][d][i] + dt[v][d][i]);
        } 

        cout << (ans<1e9 ? ans : -1) << el;
    }

}

