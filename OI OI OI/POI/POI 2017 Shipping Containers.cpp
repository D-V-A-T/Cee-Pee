ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const int S = 69;

void sol(){
    int n, q;
    cin >> n >> q;
    vect<int> ans(n);

    vect<pii> sus[S][S]; // [divisor][modulo]

    for(int i=0; n>i; i++){
        for(int j=1; S>j; j++){
            sus[j][i % j].eb(i, 0);
        }
    }

    while(q--){
        int p, l, d;
        cin >> p >> l >> d;
        p--;

        if(d < S){
            auto &v = sus[d][p % d];
            int st = lwb(bend(v), make_pair(p, INT_MIN)) - v.begin();
            assert(v[st].fi == p);
            v[st].se++;
            int ed = lwb(bend(v), make_pair(p + (l-1)*d, INT_MIN)) - v.begin() + 1;
            if(ed < v.size()) v[ed].se--;
        }else{
            for(int i=p; l; i+=d, l--){
                ans[i]++;
            }
        }
    }

    for(int i=0; S>i; i++){
        for(int j=0; i>j; j++){
            auto &v = sus[i][j];
            for(int i=1; v.size()>i; i++) v[i].se += v[i-1].se;
            for(pii i : v) ans[i.fi] += i.se;
        }
    }

    for(int i=0; n>i; i++) cout << ans[i] << ' ';
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
