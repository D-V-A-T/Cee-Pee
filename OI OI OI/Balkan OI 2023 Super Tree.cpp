int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}


int S1=0, S2=0, S=0; 
vect<int> G[N];
int fumo[N], A[N];

void DFS(int u, int num){
    fumo[u] = num & A[u];
    S = (S + fumo[u]) % MOD;
    S1 = (S1 + (fumo[u]%MOD)*(fumo[u]%MOD)) % MOD;

    for(int v : G[u]) DFS(v, fumo[u]);
}

bool on(int n, int bit){
    return n&(1ll<<bit);
}

void dfs(int u, int bit, bool state){
    if(state >= on(fumo[u], bit)) return;

    if(!state && on(fumo[u], bit)){
        S -= (1ll<<bit);
        S1 -= (fumo[u]%MOD)*(fumo[u]%MOD);
        fumo[u] -= 1ll<<bit;
        S1 += (fumo[u]%MOD)*(fumo[u]%MOD);
        S %= MOD;
        S1 %= MOD;
    }

    for(int v : G[u]) dfs(v, bit, state);
}
    
