ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n;

void sol(){
    cin >> n;

    int a[n], nxt[n+5], jump[n+5], depth[n+5], jump_cnt[n+5];
    for(int &i : a) cin >> i;


    memset(depth, 0, sizeof depth);
    memset(jump_cnt, 0, sizeof jump_cnt);
    fill(nxt, nxt+n+5, n+1);
    fill(jump, jump+n+5, n+1);


    vect<ll> zip = {0};
    ll sfs = 0;
    for(int i=n-1; i>=0; i--){
        sfs += a[i];
        zip.eb(sfs);
    }
    sort(bend(zip));
    zip.rs(unique(bend(zip)) - zip.begin());

    vect<int> seen(zip.size()+5, n+1);

    auto gz = [&](ll x){
        return lwb(bend(zip), x) - zip.begin();
    };

    seen[gz(0)] = n;
    sfs = 0;
    for(int i=n-1; i>=0; i--){
        sfs += a[i];



        nxt[i] = seen[gz(sfs)];

        nxt[i] = min(nxt[i], nxt[i+1]);

        depth[i] = depth[nxt[i]] + 1;

        int p = nxt[i];
        if(depth[p] - depth[jump[p]] == depth[jump[p]] - depth[jump[jump[p]]]){
            jump[i] = jump[jump[p]];
            jump_cnt[i] = 1 + jump_cnt[p] + jump_cnt[jump[p]];

            // cout << i << ' ' << p << ' ' << jump_cnt[p] << el;

        }else{
            jump[i] = nxt[i];
            jump_cnt[i] = nxt[i] <= n;
        }

       seen[gz(sfs)] = i;
    }

    int q;
    cin >> q;
    while(q--){
        int l, r;
        cin >> l >> r;
        int ans = 0;
        l--, r--;

        int f = l;
        while(f <= r){
            if(jump[f] <= r+1) ans += jump_cnt[f], f = jump[f];
            else ans += (nxt[f] <= r+1), f = nxt[f];
        }

        cout << ans << el;
    }

}

