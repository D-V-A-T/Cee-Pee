ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
	int n;
	string s;
	cin >> n >> s;
	
	pair<int, char> sus = {1e9, ' '};
	
	umap<int, int> cnt;
	for(char c : s) cnt[c] ++;
	sus = min(sus, make_pair(cnt['A'], 'A'));
	sus = min(sus, make_pair(cnt['T'], 'T'));
	sus = min(sus, make_pair(cnt['G'], 'G'));
	sus = min(sus, make_pair(cnt['C'], 'C'));
	
	cout << sus.fi << el;
	while(n--) cout << (char)sus.se;
	
}

