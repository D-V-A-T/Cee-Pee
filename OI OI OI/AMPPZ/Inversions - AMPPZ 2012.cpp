struct DSU{
	int par[N], len[N], mn[N];
	
	DSU(){
		fill(len, len + N, 1);
		iota(par, par + N, 0);
		iota(mn, mn + N, 0);
	}
	
	int findpar(int u){
		return u == par[u] ? u : (par[u] = findpar(par[u]));
	}
	
	void join(int u, int v){
		u = findpar(u), v = findpar(v);
		if(u != v){
			if(len[u] < len[v]) swap(u, v);
			len[u] += len[v];
			par[v] = u;
			mn[u] = min(mn[u], mn[v]);
		}
	}
	
	int getmin(int u){
		return mn[findpar(u)];
	}
} ds;

vect<int> bit[N];
int test[N];

void upd(int x, int val){
	for(;x > 0; x -= x & -x) bit[x].eb(val);
}

void get(int x, int val){
	int res = 0;
	for(;x < N; x += x & -x){
		res += test[x];
		if(bit[x].size()){
			while(bit[x].size() > 1){
				ds.join(val, bit[x].back());
				bit[x].pob();
			}
			ds.join(val, bit[x].back());
		}
	}
}

