const ll N = 3e5 + 1, MOD = 1e9+7, INF = 1e9 + 69;

mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}



void sol(){
	int n;
	cin >> n;
	
	int slash[n+5], kill[n+5], in[n+5];
	vect<int> nig[n+5], ga[n+5], sus(n+5, INF);
	
	prq<pii, vect<pii>, greater<pii>> pq;
	
	for(int i=1; n>=i; i++){
		cin >> slash[i] >> kill[i];
		int len; cin >> len;
		in[i] = len;
		nig[i].rs(len);
		for(int &j : nig[i]) cin >> j, ga[j].eb(i);
		
		sus[i] = kill[i];
		pq.push({kill[i], i});
	}
	
	
	while(pq.size()){
		auto [dmg, u] = pq.top();
		pq.pop();
		
		

		
		if(dmg > sus[u]) continue;
		sus[u] = dmg;
		
		int nathan = slash[u];
		for(int v : nig[u]){
			nathan += sus[v];
		}
		sus[u] = min(sus[u], nathan);
		
		for(int i : ga[u]){
			if(--in[i] == 0){
				int nw = slash[i];
				for(int v : nig[i]) nw += sus[v];
				if(nw < sus[i]) pq.push({nw, i});
			}
		}
	}
	
	cout << sus[1];
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
