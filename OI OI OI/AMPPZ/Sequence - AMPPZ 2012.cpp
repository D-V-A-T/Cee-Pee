ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
	int n, k;
	cin >> n >> k;
	
	int a[n];
	for(int& i : a) cin >> i;
	
	vect<vect<int>> cnt	(k, vect<int>(2)), dp(k, vect<int>(2, 1e9));
	
	
	
	for(int i=0; n>i; i++) {
		cnt[i % k][a[i] % 2] ++;
	}
	
	dp[0][0] = cnt[0][1];
	dp[0][1] = cnt[0][0];
	
	for(int i=1; k>i; i++){
		// all to odd
		dp[i][1] = dp[i-1][0] + cnt[i][0];
		dp[i][0] = dp[i-1][1] + cnt[i][0];
		
		// all to even
		dp[i][1] = min(dp[i][1], dp[i-1][1] + cnt[i][1]);
		dp[i][0] = min(dp[i][0], dp[i-1][0] + cnt[i][1]);
	}
	
	cout << dp[k-1][0];
}

