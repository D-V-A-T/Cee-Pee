ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m, q, d;

struct Seggs{
    int t[4 * N], sussy[4 * N];

    Seggs(){
        memset(t, 0, sizeof t);
        memset(sussy, 0, sizeof sussy);
    }

    void ligma(int l, int r, int id){
        if(l < r) t[id] = max((r - l + 1) * (sussy[id] > 0), t[id*2] + t[id*2+1]);
        else t[id] = (r - l + 1) * (sussy[id] > 0);
    }

    void upd(int i, int j, int x, int l=0, int r=n-1, int id=1){
        if(l > j || r < i) return;
        if(i <= l && r <= j){
            sussy[id] += x;

            ligma(l, r, id);

            return;
        }

        int mid = (l + r) / 2;
        upd(i, j, x, l, mid, id*2);
        upd(i, j, x, mid+1, r, id*2+1);

        ligma(l, r, id);
    }
};

void sol(){
    cin >> n >> m >> q >> d;

    vect<int> a(n), zip = {0}; a[0] = 0;

    for(int i=1; n>i; i++){
        cin >> a[i];
        zip.eb(a[i]);
    }  

    sort(bend(zip));
    zip.rs(unique(bend(zip)) - zip.begin());

    vect<int> b(m);

    Seggs sgt;

    auto update_range = [&](int pos, int x){
        int left = lwb(bend(zip), pos - d) - zip.begin();
        int right = upb(bend(zip), pos + d) - zip.begin() - 1;

        if(left > right) return;

        sgt.upd(left, right, x);
    };


    for(int i=0; m>i; i++){
        cin >> b[i];
        update_range(b[i], 1);
    }

    cout << sgt.t[1] << el;

    while(q--){
        int prev, nxt;
        cin >> prev >> nxt;

        update_range(prev, -1);
        update_range(nxt, 1);

        cout << sgt.t[1] << el;
    }
    
}

