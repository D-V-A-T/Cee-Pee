ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int E(vect<int> v){
	int n = v.size(), res = 0;
	vect<int> left(n), right(n), stk;
	
	for(int i=0; n>i; i++){
		while(stk.size() && v[stk.back()] >= v[i]) stk.pob();
		
		if(stk.size()) left[i] = stk.back() + 1;
		else left[i] = 0;
		
		stk.eb(i);
	}	
	
	stk.clear();
	
	for(int i=n-1; i>=0; i--){
		while(stk.size() && v[stk.back()] >= v[i]) stk.pob();
		
		if(stk.size()) right[i] = stk.back() - 1;
		else right[i] = n-1;
		
		res = max(res, v[i] * (right[i] - left[i] + 1));
		
		stk.eb(i);
	}
	
	return res;
}

void sol(){
	int n, m;
	cin >> n >> m;
	int a[n+2][m+2];
	memset(a, 0, sizeof a);
	
	for(int i=1; n>=i; i++){
		for(int j=1; m>=j; j++){
			cin >> a[i][j];
		}
	}
	
	int sz = max(n, m) + 5;
	int max_right[sz], ans = 0;
	vect<vect<int>> max_down(2, vect<int>(sz));
	
	for(int j=m; j>0; j--){
		
		swap(max_down[0], max_down[1]);
		
		for(int i=n; i>0; i--){
			if(i == n) max_down[0][i] = i;
			else if(i == n-1) max_down[0][i] = i+1;
			else{
				if(a[i][j] - a[i+1][j] == a[i+1][j] - a[i+2][j]) 
					max_down[0][i] = max_down[0][i+1];
				else max_down[0][i] = i+1;
			}
			
			if(j == m) max_right[i] = 1;
			else if(j == m-1) max_right[i] = 2;
			else{
				if(a[i][j] - a[i][j+1] == a[i][j+1] - a[i][j+2]) max_right[i]++;
				else max_right[i] = 2;
			}
			
			ans = max(ans, max_down[0][i] - i + 1);
			ans = max(ans, max_right[i]);
		}
		
		if(j < m){
			for(int i=1; n>i;){
				int next_row = min(max_down[0][i], max_down[1][i]);
				vect<int> sus;
				for(int f=i; next_row >= f; f++) sus.eb(max_right[f]);
				ans = max(ans, E(sus));
				i = next_row;
			}
		}
		
	}
	
	
	cout << ans << el;
}

