int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> G[N], visited(N), dep(N),  ans(N), car(N), sz(N);
int n;
vect<int> cur[N];

void getSZ(int u){
    sz[u] = 1;
    for(int v : G[u]) dep[v] = dep[u]+1, getSZ(v), sz[u] += sz[v];
}

void upd(int u, bool del, vect<int>& kill){
    if(del) cur[dep[u]].clear();
    if(car[u]){
        if(!del){
            if(cur[dep[u]].size()==1) kill.eb(dep[u]);
            cur[dep[u]].eb(u);
        }
    }
    for(int v : G[u]) upd(v, del, kill);

}

void DFS(int u, bool keep){
    int mx=-1, big=-1;
    for(int v : G[u]) if(mx<sz[v]) mx = sz[v], big = v;

    vect<int> kill;
    for(int v : G[u]) if(v != big) DFS(v, 0);

    if(big != -1) DFS(big, 1);

    for(int v : G[u]) if(v != big){
        upd(v, 0, kill);
    }

    if(car[u]){
        if(cur[dep[u]].size()==1) kill.eb(dep[u]);
        cur[dep[u]].eb(u);
    }

    for(int D : kill){
        for(int v : cur[D]) car[v] = 0;
        cur[D].clear();
    }
    

    if(!keep){
        upd(u, 1, kill);
    }
    
}

void sol(){
    cin >> n;
    vect<int> sub;
    for(int i=1;n>i;i++){
        int par; cin >> par;
        G[par].eb(i);        
        if(par == 0) sub.eb(i);
    }

    for(int i=0;n>i;i++)cin >> car[i];

    getSZ(0);
    for(int u : sub) DFS(u, 0);

    cout << car[0]-1 << ' ';


    for(int i=1;n>i;i++){
        if(!car[i]) cout << "-1 ";
        else cout << dep[i] << ' ';
    }

}

