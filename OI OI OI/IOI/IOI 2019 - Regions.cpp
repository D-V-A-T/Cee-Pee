ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const int S = 118, MAX_BLOCK = N / S + 2;
int nig[N], in[N], sz[N], cache[MAX_BLOCK][25001], zip[N], ZZZ=0;
vect<int> G[N], order[N], pos[N];

void dfs(int u){
    sz[u] = 1;
    in[u] = ZZZ++;
    pos[nig[u]].eb(in[u]);
    order[nig[u]].eb(u);

    for(int v : G[u]){
        dfs(v);
        sz[u] += sz[v];
    }


    // cout << u << ' ' << nig[u] << ' ' << sz[u] << el;
}

int cnt[N];

void dfsus(int u){
    for(int i=0; ZZZ>i; i++){
        cache[i][nig[u]] += cnt[i];
    }

    if(pos[nig[u]].size() > S) cnt[zip[nig[u]]]++;

    for(int v : G[u]){
        dfsus(v);
    }

    if(pos[nig[u]].size() > S) cnt[zip[nig[u]]]--;

}

void sol(){
    int n, r, q;
    cin >> n >> r >> q;
    cin >> nig[1];

    for(int i=2; n>=i; i++){
        int par;
        cin >> par >> nig[i];
        G[par].eb(i);
    }

    dfs(1);
    ZZZ = 0;

    for(int i=1; r>=i; i++){
        sort(bend(pos[i]));

        if(pos[i].size() > S) zip[i] = ZZZ++;
    }

    dfsus(1);

    auto cnt_range = [&](int l, int r, vect<int> &v){
        int x1 = lwb(bend(v), l) - v.begin(), x2 = v.size() - (upb(bend(v), r) - v.begin());
        return v.size() - x1 - x2;
    };

    while(q--){
        int r1, r2;
        cin >> r1 >> r2;

        if(pos[r1].size() > S) cout << cache[zip[r1]][r2] << endl;
        else{
            int ans = 0;

            for(int u : order[r1]){
                ans += cnt_range(in[u], in[u] + sz[u] - 1, pos[r2]);

            }   
            cout << ans << endl;
        }
    }

}

