int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[1<<20][2];

void sol(){
    int n, m;
    cin >> n >> m;
    int a[n], b[m];
    for(int& i : a)cin >> i;
    for(int& i : b)cin >> i;
    memset(dp, -5, sizeof dp);
    dp[0][0] = dp[0][1] = 0;
    for(int mask=0;1<<m >mask;mask++){
        for(int i=0;m>i;i++){
            if(!(mask&(1<<i)))continue;
            int pmask = mask ^ (1<<i);
            if(dp[pmask][0] < 0)continue;
            int x = a[dp[pmask][0]];
            if(x == dp[pmask][1] + b[i]){
                dp[mask][0] = max(dp[mask][0], dp[pmask][0]+1);
                dp[mask][1] = 0;
            }else if(x > dp[pmask][1] + b[i]){
                dp[mask][0] = dp[pmask][0];
                dp[mask][1] = dp[pmask][1] + b[i];
            }
        }
        if(dp[mask][0] == n){
            cout << "YES";
            return ;
        }
    }cout << "NO";
}

