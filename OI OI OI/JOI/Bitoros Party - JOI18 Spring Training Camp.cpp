ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const int S = 69;
vect<pii> sus[N];
vect<int> G[N];

void sol(){
    int n, m, q;
    cin >> n >> m >> q;
    vect<int> in(N), blocked(N);
    for(int i=0; m>i; i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
    }

    for(int u=1; n>=u; u++){
        sus[u].eb(0, u);
        sort(bend(sus[u]), [](pii a, pii b){
            return a.se != b.se ? a.se < b.se : a.fi > b.fi;
        });
        sus[u].rs(unique(bend(sus[u]), [](pii a, pii b){
            return a.se == b.se;
        }) - sus[u].begin());

        sort(bend(sus[u]), greater<pii>());
        while(sus[u].size() > S) sus[u].pob();

        for(int v : G[u]){
            for(pii p : sus[u]) sus[v].eb(p.fi+1, p.se);    
        }
    }

    while(q--){
        int u, x;
        cin >> u >> x;
        vect<int> v(x);
        for(int &i : v) cin >> i, blocked[i] = 1;

        int ans = -1;
        if(x < S){
            for(pii i : sus[u]) if(!blocked[i.se]){
                ans = i.fi;
                break;
            }
        }else{
            vect<int> dp(n+5);
            for(int i : v) dp[i] = -1e9;
            for(int i=1; n>=i; i++){
                for(int f : G[i]) dp[f] = max(dp[f], dp[i] + 1);
            }
            if(dp[u] < 0) ans = -1;
            else ans = dp[u];
        }

        for(int i : v) blocked[i] = 0;

        cout << ans << el;
    }

}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
