int32_t solve(int n){
    vect<int> grp[6];

    for(int i=0; n>i; i++) grp[i % 6].eb(i);

    int S1 = 0, S2 = 0, S3 = 0;

    for(int i=0; 6>i; i++){
        vect<int> tmp;
        for(int f : grp[i]) tmp.eb(f);

        S1 += query(tmp);
    }

    for(int i=0; 6>i; i++){
        for(int j=i+1; 6>j; j++){
            vect<int> tmp;
            for(int f : grp[i]) tmp.eb(f);
            for(int f : grp[j]) tmp.eb(f);

            S2 += query(tmp);
        }
    }

    for(int i=0; 6>i; i++){
        for(int j=i+1; 6>j; j++){
            for(int k=j+1; 6>k; k++){
                vect<int> tmp;

                for(int f : grp[i]) tmp.eb(f);
                for(int f : grp[j]) tmp.eb(f);
                for(int f : grp[k]) tmp.eb(f);

                S3 += query(tmp);

            }
        }
    }

    return S3 - 3 * S2 + 6 * S1;
}

