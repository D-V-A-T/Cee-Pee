int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
int __lcm(int a,int b){return a*b/__gcd(a,b);}

int n, q;
vect<int> a;

struct Seggs{
    vect<multiset<int>> t;
    Seggs(){
        t.rs(4*n+1);
        build();
    }void build(int l=0, int r=n-1, int id=1){
        if(l == r){
            t[id].insert(a[l]);
            return;
        }int mid=(l+r)/2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);
        t[id]=t[id*2];
        t[id].insert(bend(t[id*2+1]));
    }void rem(int x, int y, multiset<int>& ms){
        ms.erase(ms.find(x));
        ms.insert(y);
    }void upd(int p, int x, int y, int l=0, int r=n-1, int id=1){
        if(l > p || r < p)return;
        rem(x, y, t[id]);
        if(l == r)return;
        int mid=(l+r)/2;
        upd(p, x, y, l, mid, id*2);
        upd(p, x, y, mid+1, r, id*2+1);
    }int get(int i, int j, int k, int l=0, int r=n-1, int id=1){
        if(l > j || r < i)return INF;
        if(l >= i && r <= j){
            auto it=t[id].lwb(k);
            if(it != t[id].end())return *it;
            else return INF;
        }int mid=(l+r)/2;
        return min(get(i, j, k, l, mid, id*2), get(i, j, k, mid+1, r, id*2+1));
    }
};

void sol(){
    cin >> n >> q;
    a.rs(n);
    for(int& i : a)cin >> i;
    Seggs sgt;
    while(q--){
        int t;cin >> t;
        if(t==1){
            int i, x;cin >> i >> x;
            sgt.upd(i-1, a[i-1], x);
            a[i-1]=x;
        }else{
            int i, j, k;
            cin >> i >> j >> k;
            int fumo=sgt.get(i-1, j-1, k);
            if(fumo < INF)cout << fumo << el;
            else cout << "-1\n";
        }
    }
}

