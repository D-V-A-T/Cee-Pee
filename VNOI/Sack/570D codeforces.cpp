const ll N = 5e5 + 1, MOD = 1e9+7, INF = 1000000000000000069;

mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, q, dep[N], sz[N], in[N], cnt[N][26], parent[N];
string ans[N];
char let[N];
vect<int> G[N], tour;
vect<pii> queries[N];

void prep(int u){
    in[u] = tour.size();
    tour.eb(u);
    sz[u] = 1;

    for(int v : G[u]){
        dep[v] = dep[u] + 1;
        prep(v);
        sz[u] += sz[v];
    }
} 

void add(int u){
    cnt[dep[u]][let[u]]++;
}

void sack(int u, bool keep){
    int fat = 0;
    for(int v : G[u]) if(sz[v] > sz[fat]) fat = v;

    for(int v : G[u]) if(v != fat) sack(v, 0);

    if(fat) sack(fat, 1);

    for(int v : G[u]) if(v != fat){
        for(int i = in[v]; in[v] + sz[v] - 1 >= i; i++) add(tour[i]);
    }
    add(u);

    for(auto [dih, id] : queries[u]){
        int oddc = 0;
        for(char c=0; 26>c; c++) oddc += cnt[dih][c] & 1;
        ans[id] = oddc <= 1 ? "Yes" : "No";
    }

    if(!keep){
        for(int i = in[u]; in[u] + sz[u] - 1 >= i; i++) cnt[dep[tour[i]]][let[tour[i]]] = 0;
    }
}

void sol(){
    cin >> n >> q;
    for(int i=2; n>=i; i++){
        cin >> parent[i];
        G[parent[i]].eb(i);
    }

    for(int i=1; n>=i; i++){
        char c; cin >> c;
        let[i] = c - 'a';
    }

    for(int i=0; q>i; i++){
        int v, h;
        cin >> v >> h;
        queries[v].eb(h, i);
    }

    dep[1] = 1;
    prep(1);
    sack(1, 0);

    for(int i=0; q>i; i++) cout << ans[i] << el;    
}

