void ffopen(const string& file){
    if(file.empty())return;
    freopen((file + ".inp").c_str(), "r", stdin);
    freopen((file + ".out").c_str(), "w", stdout);
}

int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
int __lcm(int a,int b){return a*b/__gcd(a,b);}

struct house{
    int x,y,s;
};

void sol(){
    int n,m,k,r, ans=-0;
    cin >> k >> r >> m;
    pii a[m];
    for(pii& i : a)cin >> i.fi >> i.se;
    cin >> n;
    house b[n];
    vect<vect<int>> reimu(m+1);
    for(int i=0;n>i;i++){
        cin >> b[i].x >> b[i].y >> b[i].s;
        for(int f=0;m>f;f++){
            int d=sq(a[f].fi-b[i].x)+sq(a[f].se-b[i].y);
            if(d <= r*r)reimu[f].eb(i);
        }
    }for(int mask=0;1<<m >mask;mask++){
        if(popcnt(mask) != k)continue;
        vect<bool> seen(200);
        int gay=0;
        for(int i=0;m>i;i++){
            if(mask&(1<<i)){
                for(int f : reimu[i]){
                    if(!seen[f]){
                        seen[f]=1;
                        gay += b[f].s;
                    }
                }
            }
        }ans=max(ans, gay);
    }cout << ans;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
