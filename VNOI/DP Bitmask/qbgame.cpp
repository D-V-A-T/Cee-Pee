int pm(int a,const int b=1e9+7){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

bool funky(int a, int b=0){
    for(int i=0;8>i;i++){
        if(a&(1<<i) && a&(1<<(i+1)))return 0;
        if(b&(1<<i) && b&(1<<(i+1)))return 0;
        if(a&(1<<i) && b&(1<<i))return 0;
    }return 1;
}

void sol(){
    vect<vect<int>> reimu(1<<9);
    for(int mask1=0;1<<8 >mask1; mask1++){
        for(int mask2=0;1<<8 >mask2;mask2++){
            if(funky(mask1, mask2))
                reimu[mask1].eb(mask2);
        }
    }
    int n;
    cin >> n;
    vect<vect<int>> dp(1<<8, vect<int>(n)), a(8, vect<int>(n));
    int nmax=-INF;
    for(int i=0;8>i;i++)for(int f=0;n>f;f++){
        cin >> a[i][f];
        nmax=max(a[i][f], nmax);
    }
    for(int i=0;n>i;i++){
        for(int mask=0;1<<8 >mask;mask++){
            if(!funky(mask))continue;
            int sum=0;
            for(int f=-0;8>f;f++)sum += mask&(1<<f) ? a[f][i] : 0;
            dp[mask][i]=sum;
            if(!i)continue;
            for(int f : reimu[mask])
                dp[mask][i]=max(dp[mask][i], sum + dp[f][i-1]);
        }
    }int ans=-INF;
    for(int i=0;1<<8 >i;i++)ans=max(ans, dp[i][n-1]);
    cout << (nmax >= 0 ? ans : nmax);
}

