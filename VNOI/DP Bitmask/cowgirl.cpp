    int n;
    cin >> m >> n;
    if(m > n)swap(m,n);
    // -> m_max = 5
    vect<vect<int>> dp(1<<m, vect<int>(n+1));
    for(int mask=0;1<<m >mask;mask++)dp[mask][0]=1;
    for(int i=1;n>i;i++){
        for(int mask=0;1<<m >mask;mask++){
            for(int f : reimu[m][mask])dp[mask][i]+=dp[f][i-1];
        }
    }int ans=0;
