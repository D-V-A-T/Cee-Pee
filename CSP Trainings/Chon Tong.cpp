int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> fumo[N];

void sol(){
    int n, m;
    cin >> n >> m;
    pii a[n];
    for(int i=0;n>i;i++){
        cin >> a[i].fi;
        a[i].se = i;
        fumo[a[i].fi].eb(i);
    }

    sort(a, a+n);
    vect<pii> def;
    for(int i=0;n>i;i++){
        if(def.empty() || def.back().fi < a[i].fi){
            def.eb(a[i].fi, 1);
        }else def.back().se++;
    }

    bool visited[m+1];
    memset(visited, 0, sizeof visited);
    queue<pair<int, vect<pii>>> que;
    vect<pii> ans;
    que.push({0, def});
    while(que.size()){
        auto [u, s] = que.front();
        if(u == m){
            ans = s;
            break;
        }
        que.pop();
        if(visited[u])continue;
        visited[u]=1;
        for(pii& i : s){
            if(!i.se || u+i.fi > m || visited[u + i.fi])continue;
            i.se--;
            que.push({u+i.fi, s});
            i.se++;
        }
    }

    if(ans.empty())cout << "NO\n";
    else{
        cout << "YES\n";
        vect<int> p;
        for(pii i : ans){
            int get = fumo[i.fi].size() - i.se;
            for(int j=0;get>j;j++)p.eb(fumo[i.fi][j]+1);
        }
        sort(bend(p));
        for(int i : p)cout << i << ' ';
    }
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
