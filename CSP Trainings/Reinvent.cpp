int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> graph[N];
int d[N][2], n, m ,k;
queue<int> que;

void BFS(){
    umap<int, bool> visited;
    while(que.size()){
        int u = que.front();
        que.pop();
        auto [dist, root] = d[u];
        visited[u] = 1;
        for(int v : graph[u]){
            if(!visited[v]){
                d[v][0] = dist+1;
                d[v][1] = root;
                visited[v] = 1;
                que.push(v);
            }
        }
    }
    
    visited.clear();
    que.push(1);
    int ans = INF;
    while(que.size()){
        int u = que.front();
        que.pop();
        if(visited[u])continue;
        visited[u] = 1;
        for(int v : graph[u]){
            if(!visited[v])que.push(v);
            if(d[u][1] != d[v][1]) ans = min(ans, d[u][0] + d[v][0] + 1);
        }
    }
    
    cout << ans;
}

void sol(){
    cin >> n >> m >> k;
    for(int i=0;m>i;i++){
        int u, v;
        cin >> u >> v;
        graph[u].eb(v);
        graph[v].eb(u);
    }
    memset(d, 5, sizeof d);

    for(int i=0;k>i;i++){
        int u;cin >> u;
        d[u][0] = 0;
        d[u][1] = u;
        que.push(u);
    }
    BFS();
}

