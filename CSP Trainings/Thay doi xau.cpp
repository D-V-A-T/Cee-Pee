int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[679][101][269], add[269][269];

void sol(){
    int n, k;
    string s;
    cin >> s >> k;
    n = s.size();

    int q;cin >> q;
    while(q--){
        char x,y;
        int z;
        cin >> x >> y >> z;
        add[x][y] = z;
    }

    memset(dp, -5, sizeof dp);
    for(int i=0;n>i;i++){
        for(int j=0;k>=j;j++){
            for(char c='a';'z'>=c;c++){
                if(i==0){
                    if(c == s[0]) dp[i][j][c] = 0;
                    else if(j) dp[i][j][c] = 0;
                }else{
                    for(char p='a';'z'>=p;p++){
                        if(s[i] == c){
                            dp[i][j][c] = max(dp[i][j][c], dp[i-1][j][p] + add[p][c]);
                        }else if(j){
                            dp[i][j][c] = max(dp[i][j][c], dp[i-1][j-1][p] + add[p][c]);
                        }
                    }
                }
            }
        }
    }cout << *max_element(dp[n-1][k], dp[n-1][k]+269);
}   

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
