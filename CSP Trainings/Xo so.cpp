ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct custom_hash {
    static uint32_t splitmix64(uint64_t x) {
        // http://xorshift.di.unimi.it/splitmix64.c
        x += 0x9e3779b97f4a7c15;
        x = (x ^ (x >> 30)) * 0xbf58476d1ce4e5b9;
        x = (x ^ (x >> 27)) * 0x94d049bb133111eb;
        return x ^ (x >> 31);
    }

    size_t operator()(uint64_t x) const {
        static const uint64_t FIXED_RANDOM = chrono::steady_clock::now().time_since_epoch().count();
        return splitmix64(x + FIXED_RANDOM);
    }
};


umap<signed, int[101]> dp;

void sol(){
    int n, k;
    cin >> n >> k;

    signed a[n], sp[n];
    int ans=0;
    sp[0]=0;

    auto sum = [](int l, int r){
        return (l+r) * (r-l+1)/2;
    };

    for(int i=0;n>i;i++){
        cin >> a[i];
        if(i && a[i] == a[i-1] + 1) sp[i] = sp[i-1];
        else sp[i] = i;

        int l = a[sp[i]], r = a[i];
        auto &memor = dp[r], &memol = dp[l-1];
        for(int j=0;k>j;j++){
            memor[j+1] = max(memor[j+1], memol[j] + sum(l, r));
            ans = max(ans, memor[j+1]);
        }
    }
    cout << ans;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
