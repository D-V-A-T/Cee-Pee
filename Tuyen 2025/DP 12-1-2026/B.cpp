ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int B(int a, int b = MOD-2){
    int res = 1;
    while(b) {
        if(b&1) res = res * a % MOD;
        a = a * a % MOD;
        b /= 2;
    }
    return res;
}

struct DpDigit{
    int dp[67][2][67], x[67];

    int F(int idx, int smol, int sum){
        if(sum < 0) return 0;
        if(idx == -1) return sum == 0;

        int &memo = dp[idx][smol][sum];

        if(memo != -1) return memo;

        memo = 0;

        int lim = smol ? 1 : x[idx];

        for(int d = 0; lim>=d ; d++){
            memo += F(idx-1, smol || d < x[idx], sum -= d);
        }

        return memo;
    }

    int query(int num){
        memset(dp, -1, sizeof dp); 
        memset(x, 0, sizeof x);

        int i = 0;
        while(num){
            x[i] = num % 2;
            i++;
            num /= 2;
        }

        int ans = 1;

        for(int sum=1;67>sum;sum++){
            ans = (ans * B(sum, F(i-1, 0, sum))) % MOD;

            // cout << sum << ' ' << F(i-1, 0, sum) << ' ' << ans << el;
        }


        return ans;
    }
} dpd;

