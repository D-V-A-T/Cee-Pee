ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct slime{
    int sz, c, id;
    bool operator < (const slime& o) const{
        return sz < o.sz;
    }
};

int LIS(const vect<int>& a){
    vect<int> dp;
    for(int i : a){
        if(dp.empty() || dp.back()<i) dp.eb(i);
        else{
            int p = lwb(bend(dp), i) - dp.begin();
            dp[p] = i;
        }
    }return dp.size();
}

