int query(int x, int y){
    if(x >= y) return x-y;
    else{
        int ans = 0;
        int d = dih(x) + 1;
        int l=0, r=2e9, fumo;
        while(l <= r){
            int mid = (l+r)/2;
            int lst = d + mid - 1;
            if(x + (lst+d) * (lst-d+1) / 2 >= y){
                fumo = mid;
                r = mid-1;
            }else l = mid+1;
        }

        int lst = d + fumo - 1;

        x += (lst+d) * (lst-d+1) / 2;
        return fumo + (x-y);
    }
}

int query2(int x, int y){
    int ty = y;
    if(x >= y) return x-y;
    else{

        int ans = 0;
        int d = dih(y);
        int l=0, r=d, fumo;
        while(l <= r){
            int mid = (l+r)/2;
            int fi = d - mid + 1;
            if(y - (fi+d) * (d-fi+1)/2 < 0){
                l = mid+1;
                continue;
            }
            if(y - (fi+d) * (d-fi+1) / 2 <= x){
                fumo = mid;
                r = mid-1;
            }else l = mid+1;
        }
        int fi = d - fumo + 1;
        y -= (fi+d) * (d-fi+1) / 2;

        if(y>x || y<1 || query(y, ty)!=fumo) return INF;

        return fumo + (x-y);
    }
}

