void sol(){
    ll n, m, T, ans = 0;
    cin >> n >> m >> T;

    if(n==1 || m==1){
        int len = max(m, n), ans = 0;
        if(len == 1){
            cout << T+1;
            return;
        }
        for(int x=0;T>=x;x++){
            ans += (T - x) / (len - 1);
        }
        cout << ans % MOD;
        return;
    }


    for(ll x=0;T>=x;x++){
        for(ll a=1;T/(m-1)+5>=a;a++){
            if(x + a*(m-1) + (n-1) > T) break;
            for(ll b=1;T/(n-1)+5>=b;b++){
                if(x + a*(m-1) + b*(n-1) > T) break;
                ll lst = max(0ll, (ll)(T - (x + a*(m-1) + b*(n-1))) / ((m-1) * (n-1)) + a);
                if(lst < a) continue;

                ans = (ans + lst - a + 1) % MOD;
            }
        }
    }

    cout << ans;
}

