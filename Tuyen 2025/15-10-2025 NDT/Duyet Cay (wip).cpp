ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const int SZ = 1e4+1;
int sz[N], P[N], C, n, k;
vect<int> G[N];

bitset<SZ> dp[10001];

void skibidi(int u, int par){
    P[u] = par;

    for(int v : G[u]) if(!P[v]){
        skibidi(v, u);
        sz[u] += sz[v];
    }
    sz[u]++;
}

void DFS(int u, int par){

    vect<pii> a;

    for(int v : G[u]) if(v != par) {
        a.eb(v, sz[v]);
    }

    int m = a.size();
    if(!m) return;
    bitset<SZ> pre[m], suf[m], blank;

    pre[0][0] = suf[m-1][0] = pre[0][a[0].se] = suf[m-1][a[m-1].se] = blank[0] = 1;

    for(int i=1;m>i;i++) pre[i] = pre[i-1] | (pre[i-1] << a[i].se);
    for(int i=m-2;i>=0;i--) suf[i] = suf[i+1] | (suf[i+1] << a[i].se);

    for(int i=0;m>i;i++){
        const bitset<SZ> &p = (i?pre[i-1]:blank), &s = (i+1<m?suf[i+1]:blank);
        bitset<SZ> fumo;

        if(s.count() > p.count())
            for(int j=p._Find_first();sz[u]>j;j=p._Find_next(j))
                fumo |= (s << j);
        else for(int j=s._Find_first();sz[u]>j;j=s._Find_next(j))
                fumo |= (p << j);

        bool sw = 0;
        if(dp[u].count() < fumo.count()) swap(dp[u], fumo), sw = 1;

        for(int j=fumo._Find_first();sz[u]>j;j=fumo._Find_next(j))
            dp[a[i].fi] |= (dp[u] << j);

        if(sw) swap(dp[u], fumo);        
        
        dp[a[i].fi] = dp[a[i].fi] << 1;

    }

    for(int v : G[u]) if(v != par) DFS(v, u);
}

void sol(){
    cin >> C >> n >> k;

    for(int i=1;n>i;i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        G[v].eb(u);
    }

    skibidi(1, 1);

    if(C==1){
        dp[1][1] = 1;
        DFS(1, 1);  
        for(int i=1;n>=i;i++) if(dp[i][k]) cout << i << ' ';
    }

}

