int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, q;
    cin >> n >> q;
    pii que[q];
    int idx = 0;
    vect<int> rev, sor, ans(n+5);

    for(pii& i : que){
        cin >> i.fi >> i.se;
        if(i.fi == 0) rev.eb(idx);
        else sor.eb(idx);
        idx++;
    }
    
    vect<pii> pr, ps;


    // tao suffix max
    reverse(bend(rev));
    for(int i : rev){
        pii pp = {que[i].se, i};
        if(pr.empty()) pr.eb(pp);
        else pr.pb(max(pr.back(), pp));
    }
    reverse(bend(pr));
    reverse(bend(rev));

    // tao suffix min
    reverse(bend(sor));
    for(int i : sor){
        pii pp = {que[i].se, i};
        if(ps.empty()) ps.eb(pp);
        else ps.eb(min(ps.back(), pp));
    }
    reverse(bend(ps));
    reverse(bend(sor));

    int l=1, r=n;
    for(int i=n;i>0;i--){
        int lo = 0, hi = pr.size()-1, t0=-1, t1=-1;
        while(lo <= hi){
            int mid = (lo+hi)/2;
            if(pr[mid].fi >= r){
                lo = mid+1;
                t0 = pr[mid].se;
            }else hi = mid-1;
        }


        lo = 0, hi = ps.size()-1;
        while(lo <= hi){
            int mid = (lo+hi)/2;
            if(ps[mid].fi <= l){
                lo = mid+1;
                t1 = ps[mid].se;
            }else hi = mid-1;
        }

        if(t0 == -1 && t1 == -1) ans[i] = i, r--;
        else if(t0 == -1) ans[r] = i, r--;
        else if(t1 == -1) ans[l] = i, l++;
        else{
            if(t0 < t1){
                ans[r] = i;
                r--;
            }else{
                ans[l] = i;
                l++;
            }
        }
    }

    for(int i=1;n>=i;i++) cout << ans[i] << ' ';
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
