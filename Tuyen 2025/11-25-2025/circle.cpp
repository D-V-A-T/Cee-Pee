int pm(int a,const int b=MOD){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, k;
    cin >> n >> k;
    int a[n], b[k];
    vect<int> gay(n), pfs(n), A(k*2), B(k*2);
    for(int i=0;n>i;i++){
        cin >> a[i];
        pfs[i] = a[i];
    }
    for(int i=0;k>i;i++){
        cin >> b[i];
        b[i]--;
        gay[b[i]] = a[b[i]];
        pfs[b[i]] = 0;
    }

    sort(b, b+k);
    partial_sum(bend(pfs), pfs.begin());
    partial_sum(bend(gay), gay.begin());


    if(k == 1){
        cout << 0;
        return;
    }

    auto gsum = [&](int l, int r){
        if(l > r) return pfs.back() - (l ? pfs[l-1] : 0) + pfs[r];
        return pfs[r] - (l ? pfs[l-1] : 0);
    };

    auto gaysum = [&](int l, int r){
        if(l > r) return gay.back() - (l ? gay[l-1] : 0) + gay[r];
        return gay[r] - (l ? gay[l-1] : 0);
    };

    // auto 

    int left[k], right[k];
    // left[i] : sum of unimportant balls from i to the nearest important ball
    //           to the left of i
    // right[i] : same thing but right

    int ans = LLONG_MAX, S = 0;

    for(int i=0;2*k>i;i++){
        int prev = b[pm(i-1, k)];

        if(i)
            A[i] = A[i-1] + gsum(prev, b[i%k]), B[i] = B[i-1] + a[b[i%k]];

    }

    deque<pii> dq;

    for(int i=0;k-1>i;i++) {  // init 
        int x = A[i] - B[i];
        while(dq.size() && dq.back().se > x) dq.pob();
        dq.eb(i, x);
    }

    for(int i=0;k>i;i++){
        int j = i+k-1;
        int x = A[j] - B[j];
        while(dq.size() && dq.front().fi < i) dq.pof();
        while(dq.size() && dq.back().se > x) dq.pob();
        dq.eb(j, x);

        ans = min(ans, B[j] - A[i] + dq.front().se);
    }

    cout << ans;


}

