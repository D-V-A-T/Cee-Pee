ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const ll INF=902337203695775807, N=2e6+69, MOD=1e9+7;   
int B(int a, int b=MOD-2){
    int res = 1;
    a %= MOD;
    while(b){
        if(b&1) res = (res * a) % MOD;
        a = (a*a) % MOD;
        b /= 2;
    }return res;
}

int F[N];

int C(int n, int k){
    if(n < k) return 0;
    return F[n] * B(F[k] * F[n-k]) % MOD;
}

void sol(){
    int fumo = 0;
    int t, r;
    cin >> t >> r;
    int n;
    cin >> n;
    int a[n],  total = 1, fumi = t, S = 0, W = 1;
    for(int i=0;n>i;i++){
        cin >> a[i];
        W = (W * F[a[i]]) % MOD;
        S += a[i];
    }

    int idx = 0;

    auto E = [&](int s){
        int res = C(t-s, S-s) * F[S-s] % MOD;
        res = (res * B(W * B(F[s])) % MOD);
        return res % MOD;
    };

    for(int& i : a){
        fumo = (fumo + (max(0ll, (t/r - i + 1)) * r % MOD) * E(i) % MOD) % MOD;
        total = (total * C(fumi, i)) % MOD;
        fumi -= i;
        idx++;
    }

    cout << fumo * B(total) % MOD << el;
}

