ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int visited[1505][1505];
const int dx[] = {1, -1, 0, 0}, dy[] = {0, 0, 1, -1}, t[] = {'D', 'U', 'R', 'L'};

void sol(){
    int n;
    cin >> n;
    char a[(n*n-1)/2];
    for(char& c : a) cin >> c;
    vect<char> ans;
    int i=1, j=1;
    for(int i=0;n+1>=i;i++) for(int j=0;n+1>=j;j++) if(i==0 || j==0 || i==n+1 || j==n+1) visited[i][j] = 1;
    visited[1][1] = 1;
    
    auto dead = [&](int i, int j){
        return visited[i+1][j] && visited[i-1][j] && visited[i][j+1] && visited[i][j-1];
    };

    if(a[0] == 'U' || a[0] == 'L'){
        cout << "YES\n" << 0 << el;
        return;
    }else if(a[0] == 'R'){
        visited[1][2] = 1;
        j = 2;
    }else if(a[0] == 'D'){
        visited[2][1] = 1;
        i=2;
    }
    
    for(int mv = 1;(n*n-1)/2>=mv;mv++){
        char c = a[mv-1];
        umap<char, int> ok;
        for(int f=0;4>f;f++){
            int ni = i + dx[f], nj = j + dy[f];
            if(visited[ni][nj]) continue;
            ok[t[f]] = 1;
            if(dead(ni, nj)){
                ans.eb(t[f]);
                break;
            }
        }
        if(ok[c]){ // bob
            ans.eb(c);
            if(c == 'U') i --;
            else if(c == 'D') i++;
            else if(c == 'R') j++;
            else j--;
            visited[i][j] = 1;
        }else{
            for(auto& p : ok){
                c = p.fi;

                if(c == 'U') i --;
                else if(c == 'D') i++;
                else if(c == 'R') j++;
                else j--;
                visited[i][j] = 1;

                break;
            }
        }

        if(mv == (n*n-1)/2) break;
        c = a[mv]; // alice
        if(c == 'U') i --;
        else if(c == 'D') i++;
        else if(c == 'R') j++;
        else j--;
        if(visited[i][j]) break;
        visited[i][j] = 1;
    }
    cout << "YES\n" << ans.size() << el;
    for(char c : ans) cout << c << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
