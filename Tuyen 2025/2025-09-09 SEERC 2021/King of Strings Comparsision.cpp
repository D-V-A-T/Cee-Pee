ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const int base = 31;
int S[N], T[N], P[N];

int getH(int l, int r, int b){
    if(b) return T[r] - T[l-1] * P[r-l+1];
    return S[r] - S[l-1] * P[r-l+1];
}

void sol(){
    int n;
    cin >> n;
    string s, t;
    cin >> s >> t;
    s = " " + s;
    t = " " + t;

    P[0] = 1;
    for(int i=1;n>=i;i++){
        P[i] = P[i-1] * base;
        S[i] = S[i-1] * base + s[i] - 'a' + 1;
        T[i] = T[i-1] * base + t[i] - 'a' + 1;
    }

    int ans = 0;
    for(int i=1;n>=i;i++){
        if(s[i] < t[i]){
            int l=0, r=i-1, fumo = 0;
            while(l <= r){
                int mid = (l+r)/2;
                if(getH(i-mid, i-1, 0) == getH(i-mid, i-1, 1)){
                    l = mid+1;
                    fumo = mid;
                }else r = mid-1;
            }
            ans += (fumo+1) * (n-i+1);
        }
    }
    cout << ans ;
}

