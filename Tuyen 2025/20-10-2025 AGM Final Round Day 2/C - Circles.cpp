int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int cnt[6059][6069];

#define C(i, j) cnt[i+3000][j+3000]

void sol(){
    int n;
    cin >> n;

    auto sd = [&](pii a, pii b){
        return sq(a.fi - b.fi) + sq(a.se - b.se);
    };

    const int SCALE = 100;

    for(int i=0;n>i;i++){
        int X, Y, R;cin >> X >> Y >> R;
        int nx = X*SCALE, ny = Y*SCALE, nr = R*SCALE;

        for(int x=nx-nr;nx+nr>=x;x++){
            for(int y=ny-nr;ny+nr>=y;y++){
                if(sd({x,y}, {nx,ny}) <= nr*nr){
                    C(x,y)++;
                }
            }
        }
    }

    
    int pts = 0;

    for(int i=0;6059>i;i++) for(int j=0;6059>j;j++){
        pts += (cnt[i][j] == n);
    }

    cout << fixed << setprecision(15) << (dbl)pts / (SCALE * SCALE);
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
