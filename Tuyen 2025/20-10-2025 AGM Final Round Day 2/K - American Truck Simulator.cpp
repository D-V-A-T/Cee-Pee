int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct game{
    int x, id;
};


void sol(){
    int n, m, w;
    cin >> n >> m >> w;

    vect<game> a(n);
    for(int i=0;n>i;i++){
        cin >> a[i].x;
        a[i].id = i;
    }

    int idx = 0;
    vect<int> bund(n, -1), lst(n);
    vect<dbl> disc(m);
    
    for(int i=0;m>i;i++){
        int len, p;cin >> len >> p;
        vect<int> v(len);
        for(int &j : v) cin >> j, j--;

        sort(bend(v));
        lst[v.back()] = len;
        for(int j : v) bund[j] = idx;

        disc[i] = p/100.0l;
        idx++;
    }


    sort(bend(a), [&](const game& x, const game& y){
        return (bund[x.id] != bund[y.id] ? bund[x.id] < bund[y.id] : x.id<y.id);
    });
    // sau khi sort, cac game cung mot bundle thuoc doan con lien tiep


    int pfs[n];
    for(int i=0;n>i;i++) pfs[i] = (i ? pfs[i-1] : 0) + a[i].x;

    auto get = [&](int l, int r){
        return (dbl)pfs[r] - (l ? pfs[l-1] : 0);
    };

    dbl dp[n+5][n+5];
    for(auto& i : dp) for(auto& j : i) j = 1e10;
    // xet i game dau, gia it nhat de mua j game

    dp[0][0] = 0;
    dp[0][1] = a[0].x;

    if(lst[a[0].id] == 1) dp[0][1] = a[0].x * (1 - disc[bund[a[0].id]]);


    for(int i=1;n>i;i++){
        for(int j=0;n>=j;j++){
            dp[i][j] = dp[i-1][j];

            if(!j) continue;

            dp[i][j] = min(dp[i][j], dp[i-1][j-1] + a[i].x);
            
            int len = lst[a[i].id];
            if(len){
                int li = i - len + 1;
                if(len <= j){
                    dbl cost = (dbl)get(li, i) * (1 - disc[bund[a[i].id]]) ;
                    if(!li)
                        dp[i][len] = min(dp[i][len], cost);
                    else
                        dp[i][j] = min(dp[i][j], dp[li-1][j - len] + cost);
                    
                }
            }
        }
    }

    for(int i=n;i>=0;i--) if(dp[n-1][i] <= w){
        cout << i;
        return;
    } 
    
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
