ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    if(n <= 2){
        cout << 1 << el;
    }
    else if(n==3) cout << 2 << el;
    else{
        int l = 0, r = 1e9, ans;
        while(l <= r){
            int mid = (l+r)/2;
            if(mid*2 + mid-1 <= n-4) ans = mid, l = mid+1;
            else r = mid-1;
        }
        cout << ans + 2 << el;
    }
}

