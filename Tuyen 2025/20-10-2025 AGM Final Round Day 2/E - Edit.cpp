ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol() {
    string a;
    int q, d;
    cin >> a >> q >> d;
    int n = a.size();
    a = " " + a;

    while (q--) {
        string b;
        cin >> b;
        int m = b.size();
        b = " " + b;

        const int INF = 1e9;
        int dp[2][n + 5];
        memset(dp, 5, sizeof dp);

        int c = 0, p = 1;
        dp[c][0] = 0;
        dp[c][1] = 1;

        set<int> ok[2];
        ok[c].insert(0);
        ok[c].insert(1);

        for (int i = 0; i < m; i++) {
            
            vect<int> reset;
            

            for (int j : ok[c]) {
                if (j > n) break;
                if (dp[c][j] > d) continue;

                if (a[j + 1] == b[i + 1]) {
                    if (dp[p][j + 1] > dp[c][j]) {
                        dp[p][j + 1] = dp[c][j];
                        ok[p].insert(j + 1);
                    }
                } else if (dp[c][j] + 1 <= d) {
                    dp[p][j + 1] = min(dp[p][j + 1], dp[c][j] + 1);
                    ok[p].insert(j + 1);

                    dp[p][j] = min(dp[p][j], dp[c][j] + 1);
                    ok[p].insert(j);

                    dp[c][j + 1] = min(dp[c][j + 1], dp[c][j] + 1);
                    ok[c].insert(j + 1);
                    reset.eb(j+1);
                }
            }
            
            for(int j : ok[c]) dp[c][j] = dp[c][j+1] = INF;
            
            ok[c].clear();
            swap(c, p);
        }

        cout << (dp[c][n] <= d ? "CLOSE" : "FAR") << el;
    }
}


signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
