int pm(int a,const int b=MOD){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int bigtest;

ll dp[2000069];

void sol(int testnum){
    int n, m, k;
    cin >> n >> m >> k;

    ll a[n], cost[m+5];
    for(ll& i : a) cin >> i;

    fill(dp, dp+2*m+5, INF);
    dp[1] = 0;

    prq<pii, vect<pii>, greater<pii>> pq;
    
    for(int i=1;m>=i;i++){
        cin >> cost[i];
        dp[i] = min(dp[i], cost[i]);
    }

    for(int i=m;i>=1;i--) dp[i] = min(dp[i+1], dp[i]), pq.push({dp[i], i});

    m*=2;

    while(pq.size()){
        int u, d;
        tie(d, u) = pq.top();
        pq.pop();

        if(dp[u] < d) continue;

        for(int i=2;m/2>=i;i++){
            if(i * u > m) break;

            if(dp[i*u] > dp[u] + dp[i]){
                dp[i*u] = dp[u] + dp[i];
                pq.push({dp[i*u], i*u});
            }
        }
    }

    for(int i=m;i>=1;i--) dp[i] = min(dp[i+1], dp[i]);

    auto ok = [&](int x){
        vect<ll> fumo; fumo.reserve(n);

        if(x == 0){
            for(int i : a) fumo.eb(dp[i+1]);
        }else{
            for(int i : a){
                // i / p <= x
                // i / x <= p
                int need = i/(x+1) + 1;
                fumo.eb(dp[need]);
            }
        }

        sort(bend(fumo));
        ll S = 0;
        for(int i=0;n/2>=i;i++) S += fumo[i];
        return S <= k;
    };

    int l = 0, r = m, ans = m;
    while(l <= r){
        int mid = (l+r)/2;

        if(ok(mid)){
            ans = mid;
            r = mid-1;
        }else l = mid+1;
    }
    cout << ans << el;
}

