ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

bool checkRBS(const string& s){
    int op = 0;
    for(char c : s){
        if(c == ')'){
            if(op <= 0) return false;
            op--;
        }else op++;
    }
    return op == 0;
}

void sol(){
    string s;int k;
    cin >> s >> k;
    int n = s.size();
    deque<char> dq(bend(s));
    stack<int> st;
    int idx = 0;
    vect<int> pai(s.size()+5);
    for(char c : s){
        if(c == ')'){
            if(st.empty()){
                cout << "First\n";
                return;
            }
            pai[idx] = st.top();
            pai[st.top()] = idx;
            st.pop();
        }else st.push(idx);

        idx++;
    }

    if(st.size()){
        cout << "First\n";
        return;
    }

    int l = 0, r = n-1;
    while(dq.size() > k){

        // cout << l << ' ' << r << ' ' << dq.size()
        // << ' ' << pai[l] << el;
        if(pai[l] == r){
            dq.pof();
            if(dq.size() <= k) break;
            dq.pob();
            if(dq.size() <= k) break;
            l++;r--;
        }else{
            int left = 0, right = 0;
            while(l+1<n && pai[l] == l+1){
                left++;
                l += 2;
            }while(r>0 && pai[r] == r-1){
                right++;
                r -= 2;
            }
            // cout << left << ' ' << right << el;
            if((dq.size() - min(left, right)*2 > k) || k&1){
                cout << "First\n";
            }else cout << "Second\n";
            return;
        }
    }

    string S(bend(dq));
    // cout << S << ' ' << checkRBS(S) << el;
    cout << (checkRBS(S) ? "Second" : "First") << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    cin >> t;
    while(t--)sol();
}
