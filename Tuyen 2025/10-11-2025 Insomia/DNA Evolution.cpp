ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n;
umap<char, int> bd;

struct Fick{
    vect<int> bit;
    int sz;
    Fick(){
        bit.rs(n+5);
        sz = bit.size();
    }
    int get(int x){
        int res = 0;
        for(;x>0;x-=x&-x) res += bit[x];
        return res;
    }
    void upd(int x, int v){
        for(;x<sz;x+=x&-x) bit[x] += v;
    }
};

