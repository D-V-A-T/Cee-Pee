int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    string s;
    cin >> s;
    umap<char, int> cnt;
    for(char c : s) cnt[c]++;

    vect<pii> v;
    vect<pii> ans;
    prq<pii, vect<pii>, greater<pii>> pq[200];
    prq<pii> pq2;

    for(auto& i : cnt){
        v.eb(i.se, i.fi);
        pq2.push({i.se, i.fi});
    }
    sort(bend(v), greater<pii>());


    while(pq2.size()>1){
        while(pq2.size() && pq2.top().fi <= 0) pq2.pop();
        if(pq2.size() <= 1) break;

        pii tp = pq2.top();
        pq2.pop();
        pii stp = pq2.top();
        pq2.pop();
        
        pq[tp.se].push({1, ans.size()});
        ans.eb(tp.se, 1);
        pq[stp.se].push({1, ans.size()});
        ans.eb(stp.se, 1);

        tp.fi--;
        stp.fi--;
        if(tp.fi>0)pq2.push(tp);
        if(stp.fi>0)pq2.push(stp);
    }

    if(pq2.size()){
        pii pa = pq2.top();
        pq[pa.se].push({1, ans.size()});
        ans.eb(pa.se, 1);
        pa.fi--;
        pq2.pop();
        char c = 'a';
        if(pa.fi >= 0) c = pa.se, pq2.push(pa);
        int times = pa.fi;

        while(times >= 2){
            pii pa = pq[c].top();
            pq[c].pop();
            ans[pa.se].se+=2;
            pa.fi+=2;
            pq[c].push(pa);
            times -= 2;
        }
        ans.back().se+=times;
    }

    
    string ANS;
    for(auto i : ans){
        while(i.se--){
            ANS += i.fi;
        }
    }
    cout << ANS;
    nextl;


}
    
signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    cin >> t;
    while(t--)sol();
}
