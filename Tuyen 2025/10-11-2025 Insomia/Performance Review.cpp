ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m, q, a[N], pfs[N], b[N];

struct Seggs{
    int t[2*N], lz[2*N];
    Seggs(){
        memset(lz, 0, sizeof lz);
        build();
    }
    void build(int l=0, int r=m-1, int id=1){
        if(l == r){
            t[id] = b[l];
            return;
        }
        int mid = (l+r)/2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);
        t[id] = min(t[id*2], t[id*2+1]);
    }

    void push(int l, int r, int id){
        t[id] += lz[id];
        if(l < r){
            lz[id*2] += lz[id];
            lz[id*2+1] += lz[id];
        }
        lz[id] = 0;
    }

    void upd(int i, int j, int x, int l=0, int r=m-1, int id=1){
        if(i > j) return;
        push(l, r, id);
        if(l > j || r < i) return;
        if(i <= l && r <= j) {
            lz[id] += x;
            push(l, r, id);
            return;
        }
        int mid = (l+r)/2;
        upd(i, j, x, l, mid, id*2);
        upd(i, j, x, mid+1, r, id*2+1);
        t[id] = min(t[id*2], t[id*2+1]);
    }

    int get(int i, int j, int l=0, int r=m-1, int id=1){
        push(l, r, id);
        if(l > j || r < i) return 2e9;
        int mid = (l+r)/2;
        return min(get(i, j, l, mid, id*2), get(i, j, mid+1, r, id*2+1));
    }
};

void sol(){
    cin >> n >> m >> q; 
    for(int i=0;n>i;i++) cin >> a[i];
    int Randall = a[0];
    int cntsmol = 0;
    for(int i=0;n>i;i++) cntsmol += a[i] < Randall;


    vect<int> v[m];
    int pfs = 0;
    for(int i=0;m>i;i++){
        int len; cin >> len;
        v[i].rs(len);
        pfs += len;
        b[i] = cntsmol - pfs; 
        for(int& j : v[i]) cin >> j, cntsmol += j < Randall;
    }


    Seggs sgt;

    while(q--){
        int i, j, k;
        cin >> i >> j >> k;
        i--;j--;
        if(v[i][j] < Randall)
            sgt.upd(i+1, m-1, -1);
        
        if(k < Randall) 
            sgt.upd(i+1, m-1, 1);

        v[i][j] = k;

        cout << (sgt.t[1] < 0 ? 0 : 1)  << el;

    }
}

