ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const dbl PI = acosl(-1);

dbl len_x(pii x, pii y){
    dbl a = (dbl)(y.se - x.se) / (y.fi - x.fi);
    dbl b = x.se - a * x.fi;
    dbl theta = atanl(fabsl(a));

    // sin = doi / huyen <=> huyen = doi / sin

    dbl L = 1.0l / sin(theta);
    return L;
}

dbl len_y(pii x, pii y){
    dbl a = (dbl)(y.se - x.se) / (y.fi - x.fi);
    dbl b = x.se - a * x.fi;
    dbl theta = atanl(fabsl(1/a));

    // sin = doi / huyen <=> huyen = doi / sin

    dbl L = 1.0l / sin(theta);
    return L;
}

#define pdd pair<dbl,dbl>

dbl dist(pdd x, pdd y){
    return sqrt(sq(x.fi - y.fi) + sq(x.se - y.se));
}

struct Fick{
    int bit[N];
    Fick(){memset(bit, 0, sizeof bit);}
    void upd(int x, int v){
        for(;x<N;x|=x+1) bit[x] += v;
    }
    int get(int x){
        int res = 0;
        for(;x>=0;x=(x&(x+1))-1) res += bit[x];
        return res;
    }
}X, Y;

int ngang[N], doc[N];

void sol(){
    int q;cin >> q;
    while(q--){
        int t; cin >> t;
        if(t == 1){
            int x, v; cin >> x >> v;
            X.upd(x+1, v);
            ngang[x] += v;
        }
        else if(t == 2){
            int y, v; cin >> y >> v;
            Y.upd(y+1, v);
            doc[y] += v;
        }else{
            pii A, B;
            cin >> A.fi >> A.se >> B.fi >> B.se;

            int mxy = max(A.se, B.se), mny = min(A.se, B.se);
            int xxx = Y.get(mxy) - Y.get(mny);

            int mxx = max(A.fi, B.fi), mnx = min(A.fi, B.fi);
            int yyy = X.get(mxx) - X.get(mnx);

            cout << fixed << setprecision(15);

            if(A.fi == B.fi && A.se == B.se) cout << ngang[A.fi] + doc[A.se] << el;
            else if(A.fi == B.fi){
                yyy = ngang[A.fi];
                cout << yyy + xxx * (len_x(A, B) / dist(A, B)) << el;
            }else if(A.se == B.se){
                xxx = doc[A.se];
                cout << yyy * (len_y(A, B) / dist(A, B)) + xxx << el;
            }
            else{
                cout << yyy * (len_y(A, B) / dist(A, B)) + xxx * (len_x(A, B) / dist(A, B)) << el;
            }
        }
    }

}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
