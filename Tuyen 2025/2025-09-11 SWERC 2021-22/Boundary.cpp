ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int l, w;
    cin >> l >> w;
    vect<int> ans;

    auto check = [](const vect<int>& v, int d){
        for(int i : v) if(i%d) return false;
        return true;
    };

    auto F = [&](const vect<int>& v){
        int w = *min_element(bend(v));
        for(int i=1;i*i<=w;i++){
            int x=i, y=w/i;
            if(check(v, x)) ans.eb(x);
            if(check(v, y)) ans.eb(y);
        }
    };

    F({l, w-2});
    F({l-2, w});
    F({l-1, w-1});
    F({l-2, w-1, l});
    F({w-2, l-1, w});

    sort(bend(ans));
    ans.rs(unique(bend(ans)) - ans.begin());
    cout << ans.size() << ' ';
    for(int i : ans) cout << i << ' ';
    nextl;
}

