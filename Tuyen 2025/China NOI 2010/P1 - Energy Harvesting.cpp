int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int spf[N], cnt[N];

int dih(int a, int b){
    // a <= [b/x] <-> x < (b+1)/a ;
    int x = (b+1)%a==0 ? (b+1)/a - 1 : (b+1)/a;
    return x;
}

void sol(){
    int n, m;
    cin >> n >> m;

    int y=1,x=1,ans=0;

    auto upd = [&](int a){
        vect<int> v;
        while(a>1){
            int d=spf[a];
            while(a % d == 0) a /= d;
            v.eb(d);
        }
        int s = v.size();
        for(int mask=1;1<<s >mask;mask++){
            int pr = 1;
            for(int i=0;s>i;i++) if(mask&(1<<i)){
                pr *= v[i];
            }cnt[pr]++;
        }
    };

    auto CNT = [&](int a){
        int res = 0, tmp =a;
        vect<int> v;
        while(a>1){
            int d=spf[a];
            while(a % d == 0) a /= d;
            v.eb(d);
        }
        int s = v.size();
        for(int mask=1;1<<s >mask;mask++){
            int pr = 1;
            for(int i=0;s>i;i++) if(mask&(1<<i)){
                pr *= v[i];
            }
            // if(tmp==2) cout << pr << ' ' << el;
            res += popcnt(mask)&1 ? cnt[pr] : -cnt[pr];
        }
        return res;
    };

    for(x=1;n>=x;x++){
        // dem so y sao cho gcd(x, y) = 1 va [n/x] <= [m/y]
        int bound = dih(n/x, m);

        for(;y<=bound;y++){
            upd(y);
        }
        int fumo = n/x;
        // cout << y << ' ' << bound << ' ' << CNT(x) << ' ' << (bound-CNT(x)) * (fumo-1) * (fumo) << el;    
        if(bound) 
            ans += (bound-CNT(x)) * (fumo) * (fumo-1) ;
    }   

    // cout << el << ans << el;

    // deb;
    memset(cnt, 0, sizeof cnt);

    x=1;
    for(y=1;m>=y;y++){
        // dem so y sao cho gcd(x, y) = 1 va [m/y] < [n/x]
        if(m/y == 1) break;
        int bound = dih(m/y + 1, n);


        for(;x<=bound;x++)  upd(x);
    
        int fumo = m/y;
        // cout << y << ' ' << bound << ' ' << CNT(y) << ' ' << fumo << ' ' << bound-CNT(y) << ' ' << (bound-CNT(y)) * (fumo-1) * (fumo) << el;
        if(bound)
            ans += (bound-CNT(y)) * (fumo-1) * (fumo) ;
    }   

    cout << ans + n*m;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();

    iota(spf, spf+N, 0);
    for(int i=2;N>i*i;i++){
        if(spf[i] != i)continue;
        for(int j=i*i;N>j;j+=i){
            spf[j] = min(spf[j], i);
        }
    }

    int t=1;
    //cin >> t;
    while(t--)sol();
}
