ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int F[N];

int B(int a, int b=MOD-2){
    a %= MOD;
    int res = 1;
    while(b){
        if(b&1) res = (res*a)%MOD;
        a = (a*a)%MOD;
        b >>= 1;
    }return res;
}

int C(int n, int k){
    if(n < k) return 0;
    return F[n] * B(F[k] * F[n-k]) % MOD;
}

