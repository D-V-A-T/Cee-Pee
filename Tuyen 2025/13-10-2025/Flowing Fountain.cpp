class DSU{
public:
    vector<int> par, len, mx;
    DSU(const int& n){
        par.rs(n+69);
        mx.rs(n+69);
        len.rs(n+69, 1);
        iota(bend(par), 0);
        iota(bend(mx), 0);
    }int findpar(int u){
        if(u == par[u])
            return u;
        return u=findpar(par[u]);
    }void join(int u, int v){
        if(u == -1 || v==-1) return;
        u=findpar(u);
        v=findpar(v);
        if(u != v){
            if(len[u] < len[v])swap(u, v);
            par[v]=u;
            mx[u] = max(mx[u], mx[v]);
            len[u]+=len[v];
        }
    }
};

