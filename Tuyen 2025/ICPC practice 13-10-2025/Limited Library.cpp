ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, m, x, y;
    cin >> n >> m >> x >> y;
    vect<int> a(n), b(m), cap(n);
    for(int& i : a) cin >> i;
    for(int& i : b) cin >> i;

    sort(bend(a));
    sort(bend(b));

    // if(b[m-1] > a[n-1]){
    //     cout <<
    // }

    int p = n-1, q = n-1;
    for(int i=m-1;i>=0;i--){
        if(p>=0 && a[p]>=b[i]){
            if(cap[p] < y){
                // cout<< 1 << ' ' << i << ' ' << p << el;
                cap[p]++;
            }
        }
        else{
            
            if(cap[q] >= x) q--;
            if(q>=0 && a[q] < b[i]){
                // cout << a[q] << ' ' << b[i] << el;
                cout << "impossible";
                return;

            }
            if(q<0){
                cout << "impossible";
                return;
            }
            // cout << "3 " << i << ' ' << q << el;
            cap[q]++;
        }

        if(p>=0 && cap[p] >= y) p--;

        if(p<0 && q<0){
            cout << "impossible";
            return;
        }
    }
    int ans = 0;
    for(int i : cap) if(i <= y) ans++;//, cout << i << el;
    cout << ans << el;
}

