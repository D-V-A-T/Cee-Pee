int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<pii> G[N];
int D[2][N];

void dickcha(int t, int root){
    D[t][root] = 0;
    prq<pii, vect<pii>, greater<pii>> pq;
    pq.push({0, root});

    while(pq.size()){
        auto [d, u] = pq.top();
        // cout << d << ' ' << u << el;
        pq.pop();
        if(D[t][u] < d) continue;

        for(auto [v, w] : G[u]){
            if(D[t][v] > d + w){
                D[t][v] = d + w;
                pq.push({d+w, v});
            }
        }
    }
}

void sol(){
    int n, m, k;
    cin >> n >> m >> k;

    for(int i=0;m>i;i++){
        int u, v, w;
        cin >> u >> v >> w;
        G[u].eb(v, w);
        G[v].eb(u, w);
    }

    memset(D, 5, sizeof D);
    dickcha(0, 1); 
    dickcha(1, n);

    dbl ans = 0;
    bool ok = 0;
    vect<pair<int, dbl>> prob(k), fumo;
    for(auto& i : prob) cin >> i.fi >> i.se, ok = max(ok, i.se == 1);

    if(!ok) {
        cout << "impossible";
        return;
    } 

    int def = INF;


    for(auto i : prob){
        if(i.se == 1) def = min(def, D[0][i.fi] + D[1][i.fi]);
    }

    fumo.eb(def, 1);

    for(auto i : prob){
        int d = D[0][i.fi] + D[1][i.fi];
        if(d < def) fumo.eb(d, i.se);
    }
    sort(bend(fumo));

    dbl non = 1;
    for(auto [x,y] : fumo){
        ans += non * y * x;
        non *= (1-y);
    }

    cout << fixed << setprecision(15) << ans;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
