ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct fumo{
    int fi, se, id;
    bool operator < (const fumo& o ) const{
        if(fi != o.fi) return fi < o.fi;
        return se < o.se;
    }
};

void sol(){
    int n;
    cin >> n;
    vect<int> zip;
    fumo a[n];
    for(int i=0;n>i;i++){
        cin >> a[i].fi >> a[i].se;
        a[i].se--;
        a[i].id = i;
    }

    sort(a, a+n);
    vect<int> st;
    vect<char> ans(n, 'N');

    for(int i=1;n>i;i+=2){
        if(a[i].fi > a[i-1].se) continue;
        if(a[i].se <= a[i-1].se) ans[a[i-1].id] = 'T'; // bao nhau
        else if(a[i-1].se >= a[i].fi) ans[a[i].id] = 'T';
    }

    for(char i : ans) cout << i;
    nextl;

}

