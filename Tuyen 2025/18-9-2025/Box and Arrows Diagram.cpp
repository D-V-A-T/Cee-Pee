int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int in[N], del[N], visited[N], fumo[N];
vect<int> T[N], big, ans, G[N];

void DFS(int u){
    visited[u] = 1;
    for(int v : G[u]){
        fumo[v]++;
        if(!visited[v]) DFS(v);
    }
}

void sol(){
    int n, m;
    cin >> n >> m;
    vect<pii> ed;

    for(int i=0;m>i;i++){
        int u, v;cin >> u >> v;
        T[u].eb(v);
        ed.eb(u, v);
        in[v]++;
    }

    vect<pii> que;
    int q; cin >> q;
    while(q--){
        int u, v;cin >> u >> v;
        que.pb({u,v});
        if(u == 1) del[v-1] = 1;
    }

    q = que.size();
    for(int i=0;m>i;i++){
        if(!del[i]){
            G[ed[i].fi].eb(ed[i].se);  
        }
    }

    DFS(1);

    reverse(bend(que));
    for(auto [t, u] : que){
        if(t == 1){
            u--;
            G[ed[u].fi].eb(ed[u].se);
            if(visited[ed[u].fi]){
                fumo[ed[u].se]++;
                if(!visited[ed[u].se]) DFS(ed[u].se);
            }
        }else ans.eb(fumo[u]);
    }
    reverse(bend(ans));
    for(int i : ans) cout << i << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
