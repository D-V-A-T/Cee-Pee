ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    char a[n][n];
    int c1 = 0;
    for(auto& i : a) for(auto& j : i) cin >> j, c1 += j-'0';

    int row=0, col=0;

    for(int r=0;n>r;++r){
        int cc1 = 0;
        for(int c=0;n>c;c++) cc1 += a[r][c]-'0';
        if(c1 - cc1 + (n-cc1) <= n) row = 1;
    }


    for(int c=0;n>c;c++){
        int cc1=0;
        for(int r=0;n>r;r++) cc1 += a[r][c]-'0';
        if(c1 - cc1 + (n-cc1) <= n) col = 1;
    }

    if(row and col) cout << '+';
    else if(row) cout << '-';
    else if(col) cout << '|';
}

