int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int spf[N], cnt[N];

void sol(){
    int n, q;
    cin >> n >> q;

    int ans = 0;
    int a[n];
    set<int> seen;

    auto E = [&](int x){
        vect<int> p;
        while(x > 1){
            int d = spf[x];
            while(x % d == 0) x /= d;
            p.eb(d);
        }

        int m = p.size(), fumo = 0;
        for(int mask=1;1<<m >mask;mask++){
            int prod = 1;
            for(int j=0;m>j;j++){
                if(mask&(1<<j)) prod *= p[j];
            }
            fumo += popcnt(mask)&1 ? cnt[prod] : -cnt[prod];
        }
        return fumo;
    };

    auto U = [&](int x, int add){
        vect<int> p;
        while(x > 1){
            int d = spf[x];
            while(x % d == 0) x /= d;
            p.eb(d);
        }
        int m = p.size();
        for(int mask=1;1<<m >mask;mask++){
            int prod = 1;
            for(int j=0;m>j;j++){
                if(mask&(1<<j)) prod *= p[j];
            }
            cnt[prod]+=add;
        }
    };

    for(int& i : a) cin >> i;

    while(q--){
        int x;cin >> x;x--;
        if(seen.count(x)){
            ans -= seen.size() - (a[x]==1) - E(a[x]);
            U(a[x], -1);
            seen.erase(x);
        }else{
            ans += seen.size() - E(a[x]);
            U(a[x], 1);
            seen.insert(x);
        }
        if(seen.size() <= 1) ans = 0;
        cout << ans << el;
    }
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    IO();
    iota(spf, spf+N, 0);
    for(int i=2;N>i*i;i++){
        if(i != spf[i]) continue;
        for(int j=i*i;N>j;j+=i) spf[j] = min(spf[j], i);
    }
    int t=1;
    //cin >> t;
    while(t--)sol();
}
