int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int E[N], F[N];
// E[i] : so so co uoc la i

int B(int a, int b=MOD-2){
    int res = 1;
    while(b){
        if(b&1) res = (res*a)%MOD;
        a=(a*a)%MOD;
        b /= 2;
    }return res;
}

void sol(){
    int n;
    cin >> n;
    int a[n], cnt[N], ans = 0;
    memset(cnt, 0, sizeof cnt);

    for(int& i : a) cin >> i, cnt[i]++;

    for(int i=2;N>i;i++) for(int j=i;N>j;j+=i) E[i] += cnt[j];

    for(int i=N-1;i>1;i--){
        F[i] = B(2, E[i]-1) * E[i] % MOD;
        for(int j=i*2;N>j;j+=i) F[i] -= F[j];
        F[i] = pm(F[i], MOD);
        ans = (ans + F[i] * i) % MOD;
    }

    cout << ans;
    
}

