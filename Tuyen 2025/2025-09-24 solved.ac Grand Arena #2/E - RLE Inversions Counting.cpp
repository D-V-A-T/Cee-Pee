int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct Fick{
    vect<int> bit;
    int sz;
    Fick(int n){sz=n+5;bit.rs(sz);}
    int get(int x){
        int res = 0;
        for(;x<sz;x+=x&-x) res = (res + bit[x]) % MOD;
        return res;
    }
    void upd(int x, int v){
        for(;x;x-=x&-x) bit[x] = (bit[x] + v) % MOD;
    }
} pre(N);

struct query{
    int k, n;
    vect<int> v;
};

void sol(){
    int q, inv2 = (1e9+8)/2;
    cin >> q;
    query que[q];
    vect<int> zip;
    for(auto& [k,n,v] : que){
        cin >> k >> n;
        v.rs(n);
        for(int& i : v) cin >> i, zip.eb(i);
    }
    sort(bend(zip));
    zip.rs(unique(bend(zip)) - zip.begin());

    int ans = 0;

    for(auto& [k,n,v] : que){
        vect<int> zv, z;
        for(int i : v) z.eb(i);
        sort(bend(z));
        z.rs(unique(bend(z)) - z.begin());

        int in = 0, rep = 0;

        Fick ft(z.size()+5);

        for(int i : v){
            int x = lwb(bend(z), i) - z.begin() + 1;
            zv.eb(x);
            in += ft.get(x+1);
            in %= MOD;
            ft.upd(x, 1);

            x = lwb(bend(zip), i) - zip.begin() + 1;
            ans += pre.get(x+1) * k % MOD;
            ans %= MOD;
        }

        for(int i : zv){
            rep += ft.get(i+1);
            rep %= MOD;
        }

        int p1 = in*k%MOD, p2 = (k-1)*k%MOD;
        p2 = p2 * inv2 % MOD;
        p2 = p2 * rep % MOD;

        ans += p1 + p2;
        ans %= MOD;

        for(int i : v){
            int x = lwb(bend(zip), i) - zip.begin() + 1;
            pre.upd(x, k);
        }
    }

    cout << ans;
}   

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
