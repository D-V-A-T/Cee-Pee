struct fumo{
    int d; vect<int> v;
    bool operator < (const fumo& o) const{
        return d > o.d;
    }
};

void sol(){
    int n;
    cin >> n;
    vect<int> a(n);
    for(int& i : a) cin >> i;

    int m;cin >> m;
    vector<vector<int>> moves(m);
    for(int i=0;m>i;i++){
        int l, r, c;
        cin >> l >> r >> c;
        l--;r--;
        moves[i] = {l, r, c};
    }

    map<vect<int>, int> D;
    prq<fumo> pq;
    pq.push({0, a});
    D[a] = 0;

    while(pq.size()){
        auto [d, u] = pq.top();
        pq.pop();
        
        
        
        if(d > D[u]) continue;
        vect<int> v = u;
        for(auto& i : moves){
            int l=i[0], r=i[1], c=i[2];
            if(l >= 0 && r < v.size()) swap(v[l], v[r]);
            if(!D.count(v) || D[u]+c < D[v]){
                D[v] = D[u] + c;
                pq.push({D[v], v});
            }
            if(l >= 0 && r < v.size())  swap(v[l], v[r]);
        }
    }

    // deb;
    sort(bend(a));

    cout << (D.count(a) ? D[a] : -1);
}

