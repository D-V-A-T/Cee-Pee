ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, q;
    cin >> n >> q;
    bool a[n][n];
    for(auto& i : a) for(auto& j : i){
        char c;cin >> c;
        j = c-'0';
    }

    int curr[n], ans = 0;
    memset(curr, -1, sizeof curr);

    for(int j=0;n>j;j++){
        vect<pii> st;
        int fumo = 0;
        for(int i=0;n>i;i++){
            if(a[i][j]) curr[i] = j;
            int add = 1;
            while(st.size() && curr[i] > curr[st.back().fi]){
                fumo -= (j - curr[st.back().fi]) * st.back().se;
                add += st.back().se;
                st.pob();
            }

            fumo += (j - curr[i]) * add;
            ans += fumo;
            st.eb(i, add);
        }
    }

    cout << ans << el;

    while(q--){
        int r, c;cin >> r >> c;r--;c--;
        a[r][c] = !a[r][c];
        ans = 0;

        memset(curr, -1, sizeof curr);

        for(int j=0;n>j;j++){
            vect<pii> st;
            int fumo = 0;
            for(int i=0;n>i;i++){
                if(a[i][j]) curr[i] = j;
                int add = 1;
                while(st.size() && curr[i] > curr[st.back().fi]){
                    fumo -= (j - curr[st.back().fi]) * st.back().se;
                    add += st.back().se;
                    st.pob();
                }

                fumo += (j - curr[i]) * add;
                ans += fumo;
                st.eb(i, add);
            }
        }

        cout << ans << el;

    }

}

