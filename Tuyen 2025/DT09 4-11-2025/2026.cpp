void sol(const string& s){
    string cbd = s.substr(0, 4);
    for(char c : cbd){
        if(c == 'L') LEFT();
        if(c == 'R') RIGHT();
        if(c == 'U') UP();
        if(c == 'D') DOWN();
    }

    for(int i=0;n>i;i++){
        for(int j=0;m>j;j++){
            a[i][j].x = i;
            a[i][j].y = j;
        }
    }

    for(char c : cbd){
        if(c == 'L') LEFT();
        if(c == 'R') RIGHT();
        if(c == 'U') UP();
        if(c == 'D') DOWN();
    }

    vect<int> v;
    for(int i=0;n>i;i++){
        for(int j=0;m>j;j++){
            if('a' <= a[i][j] && a[i][j] <= 'z'){
                int from = f(a[i][j].x, a[i][j].y), to = f(i, j);
                spt[from][0] = to;
                v.eb(from);
            }
        }
    }

    for(int b=1;22>b;b++){
        for(int i : v){
            spt[i][b] = spt[spt[i][b-1]][b-1];
        }
    }

    int moves = (s.size()-8)/4;
    vect<int> steps;
    for(int b=22;b>=0;b--) if(moves&(1<<b)) steps.eb(b);

    vect<vect<fumo>> b = a;    
    for(int i=0;n>i;i++){
        for(int j=0;m>j;j++){
            if('a' <= a[i][j] && a[i][j] <= 'z'){
                int u = f(i, j);
                for(int st : steps) u = spt[u][st];
                int x, y;
                tie(x, y) = decode(u);

                b[x][y] = a[i][j];
            } 
        }
    }

    swap(a, b);

    moves = s.size()/4;
    for(int i=moves*4;s.size()>i;i++){
        char c = s[i];
        if(c == 'L') LEFT();
        if(c == 'R') RIGHT();
        if(c == 'U') UP();
        if(c == 'D') DOWN();
    }
    print();

}
