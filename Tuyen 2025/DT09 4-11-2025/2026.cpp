void RIGHT() {
    for(int i=0;n>i;i++){
        int pos = m - 1;
        for(int j=m-1;j>=0;j--){
            if(a[i][j].c != '.'){
                if(pos != j)
                    a[i][pos] = a[i][j];
                pos--;
            }
        }
        while(pos >= 0) a[i][pos--].c = '.';
    }
}

void LEFT() {
    for(int i=0;n>i;i++){
        int pos = 0;
        for(int j=0;j<m;j++){
            if(a[i][j].c != '.'){
                if(pos != j)
                    a[i][pos] = a[i][j];
                pos++;
            }
        }
        while(pos < m) a[i][pos++].c = '.';
    }
}

void UP() {
    for(int j=0;m>j;j++){
        int pos = 0;
        for(int i=0;n>i;i++){
            if(a[i][j].c != '.'){
                if (pos != i)
                    a[pos][j] = a[i][j];
                pos++;
            }
        }
        while(pos < n) a[pos++][j].c = '.';
    }
}

void DOWN() {
    for(int j=0;m>j;j++){
        int pos = n - 1;
        for(int i=n-1;i>=0;i--){
            if(a[i][j].c != '.'){
                if(pos != i)
                    a[pos][j] = a[i][j];
                pos--;
            }
        }
        while(pos >= 0) a[pos--][j].c = '.';
    }
}


