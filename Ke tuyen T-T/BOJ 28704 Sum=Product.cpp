ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    int a[n], no1[n];
    for(int& i : a) cin >> i;
    
    int mx = 9e10, pfs[n];

    partial_sum(a, a+n, pfs);

    auto get = [&](int l, int r){
        return pfs[r] - (l?pfs[l-1] : 0);
    };

    for(int i=0;n>i;i++){
        if(!i || a[i] > 1) no1[i] = i;
        else no1[i] = no1[i-1];
    }

    set<pii> st;

    for(int i=0;n>i;i++){
        int P = a[i], j = i-1;
        while(P <= mx && j>=0){
            if(a[j] == 1){
                int pj = j;
                j = no1[j];
                if(j+1<i && get(pj, i) <= P && P <= get(j+1, i)) st.insert({j+1, i});
            }
            P *= a[j];
            if(P == get(j, i)) st.insert({j, i});//, cout << j << ' ' << i << el;
            j--;
        }
    }cout << n+st.size() << el;

}

