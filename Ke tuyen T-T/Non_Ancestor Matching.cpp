int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> G[N];
int dp[N], visited[N], sz[N];

void DFS(int u){
    vect<int> fumo, vert;
    visited[u] = sz[u] = 1;
    for(int v : G[u]) if(!visited[v]){
        DFS(v);
        fumo.eb(dp[v]);
        vert.eb(v);
        sz[u] += sz[v];
    }
    sort(bend(fumo), greater<int>());

    int sum = 0;
    for(int i : fumo) sum += i;

    auto calc = [&](int S, int v, int cbd){
        return S - min(S/2, S - max(fumo[0], cbd))*2;
    };

    vect<int> v2;
    
    if(fumo.size()>1){
        int ellen = INF;
        for(int v : vert){
            if(sz[v] > fumo[0]){
                if(fumo[0]%2 == dp[v]%2) v2.eb(fumo[0]);
                else v2.eb(fumo[0]-1);
            }else v2.eb(sz[v]);
        }
        int S = 0;
        for(int i : v2) S += i;
        dp[u] = min(ellen, S - min(S/2, S - fumo[0])*2);
    }else if(fumo.size() == 1) dp[u] = sum;
    dp[u]++;    
    // cout << u << ' ' << dp[u] << el;
}

void sol(){
    int n;
    cin >> n;
    for(int i=1;n>=i;i++) G[i].clear(), dp[i] = visited[i] = sz[i] = 0;
    for(int i=2;n>=i;i++){
        int p;cin >> p;
        G[i].eb(p);
        G[p].eb(i);
    }
    DFS(1);
    cout << (n-dp[1])/2  << el;
}

