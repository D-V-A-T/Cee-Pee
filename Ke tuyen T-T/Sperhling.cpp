ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int LCS(string a, string b){
    int n = a.size(), m = b.size();
    a = " " + a;
    b = " " + b;
    int dp[n+5][m+5];
    memset(dp, 0, sizeof dp);
    for(int i=1;n>=i;i++){
        for(int j=1;m>=j;j++){
            if(a[i] == b[j]) dp[i][j] = dp[i-1][j-1] + 1;
            else dp[i][j] = max(dp[i-1][j], dp[i][j-1]);
        }
    }return dp[n][m];
}

