int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    string s;
    cin >> s;
    int n = s.size();
    umap<char, queue<int>> mp;
    const char ch[4] = {'A', 'G', 'T', 'C'};
    for(int i=0;n>i;i++) mp[s[i]].push(i);
    string ans;
    int p = -1;
    for(char c : ch)if(mp[c].empty()){
        cout << c;return;
    }
    for(;;){
        char x = '$';
        for(char c : ch)
           while(mp[c].size() && mp[c].front() <= p)mp[c].pop();
        for(char c : ch){
            if(mp[c].size() && mp[c].front() > p){
                p = mp[c].front();
                x = c;
            }else if(mp[c].empty() && p < 1e9){
                x = c;
                p = 1e9;
            }
        }
        if(x=='$')break;
        ans += x;
    }cout << ans;
}

