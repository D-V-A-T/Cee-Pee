ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[2][169][10001] ;
// DP open close interval trick https://codeforces.com/blog/entry/47764
// dp[i][j][k] : number of ways to divide the coders into j 
//              unfinished team with imbalance k

void sol(){
    int n, x;
    cin >> n >> x;
    vect<int> a(n+1);
    for(int i=1;n>=i;i++)cin >> a[i];
    sort(bend(a));
    
    dp[0][0][5000] = 1;
    for(int i=1;n>=i;i++){
        int c=i%2, p=(i+1)%2;
        memset(dp[c], 0, sizeof dp[c]);

        for(int j=0;n-i+1>=j;j++){
            for(int k=0;10000>=k;k++){
                // create a group with only a[i]
                dp[c][j][k] += dp[p][j][k];
                dp[c][j][k] %= MOD;

                // create a new group
                if(k-a[i]>=0){
                    dp[c][j+1][k - a[i]] += dp[p][j][k];
                    dp[c][j+1][k - a[i]] %= MOD;
                }
                
                // finish a group
                if(j && k+a[i]<=10000){
                    dp[c][j-1][k + a[i]] += j * (dp[p][j][k]);
                    dp[c][j-1][k + a[i]] %= MOD;
                }

                // add to a group                
                dp[c][j][k] += j * (dp[p][j][k]);
                dp[c][j][k] %= MOD;
            }
        }

    }
    
    int ans = 0;
    for(int i=0;x>=i;i++){
        ans = (ans + dp[n%2][0][i+5000]) % MOD;    
    }

    cout << ans;
}

