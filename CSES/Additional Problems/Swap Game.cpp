ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int grid=0, x, p10[18];

void sol(){
    p10[0]=1;
    for(int i=1;18>i;i++)p10[i] = p10[i-1] * 10;
    while(cin>>x)grid = grid*10 + x;
    queue<pii> que;
    umap<int, bool> visited;
    que.push({grid, 0});

    auto change = [](int i, int j, int& state){
        i = 9-i-1, j =9-j-1;
        int x = state, y=state;
        x /= p10[i];
        x %= 10;
        y /= p10[j];
        y %= 10;
        state += p10[i] * (y - x);
        state += p10[j] * (x - y); 
    };

    while(que.size()){
        auto [state, step] = que.front();
        que.pop();
        if(visited[state])continue;
        visited[state]=1;
        if(state == 123456789){
            cout << step;
            return;
        }
        for(int i=0;9>i;i++){
            if(i%3 != 2){
                change(i, i+1, state);
                if(!visited[state]) que.push({state, step+1});
                change(i, i+1, state);
            }if(i<6){
                change(i, i+3, state);
                if(!visited[state])que.push({state, step+1});
                change(i, i+3, state);
            }
        }
    }
}

