ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    string s;
    cin >> s;
    // it's not possible to reorder the string if (|S| + 1)/2 < max_char_count

    int n = s.size();
    umap<char, int> cnt;
    string ans;

    for(char& c : s)cnt[c]++;

    auto can_add = [&](char c){
        return (ans.empty() || (ans.back() != c)) && cnt[c];
    };

    for(int i=0;n>i;i++){
        char mpc = '#'; // min_possible_char
        pii mcc;  // max_char_count
        for(char c='Z';c>='A';c--){
            if(can_add(c)) mpc = c;
            if(cnt[c] > mcc.se) mcc = {c, cnt[c]};
        }
        if((n - (ans.size() + 1) + 1)/2 < mcc.se && can_add(mcc.fi)){
            ans += mcc.fi;
            cnt[mcc.fi]--;
        }else if(mpc != '#'){
            ans += mpc;
            cnt[mpc]--;
        }
    }

    cout << (ans.size() == n ? ans : "-1");

}

