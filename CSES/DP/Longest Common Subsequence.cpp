ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}


pair<int, pii> dp[1005][1005];

void sol(){
    int n, m;
    cin >> n >> m;
    int a[n+5], b[m+5];
    for(int i=1;n>=i;i++)cin >> a[i];
    for(int i=1;m>=i;i++)cin >> b[i];
    for(int i=1;n>=i;i++){
        for(int j=1;m>=j;j++){
            if(a[i] == b[j]){
                dp[i][j].fi = dp[i-1][j-1].fi + 1;
                dp[i][j].se = {i, j};
            }
            else dp[i][j] = max(dp[i-1][j], dp[i][j-1]);
        }
    }
    cout << dp[n][m].fi << el;
    int i = dp[n][m].se.fi, j = dp[n][m].se.se;
    vect<int> ans;
    for(int f=0;dp[n][m].fi>f;f++){
        ans.eb(a[i]);
        i--;j--;
        int ti = i;
        i = dp[i][j].se.fi, j = dp[ti][j].se.se;
    }
    reverse(bend(ans));
    for(int i : ans)cout << i << ' ';
}

