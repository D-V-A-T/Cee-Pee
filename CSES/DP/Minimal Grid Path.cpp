int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}
 
struct fumo{
    int let, d, x, y;
};
 
void sol(){
    int n, m;
    cin >> n;
    m = n;
    vect<vect<char>> a(n+5, vect<char>(m+5, 'Z'+69));
    for(int i=1;n>=i;i++){
        for(int j=1;m>=j;j++){
            cin >> a[i][j];
        }
    }
    queue<fumo> que;
    int visited[n+5][n+5], dist[N];
    memset(dist, 5, sizeof dist);
    memset(visited, 0, sizeof visited);
    que.push({a[1][1], 0, 1, 1});
    dist[0] = a[1][1];
 
    while(que.size()){
        auto [let, d, x, y]  = que.front();
        que.pop();
        if(let>dist[d] || visited[x][y])continue;
        visited[x][y] = 1;
        if(x+1<=n){
            que.push({a[x+1][y], d+1, x+1, y});
            dist[d+1] = min(dist[d+1], (int)a[x+1][y]);
        }
        if(y+1<=n){            
            que.push({a[x][y+1], d+1, x, y+1});
            dist[d+1] = min(dist[d+1], (int)a[x][y+1]);
        }
    }
    
    for(int i=0;n*2-1>i;i++)cout << (char)dist[i];
}
 
signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
