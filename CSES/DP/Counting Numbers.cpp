ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct DpDigit{
    int x[20], dp[20][2][11][2];

    int F(int idx, int smol, int pre, int start){
        
        if(idx < 0)return 1;
        
        int &memo = dp[idx][smol][pre][start];
        if(memo != -1)return memo;

        memo = 0;
        int lim = smol ? 9 : x[idx];

        for(int i=0;lim>=i;i++){
            if((i != pre) || (!start)){
                memo += F(idx-1, smol || i<x[idx], i, start || i>0);
            }
        }return memo;
    }

    int get(int n){
        memset(x, 0, sizeof x);
        memset(dp, -1, sizeof dp);
        int i=0;
        while(n){
            x[i++] = n%10;
            n/=10;
        }
        return F(i-1, 0, 10, 0);
    }
} dpd;

void sol(){
    int l, r;
    cin >> l >> r;
    cout << dpd.get(r) - dpd.get(l-1);
}

