ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    vect<vect<int>> a(n, vect<int>(3)), add[N];
    vect<int> zip, dp(N);
    for(auto& i : a){
        cin >> i[0] >> i[1] >> i[2];
        zip.eb(i[0]);
        zip.eb(i[1]);
    }
    sort(bend(zip));
    zip.rs(unique(bend(zip)) - zip.begin());
    for(auto& i : a){
        i[0] = upb(bend(zip), i[0]) - zip.begin();
        i[1] = upb(bend(zip), i[1]) - zip.begin();
        add[i[0]].pb(i);
    }
    for(int i=1;N>i;i++){
        for(auto& j : add[i]){
            dp[j[1]] = max(dp[j[1]], dp[i-1] + j[2]);
        }
        dp[i] = max(dp[i], dp[i-1]);
    }
    cout << *max_element(bend(dp));
}

