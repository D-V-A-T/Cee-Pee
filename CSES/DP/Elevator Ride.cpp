int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<pii> dp(1<<20, {1e9, 0});

void sol(){
    int n, W;
    cin >> n >> W;
    int a[n];
    for(int&i : a)cin >> i;
    dp[0] = {0, 0};
    for(int mask=0;1<<n >mask;mask++){
        for(int i=0;n>i;i++){
            if(mask&(1<<i))continue;
            int nmask = mask | (1<<i);
            pii fumo = dp[mask];
            if(dp[mask].se + a[i] <= W)fumo.se += a[i];
            else{
                fumo.fi++;
                fumo.se = a[i];
            }
            dp[nmask] = min(dp[nmask], fumo);
        }
    }
    cout << dp[(1<<n)-1].fi + !!(dp[(1<<n)-1].se);
}

