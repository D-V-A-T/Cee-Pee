ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n;

struct Seggs{
    int t[1000000];
    Seggs(){
        memset(t, 0, sizeof t);
    }
    void upd(int p, int x, int l=0, int r=n, int id=1){
        if(l > p || r < p)return;
        if(l==r){
            t[id] = x;
            return;
        }
        int mid = (l+r)/2;
        upd(p, x, l, mid, id*2);
        upd(p, x, mid+1, r, id*2+1);
        t[id] = max(t[id*2], t[id*2+1]);
    }int get(int i, int j, int l=0, int r=n, int id=1){
        if(l > j || r < i)return 0;
        if(i <= l && r <= j)return t[id];
        int mid = (l+r)/2;
        return max(get(i,j,l,mid,id*2), get(i,j,mid+1, r, id*2+1));
    }
} sgt;

void sol(){
    cin >>n;
    vect<int> a(n), st, dp(n, 1);
    vect<pii> p(n);
    map<int, vect<int>> fumo;
    for(int i=0;n>i;i++){
        cin >> a[i];
        fumo[a[i]].eb(i);
        while(st.size() && a[i] >= a[st.back()]){
            st.pob();
        }
        p[i].fi = st.empty() ? 0 : st.back()+1;
        st.eb(i);
    }
    st.clear();
    for(int i=n-1;i>=0;i--){
        while(st.size() && a[i] >= a[st.back()]){
            st.pob();
        }
        p[i].se = st.empty() ? n : st.back()-1;
        st.eb(i);
    }

    for(auto& i : fumo){
        auto& v = i.se;
        for(const int& j : v){
            dp[j] = sgt.get(p[j].fi, p[j].se) + 1;
        }
        for(const int& j : v){
            sgt.upd(j, dp[j]);
        }
    }
    cout << *max_element(bend(dp));
}

