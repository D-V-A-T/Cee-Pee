ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int F[N];

void sol(){
    int q;
    cin >> q;

    auto starplatinum = [](){
        int n, k;
        cin >> n >> k;
        vect<int> smol(n+5);
        k--;
        for(int i=1;n>=i;i++){
            int fumo = 0;
            for(int j=0;n-i>=j;j++){
                if(F[n-i] * j <= k){
                    fumo = j;
                }else break;
            }
            smol[i] = fumo;
            k -= fumo * F[n-i];
        }
        vect<int> p(n), fumo;
        iota(bend(p), 1);
        for(int i=1;n>=i;i++){
            cout << p[smol[i]] << ' ';
            p.erase(p.begin() + smol[i]);
        }
        nextl;
    };

    auto stonefree = [](){
        int n, ans = 1;
        cin >> n;
        vect<int> a(n);

        /* 
        Let s[i] be the number of smaller numbers to the right of p[i] (1-indexed)

        => we can choose one of these numbers to be at index i and have a smaller
        permutation

        => there are s[i] * F[n-i] smaller permutations
        => the total number of smaller perm is 
        sigma(1, n, i) s[i] * F[n-i]

        */

        for(int& i : a)cin >> i;
        for(int i=n-1;i>=0;i--){
            int smol = 0;
            for(int j=i+1;n>j;j++){
                smol += a[i] > a[j];
            }
            ans += smol * F[n-i-1];
        }cout << ans << el;
        
    };

    while(q--){
        int t;
        cin >> t;
        if(t == 1) starplatinum();
        else stonefree();
    }
}

