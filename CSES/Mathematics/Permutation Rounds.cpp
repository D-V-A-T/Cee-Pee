ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int spf[N];

void sol(){
    int n;
    cin >> n;
    int a[n+1], p[n+1];
    for(int i=1;n>=i;i++){
        cin >> a[i];
        p[a[i]] = i;
    }
    vect<int> visited(N);
    int ans = 1;
    map<int, int> cbd;
    for(int i=1;n>=i;i++){
        int len=1, tmp = i;
        if(visited[i])continue;
        visited[i] = 1;
        i = p[i];
        while(!visited[i]){
            visited[i] = 1;
            len++;
            i = p[i];
        }
        i = tmp;
        vect<pii> fumo;
        while(len>1){
            if(fumo.empty() || fumo.back().fi != spf[len]){
                fumo.eb(spf[len], 1);
            }else fumo.back().se++;
            len /= spf[len];
        }
        for(pii& i : fumo)cbd[i.fi] = max(cbd[i.fi], i.se);
    }
    for(auto i : cbd)while(i.se--)ans = (ans * i.fi) % MOD;
    cout << ans;   
}

