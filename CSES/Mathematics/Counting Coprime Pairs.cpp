int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int spf[N], fumo[N];

void sol(){
    int n;
    cin >> n;
    int ans = n * (n-1) / 2, a[n];
    for(int& i : a){
        cin >> i;
        vect<int> p;
        while(i>1){
            p.eb(spf[i]);
            i /= spf[i];
        }
        p.rs(unique(bend(p)) - p.begin());
        int cnt = 0, m=p.size();
        for(int mask=1;1<<m >mask;mask++){
            int pr=1;
            for(int i=0;m>i;i++){
                if(mask&(1<<i))pr *= p[i];
            }
            cnt += popcnt(mask)&1 ? fumo[pr] : -fumo[pr];
            fumo[pr]++;
        }
        ans -= cnt;
    }cout << ans;
}

