int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int B(int a, int b=MOD-2){
    int res = 1;
    b %= MOD-1;
    while(b){
        if(b&1)res = (res * a) % MOD;
        a = (a*a)%MOD;
        b >>= 1;
    }return res;
}

void sol(){
    int n;
    cin >> n;
    
    pii a[n];
    int S = 1, P = 1, C = 1;
    // S = (1 + x1 + x1^2 + ... x1^k1)*(1 + x2 + x2^2 + ... + x2^k2)*...
    //   = (x1^(k1+1) - 1)/(x1-1) * ...
    // x1, x1^2, x1^3,... shows up in C / (k1 + 1) divisors
    // => P = x1 ^ ((1 + 2 + 3 + ... + k1) * C / (k1 + 1))

    int pre[n], suf[n];
    for(pii& i : a){
        cin >> i.fi >> i.se;
        C = (C * i.se + C) % MOD;
        S = (S * pm(B(i.fi, i.se+1) - 1) % MOD * B(i.fi - 1)) % MOD;
    }

    for(int i=0;n>i;i++)pre[i] = i ? pre[i-1] * (a[i].se+1) % (MOD-1) : a[i].se+1;
    for(int i=n-1;i>=0;i--)suf[i] = i+1<n ? suf[i+1] * (a[i].se+1) % (MOD-1) : a[i].se+1;


    auto sum = [](int x){
        int res = (x+1) * x / 2;
        return res % (MOD-1);
    };

    for(int i=0;n>i;i++){
        int dc = (i ? pre[i-1] : 1) * (i+1<n ? suf[i+1] : 1) % (MOD-1);
        P = (P * B(a[i].fi, sum(a[i].se) * dc)) % MOD;
    }
    cout << C << ' ' << S << ' ' << P << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    // cin >> t;
    while(t--)sol();
}
