int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int bit[N];

int get(int x){
    int res = 0;
    for(;x<N;x+=x&-x) res += bit[x];
    return res;
}

void upd(int x, int v=1){
    for(;x>0;x-=x&-x) bit[x] += v;
}

struct seg{
    int l, r, id;
};

void sol(){
    int n;
    cin >> n;
    seg a[n];
    vect<int> zl, zr;
    for(int i=0;n>i;i++){
        cin >> a[i].l >> a[i].r;
        a[i].id = i;
        zl.eb(a[i].l);
        zr.eb(a[i].r);
    }
    sort(bend(zl));
    sort(bend(zr));

    vect<int> fumo(n), fimi(n);

    sort(a, a+n, [](seg x, seg y){
        return x.l != y.l ? x.l < y.l : x.r > y.r;
    });
    for(seg i : a){
        auto [l, r, id] = i;
        r = upb(bend(zr), r) - zr.begin();
        fumo[id] = get(r);
        upd(r);
    }

    

    memset(bit, 0, sizeof bit);
    
   
    sort(a, a+n, [](seg x, seg y){
        return x.r != y.r ? x.r < y.r : x.l > y.l;
    });
    for(seg i : a){
        auto [l, r, id] = i;
        l = upb(bend(zl), l) - zl.begin();
        fimi[id] = get(l);
        upd(l);
    }

    for(int i : fimi)cout << i << ' ';
    nextl;
    for(int i : fumo)cout << i << ' ';
}

