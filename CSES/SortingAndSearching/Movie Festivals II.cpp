int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, k;
    cin >> n >> k;
    multiset<int, greater<int>> ms;
    for(int i=0;k>i;i++)ms.insert(-1);
    pii a[n];
    for(pii& i : a)cin >> i.se >> i.fi;

    sort(a, a+n);

    int ans=0;
    for(pii i : a){
        auto it = ms.lwb(i.se);
        if(it != ms.end()){
            ms.erase(it);
            ms.insert(i.fi);
            ans++;
        }
    }
    cout << ans;
    
}

