void sol(){
    int n,m,k;
    cin >> n >> m >> k;
    vect<int> a(n), b(m);
    for(int& i : a)cin >> i;
    for(int& i : b)cin >> i;
    sort(bend(a));
    sort(bend(b));
    int p=0, t=0, ans=0;
    while(p<n && t < m){
        if(abs(a[p]-b[t]) <= k){
            ans++;
            p++;
            t++;
        }else if(a[p]-b[t] > k)t++;
        else if(b[t] - a[p])p++;
    }cout << ans;
}

