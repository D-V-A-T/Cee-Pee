void ZIP(vect<pii>& v){
    vect<int> zip;
    for(pii& i : v){
        zip.eb(i.fi);
        zip.eb(i.se);
    }sort(bend(zip));
    auto it=unique(bend(zip));
    zip.rs(it-zip.begin());
    for(pii& i : v){
        i.fi=lwb(bend(zip), i.fi)-zip.begin()+1;
        i.se=lwb(bend(zip), i.se)-zip.begin()+1;
    }
}

void sol(){
    int n;
    cin >> n;
    vect<pii> v(n);
    vect<int> fumo(1e6);
    for(pii& i : v)cin >> i.fi >> i.se;
    ZIP(v);
    for(pii i : v){
        fumo[i.fi]++;
        fumo[i.se+1]--;
    }for(int i=1;5e5>=i;i++)fumo[i]+=fumo[i-1];
    cout << *max_element(bend(fumo));
}

