ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int lg2(int i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}
 
int get(int l, int r, vect<vect<int>>& spt){
    int i=lg2(r-l+1);
    return min(spt[i][l], spt[i][r-(1<<i)+1]);
}
 
