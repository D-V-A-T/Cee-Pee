void sol(){
    int n, x;
    cin >> n >> x;
    int a[n];
    multiset<int, greater<int>> ms;
    for(int& i : a){
        cin >> i;
        ms.insert(i);
    }int ans=0;
    sort(a, a+n);
    for(int i : a){
        auto it=ms.lwb(x-i), it2=ms.find(i);
        if(it != ms.end() && it2 != ms.end()){
            ms.erase(it);
            it2=ms.find(i);
            if(it2 != ms.end())
                ms.erase(it2);
            ans++;
        }else if(it2 != ms.end()){
            ans++;
            ms.erase(it2);
        }
    }cout << ans;
}

