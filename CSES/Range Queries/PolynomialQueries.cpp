int n, q, a[N];

struct reimu{
    int x=0, y=0;
};

struct Seggs{
    vect<int> t;
    vect<reimu> lz;
    Seggs(){
        t.rs(4*n+1);
        lz.rs(4*n+1);
        build();
    }void build(int l=0, int r=n-1, int id=1){
        if(l == r){
            t[id]=a[l];
            return;
        }int mid=(l+r)/2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);
        t[id]=t[id*2]+t[id*2+1];
    }void push(int l, int r, int id){
        int len=(r-l+1), mid=(l+r)/2;
        t[id]+=lz[id].x*(len+1)*len/2 + lz[id].y*len;
        if(l < r){
            lz[id*2].x += lz[id].x;
            lz[id*2].y += lz[id].y;
            lz[id*2+1].x += lz[id].x;
            lz[id*2+1].y += lz[id].y + lz[id].x*(mid-l+1);
        }lz[id].x=lz[id].y=0;
    }
    void upd(int i, int j, int l=0, int r=n-1, int id=1){
        push(l, r, id);
        if(l > j || r < i)return;
        if(l >= i && r <= j){
            lz[id].x ++;
            lz[id].y += (l-i);
            push(l, r, id);
            return;
        }int mid=(l+r)/2;
        upd(i, j, l, mid, id*2);
        upd(i, j, mid+1, r, id*2+1);
        t[id] = t[id*2] + t[id*2+1];
    }int get(int i, int j, int l=0, int r=n-1, int id=1){
        push(l, r, id);
        if(l > j || r < i)return 0;
        if(l >= i && r <= j)return t[id];
        int mid=(l+r)/2;
        return get(i, j, l, mid, id*2) + get(i, j, mid+1, r, id*2+1);
    }
};

void sol(){
    cin >> n >> q;
    for(int i=0;n>i;i++)cin >> a[i];
    Seggs sgt;
    while(q--){
        int t,l,r;
        cin >> t >> l >> r;
        if(t==1)sgt.upd(l-1, r-1);
        else cout << sgt.get(l-1, r-1) << el;
    }
}

