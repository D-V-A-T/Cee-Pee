ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}
 
int n, q;
vect<int> v, st;
 
struct Seggs{
	vect<int> T[N+N];
	Seggs(){
		build();
	}
	void build(int l=0, int r=n-1, int id=1){
		for(int i=l;r>=i;i++)T[id].eb(v[i]);
		sort(bend(T[id]));
		if(l == r)return;
		int mid = (l+r)/2;
		build(l, mid, id*2);
		build(mid+1, r, id*2+1);
	}
	int get(int i, int j, int x, int l=0, int r=n-1, int id=1){
		if(l > j || r < i)return 0;
		if(i <= l && r <= j)return upb(bend(T[id]), x) - T[id].begin();
		int mid = (l+r)/2;
		return get(i, j, x, l, mid, id*2) + get(i, j, x, mid+1, r, id*2+1);
	}
};
 
