ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct Fick{
    int bit[N];
    int get(int x){
        int res = 0;
        for(;x>0;x-=x&-x)res += bit[x];
        return res;
    }
    void upd(int x){
        for(;x<N;x+=x&-x)bit[x] ++;
    }
};

struct fumo{
    int p, x, t, id;
    bool operator < (const fumo& o)const{
        return p < o.p;
    }
};

void sol(){
    int n, q;
    cin >> n >> q;
    vect<int> a(n), ans(q);
    for(int& i : a)cin >> i;

    
    vect<int> zip=a;
    sort(bend(zip));
    auto it=unique(bend(zip));
    zip.rs(it-zip.begin());
    for(int& i : a)i=upb(bend(zip), i)-zip.begin();

    vect<fumo> que;
    for(int i=0;q>i;i++){
        int l, r, x, y;
        cin >> l >> r >> x >> y;
        l--;r--;
        x = upb(bend(zip), x-1) - zip.begin();
        y = upb(bend(zip), y) - zip.begin();
        que.pb({r, x, -1, i});
        que.pb({l-1, x, 1, i});
        que.pb({r, y, 1, i});
        que.pb({l-1, y, -1, i});
    }

    sort(bend(que));
    int ptr = 0;
    Fick fwt;
    while(que[ptr].p == -1)ptr++;
    for(int i=0;n>i;i++){
        fwt.upd(a[i]);
        while(ptr < que.size() && que[ptr].p <= i){
            ans[que[ptr].id] += que[ptr].t * fwt.get(que[ptr].x);
            ptr++;
        }
    }

    for(int i : ans)cout << i << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
