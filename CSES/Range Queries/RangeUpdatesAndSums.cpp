int n, q, a[N];

struct Seggs{
	vect<int> t;
	vect<vect<int>> lz;
	Seggs(){
		t.rs(4*n+1);
		lz.rs(4*n+1, vect<int>({1,0}));
		build();
	}void build(int l=0, int r=n-1, int id=1){
		if(l==r){
			t[id]=a[l];
			return;
		}int mid=(l+r)/2;
		build(l, mid, id*2);
		build(mid+1, r, id*2+1);
		t[id]=t[id*2]+t[id*2+1];
	}void push(int l, int r, int id){
		t[id]=t[id]*lz[id][0] + lz[id][1]*(r-l+1);
		if(l < r){
			lz[id*2][0]*=lz[id][0];
			if(lz[id][0]) lz[id*2][1]+=lz[id][1];
			lz[id*2+1][0]*=lz[id][0];
			if(lz[id][0])lz[id*2+1][1]+=lz[id][1];
			else lz[id*2+1][1]=lz[id*2][1]=lz[id][1];
		}lz[id]={1,0};
	}
	void upd(int T, int x, int i, int j, int l=0, int r=n-1, int id=1){
		push(l, r, id);
		if(l > j || r < i)return;
		if(l >= i && r <= j){
			if(T==1){
				lz[id][1]+=x;
			}else{
				lz[id][0]=0;
				lz[id][1]=x;
			}push(l, r, id);
			return;
		}int mid=(l+r)/2;
		upd(T, x, i, j, l, mid, id*2);
		upd(T, x, i, j, mid+1, r, id*2+1);
		t[id]=t[id*2]+t[id*2+1];
	}int get(int i, int j, int l=0, int r=n-1, int id=1){
		push(l, r, id);
		if(l > j || r < i)return 0;
		if(l >= i && r <= j)return t[id];
		int mid=(l+r)/2;
		return get(i, j, l, mid, id*2) + get(i, j, mid+1, r, id*2+1);
	}
};

void sol(){
    cin >> n >> q;
    for(int i=0;n>i;i++)cin >> a[i];
    Seggs sgt;
//  (0/1, x, a, b) : set A[i] with every a <= i <= b to A[i]*x + 0/1
    while(q--){
    	int t;cin >> t;
    	if(t==3){
    		int i,j;cin >> i >> j;
    		cout << sgt.get(i-1, j-1) << el;	
    	}else{
			int i,j,x;cin >> i >> j >> x;
			sgt.upd(t, x, i-1, j-1);
    	}
    }
}

