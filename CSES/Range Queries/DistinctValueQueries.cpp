int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int n,q;

struct FenwickTree{
    vect<int> bit;
    FenwickTree(){
        bit.rs(N);
    }void upd(int x, int p){
        for(;x<N;x|=x+1)
            bit[x]+=p;
    }int get(int x){
        int res=0;
        for(;x>=0;x=(x&(x+1))-1)
            res+=bit[x];
        return res;
    }
};

struct query{
    int l, r, id;
    bool operator < (query& o) const {
        return r < o.r;
    };
};

void sol(){
    cin >> n >> q;
    vect<int> a(n), ans(q);
    map<int,int> ptr;
    map<int, vect<int>> reimu;
    int i;
    for(i=0;n>i;i++){
        cin >> a[i];
        reimu[a[i]].eb(i);
    }vect<query> que(q);
    for(i=0;q>i;i++){
        cin >> que[i].l >> que[i].r;
        que[i].id=i;
        que[i].l--;que[i].r--;
    }sort(bend(que));
    i=0;
    FenwickTree fwt;
    for(auto f : que){
        while(i <= f.r){
            fwt.upd(i, 1);
            int sus=ptr[a[i]];
            if(sus)fwt.upd(reimu[a[i]][sus-1], -1);
            ptr[a[i]]++;
            i++;
        }ans[f.id]=fwt.get(f.r)-fwt.get(f.l-1);
    }for(int i : ans)cout << i << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
