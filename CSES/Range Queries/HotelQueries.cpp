int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int n, m;
vect<int> a, b;

struct Seggs{
    vect<pii> t;
    Seggs(){
        t.rs(4*n+1);
        build();
    }void build(int l=0, int r=n-1, int id=1){
        if(l==r){
            t[id]={a[l], l};
            return;
        }int mid=(l+r)/2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);
        t[id]=max(t[id*2], t[id*2+1]);
    }void upd(int p, int x, int l=0, int r=n-1, int id=1){
        if(l > p || r < p)return;
        if(l==r){
            t[id]={x,l};
            return;
        }int mid=(l+r)/2;
        upd(p, x, l, mid, id*2);
        upd(p, x, mid+1, r, id*2+1);
        t[id]=max(t[id*2], t[id*2+1]);
    }pii get(int i, int j, int l=0, int r=n-1, int id=1){
        if(l > j || r < i)return {-1, -1};
        if(l >= i && r <= j)return t[id];
        int mid=(l+r)/2;
        return max(get(i, j, l, mid, id*2), get(i, j, mid+1, r, id*2+1));
    }
};

void sol(){
    cin >> n >> m;
    a.rs(n);b.rs(m);
    for(int& i : a)cin >> i;
    for(int& i : b)cin >> i;
    Seggs sgt;
    for(int i : b){
        int l=0, r=n;
        pii ans={-1,-1};
        while(l <= r){
            int mid=(l+r)/2;
            pii reimu=sgt.get(0, mid);
            if(reimu.fi >= i){
                r=mid-1;
                ans=reimu;
            }else l=mid+1;
        }if(ans.fi == -1)cout << "0 ";
        else{
            cout << ans.se+1 << ' ';
            sgt.upd(ans.se, ans.fi-i);
        }
    }
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
