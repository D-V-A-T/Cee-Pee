int pm(int a,const int b=1e9+7){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int n;
vect<int> v;

struct Seggs{
    vect<int> t;
    Seggs(){
        t.rs(4*n+1);
        build();
    }void build(int l=0, int r=n-1, int id=1){
        if(l == r){
            t[id]=v[l];
            return;
        }int mid=(l+r)/2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);
        t[id]=min(t[id*2], t[id*2+1]);
    }void upd(int p, int x, int l=0, int r=n-1, int id=1){
        if(l > p || r < p)return;
        if(l==r && l==p){
            t[id]=x;
            return;
        }int mid=(l+r)/2;
        upd(p, x, l ,mid, id*2);
        upd(p, x, mid+1, r, id*2+1);
        t[id]=min(t[id*2], t[id*2+1]);
    }int get(int i, int j, int l=0, int r=n-1, int id=1){
        if(l > j || r < i)return INF;
        if(l >= i && r <= j)return t[id];
        int mid=(l+r)/2;
        return min(get(i, j, l, mid, id*2), get(i, j, mid+1, r, id*2+1));
    }
};


void sol(){
    int q;
    cin >> n >> q;
    v.rs(n);
    for(int& i : v)cin >> i;
    Seggs sgt;
    while(q--){
        int t;cin >> t;
        if(t==1){
            int p,x;cin >> p >> x;
            sgt.upd(p-1, x);
        }else{
            int l, r;cin >> l >> r;
            cout << sgt.get(l-1, r-1) << el;    
        }
    }
}

