int n;

struct Seggs{
    vect<int> t, v, lz;
    Seggs(const vect<int>& pfs){
        t.rs(4*n+1);
        lz.rs(4*n+1);
        v=pfs;
        build();
    }void build(int l=0, int r=n-1, int id=1){
        if(l == r){
            t[id]=v[l];
            return;
        }int mid=(l+r)/2;
        build(l, mid ,id*2);
        build(mid+1, r, id*2+1);
        t[id]=max(t[id*2], t[id*2+1]);
    }void push(int l, int r, int id, int x=0){
        lz[id]+=x;
        t[id]+=lz[id];
        if(l < r){
            lz[id*2]+=lz[id];
            lz[id*2+1]+=lz[id];
        }lz[id]=0;
    }void upd(int i, int j, int x, int l=0, int r=n-1, int id=1){
        push(l, r, id);
        if(l > j || r < i)return;
        if(l >= i && r <= j){
            push(l, r, id, x);
            return;
        }int mid=(l+r)/2;
        upd(i, j, x, l, mid, id*2);
        upd(i, j, x, mid+1, r, id*2+1);
        t[id]=max(t[id*2], t[id*2+1]);
    }int get(int i, int j, int l=0, int r=n-1, int id=1){
        push(l, r, id);
        if(l > j || r < i)return -INF;
        if(l >= i && r <= j)return t[id];
        int mid=(l+r)/2;
        return max(get(i, j, l, mid, id*2), get(i, j, mid+1, r, id*2+1));
    }
};

