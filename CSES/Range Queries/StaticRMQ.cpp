void ffopen(const string& file){
    if(file.empty())return;
    freopen((file + ".inp").c_str(), "r", stdin);
    freopen((file + ".out").c_str(), "w", stdout);
}

int pm(int a,const int b=1e9+7){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int lg2(int i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}

void sol(){
    int n, q;
    cin >> n >> q;
    vect<int> v(n);
    for(int& i : v)cin >> i;
    vect<vect<int>> spt(n+1, vect<int>(lg2(n)+1, INF));
    for(int i=0;n>i;i++)spt[i][0]=v[i];
    for(int i=1;lg2(n)>=i;i++)
        for(int f=0;n>=f+(1<<i);f++)
            spt[f][i]=min(spt[f][i-1], spt[f+(1<<(i-1))][i-1]);
    while(q--){
        int l,r;cin >> l >> r;
        l--;r--;
        int len=r-l+1, p=lg2(len);
        cout << min(spt[l][p], spt[r-(1<<p)+1][p]);
        nextl;
    }    
}

