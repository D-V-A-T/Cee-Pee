int n;
vect<int> a;

struct Seggs{
    vect<vect<int>> t;
    // [0] : range sum
    // [1] : max sum in range
    // [2] : max sum l -> k
    // [3] : max sum k -> r
    Seggs(){
        t.rs(4*n+1, vect<int>(4));
        build();
    }void add(const vect<int>& a, const vect<int>& b, vect<int>& res){
        res[0]=a[0]+b[0];
        res[1]=max({a[1], b[1], a[3]+b[2]});
        res[2]=max(a[2], a[0]+b[2]);
        res[3]=max(b[3], b[0]+a[3]);
    }void build(int l=0, int r=n-1, int id=1){
        if(l==r){
            t[id][0]=a[l];
            t[id][1]=t[id][2]=t[id][3]=max(a[l], 0ll);
            return;
        }int mid=(l+r)/2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);
        add(t[id*2], t[id*2+1], t[id]);
    }void upd(int p, int l=0, int r=n-1, int id=1){
        if(l > p || r < p)return;
        if(l==r){
            t[id][0]=a[l];
            t[id][1]=t[id][2]=t[id][3]=max(a[l], 0ll);
            return;
        }int mid=(l+r)/2;
        upd(p, l, mid, id*2);
        upd(p, mid+1, r, id*2+1);
        add(t[id*2], t[id*2+1], t[id]);
    }
};

void sol(){
    int q;cin >> n >> q;
    a.rs(n);
    for(int& i : a)cin >> i;
    Seggs sgt;
    while(q--){
        int p, x;
        cin >> p >> x;
        a[p-1]=x;
        sgt.upd(p-1);
        cout << sgt.t[1][1] << el;
    }
}

