int pm(int a,const int b=1e9+7){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int n, q, num=1;

struct query{
    char t;
    int x,y;
};

struct FenwickTree{
    vect<int> bit;
    FenwickTree(){
        bit.rs(N);
    }void upd(int x, int p){
        for(;num>=x;x+=x&-x)
            bit[x]+=p;
    }int get(int x){
        int res=0;
        for(;x>0;x-=x&-x)    
            res+=bit[x];
        return res;
    }
};

void sol(){
    cin >> n >> q;
    vect<int> a(n), v;
    for(int&i : a){
        cin >> i;
        v.eb(i);
    }map<int,int> comp;
    vect<query> que(q);
    for(auto& i : que){
        cin >> i.t >> i.x >> i.y;
        v.eb(i.y);
        if(i.t == '?')v.eb(i.x);
        else i.x--;
    }sort(bend(v));
    for(int i : v)comp[i]=num++;
    FenwickTree fwt;
    for(int& i : a){
        i=comp[i];
        fwt.upd(i, 1);
    }
    for(auto& i : que){
        i.y=comp[i.y];
        if(i.t == '!'){
            fwt.upd(a[i.x], -1);
            a[i.x]=i.y;
            fwt.upd(a[i.x], 1);
        }else{
            i.x=comp[i.x];
            cout << fwt.get(i.y)-fwt.get(i.x-1) << el;
        }
    }
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
