int pm(int a,const int b=1e9+7){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int n;
vect<int> a;

struct Seggs{
    vect<vect<int>> t;
    Seggs(){
        t.rs(4*n+5, vect<int>(2));
        build();
    }void build(int l=1, int r=n, int id=1){
        if(l == r){
            t[id][0]=a[l]-l;
            t[id][1]=a[l]+l;
            return;
        }int mid=(l+r)/2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);
        t[id][0]=min(t[id*2][0], t[id*2+1][0]);
        t[id][1]=min(t[id*2][1], t[id*2+1][1]);
    }void upd(int p, int l=1, int r=n, int id=1){
        if(l > p || r < p)return;
        if(l == r){
            t[id][0]=a[l]-l;
            t[id][1]=a[l]+l;
            return;
        }int mid=(l+r)/2;
        upd(p, l, mid, id*2);
        upd(p, mid+1, r, id*2+1);
        t[id][0]=min(t[id*2][0], t[id*2+1][0]);
        t[id][1]=min(t[id*2][1], t[id*2+1][1]);
    }int get(int i, int j, int f, int l=1, int r=n, int id=1){
        if(l > j || r < i || l > r)return INF;
        if(l >= i && r <= j)return t[id][f];
        int mid=(l+r)/2;
        return min(get(i, j, f, l, mid, id*2), get(i, j, f, mid+1, r, id*2+1));
    }
};

void sol(){
    int q;
    cin >> n >> q;
    a.rs(N);
    for(int i=1;n>=i;i++)cin >> a[i];
    Seggs sgt;
    while(q--){
        int t;cin >> t;
        if(t==1){
            int p, x;
            cin >> p >> x;
            a[p]=x;
            sgt.upd(p);
        }else{
            int k;
            cin >> k;
            cout << min({sgt.get(k+1, n, 1)-k, sgt.get(1, k-1, 0)+k, a[k]}) << el;
        }
    }
}

