ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, m;
    cin >> n >> m;
    char a[n][m], moves[n][m];
    pii A, B;
    for(int i=0;n>i;i++) for(int j=0;m>j;j++){
        cin >> a[i][j];
        if(a[i][j] == 'A') A = {i,j};
        if(a[i][j] == 'B') B = {i,j};
    }

    bool visited[n][m];
    memset(visited, 0, sizeof visited);
    memset(moves, 0, sizeof moves);
    queue<pii> bfs;bfs.push(A);

    while(bfs.size()){
        auto [u, v] = bfs.front();
        bfs.pop();

        if(visited[u][v]) continue;
        visited[u][v] = 1;
        if(a[u][v] == '#') continue;

        if(u+1 < n && !visited[u+1][v]) bfs.emplace(u+1, v), moves[u+1][v] = 'D';
        if(u && !visited[u-1][v]) bfs.emplace(u-1, v), moves[u-1][v] = 'U';
        if(v+1 < m && !visited[u][v+1]) bfs.emplace(u, v+1), moves[u][v+1] = 'R';
        if(v && !visited[u][v-1]) bfs.emplace(u, v-1), moves[u][v-1] = 'L';
    }

    if(moves[B.fi][B.se] == 0){
        cout << "NO";
        return;
    }

    string ans;
    int i = B.fi, j = B.se;
    while(i != A.fi || j != A.se){

        ans += moves[i][j];

        if(moves[i][j] == 'L') j++;
        else if(moves[i][j] == 'R') j--;
        else if(moves[i][j] == 'U') i++;
        else if(moves[i][j] == 'D') i--;
    }
    reverse(bend(ans));
    cout << "YES\n" <<  ans.size() << el << ans;    
    
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    //cin >> t;
    while(t--)sol();
}
