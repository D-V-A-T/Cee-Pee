ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m, in[N], low[N], scc[N], visited[N], T = 0;
vect<int> G[N], C[N], trace;

const int offset = 1e5;

int flip(int x){
    return x > offset ? x - offset : x + offset;
}

void suchcardcheo(int u){
    trace.eb(u);
    in[u] = low[u] = ++T;
    visited[u] = 1;

    for(int v : G[u]){
        if(!visited[v]){
            suchcardcheo(v);
            low[u] = min(low[u], low[v]);
        }else if(!scc[v]){
            low[u] = min(low[u], in[v]);
        }
    }

    if(low[u] == in[u]){
        // cout << "root: " << u << el;
        while(trace.back() != u) scc[trace.back()] = u, trace.pob();
        scc[u] = u, trace.pob();

        // cout << "\n ------------------ \n";
    }
}

void sol(){
    cin >> n >> m;
    vect<pii> edges;
    for(int i=0; n>i ;i++){
        char c;
        int u, v;
        cin >> c >> u;
        if(c == '+') u = flip(u);
        cin >> c >> v;
        if(c == '+') v = flip(v);

        // cout << flip(u) << ' ' << v << el;
        // cout << flip(v) << ' ' << u << el;

        G[flip(u)].eb(v);
        edges.eb(flip(u), v);
        G[flip(v)].eb(u);
        edges.eb(flip(v), u);
    }

    for(int i=1; m+offset>=i; i++){
        if(!visited[i]) suchcardcheo(i);
    }

    for(int i=1; m>=i; i++) if(scc[i] == scc[flip(i)]){
        cout << "IMPOSSIBLE";
        return;
    }



    vect<int> topo(N), indeg(N);
    queue<int> que;

    for(pii e : edges){
        if(scc[e.fi] != scc[e.se])
            C[scc[e.fi]].eb(scc[e.se]), indeg[scc[e.se]]++;
    }
    for(int i=1; m+offset>=i; i++) if(indeg[i] == 0) que.push(i);

    nextl;

    while(que.size()){
        int u = que.front();
        que.pop();
        topo[u] = ++T;

        for(int v : C[u]) if(--indeg[v] == 0) que.push(v);
    }


    for(int i=1; m>=i; i++){
        if(topo[scc[i]] < topo[scc[flip(i)]]) cout <<"+ ";
        else cout << "- ";
    }


}

