ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m, k;
vect<pii> G[N];
prq<int> sus[N];

void sol(){
	cin >> n >> m >> k;
	for(int i=0; m>i; i++){
		int u, v, w;
		cin >> u >> v >> w;
		G[u].eb(v, w);
		
	}
	
	prq<pii, vect<pii>, greater<pii>> pq;
	
	sus[1].push(0);
	pq.push({0, 1});
	
	
	while(pq.size()){
		auto [d, u] = pq.top();
		pq.pop();
		
		
		if(d > sus[u].top()) continue;
		
		
		for(auto [v, w] : G[u]){
			if(sus[v].size() < k){
				sus[v].push(w + d);
				pq.push({w + d, v});
			}else{
				if(sus[v].top() > w + d){
					sus[v].pop();
					sus[v].push(w + d);
					pq.push({w + d, v});
				}
			}
		}
	}
	
	
	vect<int> ans;
	while(sus[n].size()) ans.eb(sus[n].top()), sus[n].pop();
	reverse(bend(ans));
	for(int i : ans) cout << i << ' ';
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
