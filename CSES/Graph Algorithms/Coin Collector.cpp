ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m, in[N], low[N], scc[N], visited[N], T = 0;
vect<int> G[N], C[N], trace, grp[N];

void suchcardcheo(int u){
    in[u] = low[u] = ++T;
    visited[u] = 1;
    trace.eb(u);

    for(int v : G[u]){
        if(!visited[v]){
            suchcardcheo(v);
            low[u] = min(low[u], low[v]);
        }else if(!scc[v]){
            low[u] = min(low[u], in[v]);
        }
    }

    if(low[u] == in[u]){
        while(trace.back() != u) scc[trace.back()] = u, grp[u].eb(trace.back()), trace.pob();
        scc[u] = u, grp[u].eb(trace.back()), trace.pob();
    }
}

void sol(){
    cin >> n >> m;
    int val[n+5];
    for(int i=1; n>=i; i++) cin >> val[i];
    for(int i=0; m>i; i++){ 
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
    }

    for(int i=1; n>=i; i++) if(!visited[i]) suchcardcheo(i);

    queue<int> que;
    vect<int> topo, dp(n+5), indeg(n+5);

    for(int i=1; n>=i; i++) if(i == scc[i]){
        for(int u : grp[i]){
            for(int v : G[u]) if(scc[v] != i)
                C[i].eb(scc[v]), indeg[scc[v]]++;
        }
    }

    for(int i=1; n>=i; i++) if(i == scc[i] && indeg[i] == 0) que.push(i);

    while(que.size()){
        int u = que.front();
        que.pop();
        topo.eb(u);

        for(int v : C[u]) if(--indeg[v] == 0) que.push(v);
    }

    for(int i=topo.size()-1; i>=0; i--){
        int u = topo[i];
        int total = 0;
        for(int v : grp[u]) total += val[v];
        dp[u] = total;
        for(int v : C[u]) dp[u] = max(dp[u], dp[v] + total);
    }

    cout << *max_element(bend(dp));
}

