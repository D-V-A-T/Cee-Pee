ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m;
vect<vect<char>> a;


void BFS(vect<pii> sources, vect<vect<int>>& D, vect<vect<char>>& moves){

    bool visited[n+1][m+1];
    memset(visited, 0, sizeof visited);

    queue<pii> bfs;
    for(pii i : sources) bfs.push(i), D[i.fi][i.se] = 0;
    int i, j;
    while(bfs.size()){
        tie(i, j) = bfs.front();
        bfs.pop();


        if(visited[i][j]) continue;
        visited[i][j] = 1;
        if(a[i][j] == '#') continue;
        int d = D[i][j] + 1;

        if(i && d < D[i-1][j])
            bfs.emplace(i-1, j), moves[i-1][j] = 'U', D[i-1][j] = d;
        if(i+1<n && d < D[i+1][j])
            bfs.emplace(i+1, j), moves[i+1][j] = 'D', D[i+1][j] = d;
        if(j && d < D[i][j-1]) 
            bfs.emplace(i, j-1), moves[i][j-1] = 'L', D[i][j-1] = d;
        if(j+1<m && d < D[i][j+1]) 
            bfs.emplace(i, j+1), moves[i][j+1] = 'R', D[i][j+1] = d;
    }
}

void sol(){
    cin >> n >> m;
    a.rs(n, vect<char>(m));
    for(auto& i : a) for(auto& j : i) cin >> j;

    vect<vect<char>> moves(n, vect<char>(m));
    vect<vect<int>> M(n, vect<int>(m, 1e9)), A(n, vect<int>(m, 1e9));
    
    vect<pii> sM, sA;
    for(int i=0;n>i;i++) for(int j=0;m>j;j++){
        if(a[i][j] == 'M') sM.eb(i, j);
        if(a[i][j] == 'A') sA.eb(i, j);
    }

    BFS(sM, M, moves);
    BFS(sA, A, moves);


    for(int i=0;n>i;i++) for(int j=0;m>j;j++){
        if(a[i][j] == '#') continue;
        if(i == 0 || j == 0 || i==n-1 || j == m-1){
            if(M[i][j] > A[i][j]){
                pii End = {sA[0].fi, sA[0].se};
                string ans;
                while(i != End.fi || j != End.se){

                    ans += moves[i][j];

                    if(moves[i][j] == 'L') j++;
                    else if(moves[i][j] == 'R') j--;
                    else if(moves[i][j] == 'U') i++;
                    else if(moves[i][j] == 'D') i--;
                }
                reverse(bend(ans));
                cout << "YES\n" <<  ans.size() << el << ans;   
                return;
            }
        }
    }

    cout << "NO";
}

