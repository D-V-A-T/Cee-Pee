ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dist[2][N];

void dickcha(int source, int ix, const vect<vect<pii>> &G){
	prq<pii, vect<pii>, greater<pii>> pq;
	
	auto &D = dist[ix];
	
	pq.push({0, source});
	memset(D, 5, sizeof D);
	D[source] = 0;
	
	while(pq.size()){
		int d, u;
		tie(d, u) = pq.top();
		pq.pop();
		
		if(d > D[u]) continue;
		
		for(auto [v, w] : G[u]){
			if(d + w < D[v]){
				D[v] = d + w;
				pq.push({d + w, v});
			}
		}
	}
}

void sol(){
	int n, m;
	cin >> n >> m;
	
	vect<vect<pii>> G(n+5), R(n+5);
	vect<vect<int>> edges, SG(n+5);
	
	for(int i=0; m>i; i++){
		int u, v, w;
		cin >> u >> v >> w;
		G[u].eb(v, w);
		R[v].eb(u, w);
		edges.pb({u, v, w});
	}
	
	dickcha(1, 0, G);
	dickcha(n, 1, R);
	
	vect<int> topo, in(n+5);
	queue<int> que;
	
	for(auto e : edges){
		int u = e[0], v = e[1], w = e[2];
		
		if(dist[0][u] + w + dist[1][v] == dist[0][n]) SG[u].eb(v), in[v]++;
	}
	
	for(int i=1; n>=i; i++) if(in[i] == 0) que.push(i);
	
	while(que.size()){
		int u = que.front();
		que.pop();
		topo.eb(u);
		
		for(int v : SG[u]) if(--in[v] == 0) que.push(v);
	}
	
	vect<int> mn(n+5, 1e9), mx(n+5), cnt(n+5);
	
	mn[1] = mx[1] = cnt[1] = 1;
	
	
	for(int u : topo){
		for(int v : SG[u]){
			mn[v] = min(mn[v], mn[u] + 1);
			mx[v] = max(mx[v], mx[u] + 1);
			cnt[v] = (cnt[v] + cnt[u]) % MOD;
		}
	}
	
	cout << dist[0][n] << ' ' << cnt[n] << ' ' << mn[n]-1 << ' ' << mx[n]-1 << el;
}

