ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dist[505][505];

void sol(){
    int n, m, q;
    cin >> n >> m >> q;
    memset(dist, 5, sizeof dist);

    for(int i=1;n>=i;i++) dist[i][i] = 0;
    for(int i=0;m>i;i++){
        int u, v, w;
        cin >> u >> v >> w;
        dist[u][v] = dist[v][u] = min(dist[u][v], w);
    }

    for(int k=1;n>=k;k++){
        for(int u=1;n>=u;u++){
            for(int v=1;n>=v;v++){
                dist[u][v] = min(dist[u][v], dist[u][k] + dist[k][v]);
            }
        }
    }

    while(q--){
        int u, v;
        cin >> u >> v;
        if(dist[u][v] < 1e12) cout << dist[u][v] << el;
        else cout << "-1\n";
    }

}

