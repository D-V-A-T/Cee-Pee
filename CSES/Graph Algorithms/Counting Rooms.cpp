ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n,m;

inline int f(int i, int j) {
    return i*m + j;
}


class DSU{
public:
    vector<int> par, len;
    DSU(const int& n){
        par.rs(n+69);
        len.rs(n+69, 1);
        iota(bend(par), 0);
    }int findpar(int u){
        if(u == par[u])
            return u;
        return u=findpar(par[u]);
    }void join(int u, int v){
        u=findpar(u);
        v=findpar(v);
        if(u != v){
            if(len[u] < len[v])swap(u, v);
            par[v]=u;
            len[u]+=len[v];
        }
    }
 }; 

void sol(){
    cin >> n >> m;
    DSU A(2e6);

    char a[n][m];
    for(auto& i : a) for(auto& j : i) cin >> j;
    for(int i=0;n>i;i++){
        for(int j=0;m>j;j++){
            if(a[i][j] == '#') continue;

            if(i && a[i-1][j] == '.') A.join(f(i, j), f(i-1, j));
            if(j && a[i][j-1] == '.') A.join(f(i, j), f(i, j-1));
            if(i+1<n && a[i+1][j] == '.') A.join(f(i,j), f(i+1, j));
            if(j+1<m && a[i][j+1] == '.') A.join(f(i,j), f(i, j+1));
        }
    }

    set<int> s;
    for(int i=0;n>i;i++) for(int j=0;m>j;j++) if(a[i][j] == '.'){
        s.insert(A.findpar(f(i,j)));
    }


    cout << s.size() << el; 
}

