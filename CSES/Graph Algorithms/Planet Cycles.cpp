    in_path[u] = 1;
    in[u] = path.size();
    path.eb(u);
    visited[u] = 1;

    if(in_path[nxt[u]]){
        for(int i=in[nxt[u]]; in[u]>=i; i++){
            cyc[path[i]] = in[u] - in[nxt[u]] + 1;
        }
    }else if(!visited[nxt[u]]) DFS(nxt[u]);

