ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}
 
int n, m;
vect<pii> G[N], R[N];
vect<ll> visited(N), curr(N, -INF), path, pos(N);
 
void DFS(int u){
	visited[u] = 1;
	
	pos[u] = path.size();
	path.eb(u);
	
	for(auto [v, w] : G[u]){
		if(curr[v] != -INF){
			int cycle_len = w + curr[u] - curr[v];
			if(cycle_len < 0) {
				cout << "YES\n";
				for(int i=pos[v]; pos[u] >= i; i++) cout << path[i] << ' ';
				cout << v;
				exit(0);
			}
		}
		
		if(!visited[v]) curr[v] = curr[u] + w, DFS(v);
	}
	
	curr[u] = -INF;
	path.pob();
}
 
 
int dist[N];
 
void sol(){
	cin >> n >> m;
	vect<int> in(n+5);
	vect<vect<int>> edges;
	memset(dist, 36, sizeof dist);
	for(int i=0; m>i; i++){
		int u, v, w;
		cin >> u >> v >> w;
		in[v] ++;
		edges.pb({u, v, w});
	}
	
	for(int _=0; n>_; _++){
		for(int i=0; m>i; i++){
			int u = edges[i][0], v = edges[i][1], w = edges[i][2];
			dist[v] = min(dist[v], dist[u] + w);
		}
	}
	
	for(int _=0; n>_; _++)
		for(int i=0; m>i; i++){
			int u = edges[i][0], v = edges[i][1], w = edges[i][2];
			if(dist[v] > dist[u] + w) G[u].eb(v, w);
			dist[v] = min(dist[v], dist[u] + w);
		}
	
	for(int i=1; n>=i; i++){
		if(G[i].empty()) continue;
		fill(bend(curr), -INF);
		curr[i] = 0;
		fill(bend(visited), 0);
		DFS(i);
	}
 
	
	cout << "NO";
 
	
}
 
