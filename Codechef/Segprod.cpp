int pm(int a,const int b=MOD){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dt[N][21], n, q, P, a[N], mask[N];

int op(int a, int b){
    return a * b % P;
}

void dnc(int l, int r, int dep){
    stack<vect<int>> st;
    st.push({0, n-1, 0});

    while(st.size()){
        auto it = st.top();
        st.pop();
        int l = it[0], r = it[1], dep = it[2];
        if(l == r) continue;

        int mid = (l+r)/2;

        dt[mid][dep] = a[mid];
        for(int i=mid-1;i>=l;i--) dt[i][dep] = op(a[i], dt[i+1][dep]);

        dt[mid+1][dep] = a[mid+1];
        for(int i=mid+2;r>=i;i++) dt[i][dep] = op(dt[i-1][dep], a[i]);

        for(int i=mid+1;r>=i;i++) mask[i] ^= 1<<dep;

        st.push({l, mid, dep+1});
        st.push({mid+1, r, dep+1});
    }
}

void sol(){
    cin >> n >> P >> q;
    for(int i=0;n>i;i++){
        cin >> a[i];
        mask[i] = mask[i+1] = 0;
    }

    dnc(0, n-1, 0);


    int b[(q/64)+ 2];
    for(int& i : b) cin >> i;
    
    int x = 0, preL, preR;
    for(int i=0;q>i;i++){
        int L, R;
        if(i % 64 == 0)  L = (b[i/64] + x) % n, R = (b[i/64 + 1] + x) % n;
        else {
            L = (preL + x) % n, 
            R = (preR + x) % n;
        }
        if(L > R) swap(L, R);


        int d = mask[L] ^ mask[R];
        if(!d) x = a[L];
        else{
            d = __builtin_ctzll(d);
            x = op(dt[L][d], dt[R][d]);
        }
        x++;
        x %= P;

        preL = L;
        preR = R;
    }

    cout << x << el;
}

