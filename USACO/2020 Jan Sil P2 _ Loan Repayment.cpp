ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m, k;

bool fumofumo(int X){
    int Y = n/X;
    int N = n, nextn = Y * X - 1;
    int /*sakamoto*/days=0;
    for(;Y>m;){
        int d = (N - nextn)/Y + !!((N - nextn) % Y);
        days += d;
        N -= d * Y;
        Y = N/X;
        nextn = Y * X - 1;
        if(N<=0 || Y <= m || days > k)break;
    }
    int d = N/m + !!(N % m);
    days += d;
    return days <= k;
}

void sol(){
    cin >> n >> k >> m;
    int l = 1, r = 1e12, ans=1;
    while(l <= r){
        int mid = (l+r)/2;
        if(fumofumo(mid)){
            ans=mid;
            l=mid+1;
        }else r=mid-1;
    }cout << ans;
}

