ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

pair<int, pii> E(string s, string t){

    int n = s.size(), m = t.size();

    string left, right, rt = t;
    reverse(bend(rt));

    int max_len = 0;
    pii pos;

    for(int f=0; n>f; f++){
        left += s[f];
        right = s.substr(f+1, n - left.size());

        string tmp = right + " " + t;

        vect<int> l1(m), r1(m), l2(m), r2(m), pi(6009);
        for(int i=1; tmp.size()>i; i++){
            int j = pi[i-1];
            while(j && tmp[j] != tmp[i]) j = pi[j-1];
            pi[i] = j + (tmp[i] == tmp[j]);

            if(i > right.size() && pi[i]){
                int p = i - right.size() - 1;
                r2[p - pi[i] + 1] = max(r2[p - pi[i] + 1], pi[i]);
                r1[p] = pi[i];


            }
        }

        tmp = left + " " + rt;
        reverse(tmp.begin(), tmp.begin()+left.size());

        // if(left.size() == right.size()) cerr << tmp << el;

        for(int i=1; tmp.size()>i; i++){
            int j = pi[i-1];
            while(j && tmp[i] != tmp[j]) j = pi[j-1];
            pi[i] = j + (tmp[i] == tmp[j]);

            if(i > left.size() && pi[i]){
                int p = i - left.size() - 1;
                l1[p - pi[i] + 1] = max(l1[p-pi[i]+1], pi[i]);
                l2[p] = pi[i];

                // if(left.size() == right.size()){
                //     cerr << p << ' ' << pi[i] << el;
                // }
            }
        }
        reverse(bend(l1));
        reverse(bend(l2));

        // cout << left << ' ' << right << el;

        for(int i=0; m>i+1; i++){

            // if(left.size() == right.size()){
            //     cout << r1[i] << ' ' << l2[i+1] << el;
            // }

            if(l1[i] + r2[i+1] > max_len){
                max_len = l1[i] + r2[i+1];
                pos = {f, i - l1[i] + 1};

                // if(max_len == 4){
                //     cout << f << el;
                // }
            }

            if(r1[i] + l2[i+1] > max_len){
                max_len = r1[i] + l2[i+1];
                pos = {f - l2[i+1] + 1, i - r1[i] + 1};

                // if(max_len == 6){
                //     cout << f << ' ' << l2[i+1] << ' ' << r1[i] << el;
                // }
            }
        }
    }

    cerr << max_len << el;

    return {max_len, pos};
}

void sol(){
    string s, t;
    cin >> s >> t;

    auto a1 = E(s, t);
    reverse(bend(s));
    auto a2 = E(s, t);

    if(a2 > a1){
        a2.se.fi = s.size() - a2.se.fi - 1 - a2.fi + 1;
        cout << a2.fi << el;
        cout << a2.se.fi << ' ' << a2.se.se;
    }else{
        cout << a1.fi << el;
        cout << a1.se.fi << ' ' << a1.se.se;
    }
}

