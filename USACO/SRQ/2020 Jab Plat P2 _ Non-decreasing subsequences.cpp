int pm(int a,const int b=MOD){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dt[50005][18][22], mask[N], a[N], n, k, q;

void dnc(int l, int r, int dep){
    if(l == r) return ;

    int mid = (l+r)/2;

    int F[22][22]; memset(F, 0, sizeof F); // F[i][j] : starts with i, ends with j
    
    F[a[mid]][a[mid]] = dt[mid][dep][a[mid]] = 1;
    for(int i=mid-1;i>=l;i--){

        for(int j=a[i];k>=j;j++){
            for(int f=a[i];j>=f;f++){
                F[a[i]][j] = (F[a[i]][j] + F[f][j]) % MOD;
            }
        }F[a[i]][a[i]]++;

        for(int j=1;k>=j;j++){
            int add = 0;
            for(int f=j;f>=1;f--) add += F[f][j];
            dt[i][dep][j] = add % MOD;
        }
        
    }

    memset(F, 0, sizeof F);
    F[a[mid+1]][a[mid+1]] = dt[mid+1][dep][a[mid+1]] = 1;
    for(int i=mid+2;r>=i;i++){

        for(int j=a[i];j>=1;j--){
            for(int f=a[i];f>=j;f--) F[j][a[i]] = (F[j][a[i]] + F[j][f]) % MOD;
        }F[a[i]][a[i]]++;


        for(int j=1;k>=j;j++){
            int add = 0;
            for(int f=j;k>=f;f++) add += F[j][f];
            dt[i][dep][j] = add % MOD;
        }
    } 

    for(int i=mid+1;r>=i;i++) mask[i] ^= 1<<dep;

    dnc(l, mid, dep+1);
    dnc(mid+1, r, dep+1);
}


void sol(){
    cin >> n >> k;
    for(int i=0;n>i;i++) cin >> a[i];
    dnc(0, n-1, 0);
    
    cin >> q;
    while(q--){
        int l, r;cin >> l >> r;l--;r--;

        
        if(l == r) cout << "2\n";
        else{
            int d = __builtin_ctz(mask[l] ^ mask[r]);
            int ans = 1;
            for(int i=1;k>=i;i++){
                for(int j=i;k>=j;j++){
                    ans = (ans + dt[l][d][i] * dt[r][d][j]) % MOD;
                }
                ans = (ans + dt[l][d][i] + dt[r][d][i]) % MOD;
            }

            cout << ans << el;
        }
    }
}

