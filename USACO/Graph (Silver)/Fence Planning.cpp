int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m, visited[N];
vect<int> G[N];
pii a[N];

void DFS(int u, int& mnX, int& mxX, int& mnY, int& mxY){
    visited[u] = 1;
    mnX = min(mnX, a[u].fi);
    mxX = max(mxX, a[u].fi);
    mnY = min(mnY, a[u].se);
    mxY = max(mxY, a[u].se);

    for(int v : G[u]){
        if(!visited[v])DFS(v, mnX, mxX, mnY, mxY);
    }
}

void sol(){
    cin >> n >> m;
    for(int i=1;n>=i;i++){
        cin >> a[i].fi >> a[i].se;
    }
    for(int i=0;m>i;i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        G[v].eb(u);
    }

    int ans = INF;

    for(int i=1;n>=i;i++){
        if(visited[i])continue;
        int mnX = 1e9, mxX = -1, mnY = 1e9, mxY = -1;
        DFS(i, mnX, mxX, mnY, mxY);

        int lx = mxX - mnX, ly = mxY - mnY;
        if(min(lx, ly) == 0) ans = min(ans, lx + ly);
        else ans = min(ans, (lx+ly) * 2);
    }

    cout << ans;

}

