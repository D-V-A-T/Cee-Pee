ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int mid, n, root[N], a[N];
vect<pii> G[N];

void DFS(int u, int par){
    root[u] = par;
    for(auto [v, w] : G[u]) if(!root[v] && w>=mid) DFS(v, par);
}

void sol(){
    int m;
    cin >> n >> m;
    for(int i=1;n>=i;i++)cin >> a[i];
    for(int i=0;m>i;i++){
        int u, v, w;
        cin >> u >> v >> w;
        G[u].eb(v, w);
        G[v].eb(u, w);
    }

    int l=1, r=1e10, ans=-1;
    while(l <= r){
        mid = (l+r)/2;

        memset(root, 0, sizeof root);
        for(int i=1;n>=i;i++){
            if(!root[i]) DFS(i, i);
        }
        bool ok = 1;
        for(int i=1;n>=i;i++) ok = min(ok, root[i] == root[a[i]]);

        if(ok){
            ans = mid;
            l = mid+1;
        }else r = mid-1;
    }
    cout << (ans==1e10 ? -1 : ans);

}

