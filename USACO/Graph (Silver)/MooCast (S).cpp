int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int visited[500], n;

struct cow{
    int x, y, p;
} a[500];

bool ok(cow a, cow b){
    int ds = sq(a.x - b.x) + sq(a.y - b.y);
    return a.p*a.p >= ds;
}


