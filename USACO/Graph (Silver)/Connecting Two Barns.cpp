int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<vect<int>> G, fumo;
int visited[N];

void DFS(int u, int S){
    fumo[S].eb(u);
    visited[u] = 1;
    for(int v : G[u]){
        if(!visited[v])DFS(v, S);
    }
} 

void sol(){
    int n, m, ans = INF;
    cin >> n >> m;
    int D[2][N];

    memset(D, 5, sizeof D);
    fill(visited, visited+n+4, 0);
    G.assign(n+5, {});
    fumo.assign(n+5, {});
    

    for(int i=0;m>i;i++){
        int u, v;cin >> u >> v;
        G[u].eb(v);
        G[v].eb(u);
    }

    DFS(1, 1);
    DFS(n, n);
    for(int i=2;n>i;i++){
        if(!visited[i])DFS(i, i);
    }

    queue<int> que;


    auto BFS = [&](int s){
        fill(visited, visited+n+4, 0);
        while(que.size()){
            int u = que.front();
            que.pop();
            if(visited[u] || u<1 || u>n)continue;

            visited[u] = 1;
            D[s][u+1] = min(D[s][u+1], D[s][u]+1);
            D[s][u-1] = min(D[s][u-1], D[s][u]+1);
            que.push(u+1);
            que.push(u-1);
        }
    };
    

    for(int i : fumo[1])que.push(i), D[0][i] = 0;
    BFS(0);

    for(int i : fumo[n])que.push(i), D[1][i] = 0;
    BFS(1);

    for(auto& i : fumo){
        if(i.empty())continue;
        int mn0 = 1e9, mn1 = 1e9;
        for(int j : i) mn0 = min(mn0, D[0][j]), mn1 = min(mn1, D[1][j]);
        ans = min(ans, sq(mn0) + sq(mn1));
    }
    
    cout << ans << el;
}   

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    cin >> t;
    while(t--)sol();
}
