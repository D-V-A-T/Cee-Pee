int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> G[N];
int X, visited[5000], del[5000];
set<int> s;


void DFS(int u){
    visited[u] = 1;
    for(int v : G[u])if(!visited[v] && !del[v])DFS(v);
}


void sol(){
    int n, m;
    cin >> n >> m;

    for(int i=0;m>i;i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        G[v].eb(u);
    }

    for(int i=1;n>=i;i++)s.insert(i);

    DFS(1);

    if(count(visited+1, visited+n+1, 1) == n)cout << "YES\n";

    for(int i=1;n>i;i++){
        cin >> X;
        s.erase(X);
        del[X] = 1;

        if(i==n-1){
            cout << "YES\n";
            continue;
        }

        memset(visited, 0, sizeof visited);
        DFS(*s.begin());
        
        if(count(visited+1, visited+n+1, 1) == n-i)cout << "YES\n";
        else cout << "NO\n";
    }
}

