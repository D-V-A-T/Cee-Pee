ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    vect<int> a(n), all(n), alr(n);
    // minimum number of blast radius that a[i] must have to engulf all 
    // a[j] (j<i and j>i) 
    for(int& i : a)cin >> i;
    sort(bend(a));
    all[0] = 0;
    for(int i=1;n>i;i++){
        int L = 0, R = 1e9;
        while(L <= R){
            int M = (L+R)/2;
            int l = 0, r=i-1, p = i;
            while(l <= r){
                int mid = (l+r)/2;
                if(a[i] - M <= a[mid]){
                    p = mid;
                    r = mid-1;
                }else l = mid+1;
            }if(p != i && all[p] <= M-1){
                all[i] = M;
                R = M - 1;
            }else L = M + 1;
        }
    }
    alr[n-1] = 0;
    for(int i=n-2;i>=0;i--){
        int L = 0, R = 1e9;
        while(L <= R){
            int M = (L+R)/2;
            int l = i+1, r=n-1, p = i;
            while(l <= r){
                int mid = (l+r)/2;
                if(a[i] + M >= a[mid]){
                    p = mid;
                    l = mid+1;
                }else r = mid-1;
            }
            if(p != i && alr[p] <= M-1){
                alr[i] = M;
                R = M - 1;
            }else L = M + 1;
        }
    }
    dbl l = 0, r = 1e9, ans = 1e9;
    auto cbd = [&](dbl mid){
        int R = mid, D = 2.0 * mid;
        for(int i=0;n>i;i++){
            int l = 0, r = i-1, p = i;
            while(l <= r){
                int mid = (l+r)/2;
                if(a[i] - a[mid] + 1 <= D + 1){
                    r=mid-1;
                    p = mid;
                }else l=mid+1;
            }if(alr[i] <= R-1 && all[p] <= R-1) return true;
        }return false;
    };

    while(r-l > 1e-2){
        dbl mid = (l+r)/2;
        if(cbd(mid)){
            ans = mid;
            r = mid;
        }else l = mid;
    }cout << fixed << setprecision(1) << ans;       
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //ffopen("angry");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
