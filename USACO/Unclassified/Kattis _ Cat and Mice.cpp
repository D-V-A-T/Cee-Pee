int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct jerry{
    dbl x, y, t;
};

int lg2(int i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}

void sol(){
    int n;
    dbl m;
    cin >> n;
    jerry a[n];
    for(auto& i : a)cin >> i.x >> i.y >> i.t;
    cin >> m;

    dbl pw[69];
    pw[0]=1;
    for(int i=1;69>i;i++)pw[i] = pw[i-1] * m;

    auto dist = [](pair<dbl, dbl> a, pair<dbl, dbl> b){
        return sqrt((a.fi - b.fi) * (a.fi - b.fi) + (a.se - b.se) * (a.se - b.se));
    };

    dbl D[n][n];
    for(int i=0;n>i;i++)for(int j=0;n>j;j++)D[i][j] = dist({a[i].x, a[i].y}, {a[j].x, a[j].y});

    
    dbl l=0, r=1e9, ans = 1e9;
    while(r-l > 1e-5){
        dbl mid = (l+r)/2;
        bool ok = 0;
        vect<vect<dbl>> dp(1<<n, vect<dbl>(n, 1e9));
        for(int mask=1;1<<n >mask;mask++){
            int cnt = popcnt(mask);

            if(cnt == 1){
                int i = lg2(mask);
                dbl time = dist({0,0}, {a[i].x, a[i].y}) / mid;
                if(time <= a[i].t){
                    if(mask == ((1<<n )- 1))ok = 1;
                    dp[mask][i] = time;
                }
            }

            for(int i=0;n>i;i++){ // start
                if(mask&(1<<i)){

                    for(int j=0;n>j;j++){// end
                        if(mask&(1<<j))continue;
                        int nmask = mask | (1<<j);
                        dbl time = D[i][j] / (mid * pw[cnt]);
                        time += dp[mask][i];

                        if(time <= a[j].t){
                            if(nmask == ((1<<n) - 1))ok = 1;
                            dp[nmask][j] = min(dp[nmask][j], time);
                        }
                    }

                }
            }
        }
        if(ok){
            ans = mid;
            r = mid;
        }else l = mid;
    }
    cout << ans;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
