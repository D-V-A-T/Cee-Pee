int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct fumo{
    int id, d, s;
    bool operator < (const fumo& o)const{
        return d < o.d;
    }
};

void sol(){
    int n, m;
    cin >> n >> m;
    set<int> s = {0, n-1};
    set<int, greater<int>> sr = {0, n-1};

    vect<pii> a;
    for(int i=0;n>i;i++){
        int d;
        cin >> d;
        if(i != 0 && i != n-1)a.eb(d, i);
    }
    sort(bend(a));

    fumo b[m];
    for(int i=0;m>i;i++){
        cin >> b[i].d >> b[i].s;
        b[i].id = i;
    }vect<int> ans(m);
    sort(b, b+m);

    int ptr = 0;
    multiset<int> mxd = {n-1};
    for(int i=0;m>i;i++){
        while(ptr<n-2 && a[ptr].fi <= b[i].d){
            auto it1 = *s.lwb(a[ptr].se), it2 = *sr.lwb(a[ptr].se);
            // cout << it1 - it2 << el;
            auto it = mxd.find(abs(it1 - it2));
            // cout << it1 << ' ' << it2 << ' ' << a[ptr].se << el;
            if(it != mxd.end()) mxd.erase(it);
            s.insert(a[ptr].se);
            sr.insert(a[ptr].se);
            mxd.insert(abs(a[ptr].se - it1));
            mxd.insert(abs(a[ptr].se - it2));
            ptr++; 
        }
        // cout << *mxd.rbegin() << el;
        ans[b[i].id] = b[i].s >= *mxd.rbegin();
    }

    for(int i : ans)cout << i << el;
    
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("snowboots");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
