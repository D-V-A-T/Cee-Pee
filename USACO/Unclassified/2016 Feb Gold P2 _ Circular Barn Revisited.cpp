ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[169][9];

void sol(){
    int n, k;
    cin >> n >> k;
    deque<int> dq;
    for(int i=0, x;n>i;i++)cin >> x, dq.pb(x);

    auto cbd = [&](){
        vect<int> a(bend(dq));
        memset(dp, 1, sizeof dp);
        dp[n][0] = 0;
        for(int i=n-1;i>=0;i--){
            for(int j=1;k>=j;j++){
                int S = 0;
                for(int f=i;n-1>=f;f++){
                    S += (f-i) * a[f];
                    dp[i][j] = min(dp[i][j], S + dp[f+1][j-1]);
                }
            }
        }return dp[0][k];
    };
    
    int ans = INF;
    for(int i=0;n>i;i++){
        ans = min(ans, cbd());
        dq.pb(dq.front());
        dq.pof();
    }
    cout << ans;

}

