int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[10001][1690];

void sol(){
    int n, M;
    cin >> n >> M;
    vect<int> a;
    a.eb(0);
    auto pc = [](int n){
        if(n==2)return true;
        for(int i=2;i*i<=n;i++)if(n%i==0)return false;
        return true;
    };

    for(int i=2;n>=i;i++)if(pc(i))a.eb(i);
    int c=0, p=1;
    for(int i=0;n>=i;i++)dp[i][0] = dp[0][i] = 1;
    for(int i=0;n>=i;i++){
        for(int j=1;a.size()>j;j++){
            int p=a[j];
            dp[i][j] = dp[i][j-1];
            while(p <= i){
                dp[i][j] = (dp[i][j] + dp[i - p][j-1] * p) % M;
                p *= a[j];
                p %= M;
            }
        }
    }
    cout << dp[n][a.size()-1];
}

