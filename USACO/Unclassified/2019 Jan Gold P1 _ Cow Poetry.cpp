int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[5001];
vect<int> fumo[5001];

void sol(){
    int n, m, k;
    cin >> n >> m >> k;

    pii a[n+1];
    for(int i=1;n>=i;i++){
        cin >> a[i].fi >> a[i].se;
        fumo[a[i].se].eb(a[i].fi);
    }
    
    dp[0] = 1;
    for(int j=0;k>=j;j++){
        for(int i=1;n>=i;i++){
            if(j < a[i].fi)continue;
            dp[j] = (dp[j] + dp[j-a[i].fi]) % MOD;
        }
    }

    int fimi = 1;
    vect<int> ways(n+5);

    for(int i=1;n>=i;i++){
        for(int j : fumo[i]){
            ways[i] = (ways[i] + dp[k - j]) % MOD;
        }
        fimi += ways[i];
    }

    /*
        121  123
        211  213
        321  323
        231  233
        312
        132
        112
        332
    */

    umap<char, int> cnt;
    while(m--){
        char c;cin >> c;
        cnt[c]++;
    }

    auto bp = [](int a, int b){
        int res = 1;
        while(b){
            if(b&1)res = (res*a)%MOD;
            a=(a*a) % MOD;
            b /= 2;
        }return res;
    };

    vect<int> F(200, 1);
    for(char i='A';'Z'>=i;i++){
        int f=0;
        for(int j=1;n>=j && cnt[i];j++)f = (f+bp(ways[j], cnt[i])) % MOD;
        f=max(f, 1ll);
        F[i] = (F[i-1] * f) % MOD;
    }
    cout << F['Z'];

}

