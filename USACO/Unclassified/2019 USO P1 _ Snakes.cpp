ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[469][469][2], zip[1000005], unzip[469];
// dp[i][j][0] : answer when first i elements with j changes and the last change is a[i]
// dp[i][j][1] : __________________________________________________________________ NOT a[i]

void sol(){
    int n, k;
    cin >> n >> k;
    int a[n+1], num=1;
    for(int i=1;n>=i;i++){
        cin >> a[i];
        if(!zip[a[i]]){
            zip[a[i]] = num;
            unzip[num++] = a[i];
        }
    }

    memset(dp, 5, sizeof dp);
    for(int i=0;469>i;i++)dp[0][i][0] = dp[0][i][1] = 0;

    for(int i=1;n>=i;i++){
        for(int j=0;k>=j;j++){
            int S = 0, ok = 1;
            for(int p=i;p>=1;p--){
                if(a[i] < a[p]){
                    ok = 0;
                    break;
                }
                S += a[i] - a[p];
                if(j)
                    dp[i][j][0] = min(dp[i][j][0], min(dp[p-1][j-1][1], dp[p-1][j-1][0]) + S);
            }
            if(ok) dp[i][j][0] = min(dp[i][j][0], S);
            
            S = 0;
            for(int p=i+1;n>=p;p++){
                if(a[i] < a[p])break;
                S += a[i] - a[p];
                int x = j ? dp[i][j-1][1] + S : INF;
                dp[p][j][1] = min({dp[p][j][1], dp[i][j][0] + S, x});
            }
        }
        
    }
    cout << min(dp[n][k][0], dp[n][k][1]);
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //ffopen("snakes");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
