ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}
 
int P2[N];
 
int CC(int n, int k){
    return P2[n] - P2[n-k] - P2[k] > 0;
}
 
int PATH(int si, int sj, int ei, int ej){
    int ver = abs(si - ei), hori = abs(sj - ej) - ver;
 
 
    return !CC(ver + hori, ver);
}
 
