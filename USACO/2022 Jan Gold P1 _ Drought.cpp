ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[2][1005][1005], pfs[1005][1005];
// dp[i][j][k] : the number of ways so that the element i is at least j 
//               and all the cows have their hunger value equal to k

void sol(){
    int n;
    cin >> n;
    int a[n];

    auto get = [](int v, int l, int r){
        if(l > r)return 0;
        return pfs[v][r] - (l ? pfs[v][l-1] : 0);
    };
    
    for(int i=0;n>i;i++){
        cin >> a[i];
        memset(dp[i%2], 0, sizeof dp[i%2]);
        if(i){
            for(int j=0;a[i]>=j;j++){
                for(int v=0;j >= v;v++){
                    int de = j - v;
                    if(a[i-1] - de >= 0) dp[i%2][j][v] = (dp[i%2][j][v] + pfs[v][a[i-1] - de]) % MOD;
                }
            }            
        }else for(int j=0;a[i]>=j;j++)dp[0][j][j] = 1;

        for(int v=0;a[i]>=v;v++){
            for(int j=0;a[i]>=j;j++){
                pfs[v][j] = (j ? pfs[v][j-1] + dp[i%2][j][v] : dp[i%2][j][v]) % MOD;
            }
        }
    }

    int ans = 0;
    if(n&1) for(int i=0;a[n-1]>=i;i++)for(int j=0;a[n-1]>=j;j++)ans = (ans + dp[(n+1)%2][i][j]) % MOD;
    else for(int j=0;a[n-1]>=j;j++)ans = (ans + dp[(n+1)%2][j][0]) % MOD;
    cout << (ans + MOD) % MOD;

}

