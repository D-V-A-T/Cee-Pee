ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> a, nimber(N, -1);

int F(int x){
    if(nimber[x] != -1) return nimber[x];

    unordered_set<int> sus;

    for(int i : a){
        if(x - i >= 0) sus.insert(F(x - i));
    }

    for(int i=0; sus.size()>=i; i++){
        if(sus.find(i) == sus.end()) return nimber[x] = i;
    }

    return -69;
}

