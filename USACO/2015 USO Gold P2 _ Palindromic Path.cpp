ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[2][505][505];
// dp[step][i1][i2]
// j1 = step - (i1-1) + 1
// j2 = n - (step - (n - i2))

void sol(){
    int n;
    cin >> n;
    char a[n+1][n+1];
    for(int i=1;n>=i;i++)for(int j=1;n>=j;j++)cin >> a[i][j];

    auto touching = [](pii x, pii y){
        return (y.fi-x.fi <= 1 && y.fi - x.fi >= 0 && y.se == x.se) ||
        (y.fi == x.fi && y.se-x.se<=1 && y.se-x.se>=0);
    };

    int c=0, p=1, ans = 0;
    dp[0][1][n] = a[1][1] == a[n][n];
    for(int k=1;n>=k;k++){
        swap(c,p);
        memset(dp[c], 0, sizeof dp[c]);
        for(int i1=1;n>=i1;i1++){
            for(int i2=n;i2>=1;i2--){
                int j1 = k - i1 + 2;
                int j2 = 2*n - i2 - k;
                
                if(a[i1][j1] != a[i2][j2])continue;
                dp[c][i1][i2] += dp[p][i1][i2];
                dp[c][i1][i2] += dp[p][i1-1][i2];
                dp[c][i1][i2] += dp[p][i1][i2+1];
                dp[c][i1][i2] += dp[p][i1-1][i2+1];
                dp[c][i1][i2] %= MOD;

                if(touching({i1, j1}, {i2, j2}))
                    ans = (ans + dp[c][i1][i2]) % MOD;
            }
        }
    }
    cout << ans;

}

