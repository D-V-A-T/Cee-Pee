const ll N = 3e5 + 1, MOD = 1e9+7, INF = 1000000000000000069;

mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, sz[N], del[N];
char ans[N];
vect<int> G[N];

void prep(int u, int par){
    sz[u] = 1;
    for(int v : G[u]) if(v != par && !del[v]){
        prep(v, u);
        sz[u] += sz[v];
    }
}

int find_centroid(int u, int par, int root_size){
    for(int v : G[u]) if(v != par && !del[v]){
        if(sz[v] * 2 > root_size) return find_centroid(v, u, root_size);
    }

    return u;   
}

void solve(int u, char c){
    prep(u, 0);
    u = find_centroid(u, 0, sz[u]);
    ans[u] = c;
    del[u] = 1;

    for(int v : G[u]) if(!del[v]) solve(v, c+1);
}

void sol(){
    cin >> n;
    for(int i=1; n>i; i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        G[v].eb(u);
    }

    solve(1, 'A');
    for(int i=1; n>=i; i++) cout << ans[i] << ' ';
}

