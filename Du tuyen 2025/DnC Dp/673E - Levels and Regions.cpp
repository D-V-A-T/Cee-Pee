ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, k;
    cin >> n >> k;

    dbl pfs[n+5], cac[n+5], sus[n+5], a[n+5];
    pfs[0] = cac[0] = sus[0] = 0;

    for(int i=1; n>=i; i++){
        cin >> a[i];
        pfs[i] = pfs[i-1] + a[i];
        cac[i] = cac[i-1] + 1.0l / a[i];
        sus[i] = sus[i-1] + pfs[i] / a[i];
    }

    auto E = [&](int l, int r){
        return l > r ? 0 : sus[r] - sus[l-1] - pfs[l-1] * (cac[r] - cac[l-1]);
    };

    dbl dp[k+5][n+5];
    memset(dp, 5, sizeof dp);
    dp[0][0] = 0;

    for(int j=1; k>=j; j++){
        dp[j][0] = 0;
        deque<array<int, 3>> dq = {{1, n, 0}};
        for(int i=1; n>=i; i++){
            while(dq.size() && dq.front()[1] < i) dq.pof();

            int opt = dq.front()[2];

            dp[j][i] = dp[j-1][opt] + E(opt+1, i);

            while(dq.size()){
                int l = dq.back()[0], sus = dq.back()[2];

                if(dp[j-1][i] + E(i+1, l) < dp[j-1][sus] + E(sus+1, l)){
                    dq.pob();
                    continue;
                }

                int lo = l, hi = dq.back()[1], split = -1;

                // assert(lo <= hi);

                while(lo <= hi){
                    int mid = (lo + hi) / 2;

                    if(dp[j-1][i] + E(i+1, mid) < dp[j-1][sus] + E(sus+1, mid)){
                        hi = mid - 1;
                    }else{
                        split = mid;
                        lo = mid + 1;
                    }
                }

                dq.back()[1] = split;
                if(split+1 <= n){
                    dq.pb({split+1, n, i});
                }
                break;
            }
        }
    }

    cout << fixed << setprecision(6) << dp[k][n];   
}

