int NIM(string state){

    if(cac.count(state)) return cac[state];

    unordered_set<int> us;

    for(int i=1; state.size()>i+1; i++){
        if(state[i] == '0'){
            if(state[i-1] != '1' && state[i+1] != '1'){
                state[i] = '1';
                us.insert(NIM(state));
                state[i] = '0';
            }

            if(state[i-1] != '2' && state[i+1] != '2'){
                state[i] = '2';
                us.insert(NIM(state));
                state[i] = '0';
            }
        }
    }

    if(us.empty()) return cac[state] = 0;

    for(int i=0; us.size()>=i; i++){
        if(us.find(i) == us.end()) return cac[state] = i;
    }

    return -1;
}

