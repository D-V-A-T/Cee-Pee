int F(int state){
    if(popcnt(state) == 0) return 0;
    if(mp.count(state)) return mp[state];
    vect<bool> seen(32);

    int sus = 0;

    for(int i=0; 30>i; i++){
        int x = (state >> (i+1)) | sus; 
        if(state == x) continue;

        // cout << state << ' ' << x << ' ' << F(x)  << el;

        seen[F(x)] = 1;

        sus |= getbit(state, i) * (1<<i);
    }

    for(int i=0; 32>i; i++) if(!seen[i]) return mp[state] = i;
    return -1;
}

