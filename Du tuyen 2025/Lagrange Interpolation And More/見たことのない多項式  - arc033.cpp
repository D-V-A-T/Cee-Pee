ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int B(int a, int b=MOD-2){
    int res = 1;
    a %= MOD;
    while(b){
        if(b&1) res = res * a % MOD;
        a = a * a % MOD;
        b >>= 1;
    }
    return res;
}

struct Lagrange{

int l, r, len;
vect<int> I, F, E;

Lagrange(vect<int> E, int l, int r){
    this -> l = l;
    this -> r = r;
    len = r - l + 1;
    this -> E = E;

    F.rs(N);
    I.rs(N);

    F[0] = 1;
    I[0] = 1;
    for(int i=1; N>i; i++) F[i] = F[i-1] * i % MOD, I[i] = B(F[i]);
}

int f(int x){

    if(l <= x && x <= r) return E[x - l];

    vect<int> pref(len+5), suff(len+5);

    pref[0] = x - l;
    for(int i=1; len>i; i++) pref[i] = (x - (i+l)) * pref[i-1] % MOD;

    suff[len-1] = x - r;
    for(int i=len-2; i>=0; i--) suff[i] = (x - (i+l)) * suff[i+1] % MOD;

    int res = 0;

    for(int i=l; r>=i; i++){
        int nu = 1, de = 1, j = i - l;

        de = F[i-l] * F[r-i] * ((r-i)&1 ? -1 : 1) % MOD;
        de %= MOD;

        if(i > l) nu = (nu * pref[j-1]) % MOD;
        if(i < r) nu = (nu * suff[j+1]) % MOD;

        res += nu * B(de) % MOD * E[j] % MOD;
        res %= MOD;
    }

    return pm(res);
}

};

