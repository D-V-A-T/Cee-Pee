vect<int> G[2002];
bool adj[2000][2000];
int sus[2002];
set<int> bitverts;

void findbits(int u, int prev, int bit){
	sus[u] = bit;
	for(int v : G[u]) if(bitverts.count(v) && v != prev) findbits(v, u, bit+1);
}

void Bob(int n, int m, int *c, int *d){
	vect<int> deg(n+5);

	for(int i=0; m>i; i++){
		adj[c[i]][d[i]] = adj[d[i]][c[i]] = 1;
		deg[c[i]]++;
		deg[d[i]]++;
		G[c[i]].eb(d[i]);
		G[d[i]].eb(c[i]);
	}

	int MARK1, MARK2, mxdeg = 0;

	for(int i=0; n>i; i++) {
		if(deg[i] > mxdeg){
			MARK1 = i;
			mxdeg = deg[i];
		}
	}

	for(int i=0; n>i; i++) if(!adj[MARK1][i] && i != MARK1) MARK2 = i;

	for(int i=0; n>i; i++) if(adj[MARK2][i]) bitverts.insert(i);

	// cerr << MARK1 << ' ' << MARK2 << el;
	// for(int i : bitverts) cerr << i << ' ';
	// deb;

	pii bit0 = {-1, -1};
	for(int i : bitverts){
		int adjbits = 0;
		for(int j : G[i]) adjbits += bitverts.count(j);

		if(adjbits == 1) bit0 = max(bit0, make_pair(deg[i], i));
	}
	cerr << bit0.se << el;
	findbits(bit0.se, -1, 0);

	auto goodboy = [&](int i){
		return i != MARK1 && i != MARK2 && !bitverts.count(i);
	};


	for(int i=0; n>i; i++){
		if(!goodboy(i)) continue;


		int val = 0;
		for(int b : bitverts){
			if(adj[i][b]) val |= 1<<sus[b];
		}

		cerr << i << ' ' << val << el;
		sus[i] = val;
	}

	vect<pii> edges;
	for(int i=0; n>i; i++){
		if(!goodboy(i)) continue;
		for(int j : G[i]) if(goodboy(j)){
			edges.eb(sus[i], sus[j]);
		}
	}
        	for(pii &e : edges){
		if(e.fi > e.se) swap(e.fi, e.se);
	}
	sort(bend(edges));
	edges.rs(unique(bend(edges)) - edges.begin());

	InitMap(n-12, edges.size());


	for(pii e : edges){
		MakeMap(e.fi, e.se);
	}
}




