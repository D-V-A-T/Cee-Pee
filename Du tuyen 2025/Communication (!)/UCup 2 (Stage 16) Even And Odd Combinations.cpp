int C(int n, int k){
    if(n < k || min(n, k) < 0) return 0;
    // cerr << n << ' ' << k << el;
    return CAC[n][k];
}

int cnt_smaller(int n, int k, vect<int> a){

    if(k == 0) return 0;

    int sz = k;

    int smaller = 0, lst = 0;

    // cerr << sz << el;

    for(int i=0; sz>i; i++){
        if(!a[i]) continue;
        // if(k % 2 == 0) cerr << sz-i << ' ' << C(n - lst, sz-i) - C(n-a[i], sz-i) << el;

        int add = C(n - lst, sz-i) - C(n-a[i]+1, sz-i);
        if(C(n - a[i]+1, sz-i) == 0) return INF;
        smaller += add;

        lst = a[i];
    }

    for(int j=k&1; k>j; j+=2) smaller += C(n, j);

    return smaller;
}

