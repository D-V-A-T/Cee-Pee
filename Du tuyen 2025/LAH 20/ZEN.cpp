mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int a[2005][2005], n, m;
bool seen[2005][2005];

bool gay(int i, int j){
    if(i<1 || j<1 || i>n || j>m) return false;
    return a[i][j] + a[i+1][j] + a[i][j+1] + a[i+1][j+1] == 1;
}

void sol(){ 
    cin >> n >> m;

    for(auto &i : a) for(auto &j : i) j = 1;

    for(int i=1; n>=i; i++){
        for(int j=1; m>=j; j++){
            char c; cin >> c;
            a[i][j] = c == '#';
        }
    }

    vect<pii> que;

    for(int i=1; n>=i+1; i++){
        for(int j=1; m>=j+1; j++){
            if(gay(i, j)){
                que.pb({i, j});
            }
        }
    }

    while(que.size()){
        int i, j;
        tie(i, j) = que.back();
        que.pob();

        a[i][j] = a[i+1][j] = a[i][j+1] = a[i+1][j+1] = 0;

        if(seen[i][j]) continue;
        seen[i][j] = 1;

        for(int x=-2; 2>=x; x++){
            for(int y=-2; 2>=y; y++){
                if(gay(i+x, j+y) && !seen[i+x][j+y]){
                    que.pb({i+x, j+y});
                }
            }        
        }

    }

    for(int i=1; n>=i; i++) {
        for(int j=1; m>=j; j++) cout << (a[i][j] ? '#' : '.');
        nextl;
    }

}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
