const ll N = 3e5 + 10, MOD = 1e9+7, INF = 1000000000000000069;

mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, q, up[N][30], spt[N][20], dep[N];
vect<int> G[N], letter[N];
string s;

void dfs(int u, int par){
    spt[u][0] = par;

    letter[s[u] - 'a'].eb(u);

    for(int i=0; 26>i; i++) if(letter[i].size())
        up[u][i] = letter[i].back();

    for(int v : G[u]) if(v != par){
        dep[v] = dep[u] + 1;
        dfs(v, u);
    }

    letter[s[u] - 'a'].pob();
}

int LCA(int u, int v){
    if(dep[u] < dep[v]) swap(u, v);

    int dih = dep[u] - dep[v];
    for(int b=0; 20>b; b++) if(dih>>b&1) u = spt[u][b];

    if(u == v) return u;

    for(int b=19; b>=0; b--){
        if(spt[u][b] != spt[v][b]) u = spt[u][b], v = spt[v][b];
    }

    return spt[u][0];
}

void sol(){
    cin >> n >> q;

    cin >> s;
    s = " " + s;

    for(int i=1; n>i; i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        G[v].eb(u);
    }

    dep[1] = 1;
    dfs(1, 0);

    for(int b=1; 20>b; b++){
        for(int i=1; n>=i; i++){
            spt[i][b] = spt[spt[i][b-1]][b-1];
        }
    }

    while(q--){
        int u, v;
        cin >> u >> v;
        string t;
        cin >> t;
        deque<char> dq(bend(t));

        int L = LCA(u, v);

        int usedL = 0;

        while(dq.size() && dep[up[u][dq.front()-'a']] >= dep[L]){
            if(up[u][dq.front()-'a'] == L) usedL = 1;
            u = spt[up[u][dq.front()-'a']][0];
            dq.pof();
        }
        while(dq.size() && dep[up[v][dq.back()-'a']] >= dep[L]+usedL){
            v = spt[up[v][dq.back()-'a']][0];
            dq.pob();
        }

        if(dq.empty()) cout << "YES\n";
        else cout << "NO\n";
    }
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
