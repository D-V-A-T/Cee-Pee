ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}
 
int n, m, in[N], low[N], visited[N], par[N], child[N];
vect<int> G[N], order, stk;
 
void dfs(int u){
    visited[u] = 1;
    low[u] = in[u] = order.size();
    order.eb(u);
    stk.eb(u);
 
    for(int v : G[u]){
        if(!visited[v]){
            dfs(v);
            low[u] = min(low[u], low[v]);
        }else if(!par[v]){
            low[u] = min(low[u], in[v]);
        }
    }
 
    if(low[u] == in[u]){
        while(true){
            int cac = stk.back();
            stk.pob();
            child[u]++;
            par[cac] = u;
            if(cac == u) break;
        }
    }
}
 
void sol(){
    cin >> n >> m;
 
    vect<pii> edges;
 
    for(int i=0; m>i; i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        edges.eb(u, v);
    }
 
    for(int i=1; n>=i; i++){
        if(!visited[i]) dfs(i);
    }
 
    map<int, int> sus;
    set<pii> st;
 
    for(int i=1; n>=i; i++) sus[par[i]]++;
 
    int ans = 0;
    for(auto i : sus) if(i.se > 1) ans += i.se;
 
    vect<int> tmp[N], r[N], indeg(n+5);
 
    for(auto i : edges){
        int u = par[i.fi], v = par[i.se];
        if(u != v){
            tmp[u].eb(v);
            tmp[v].eb(u);
        }
    }
 
    memset(visited, 0, sizeof visited);
    function<pii(int)> cntcc = [&](int u){
        visited[u] = 1;

        pii res = {child[u] > 1, child[u]};
        for(int v : tmp[u]) if(!visited[v]){
            pii p = cntcc(v);
            res.fi |= p.fi;
            res.se += p.se;
        }
        return res;
    };
 
    ans = 0;
    for(int i=1; n>=i; i++) if(par[i] == i){
 
        if(!visited[i]){
            pii p = cntcc(i);
            if(p.fi){
                ans += p.se;
            }else{
                ans += p.se-1;
            }
        }
    }
 
    cout << ans << el;
}
 
