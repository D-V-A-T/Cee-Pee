ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct DPDigit{
	int targ, x[20], dp[20][2][2][20][20];
	int a1, a2, dipi[20][2][2][20][20][20];

	int G(int idx, int smol, int st, int len, int c1, int c2){
		if(idx == -1) return c1 == c2 && c1+c2 == len && st;

		int &memo = dipi[idx][smol][st][len][c1][c2];

		if(memo != -1) return memo;

		memo = 0;

		int lim = smol ? 9 : x[idx];

		for(int d=0; lim >= d; d++){
			if((d == a1 || d == a2 || d == 0)){

				int nst = (d!=0)||st;

				memo += G(idx-1, smol || (d<x[idx]), nst, len+nst,
						c1+(d==a1)*nst, c2+(d==a2)*nst);

			} 
		}

		return memo;
	}

	int F(int idx, int smol, int st, int len, int cnt){
		if(idx == -1) return cnt >= (len+1)/2 && st;

		int &memo = dp[idx][smol][st][len][cnt];

		if(memo != -1) return memo;

		memo = 0;

		int lim = smol ? 9 : x[idx];

		for(int d=0; lim>=d; d++){
			int nst = (d!=0)||st;
			memo += F(idx-1, smol || (d<x[idx]), nst, len+nst, cnt+(d==targ)*nst);
		}

		return memo;
	}

	int query(int n){
		memset(x, 0, sizeof x);
		int i = 0;
		while(n){
			x[i++] = n%10;
			n /= 10;
		}

		int res = 0;

		for(targ=0; 9>=targ; targ++){
			memset(dp, -1, sizeof dp);
			res += F(i-1, 0, 0, 0, 0);

			// cout << targ << ' ' << F(i-1, 0, 0, 0) << el;	
		}

		for(a1=0; 9>=a1; a1++){
			for(a2=a1+1; 9>=a2; a2++){
				memset(dipi, -1, sizeof dipi);
				res -= G(i-1, 0, 0, 0, 0, 0);
			}
		}

		return res;
	}
} dpd;

void sol(){
	int l, r;
	cin >> l >> r;

	cout << dpd.query(r) - dpd.query(l-1);
}

