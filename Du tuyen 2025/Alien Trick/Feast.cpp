ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, k;
    cin >> n >> k;

    vect<int> a(n);
    for(int i=0; n>i; i++) cin >> a[i];

    auto E = [&](int cac){
        int dp[n][2], cnt[n][2];

        dp[0][0] = cnt[0][0] = 0;
        dp[0][1] = a[0]-cac, cnt[0][1] = 1;

        for(int i=1; n>i; i++){
            tie(dp[i][0], cnt[i][0]) = max(pii(dp[i-1][0], cnt[i-1][0]),
                                            pii(dp[i-1][1], cnt[i-1][1]));

            tie(dp[i][1], cnt[i][1]) = max(pii(dp[i-1][0]-cac+a[i], cnt[i-1][0]+1),
                                            pii(dp[i-1][1]+a[i], cnt[i-1][1]));
        }

        return max(pii(dp[n-1][0], cnt[n-1][0]), pii(dp[n-1][1], cnt[n-1][1]));
    };

    int l = 0, r = 1e15, opt = 0;

    while(l <= r){
        int mid = (l + r) / 2;

        auto res = E(mid);

        if(res.se >= k){
            opt = mid;
            l = mid + 1;
        }else r = mid - 1;
    }

    cout << E(opt).fi + opt * k << el;
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
