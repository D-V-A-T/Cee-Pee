ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n,k,l;
string s;

pii U(int cac){
    pii dp[n+1];
    for(auto &i : dp) i = {INF, INF};
    dp[0].fi = dp[0].se = 0;

    for(int i=1; n>=i; i++){
        dp[i] = dp[i-1];
        dp[i].fi += s[i] < 'a';

        if(i < l && dp[i].fi > cac){
            dp[i] = {cac, 1};
        }

        if(i-l >= 0 && dp[i].fi > dp[i-l].fi + cac){
            dp[i].fi = dp[i-l].fi + cac;
            dp[i].se = dp[i-l].se + 1;
        }
    }

    return dp[n];
}

pii L(int cac){
    pii dp[n+1];    
    for(auto &i : dp) i = {INF, INF};
    dp[0].fi = dp[0].se = 0;

    for(int i=1; n>=i; i++){
        dp[i] = dp[i-1];
        dp[i].fi += s[i] >= 'a';

        if(i-l >= 0 && dp[i].fi > dp[i-l].fi + cac){
            dp[i].fi = dp[i-l].fi + cac;
            dp[i].se = dp[i-l].se + 1;
        }
    }

    return dp[n];
}

void sol(){
    cin >> n >> k >> l >> s;

    s = " " + s;

    int lo = 0, hi = 1e15, optu = 0, optl = 0;
    while(lo <= hi){
        int mid = (lo + hi) / 2;

        if(U(mid).se <= k){
            optu = mid;
            hi = mid - 1;
        }else lo = mid + 1;
    }

    lo = 0, hi = 1e15;
    while(lo <= hi){
        int mid = (lo + hi) / 2;


        if(L(mid).se <= k){
            optl = mid;
            hi = mid - 1;
        }else lo = mid + 1;
    }

    cout << min(U(optu).fi - optu * k,
                L(optl).fi - optl * k);

    // cout << U(optu).fi - optu*k << ' ' << L(optl).fi - optl * k;
}

