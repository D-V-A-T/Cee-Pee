ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    ll n, k;
    cin >> n >> k;

    dbl a[n];

    for(dbl &i : a) cin >> i;

    auto E = [&](dbl cac){
        dbl sus = 0;
        ll cnt = 0;
        for(const auto &i : a){
            // cac * x + i / x 

            ll x = sqrtl(i / cac);

            pair<dbl, ll> opt = {1e18, 1e18};

            for(int j=-1; 1>=j; j++){
                if(x+j <= 0) continue;
                opt = min(opt, {(dbl)cac * (x+j) + (dbl)i/(x+j), x+j});
            }

            sus += opt.fi;
            cnt += opt.se;
        }

        return make_pair(sus, cnt);
    };

    dbl l = 0, r = 1e16, opt = 0;

    while(r-l > 1e-9){
        dbl mid = (l + r) / 2.0l;

        if(E(mid).se >= k){
            opt = mid;
            l = mid;
        }else r = mid;
    }

    cout << (ll)round(E(opt).fi - opt * k + 1e-5);
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
