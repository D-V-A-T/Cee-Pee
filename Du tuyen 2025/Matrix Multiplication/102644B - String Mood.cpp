ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct Matrix{
    vect<vect<ll>> mat;

    int n, m;

    Matrix(int n, int m){
        this->n = n;
        this->m = m;

        mat.rs(n, vect<ll>(m));

    }

    void make_identity(){
        assert(n == m);
        for(int i=0; n>i; i++) mat[i][i] = 1;
    }

    auto & operator [] (int id){
        return mat[id];
    }

    const auto & operator [] (int id) const{
        return mat[id];
    }

    Matrix operator * (const Matrix& o) const{
        auto &a = mat;
        auto &b = o.mat;
        assert(a[0].size() == b.size());
        int x = a.size(), y = b.size(), z = b[0].size();

        Matrix res(x, z);
        auto &c = res.mat;

        for(size_t i=0; x>i; i++){
            for(size_t j=0; y>j; j++){
                for(size_t k=0; z>k; k++){
                    c[i][k] = (c[i][k] + a[i][j] * b[j][k]) % MOD;
                }
            }
        }

        return res;
    }

    Matrix operator ^ (int P) const{
        Matrix res(n, m), base(n, m);

        base.mat = mat;
        res.make_identity();

        while(P){
            if(P&1) res = res * base;

            base = base * base;

            P >>= 1;
        }

        return res;
    }

    void print(){
        for(int i=0; n>i; i++){
            for(int j=0; m>j; j++){
                cout << mat[i][j] << " \n"[j ==m-1];
            }
        }
    }
};

