ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

#define point pair<dbl, dbl>

dbl SUS(dbl r, dbl l){
    dbl cc = sqrtl(r * r - l*l/4);
    dbl h = r - cc;
    return h * l / 2.0l;
}

dbl dist(point a, point b){
    return sqrtl(sq(a.se - b.se) + sq(a.fi - b.fi));
}

dbl triS(dbl x, dbl y, dbl z){
    dbl p = (x + y + z) / 2.0l;

    return sqrtl((p - x) * (p - y) * (p - z) * p);
}

dbl E(dbl r, dbl l, int dep = 0){

   

    if(dep == 1e5){
        return 0;
    }

    dbl cc = sqrtl(r*r - l*l/4);
    dbl c = r - cc;

    dbl res = E(r, sqrtl(c*c + l*l/4), dep+1) ;

    // if(dep >= 1e5 - 2)cerr << r << ' ' << dep << ' ' << sqrtl(c*c + l*l/4) << ' ' << SUS(r, sqrtl(c*c + l*l/4)) << ' ' << res << el;

    return SUS(r, l) + (res * 2);
}

dbl CS(dbl r){
    return r * r * acosl(-1);
}

