ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct Line{
    int a = 0, b= -1e18;
    int val(int x){return a*x + b;};
};

struct Node{
    Line f;
    int l, r;
    Node *left = nullptr, *right = nullptr;

    Node(int l_, int r_){
        l = l_;
        r = r_;
    }

    void extend(){
        if(!left && l < r){
            int mid = (l+r) / 2;
            left = new Node(l, mid);
            right = new Node(mid+1, r);
        }
    }   

    void push(Line g){
        extend();
        int mid = (l + r) / 2;

        if(f.val(mid) < g.val(mid)) swap(g, f);

        if(l == r) return;

        if(g.val(l) > f.val(l)) left->push(g);
        if(g.val(r) > f.val(r)) right->push(g); 
    }

    int get(int x){
        extend();
        if(l == r) return f.val(x);        
        if(x <= left->r) return max(f.val(x), left->get(x));
        else return max(f.val(x), right->get(x));
    }
};

struct LichaoTree{
    Node *root = nullptr;
    int L, R;

    LichaoTree(int L_, int R_){
        L = L_;
        R = R_;
        root = new Node(L, R);
    }

    void add_line(Line g){
        root->push(g);
    }

    int get(int x){
        return root->get(x);
    }
};  

