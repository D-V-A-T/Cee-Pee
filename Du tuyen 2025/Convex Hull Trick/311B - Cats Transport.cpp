ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct Line{
    int a, b;
    int val(int x){return a*x+b;}
};

bool intersex(Line a, Line b, Line c){
    return (dbl)(c.b - a.b) / (a.a - c.a) > (dbl)(c.b - b.b) / (b.a - c.a);
}

void sol(){
    int n, m, p;
    cin >> n >> m >> p;

    int a[n+5], d[n+5];

    a[1] = 0;
    for(int i=2; n>=i; i++){
        cin >> d[i];
        a[i] = a[i-1] + d[i];
    }

    pii cats[m+5];
    // map<int, vect<pii>> mp;
    for(int i=1; m>=i;i ++){
        cin >> cats[i].fi >> cats[i].se;
        // cout << cats[i].se - a[cats[i].fi] << el;
        // mp[a[cats[i].fi] + cats[i].se].eb(i);
    }

    // exit(0);

    sort(cats+1, cats+m+1, [&](pii x, pii y){
        return x.se - a[x.fi] < y.se - a[y.fi];
    });
    
    deque<Line> cht[p+1];

    for(int i=0; p>=i; i++) cht[i].pb({0, 0});

    int dp[m+5][p+5];
    memset(dp, 5, sizeof dp);

    vect<int> pfs(m+5);
    for(int i=1; m>=i; i++){   

        int st = cats[i].se - a[cats[i].fi];

        pfs[i] = pfs[i-1] + st;

        // cout << st << ' ' << pfs[i] << el;

        // st * (j - i) - (pfs[j] - pfs[i])


        for(int j=p; j>0; j--){
            auto &h = cht[j-1];

            while(h.size()>=2 &&
                h[0].val(st) > h[1].val(st)) h.pof();

            dp[i][j] = h[0].val(st) + st * i - pfs[i];


            auto &g = cht[j];

            Line curr = {-i, pfs[i] + dp[i][j]};

            while(g.size()>= 2){
                int sz = g.size();
                if(intersex(g[sz-2], g[sz-1], curr)) g.pob();
                else break;
            }

            g.pb(curr);

        }
    }

    cout << dp[m][p] << el;
}

