ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m, k, W, spec[N];
vect<pii> G[N];

bool comp(const array<int, 3> &a, const array<int, 3> &b){
	return a[2] != b[2] ? a[2] < b[2] : ((spec[a[0]] ^ spec[a[1]]) < (spec[b[0]] ^ spec[b[1]]));
}

bool comp2(const array<int, 3> &a, const array<int, 3> &b){
	return a[2] != b[2] ? a[2] < b[2] : (spec[a[0]] ^ spec[a[1]] > spec[b[0]] ^ spec[b[1]]);
}

struct DSU{
	int par[N], len[N];

	DSU(){
		iota(par, par+N, 0);
		fill(len, len+N, 1);
	}
	
	int findpar(int u){
		return par[u] == u ? u : par[u] = findpar(par[u]);
	}
	
	void join(int u, int v){
		u = findpar(u), v = findpar(v);
		if(u != v){
			if(len[u] < len[v]) swap(u, v);
			len[u] += len[v];
			par[v] = u;
		}
	}
};

void sol(){
	cin >> n >> m >> k >> W;
	
	for(int i=0; k>i; i++){
		int u; cin >> u;
		spec[u] = 1;
	}
		
	vect<array<int, 3>> edges;
	
	for(int i=0; m>i; i++){
		int u, v, w;
		cin >> u >> v >> w;
		G[u].eb(v, w);
		G[v].eb(u, w);
		edges.pb({u, v, w});
	}
	
	auto E = [&](int cac){
		for(auto &i : edges) if(spec[i[0]] ^ spec[i[1]]){
			// cerr << i[0] << ' ' << i[1] << el;
			i[2] += cac;
		}
		
		DSU ds;
		sort(bend(edges), comp);
		
		int cost = 0, cnt = 0;
		
		for(auto &i : edges){
			if(ds.findpar(i[0]) != ds.findpar(i[1])){
				
				// cerr << i[0] << ' ' << i[1] << ' ' << i[2] << el;
				
				cost += i[2];
				cnt += spec[i[0]] ^ spec[i[1]];
				ds.join(i[0], i[1]);
			}
		}
		
		if(ds.len[ds.findpar(1)] != n){
			cout << "-1";
			exit(0);
		}
		
		for(auto &i : edges) if(spec[i[0]] ^ spec[i[1]]){
			i[2] -= cac;
		}
		
		return make_pair(cost, cnt);
	};
	
	int l = -1e12, r = 1e12, opt = -INF;
	
	while(l <= r){
		int mid = (l + r) / 2;
		
		// mid = 1;
		
		// cout << mid << ' ' << E(mid).se << el;// << ' '<< E(mid).fi << el;
		
		if(E(mid).se > W){
			l = mid + 1;
		}else {
			// cerr << mid << el;
			opt = mid;
			r = mid - 1;
		}
		
		// break;
	}
	
	// cout << opt << el;
	
	
	if(opt == -INF || abs(E(opt).fi - opt * W) > 1e10) cout << -1;
	else{
		cout << E(opt).fi - opt * W;
	}
}	

