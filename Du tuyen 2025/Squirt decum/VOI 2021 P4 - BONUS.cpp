ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct node{
    int32_t nxt[27];
    ll sus = 0;
    node(){
        memset(nxt, -1, sizeof nxt);
    }
};

struct Trie{
    node t[N];
    int32_t id = 0;

    pii add(const string &s){
        ll res = 0, p = 0;
        for(int i=0; s.size()>i; i++){

            res = max(res, t[p].sus);

            int c = s[i] - 'A';
            if(t[p].nxt[c] == -1) t[p].nxt[c] = ++id;
            p = t[p].nxt[c];
        }

        return {res, p};
    }
};

const int S = 567;

void sol(){
    int n, m;
    cin >> n >> m;

    pair<string, int> a[n];
    for(auto &i : a) cin >> i.fi >> i.se;

    vect<int> G[N];

    for(int i=0; m>i; i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        G[v].eb(u);
    }

    for(int i=0; N>i; i++){
        if(G[i].empty()) continue;
        sort(bend(G[i]), [&](int x, int y){
            return G[x].size() > G[y].size();
        });
    }

    ll cache[N], dp[N], nig[N];
    memset(nig, 0, sizeof nig);
    memset(cache, 0, sizeof cache);
    memset(dp, 0, sizeof dp);
    Trie tr;

    for(int i=n-1; i>=0; i--){
        ll cand, lstpos;
        tie(cand, lstpos) = tr.add(a[i].fi);

        int u = a[i].se;

        if(G[u].size() < S){
            for(int v : G[u]) cand = max(cand, nig[v]);
        }else cand = max(cand, cache[u]);

        dp[i] = cand + u;
        tr.t[lstpos].sus = max(tr.t[lstpos].sus, dp[i]);

        nig[u] = max(nig[u], dp[i]);

        for(int j=0; G[u].size()>j; j++){
            int v = G[u][j];
            if(G[v].size() < S) break;

            cache[v] = max(cache[v], dp[i]);
        }
    }

    cout << *max_element(dp, dp+N);
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
