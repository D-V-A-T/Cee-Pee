const ll N = 2e5 + 1, MOD = 1e9+7, INF = 1000000000000000069;

mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a >= INF || b >= INF)return INF;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return INF;
    return (a/g)*b;
}

struct query{
    int t, l, r, x, id;
};

int n, q, a[N];

struct Seggs{
    int L[4 * N];
    unsigned S[4 * N];

    Seggs(){
        build();
    }

    void build(int l=0, int r=n-1, int id=1){
        if(l == r){
            S[id] = L[id] = a[l];
            return;
        }

        int mid = (l+r) / 2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);


        S[id] = S[id*2] + S[id*2 + 1];
        L[id] = __lcm(L[id*2], L[id*2 + 1]);
    }

    void upd(int i, int j, int x, int l=0, int r=n-1, int id=1){
        if(l > j || r < i || x % L[id] == 0) return;

        if(l == r){

            S[id] = L[id] = __gcd(L[id], x);

            return;
        }

        int mid = (l+r)/2;

        upd(i, j, x, l, mid, id*2);
        upd(i, j, x, mid+1, r, id*2+1);

        L[id] = __lcm(L[id*2], L[id*2+1]);
        S[id] = S[id*2] + S[id*2+1];
    }

    unsigned get(int i, int j, int l = 0, int r = n-1, int id=1){
        if(l > j || r < i) return 0;
        if(i <= l && r <= j) return S[id];
        int mid = (l+r)/2;

        return get(i, j, l, mid, id*2) + get(i, j, mid+1, r, id*2+1);
    }


};

void sol(){
    cin >> n >> q;

    for(int i=0; n>i; i++){
        cin >> a[i];
    }

    Seggs sgt;


    while(q--){
        int t, l, r, x;
        cin >> t >> l >> r;
        l--;r--;

        if(t == 1){
            cin >> x;
            sgt.upd(l, r, x);
        }
        else cout << sgt.get(l, r) << el;
    }


}

