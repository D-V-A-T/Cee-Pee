ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const int S = 200;
int lz[N];

void sol(){
	int n, q;
	cin >> n >> q;
	int a[n], b[n];
	for(int i=0;n>i;i++){
		cin >> a[i];
		b[i] = a[i];
	}
	
	for(int bl = 0; (n-1)/S >= bl; bl++){
		int ed = min(n, (bl+1) * S);
		sort(b + bl * S, b + ed);
	}
	
	while(q--){
		int t, l, r, k;
		cin >> t >> l >> r >> k;
		l--;r--;
		int block_l = l / S, block_r = r / S;
		int lim_l = min(n, (block_l + 1) * S), lim_r = min(n, (block_r + 1) * S);
		
		if(t == 2){
			if(block_l == block_r){
				for(int j = l; r>=j; j++) a[j] += k;
				copy(a + block_l * S, a + lim_l, b + block_l * S);
				sort(b + block_l * S, b + lim_l);
			}else{
				
				for(int j = l; lim_l > j; j++)	a[j] += k;
				copy(a + block_l * S, a + lim_l, b + block_l * S);
				sort(b + block_l * S, b + lim_l);
				
				for(int j = block_l + 1; block_r > j; j++) lz[j] += k;
				
				for(int j = block_r * S; r >= j; j++) a[j] += k;
				copy(a + block_r * S, a + lim_r, b + block_r * S);
				sort(b + block_r * S, b + lim_r);
			}
		}else{
			
			ll lo = -2e9, hi = 2e9, ans;
			while(lo <= hi){
				ll mid = (hi - lo) / 2 + lo;
			
				
				int cnt_smol = 0;
				
				if(block_l == block_r){
					for(int j = l; r >= j; j++)  cnt_smol += a[j] + lz[block_l] <= mid;
				}else{
					for(int j = l; (block_l + 1) * S > j; j++){
						cnt_smol += a[j] + lz[block_l] <= mid;
					}
					for(int j = block_l + 1; block_r > j; j++){
						// a[i] + lz[j] <= mid
						if(b[j * S] > mid - lz[j]) continue;
						if(b[(j+1) * S - 1] <= mid - lz[j]){
							cnt_smol += S;
							continue;
						}
						int smol = upb(b + j * S, b + (j + 1) * S, mid - lz[j]) - (b + j * S);
						cnt_smol += smol;
					}
					for(int j = block_r * S; r >= j; j++){
						cnt_smol += a[j] + lz[block_r] <= mid;
					}
				}
				
				if(cnt_smol >= k){
					ans = mid;
					hi = mid - 1;
				}else lo = mid + 1;
			}
			cout << ans << el;
		}
	}
	
}
	
