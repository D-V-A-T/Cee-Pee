const ll N = 1.2e6 + 1, MOD = 1e9+7, INF = 1000000000000000069;

mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a >= INF || b >= INF)return INF;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return INF;
    return (a/g)*b;
}

struct Point{
    int x, y;
    ll val;
};

int B(ll a, int b){
    ll res = 1;

    while(b){
        if(b&1) res = res * a % MOD;
        a = a * a % MOD;
        b /= 2;
    }

    return res;
}

const int S = sqrt(N * log2(N)) + 2;
int n, q, cnt_x[N] ;
ll sum_y[N], lz[N];
vect<Point> fat[N], smol[N], smol_x[N];

void sol(){
    cin >> n >> q;

    Point a[n];

    for(Point& i : a){
        cin >> i.x >> i.y >> i.val;
        cnt_x[i.x]++;
    }

    for(Point& i : a){
        if(cnt_x[i.x] > S) fat[i.y].pb(i);
        else{
            sum_y[i.y] = (sum_y[i.y] + i.val) % MOD;
            smol[i.y].pb(i);
        }

        smol_x[i.x].pb(i);
    }

    fill(lz, lz+N, 1);

    while(q--){
        int t, x, y;
        cin >> t >> x;
        y = x;

        if(t == 1){
            if(cnt_x[x] > S) lz[x] = lz[x] * 2 % (MOD - 1);
            else{
                for(Point &i : smol_x[x]){
                    sum_y[i.y] = (sum_y[i.y] - i.val + i.val * i.val % MOD);
                    i.val = i.val * i.val % MOD;
                }
            }
        }else{
            ll ans = sum_y[y];

            for(Point i : fat[y]){
                ans = (ans + B(i.val, lz[i.x])) % MOD;
            }

            cout << pm(ans, MOD) << el;
        }
    }

}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();

    int t = 1;
    // cin >> t;
    while(t--) sol();
}
