const ll N = 5e5+1, MOD = 998244353, INF = 1000000000000000069;

mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const int S = sqrt(N);
const int MAX_BLOCK = N / S + 2;

int dt[MAX_BLOCK][MAX_BLOCK], position[N];
int cnt[N];

void sol(){
    
    
    int n, q;
    cin >> n >> q;
    int a[n];
    
    
    vect<int> pos[N];
    
    for(int i=0;n>i;i++){
        cin >> a[i];
        position[i] = pos[a[i]].size();
        pos[a[i]].eb(i);
    }
    
    for(int bli = 0; (n-1)/S >= bli; bli++){
        int curr_max = 0;
        for(int blj = bli; (n-1)/S >= blj; blj++){
            int lim_i = min(n, (bli + 1) * S), lim_j = min(n, (blj + 1) * S);
            
            for(int j = blj * S; lim_j > j; j++){               
                if(position[j] - curr_max >= 0 && pos[a[j]][position[j] - curr_max] >= bli * S) curr_max ++;
            }
            
            dt[bli][blj] = curr_max;
        }
    }
    
    int lastans = 0;
    
    while(q--){
        int l, r; cin >> l >> r;
        l ^= lastans;
        r ^= lastans;
        l--; r--;
        
        assert(l <= r);
        assert(0 <= l &&  r < n);
        
        
        int block_l = l / S, block_r = r / S;
        int lim_l = min(n, (block_l + 1) * S), lim_r = min(n, (block_r + 1) * S);
        
        
        int ans = 0;
        if(block_l == block_r){
            
            for(int i=l; r>=i; i++){
                cnt[a[i]]++;
                ans = max(ans, cnt[a[i]]);
            } 
            
            for(int i=l; r>=i; i++) cnt[a[i]] = 0;
            
        }else{
            ans = dt[block_l + 1][block_r - 1];
            
            for(int i = l; lim_l > i; i++){             
                while(position[i] + ans < pos[a[i]].size() && pos[a[i]][position[i] + ans] <= r) ans ++;    
            }
            
            for(int i = block_r * S; r >= i; i++){              
                while(position[i] - ans >= 0 && pos[a[i]][position[i] - ans] >= l) ans ++;
            }
        }
        
        // cout << l+1 << ' ' << r+1 << ' ' << ans << el;
        cout << ans << el;
        lastans = ans;
    }
}
    
