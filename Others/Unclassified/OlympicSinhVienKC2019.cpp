void Bai1(){
	int n, a, b, ans=0;
    cin >> n >> a >> b;
    int v[n][3];
    for(auto& i : v)cin >> i[0] >> i[1] >> i[2];
    int l=0, r=4e9;
    while(l <= r){
        int mid=(l+r)/2, fumo=1;
        for(auto& i : v){
            int d = sq(i[0] - a) + sq(i[1] - b);
            int dist = sq(mid + i[2]);
            if(d <= dist)fumo=0;
        }if(fumo){
            ans=mid;
            l=mid+1;
        }else r=mid-1;
    }cout << ans << el;
}

int lg2(int i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}

