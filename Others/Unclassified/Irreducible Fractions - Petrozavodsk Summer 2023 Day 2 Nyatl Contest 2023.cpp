ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    bitset<2000> bs[n];
    ll a[n];
    for(ll& i : a) cin >> i;

    for(int i=0; n>i; i++){
        for(int j=0; n>j; j++){
            if(i == j) continue;
            if(__gcd(a[i], a[j]) == 1) bs[i][j] = 1;
        }
    }

    ll ans = 0;
    for(size_t i=0; n>i; i++){
        for(size_t j=0; n>j; j++){
            if(i == j) continue;
            ll potential = (bs[i] & bs[j]).count() ;
            ans += potential * (potential - 1) ;
        }
    }
    cout << ans << el;
}

