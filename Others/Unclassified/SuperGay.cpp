int posmod(int a,int b){
    return ((a%b)+b)%b;
}

int __lcm(int a,int b){
    return a*b/__gcd(a,b);
}

int dp[20][20][20], n, k, x1, x2, yy1, y2;

bool c(int x, int y, int nm){
    return x==x2 && y==y2 && nm != k;
}

void r(int& p, int x){
    p = (p + x) % MOD;
}

void funky(int x, int y, int ways, int moves){
    int ox=x, oy=y, nm=moves+1;
    while(ox >= 1){
        ox--;
        if(c(ox, oy, nm))break;
        r(dp[ox][oy][nm], ways);
    }ox=x;
    while(ox <= n){
        ox++;
        if(c(ox, oy, nm))break;
        r(dp[ox][oy][nm], ways);
    }ox=x;oy=y;
    while(oy >= 1){
        oy--;
        if(c(ox, oy, nm))break;
        r(dp[ox][oy][nm], ways);
    }oy=y;
    while(oy <= n){
        oy++;
        if(c(ox, oy, nm))break;
        r(dp[ox][oy][nm], ways);
    }ox=x, oy=y;
    while(ox >= 1 && oy >= 1){
        ox--;oy--;
        if(c(ox, oy, nm))break;
        dp[ox][oy][moves+1]=(dp[ox][oy][moves+1] + ways) % MOD;
    }ox=x;oy=y;
    while(ox >= 1 && oy <= n){
        ox--;oy++;
        if(c(ox, oy, nm))break;
        dp[ox][oy][moves+1]=(dp[ox][oy][moves+1] + ways) % MOD;
    }ox=x;oy=y;
    while(ox <= n && oy <= n){
        ox++;oy++;
        if(c(ox, oy, nm))break;
        dp[ox][oy][moves+1]=(dp[ox][oy][moves+1] + ways) % MOD;
    }ox=x;oy=y;
    while(ox <= n && oy >= 1){
        ox++;oy--;
        if(c(ox, oy, nm))break;
        dp[ox][oy][moves+1]=(dp[ox][oy][moves+1] + ways) % MOD;
    }
}

void sol(){
    cin >> n >> k >> x1 >> yy1 >> x2 >> y2;
    dp[x1][yy1][0]=1;
    for(int moves=0;k>moves;moves++){
        for(int i=1;n>=i;i++){
            for(int f=1;n>=f;f++){
                if(i == x2 && f == y2)continue;
                funky(i, f, dp[i][f][moves], moves);
            }
        }
    }cout << dp[x2][y2][k];
}

