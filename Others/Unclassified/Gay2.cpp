// Calculte x^k % m

int bpow(int a, int b, int c){
	int ans=1;
	a%=c;
	while(b){
		if(b%2)
			ans=(ans*a)%c;
		a=(a*a)%c;
		b/=2;
	}return ans;
}

void sol(){
    int k,m,x;
    cin >> k >> m >> x;
    cout << bpow(x, k ,m);
}

