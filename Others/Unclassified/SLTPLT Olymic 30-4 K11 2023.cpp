int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int n, m;

struct DSU{
    vect<int> par, len;
    DSU(){
        par.rs(n+1);
        iota(bend(par), 0);
        len.rs(n+1, 1);
    }
    int findpar(int u){
        if(u == par[u])return u;
        return par[u] = findpar(par[u]);
    }
    void join(int u, int v){
        u = findpar(u);
        v = findpar(v);
        if(u != v){
            if(len[u] > len[v])swap(u ,v);
            len[v] += len[u];
            par[u] = v;
        }
    }
};

void sol(){
    cin >> n >> m;
    vect<int> del[n+1];
    for(int i=0;m>i;i++){
        int u, v;
        cin >> u >> v;
        del[u].eb(v);
        del[v].eb(u);
    }

    vect<vect<int>> segs;
    for(int i=1;n>=i;i++){
        sort(bend(del[i]));
        del[i].eb(n+1);
        int pre=1;
        for(int j : del[i]){
            int nxt = j-1;
            if(nxt >= pre)segs.pb({pre, nxt, i});
            pre = j+1;
        }
    }
    sort(bend(segs));

    int endp = 0;
    DSU A;
    for(auto& i : segs){
        i[0] = max(i[0], endp);
        A.join(i[2], i[0]);
        for(int j=i[0];i[1]>=j;j++)A.join(j, i[0]);
        endp = max(endp, i[1]);
    }

    vect<int> seen(n+1), ans;
    int cbd = n;
    for(int i=1;n>=i;i++){
        int pi = A.findpar(i);
        if(seen[pi])continue;
        seen[pi] = 1;
        cbd -= A.len[pi];
        ans.eb(A.len[pi]);
    }
    if(cbd)ans.eb(cbd);
    sort(bend(ans));
    cout << ans.size() << el;
    for(int i : ans)cout << i << ' ';
    nextl;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    cin >> t;
    while(t--)sol();
}
