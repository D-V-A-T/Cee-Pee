// Given A, B, C
// Choose u, v so that B[u] + C[v] + A[i] is maximum (1 <= i <= n; i != u, v)
// Print max B[u] + C[v] + A[i]

struct reimu
{
	int a,b,c, diffb, diffc, id;
};

bool sortb(reimu x, reimu y){
	return x.diffb < y.diffb;
}

bool sortc(reimu x, reimu y){
	return x.diffc < y.diffc;
}

void sol(){
    int n, sum=0, ans;
    cin >> n;
    vect<reimu> v(n), db(n), dc(n);
    for(int i=0;n>i;i++){
    	cin >> v[i].a >> v[i].b >> v[i].c;
    	sum+=v[i].a;
    	v[i].diffb=v[i].b-v[i].a;
    	v[i].diffc=v[i].c-v[i].a;
    	v[i].id=i;
    	db[i]=dc[i]=v[i];
    }sort(bend(db), sortb);
    sort(bend(dc), sortc);
    if(db.back().id != dc.back().id){
    	reimu x=db.back(), y=dc.back();
    	ans=sum - x.a - y.a + x.b + y.c;
    }else{
    	reimu x=db.back(), r=db[n-2], m=dc[n-2];
    	ans=max(sum - x.a - r.a + x.c + r.b,
    			sum - x.a - m.a + x.b + m.c);
    }cout << ans;
}

