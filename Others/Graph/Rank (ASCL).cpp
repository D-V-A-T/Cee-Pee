ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

bool ok = 0;
vect<int> G[69], visited(21);
int ans = 0;

void DFS(int u, int mask, int root, int cmoon){
    visited[u] = 1;
    cmoon |= 1<<u;
    for(int v : G[u]){
        if(mask&(1<<v) && v == root && cmoon == mask){
            ok = 1;
            return;
        }
        if(mask&(1<<v) && !visited[v])DFS(v, mask, root, cmoon);
    }
}

