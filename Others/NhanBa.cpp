int binpow(int a ,int b ,int c){
    int result = 1;
    a = a % c; 
    while (b > 0) {
        if (b % 2 != 0) {
            result = (result * a) % c;
        }
        a = (a * a) % c;
        b /= 2;
    }
    
    return result;
}

