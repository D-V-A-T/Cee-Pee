void sol(){
    int n, q;
    cin >> n >> q;
    const int S=sqrt(n);
    vect<int> a(n), cnt(n), f(n);
    for(int& i : a)cin >> i;
    for(int i=n-1;i>=0;i--){
        f[i]=n;
        cnt[i]=1;
        int k=i+a[i];
        if(k < n){
            if(i/S == k/S){
                f[i] = f[k];
                cnt[i]+=cnt[k];
            }else {
                f[i] = k;
            }
        }
    }while(q--){
        int t;
        cin >> t;
        if(!t){
            int j, x;cin >> j >> x;j--;
            a[j]=x;
            for(int i=j;i >= (j/S)*S;i--){
                f[i]=n;
                cnt[i]=1;
                int k=i+a[i];
                if(k < n){
                    if(i/S == k/S){
                        f[i] = f[k];
                        cnt[i]+=cnt[k];
                    }else {
                        f[i] = k;
                    }
                }
            }
        }else{
            int j;cin >> j;j--;
            int fimi=0, fumo=j, p=j;
            for(;j<n; j=f[j]){
                fimi += cnt[j];
                fumo=j;
            }
            for(int i=fumo;min((fumo/S +1)*S, n)>i; i += a[i]){
                if(f[i] < n) fumo = max(fumo, f[i]);
                else fumo = max(fumo, i);
            }            
            cout << fumo+1 << ' ' << fimi << el;
        }
    }
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
