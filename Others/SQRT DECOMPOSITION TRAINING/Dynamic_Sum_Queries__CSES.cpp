void sol(const int n){
    int q;cin >> q;
    const int BS=sqrt(n+.0)+1;
    vect<int> b(BS), a(n);
    for(int i=0;n>i;i++){
        cin >> a[i];
        b[i/BS]+=a[i];
    }while(q--){
        int t;
        cin >> t;
        if(t==1){
            int i, x;
            cin >> i >> x;
            i--;
            b[i/BS]=b[i/BS] - a[i] + x;
            a[i]=x;
        }else{
            int l, r;
            cin >> l >> r;
            l--;r--;
            int cl=l/BS, cr=r/BS, ans=0;
            if(cl == cr)for(int i=l;r>=i;i++)ans +=a[i];
            else{
                for(int i=l, lim=(cl+1)*BS;i<lim;i++){
                    ans += a[i];
                }for(int i=cl+1;cr>i;i++){
                    ans += b[i];
                }for(int i=cr*BS;r>=i;i++){
                    ans += a[i];
                }
            }cout << ans << el;
        }
    }
}

