int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
int __lcm(int a,int b){return a*b/__gcd(a,b);}

int n, q, SQRT;

struct query{
    int l, r, id;
    bool operator < (const query& o) const {
        const int S=SQRT;
        if(l/S != o.l/S)return l < o.l;
        if((l/S)&1)return r < o.r;
        return r > o.r;
    }
};

int cnt[N];
ll fumo=0;

void upd(const ll x, const int p){
    fumo -= sq(cnt[x])*x;
    cnt[x] += p;
    fumo += sq(cnt[x])*x;
}

void sol(){
    cin >> n >> q;
    SQRT=sqrt(n);
    vect<int> a(n);
    for(int& i : a)cin >> i;
    vect<query> que(q);
    ll ans[q];
    for(int i=0;q>i;i++){
        cin >> que[i].l >> que[i].r;
        que[i].l--;que[i].r--;
        que[i].id=i;
    }sort(bend(que));
    int l=0, r=-1;
    for(auto& i : que){
        while(l < i.l)upd(a[l++], -1);
        while(l > i.l)upd(a[--l], 1);
        while(r < i.r)upd(a[++r], 1);
        while(r > i.r)upd(a[r--], -1);
        ans[i.id]=fumo;
    }for(ll i : ans)cout << i << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
