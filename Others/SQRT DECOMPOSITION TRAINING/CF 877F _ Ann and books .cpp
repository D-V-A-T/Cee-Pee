int SQRT, q, n, k, fumo=0;
int cnt[N];

struct query{
    int l, r, id;
    bool operator < (const query& o) const{
        const int S=SQRT;
        if(l/S != o.l/S)return l < o.l;
        if((l/S)&1)return r < o.r;
        return r > o.r;
    }
};

void upd(int x, int y, bool p){
    if(p){
        fumo += cnt[x];
        cnt[y]++;
    }else{
        cnt[y]--;
        fumo -= cnt[x];
    }
}

void ZIP(vect<int>& v, vect<int>& a, vect<int>& b){
    vect<int> zip=v;
    for(int i : a)zip.eb(i);
    for(int i : b)zip.eb(i);
    sort(bend(zip));
    auto it=unique(bend(zip));
    zip.rs(it-zip.begin());
    for(int& i : v)i=lwb(bend(zip), i)-zip.begin()+1;
    for(int& i : a)i=lwb(bend(zip), i)-zip.begin()+1;
    for(int& i : b)i=lwb(bend(zip), i)-zip.begin()+1;
}

void sol(){
    cin >> n >> k;
    vect<int> p(n+1), a(n+1), pfs(n+1), ap(n+1), mp(n+1);
    SQRT=sqrt(n);
    for(int i=1;n>=i;i++)cin >> p[i];
    ap[0]=k;mp[0]=-k;
    for(int i=1;n>=i;i++){
        cin >> a[i];
        if(p[i] == 1)pfs[i]=a[i];
        else pfs[i]=-a[i];
        pfs[i] += pfs[i-1];
        ap[i]=pfs[i]+k;
        mp[i]=pfs[i]-k;
    }ZIP(pfs, ap, mp);
    int q;cin >> q;
    vect<query> que(q);
    vect<int> ans(q);
    for(int i=0;q>i;i++){
        cin >> que[i].l >> que[i].r;
        que[i].id=i;
        que[i].l--;
    }sort(bend(que));
    int l=1, r=0;
    for(auto& i : que){
        while(l < i.l){
            upd(ap[l], pfs[l], 0);
            l++;
        }
        while(l > i.l){
            l--;
            upd(ap[l], pfs[l], 1);  
        }
        while(r < i.r){
            r++;
            upd(mp[r], pfs[r], 1);
        }
        while(r > i.r){
            upd(mp[r], pfs[r], 0);
            r--;
        }
        ans[i.id]=fumo;
    }for(int i : ans)cout << i << el;
}

