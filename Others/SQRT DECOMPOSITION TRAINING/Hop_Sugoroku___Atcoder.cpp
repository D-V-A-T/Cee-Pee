void sol(const int n){
    vect<int> a(n), dp(n, 1);
    const int S=sqrt(n);
    vect<vect<int>> pfs(S+5, vect<int>(S+5));   
    for(int& i : a)cin >> i;
    for(int i=n-1;i>=0;i--){
        if(a[i] >= S){  
            for(int j=1;i+a[i]*j < n;j++){  
                dp[i] += dp[i+a[i]*j];
                dp[i] %= MOD;
            }
        }else{
            dp[i] += pfs[a[i]][i % a[i]];
            dp[i] %= MOD;
        }
        for(int j=1;S>j;j++){
            pfs[j][i % j] += dp[i];
            pfs[j][i % j] %= MOD;   
        }
    }cout << dp[0];
}

