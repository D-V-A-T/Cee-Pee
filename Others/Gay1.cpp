// Given n. Find the number of pairs (x, y) so that x^2 - y^2 = n

int n;

bool check(int a, int b){
	// a=(x-y), b=(x+y)
	if((a+b)%2)return 0;
	int x=(a+b)/2, y=b-x;;
	return (x*x - y*y == n && x>0 && y>0);
}

void sol(){
    cin >> n;
    int ans=0;
    for(int i=1;i*i <= n;i++){
    	if(n%i==0)
    		ans+=check(i, n/i);
    }cout << ans;
}

