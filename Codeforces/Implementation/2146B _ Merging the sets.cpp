ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, m;
    cin >> n >> m;
    vect<int> cnt(m+5), v[n];
    for(int i=0;n>i;i++){
        int l; cin >> l;
        v[i].rs(l);
        for(int& j : v[i]){
            cin >> j;
            cnt[j]++;
        }
    }
    
    for(int i=1;m>=i;i++) if(!cnt[i]) {
        cout << "NO\n";
        return;
    }

    int ellen = 0;
    for(int i=0;n>i;i++){
        int fumo = 1;
        for(int j : v[i]) if(cnt[j] == 1) fumo = 0;
        ellen += fumo;
    }
    cout << (ellen>1 ? "YES\n" : "NO\n");
}

