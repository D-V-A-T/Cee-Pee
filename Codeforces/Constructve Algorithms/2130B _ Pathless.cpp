int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, s;
    cin >> n >> s;
    int S = 0;
    vect<int> cnt(3);
    for(int i=0;n>i;i++){
    	int x;cin >> x;cnt[x]++;
    	S += x;
    }
    if(S > s){
    	while(cnt[0]--)cout << "0 ";
    	while(cnt[2]--)cout << "2 ";
    	while(cnt[1]--)cout << "1 ";
    	nextl;
    }
    else{
    	if(S%2 == s%2) cout << "-1\n";
    	else if(S%3 == s%3) cout << "-1\n";
    	else if(S+3 <= s) cout << "-1\n";
    	else {
    		while(cnt[0]--)cout << "0 ";
    		while(cnt[2]--)cout << "2 ";
    		while(cnt[1]--)cout << "1 ";
    		nextl;
    	}
    }

}

