void sol(int tnum){
    int n;
    cin >> n;
    int d[n], ans[n];
    for(int& i : d)cin >> i;
    int h=0;
    pii a[n];
    vect<int> v;
    for(pii& i : a) cin >> i.fi >> i.se;
    for(int j=0;n>j;j++){
        ans[j] = d[j];

        if(d[j] == 1)h++;
        if(d[j] == -1) v.eb(j);

        pii i = a[j];

        if(i.fi <= h && h <= i.se) continue;
        if(h > i.se){
            cout << "-1\n";
            return;
        }
        if(h < i.fi){
            if(v.size() < i.fi-h){
                cout << "-1\n";
                return;
            }
            for(int f=0;i.fi-h>f;f++) ans[v.back()]=1, v.pob();
            h = i.fi;
        }
    }

    h = 0;
    for(int j=0;n>j;j++){
        pii i = a[j];
        if(ans[j] == 1) h++;
        if(ans[j] == -1) ans[j] = 0;

        if(i.fi <= h && h <= i.se) continue;
        cout << "-1\n";
        return;
    }
    for(int i : ans) cout << i << ' ';
    nextl;
}

