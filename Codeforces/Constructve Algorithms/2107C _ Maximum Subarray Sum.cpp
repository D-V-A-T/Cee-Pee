void sol(int tnum){
    int n, k;
    cin >> n >> k;
    string s, fumo = "YES\n";
    int a[n];
    cin >> s;
    int pfs = 0, mn = 0, P[n], MP[n], S[n], MS[n];
    for(int i=0;n>i;i++){
        cin >> a[i];
        pfs += a[i];
        mn = min(mn, pfs);
        P[i] = pfs;
        MP[i] = mn;
        if(s[i] == '0'){
            pfs = mn = 0;
        }else{
            if(pfs - mn > k) fumo = "NO\n";
        }
    }

    pfs = mn = 0;
    for(int i=n-1;i>=0;i--){
        pfs += a[i];
        mn = min(mn, pfs);
        S[i] = pfs;
        MS[i] = mn;
        if(s[i] == '0') pfs = mn = 0;
    }

    if(!count(bend(s), '0')){
        int mx = 0;
        for(int i=0;n>i;i++) mx = max(mx, P[i] - MP[i]);
        if(mx != k) fumo = "NO\n";
    }

    if(fumo == "NO\n"){
        cout << fumo;return;
    }

    for(int i=0;n>i;i++){
        if(s[i] == '0'){
            int sum = P[i] - MP[i] + S[i] - MS[i];
            a[i] = k - sum;
            for(int j=i+1;n>j;j++) if(s[j] == '0') a[j] = -1e13;
            break;
        }
    }

    cout << fumo;
    for(int i : a)cout << i << ' ';
        nextl;


}

