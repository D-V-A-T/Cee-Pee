void sol(int tnum){
    int n;
    cin >> n;
    int a[n], G = 0;
    bool visited[5001];
    memset(visited, 0, sizeof visited);

    for(int& i : a) cin >> i, G = __gcd(G, i);
    queue<pii> que;
    for(int i : a) que.push({i, 0});

    while(que.size()){  
        auto [x, step] = que.front();
        que.pop();
        if(x == G){
            if(step == 0) cout << n - count(a, a+n, G) << el;
            else cout << step + n-1 << el;
            return;
        }

        if(visited[x]) continue;
        visited[x] = 1;

        for(int i : a) if(!visited[g[i][x]]) que.push({g[i][x], step+1});
    }


}

