int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int findx(int m){
    m*=2;
    int res = sqrt(m);
    if(sq(res+1) <= m) return res+1;
    return res;
}

void sol(int tnum){
    int n, m;
    cin >> n >> m;
    if(m > n*(n+1)/2 || m<n) {
        cout << "-1\n";
        return;
    }
    
    vect<int> ans(2*n+5);
    for(int i=1;n>=i;i++) ans[i] = i;
    int pos = 2*n+1, d = n, f=1;
    vect<int> nodes;
    for(int lc=0;1e10>lc;lc++){
        if(d + n-f < m){
            ans[f] = 0;
            ans[pos--] = f;
            d += n-f;
        }else{
            int down = m-d;
            int newp = f+down;
            ans[f] = 0;
            for(int i=1;2*n+5>i;i++){
                if(ans[i]) nodes.eb(ans[i]);

                if(i == newp) nodes.eb(f);

            }break;
        }
        f++;
    }
    cout << nodes[0] << el;
    for(int i=0;nodes.size()>i+1;i++) cout << nodes[i] << ' ' << nodes[i+1] << el;
}

