int __lcm(int a,int b){return a*b/__gcd(a,b);}

bool comp(pii a, pii b){
    return abs(a.fi+a.se) < abs(b.fi+b.se);
}

void sol(){
    int n;
    cin >> n;
    vect<pii> v(n);
    for(pii& i : v)cin >> i.fi;
    for(pii& i : v)cin >> i.se;
    sort(bend(v), comp);
    int a=0, b=0;
    for(pii i : v){
        if(i.fi > i.se)a+=i.fi;
        else if(i.fi < i.se)b += i.se;
        else{
            if(i.fi == -1){
                if(a > b)a--;
                else b--;
            }else if(i.fi == 1){
                if(a > b)b++;
                else a++;
            }
        }
    }cout << min(a, b) << el;
}
signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    cin >> t;
    while(t--)sol();
}
