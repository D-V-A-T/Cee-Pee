int funky(int l, int r, const vect<int>& pfs){
    return pfs[r] - (l ? pfs[l-1] : 0);
}

void sol(){
    int n, m, k, ans=-1;
    cin >> n >> m >> k;
    vect<int> v(n), a(n), b(n+1);
    for(int& i : v)cin >> i;
    vect<int> pfs(n);
    partial_sum(bend(v), pfs.begin());
    int curr=0;
    for(int i=0;n>i;i++){
        if(i)a[i]+=a[i-1];
        curr += v[i];
        if(curr >= k){
            curr=0;
            a[i]++;
        }
    }curr=0;
    for(int i=n-1;i>=0;i--){
        b[i]+=b[i+1];
        curr += v[i];
        if(curr >= k){
            curr=0;
            b[i]++;
        }
    }
    for(int i=0;n>i;i++){
        int l=0, r=i-1, fumo=-1;
        int x=(i<n-1 ? b[i+1] : 0);
        while(l <= r){
            int mid=(l+r)/2;
            if(a[mid]+x >= m){
                r=mid-1;
                fumo=mid;
            }else l=mid+1;
        }if(x >= m)ans=max(ans, funky(0, i, pfs));
        if(fumo==-1)continue;
        else ans=max(ans, funky(fumo+1, i, pfs));
    }if(ans == -1 && a[n-1] >= m)cout << "0\n";
    else cout << ans << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    cin >> t;
    while(t--)sol();
}
