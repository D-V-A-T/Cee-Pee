void ffopen(const string& file){
    if(file.empty())return;
    freopen((file + ".inp").c_str(), "r", stdin);
    freopen((file + ".out").c_str(), "w", stdout);
}

int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
int __lcm(int a,int b){return a*b/__gcd(a,b);}

void sol(){
    string s, t, ans;
    cin >> s >> t;
    umap<char, int> fumo;
    int n=t.size(), p=-1;
    for(int i=0;n>i+1;i++)fumo[t[i]]=i;
    vect<int> reimu(3);
    reimu[0]=INF;
    for(char c : s){
        ans += c;
        p++;
        if(!p)continue;
        if(fumo.count(c)){
            int si=ans.size() + (n-1 - (fumo[c]+1) + 1);
            if(si < reimu[0]){
                reimu[0]=si;
                reimu[1]=p+1;
                reimu[2]=fumo[c]+1;
            }
        }
    }if(reimu[0] == INF)cout << -1 << el;
    else{
        for(int i=0;reimu[1]>i;i++)cout << s[i];
        for(int i=reimu[2];n>i;i++)cout << t[i];
        nextl;
    }
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
