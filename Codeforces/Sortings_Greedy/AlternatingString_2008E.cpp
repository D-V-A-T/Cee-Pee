void sol(){
    int n, ans=INF;
    string s;
    cin >> n >> s;
    if(n==1 || n==3){
        cout << "1\n";return;
    }if(n==2){cout << "0\n";return;}
    vect<vect<int>> a(n, vect<int>(30, INF)), b(n, vect<int>(30, INF));
    for(int i=0;n>i;i++){
        for(char c='a';'z'>=c;c++){
            if(i-2 >= 0)a[i][c-'a']=min(a[i][c-'a'], a[i-2][c-'a']+(c != s[i]));    
            else a[i][c-'a']=(c!=s[i]);   
        }
    } 
    if(n&1){
        for(int i=n-1;i>=0;i--){
            for(char c='a';'z'>=c;c++){
                if(i+2 < n)b[i][c-'a']=min(b[i][c-'a'], b[i+2][c-'a']+(c != s[i]));    
                else b[i][c-'a']=(c!=s[i]);        
            }
        }for(int i=0;n>i;i++){
            int fumo1=INF, fumo2=INF;
            for(int c=0;'z'-'a'>=c;c++){
                if(i && i+2<n)fumo1=min(fumo1, a[i-1][c]+b[i+2][c]);
                else if(i+2 < n)fumo1=min(fumo1, b[i+2][c]);
                else if(i)fumo1=min(fumo1, a[i-1][c]);

                if(i-2 >= 0 && i+1<n)fumo2=min(fumo2, a[i-2][c]+b[i+1][c]);
                else if(i+1<n)fumo2=min(fumo2, b[i+1][c]);
                else if(i-2 >= 0)fumo2=min(fumo2, a[i-2][c]);
            }ans=min(ans, fumo1+fumo2+1);
        }cout << ans << el;
    }else cout << *min_element(bend(a[n-1])) + *min_element(bend(a[n-2])) << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    cin >> t;
    while(t--)sol();
}
