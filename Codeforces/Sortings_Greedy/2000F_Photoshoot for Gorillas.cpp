void sol(){
    int n,m,k;
    cin >> n >> m >> k;
    vect<vect<int>> diff(n+5, vect<int>(m+5));
    for(int i=1;n>=i;i++){
        for(int j=1;m>=j;j++){
            int r=i+k-1, c=j+k-1;
            if(r > n || c > m)continue;
            diff[i][j]++;
            diff[r+1][c+1]++;
            diff[i][c+1]--;
            diff[r+1][j]--; 
        }
    }int f;
    cin >> f;
    vect<int> a(f), fumo;
    for(int& i : a)cin >> i;
    sort(bend(a));
    for(int i=1;n>=i;i++){
        for(int j=1;m>=j;j++){
            diff[i][j] += diff[i-1][j] + diff[i][j-1] - diff[i-1][j-1];
            fumo.eb(diff[i][j]);
        }
    }sort(bend(fumo));
    int ans=0;
    for(int i=1;f>=i;i++)
        ans += a[f-i] * fumo[fumo.size()-i];
    cout << ans << el;

}
