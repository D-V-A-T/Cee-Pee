int dp[269][101][101][2];
// dp[i][j][k][0/1] : number of beautiful arrangements with the i-th person 
//                     being 0/1 and j 0s and k 1s have been used 

void sol(){
    int n1, n2, k1, k2;
    cin >> n1 >> n2 >> k1 >> k2;
    dp[0][0][0][0]=dp[0][0][0][1]=dp[1][1][0][0]=dp[1][0][1][1]=1;
    
    for(int i=2;n1+n2>=i;i++){
        for(int j=0;n1>=j;j++){
            for(int k=0;n2>=k;k++){
                if(j+k != i)continue;
                for(int f=1;k1>=f;f++){
                    if(i-f < 0 || j-f < 0)continue;
                    dp[i][j][k][0] += dp[i-f][j-f][k][1];
                }for(int f=1;k2>=f;f++){
                    if(i-f < 0 || k-f < 0)continue;
                    dp[i][j][k][1] += dp[i-f][j][k-f][0];
                }
                dp[i][j][k][0] %= MOD;
                dp[i][j][k][1] %= MOD;
            }
        }
    }
    cout << (dp[n1+n2][n1][n2][0] + dp[n1+n2][n1][n2][1])%MOD;
}

