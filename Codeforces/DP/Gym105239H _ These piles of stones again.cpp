ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

bool dp[105][105][105];

void sol(){
    int a, b, c;
    cin >> a >> b >> c;
    int n;
    cin >> n;
    int v[n];
    for(int& i : v) cin >> i;

    for(int i=0;a>=i;i++){
        for(int j=0;b>=j;j++){
            for(int k=0;c>=k;k++){
                bool fumo = 0;
                for(int x : v){
                    if(x <= i) fumo = max(fumo, !dp[i-x][j][k]);
                    if(x <= j) fumo = max(fumo, !dp[i][j-x][k]);
                    if(x <= k) fumo = max(fumo, !dp[i][j][k-x]);
                }
                dp[i][j][k] = fumo;
            }
        }
    }

    cout << (dp[a][b][c] ? "Peter" : "Vasya");
}

