int dp[1069][15];
// dp[i][j] : max profit when use i grams of dough to make the first j types of bun

void sol(){
    int n, m, c, d;
    cin >> n >> m >> c >> d;
    vect<vect<int>> a(m ,vect<int>(4));
    for(auto& i : a)cin >> i[0] >> i[1] >> i[2] >> i[3];
    a.pb({INF, 1, c, d});
    for(int i=1;n>=i;i++)
        dp[i][0]=min(a[0][0]/a[0][1], i/a[0][2])*a[0][3];
    for(int j=1;m>=j;j++){
        for(int i=0;n>=i;i++){// total dough used
            for(int f=0;n>=f;f++){// # of dough used to make j-th type
                if(i-f < 0)continue;
                dp[i][j]=max(dp[i][j], dp[i-f][j-1] + min(a[j][0]/a[j][1], f/a[j][2])*a[j][3]);
                if(i) dp[i][j]=max(dp[i][j], dp[i-1][j]);
            }
        }
    }cout << dp[n][m];
}

