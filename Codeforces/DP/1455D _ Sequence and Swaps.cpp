ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, x;
    cin >> n >> x;
    int a[n], dp[2][505][505];
    // dp[i][j][k] : dap an khi xet i ki tu dau, so cuoi la j, x = k

    for(int& i : a)cin >> i;
   
    memset(dp, 5, sizeof dp);
    const int inf = dp[0][0][0];


    int p=0, c=1;
    dp[0][a[0]][x] = 0;
    if(a[0] > x) dp[0][x][a[0]] = 1;

    for(int i=1;n>i;i++){
        memset(dp[c], 5, sizeof dp[c]);
        for(int j=0;500>=j;j++){
            for(int k=0;500>=k;k++){

                if(a[i] >= j)
                    dp[c][a[i]][k] = min(dp[c][a[i]][k], dp[p][j][k]);
                if(a[i] > k && k >= j)
                    dp[c][k][a[i]] = min(dp[c][k][a[i]], dp[p][j][k]+1);
            }
        }
        swap(c, p);
    }

    int ans = inf;
    for(int j=0, i=n-1;500>=j;j++){
        for(int k=0;500>=k;k++){
            ans = min(ans, dp[p][j][k]);
        }
    }

    cout << (ans==inf ? -1 : ans) << el;
}

