//Operation: Choose a vertex i which has at least 1 child and increase
//           the value at vertex i by 1 and decrease all of its children's
//           value by 1

umap<int, bool> visited;
vect<int> dp;

void DFS(int start, vect<vect<int>>& tree, vect<int>& a){
    visited[start]=1;
    int fumo=INF;
    for(int i : tree[start]){
        if(!visited[i]){
            DFS(i, tree, a);
            fumo=min(fumo, dp[i]);
        }
    }if(fumo < INF){
        if(start == 1){
            dp[start]=a[start]+fumo;
        }else{
            if(fumo < a[start])dp[start]=fumo;
            else dp[start]=(a[start]+fumo)/2;
        }        
    }else dp[start]=a[start];
}

