ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}


int lg2(unsigned long long i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}

void sol(){
    int n;
    cin >> n;
    vect<vect<int>> spt(n+5, vect<int>(lg2(n)+5));
    vect<int> a(n);
    for(int i=0;n>i;i++){
        cin >> a[i];
        spt[i][0] = a[i];
    }

    for(int lg=1;lg2(n)>=lg;lg++){
        for(int i=0;n+1>i + (1<<(lg-1));i++){
            spt[i][lg] = max(spt[i][lg-1], spt[i + (1<<(lg-1))][lg-1]);
        }
    }

    auto get = [&](int l, int r){
        int lg = lg2(r-l+1);
        return max(spt[l][lg], spt[r - (1<<lg)+1][lg]);
    };

    int m;cin >> m;
    vect<pii> b(m);
    for(pii& i : b)cin >> i.fi >> i.se;
    sort(bend(b));
    vect<int> mx(m);
    for(int i=m-1;i>=0;i--)mx[i] = i+1<m ? max(mx[i+1], b[i].se) : b[i].se;

    multiset<int> mn;

    auto get_farthest = [&](int p){
        int l = p, r = n-1, res = -1;
        while(l <= r){
            int mid = (l+r)/2;
            int mxp = get(p, mid);
            pii fumi = {mxp, 0};
            int fumo = lwb(bend(b), fumi) - b.begin();
            if(fumo >= b.size() || mx[fumo] < mid-p+1)r = mid-1;
            else{
                res = mid;
                l = mid+1;
            }
        }return res;
    };

    vect<int> del[n+1], dp(n+1, 1e9);
    for(int i=0;n>i;i++){
        int nxt = get_farthest(i), pre = i ? dp[i-1] : 0;
        if(nxt != -1){
            mn.insert(pre+1);
            del[nxt].eb(pre+1);
        }
        if(mn.size())dp[i] = *mn.begin();
        for(int j : del[i])mn.erase(mn.find(j));
    }
    cout << (dp[n-1]<1e9 ? dp[n-1] : -1)<< el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    cin >> t;
    while(t--)sol();
}
