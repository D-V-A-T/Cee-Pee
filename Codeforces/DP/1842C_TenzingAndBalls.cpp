// Given n balls. You can choose 2 balls of the same number and
// remove them and every balls in between

//Find the maximum number of balls you can remove

void sol(){
    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        vect<int> dp(n+1), v(n+1);
        // dp[i] : max balls can remove when i first balls
        // dp[i] have 2 options
        // dont remove => dp[i]=dp[i-1]
        // remove : choose an index(j) where v[j]=v[i] so that
        // the number of balls can be removed from 1->j-1 + j->i is max
        // ==> dp[i] = dp[j-1] + i-j+1 = (dp[j-1] - j + 1) + i
        // We can store the max for each v[i] (dp[j-1] - j + 1) in an umap 
        for(int i=1;n>=i;i++)
            cin >> v[i];
        vect<int> maxballz(n+69, -INF); 
        for(int i=1;n>=i;i++){
            dp[i]=dp[i-1];
            if(maxballz[v[i]] > -INF)
                dp[i]=max(dp[i], i + maxballz[v[i]]);
            maxballz[v[i]]=max(maxballz[v[i]],dp[i-1]-i+1);
        }cout << dp[n] << el;
    }
}

