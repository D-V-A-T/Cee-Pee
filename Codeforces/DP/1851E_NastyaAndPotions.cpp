//There are n types of potions, and a potion of type i costs c_i coins.
//Some potions can be created by mixing other potions, and when you mix them,
//the used potions are consumed. No potion can be made from itself.

//For each type of potion. Determine the minimum cost to get it

vect<int> dp(2e5+69, INF);
//dp[i] : minimum cost to get potion i

void func(int reimu, const vect<vect<int>>& pot, const vect<int>& cost){
    //already find the min cost of i
    if(dp[reimu] < INF)return;
    if(cost[reimu] == 0){
        dp[reimu]=0;
        return;
    }int marisa=0;
    //find the min cost of all the potions used to make i
    for(auto& i : pot[reimu]){
        if(dp[i] == INF)
            func(i,pot,cost);
        marisa+=dp[i];
    }dp[reimu]=min(marisa, cost[reimu]);
}

