// Given array A. Find the minimum number of deletions to make A beautiful

// A sequence is called beautiful if 
// first comes the length of the block, and then its elements. 
// For example, the sequences [(3, 3, 4, 5), (2, 6, 1)] and [(1, 8), (4, 5, 2, 6, 1)] 
// are beautiful (blocks are in "()"), 
// while [1], [1, 4, 3], [3, 2, 1] are not.

void sol(){
    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        vect<int> v(n+1), dp(n+1, INF);
        // dp[i] : number of deletions to make v[1->i] beautiful
        // if v[i] is the first number in a block
        // => dp[i+v[i]] = dp[i-1] (since dp[i -> i+v[i]] is beautiful)
        // if we delete v[i]
        // => dp[i]=dp[i-1]
        for(int i=1;n>=i;i++)cin >> v[i];
        dp[0]=0;
        for(int i=1;n>=i;i++){
            dp[i]=min(dp[i], dp[i-1]+1);
            if(i+v[i] <= n)
                dp[i+v[i]]=min(dp[i+v[i]], dp[i-1]);
        }cout << dp[n] << el;
    }
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
//  freopen("","r",stdin);
//  freopen("","w",stdout);
    sol();
}
