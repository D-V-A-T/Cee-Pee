int sq(int x){return x*x;}
int __lcm(int a,int b){return a*b/__gcd(a,b);}

void sol(){
    int n, k;
    cin >> n >> k;
    vect<int> a(n), fumo(n), pfs(n), dp(n);
    for(int& i : a)cin >> i;
    partial_sum(bend(a), pfs.begin());
    for(int i=0;n>i;i++){
        int reimu=k;
        if(i)reimu+=pfs[i-1];
        int pos=upb(bend(pfs), reimu)-pfs.begin()-1;
        if(pos < i) continue;
        if(pos >= n)pos=n-1;
        fumo[i]=pos-i+1;
    }for(int i=n-1;i>=0;i--){
        dp[i]=fumo[i];
        if(i+fumo[i]+1<n)dp[i]+=dp[i+fumo[i]+1];
    }cout << accumulate(bend(dp), 0ll) << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    cin >> t;
    while(t--)sol();
}
