// Given string s. Find the minimum number of operations to make it alternating
// Operations: Delete 1 character(at most 1 time) or Change a character to another
// Alternating string : Characters with the same index parity is the same
// Examples: abab, ahahahah (alternating strings can't have an odd length)
void sol(){
    int t;
    cin >> t;
    while(t--){
        int n;
        string s;
        cin >> n >> s;
        vect<vect<vect<int>>> dp(2, vect<vect<int>>(n+69, vect<int>(26)));
        //dp[0][i][c] : number of operations to make 1->i(same parity) c
        //dp[1][i][c] : number of operations to make n->i(same parity) c
        for(int i=0;n>i;i++){
            for(int f=0;26>f;f++){
                if(i-2 >= 0)
                    dp[0][i][f]=dp[0][i-2][f]+(('a'+f)!=s[i]);
                else dp[0][i][f]=(('a'+f)!=s[i]);
            }
        }if(n%2==0){
            cout << *min_element(bend(dp[0][n-1]))+
                    *min_element(bend(dp[0][n-2])) << el;
        }else{
            for(int i=n-1;i>=0;i--){
                for(int f=0;26>f;f++){
                    dp[1][i][f]=dp[1][i+2][f]+(('a'+f)!=s[i]);
                }
            }int ans=INF;
            //delete the i-th element
            for(int i=0;n>i;i++){
                int reimu=INF, marisa=INF;
                if(i-2 >= 0){
                    for(int f=0;26>f;f++){
                        reimu=min(reimu, dp[0][i-1][f]+dp[1][i+2][f]);
                        marisa=min(marisa, dp[0][i-2][f]+dp[1][i+1][f]); 
                    }
                }else if(i){
                    for(int f=0;26>f;f++){
                        reimu=min(reimu, dp[1][i+2][f]+dp[0][i-1][f]);
                        marisa=min(marisa, dp[1][i+1][f]);
                    }
                }else{
                    for(int f=0;26>f;f++){
                        reimu=min(reimu, dp[1][i+1][f]);
                        marisa=min(marisa, dp[1][i+2][f]);
                    }                    
               }ans=min(ans, reimu+marisa+1);
            }cout << ans << el;
        }
    }
}

