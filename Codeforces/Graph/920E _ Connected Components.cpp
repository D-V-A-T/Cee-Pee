void sol(){
    int n, m;
    cin >> n >> m;
    vect<int> NG[n+1];
    for(int i=0;m>i;i++){
        int u, v;
        cin >> u >> v;
        fumo[u][v] = fumo[v][u] = 1;
    }

    for(int i=1;n>=i;i++)avail.pb(i);

    auto BFS = [&](int S, int& cnt){
        queue<int> que;
        que.push(S);

        while(que.size()){
            int u = que.front();
            que.pop();

            if(visited[u])continue;
            visited[u] = 1;
            cnt++;

            vect<int> v;
            auto it = avail.begin();
            while(it != avail.end()){
                int v = *it;
                if(fumo[u][v]) it++;
                else{
                    que.push(v);
                    it = avail.erase(it);
                }
            }
        }
    };

    vect<int> ans;
    for(int i=1;n>=i;i++){
        if(visited[i])continue;
        int cnt = 0;
    
        BFS(i, cnt);

        ans.eb(cnt);
    }

    sort(bend(ans));
    cout << ans.size() << el;
    for(int i : ans)cout << i << ' ';   
}

