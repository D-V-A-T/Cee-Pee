int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int ed[N], visited[N], C, mned;
vect<vect<int>> G;

void DFS(int u){
    visited[u] = 1;
    mned = min(mned, ed[u]);
    for(int v : G[u])if(!visited[v]) DFS(v);
}

void sol(){
    int n;
    cin >> n;

    fill(ed, ed+n+1, 0);
    fill(visited, visited+n+1, 0);
    G.assign(n+5, {});

    map<pii, bool> chk;

    for(int i=1;n>=i;i++){
        int u;
        cin >> u;
        if(chk[{u,i}])continue;
        G[u].eb(i);
        G[i].eb(u);
        ed[u]++;
        ed[i]++;
        chk[{u,i}] = chk[{i,u}] = 1;
    }

    C = 0;
    int moon = 0;
    for(int i=1;n>=i;i++){
        if(visited[i])continue;
        mned = INF;
        C++;
        DFS(i);
        moon += mned == 1;
    }

    if(moon>0)moon--;

    cout << C-moon << ' ' << C << el; 
}

