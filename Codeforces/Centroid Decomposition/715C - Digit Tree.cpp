ll pm(ll a,const ll b=MOD){return ((a%b)+b)%b;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}


int n, m, sz[N], dep[N], del[N], I10[N];
vect<pii> G[N];

void prep(int u, int par){
    sz[u] = 1;
    for(auto [v, w] : G[u]) if(v != par && !del[v]){
        dep[v] = dep[u] + 1;
        prep(v, u);
        sz[u] += sz[v];
    }
}

int find_centroid(int u, int par, int rt){
    for(auto [v, w] : G[u]) if(v != par && !del[v]){
        if(sz[v] * 2 > sz[rt]) return find_centroid(v, u, rt);
    }
    return u;
}

ll B(ll a, ll b, ll c){
    ll res = 1;
    while(b){
        if(b&1) res = res * a % c;
        a = a * a % c;
        b /= 2;
    }
    return res;
}

ll ans = 0;

map<int, ll> sus;

void diddyparty(int u, int par, int num, int dc){
    ll x = pm(-num, m) * I10[dc] % m;
    if(sus.count(x)) ans += sus[x];

    
    for(auto [v, w] : G[u]) if(!del[v] && v != par) {
        diddyparty(v, u, (num * 10 + w) % m, dc+1);
    }
}

void epsteinisland(int u, int par, int num, int p10){
    num %= m;
    sus[num]++;
    
    for(auto [v, w] : G[u]) if(!del[v] && v != par){
        epsteinisland(v, u, (num + w * p10) % m, p10 * 10 % m);
    }
}

void solve(int u){
    prep(u, -1);
    u = find_centroid(u, -1, u);
    
    sus[0] = 1;
    
    for(auto [v, w] : G[u]) if(!del[v]) diddyparty(v, u, w, 1), epsteinisland(v, u, w, 10);

    sus.clear();    
    reverse(bend(G[u]));
    
    for(auto [v, w] : G[u]) if(!del[v]) diddyparty(v, u, w, 1), epsteinisland(v, u, w, 10);
    ans += sus[0];
    sus.clear();    
    
    del[u] = 1;
    
    for(auto [v, w] : G[u]) if(!del[v]) solve(v);
}

void euclideansex(int a, int b, int &x, int &y){
    if(b == 0){
        x = 1;
        y = 0;
        return ;
    }

    int nx, ny;
    euclideansex(b, a % b, nx, ny);
    x = ny;
    y = nx - ny * (a / b);
}

void sol(){
    cin >> n >> m;
    
    I10[0] = 1;
    
    int i10, y;
    
    euclideansex(10, m, i10, y);
    
    i10 = pm(i10, m);
    
    for(int i=1; N>i; i++) I10[i] = I10[i-1] * i10 % m;
    
    for(int i=1; n>i; i++){
        int u, v, w;
        cin >> u >> v >> w;
        G[u].eb(v, w);
        G[v].eb(u, w);
    }
    
    solve(0);
    
    cout << ans;
}

