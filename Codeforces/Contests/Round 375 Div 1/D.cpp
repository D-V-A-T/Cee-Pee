ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

const int S = 267, MAX_BLOCK = N / S + 2;
int n, q, sz[N], in[N], a[N], ans[N];
vect<int> G[N], order;

struct query{
    int l, r, k, id;
    bool operator < (const query &o) const{
        if(l/S == o.l/S){
            if((l/S) & 1) return r > o.r;
            return r < o.r;
        }
        return l/S < o.l/S;
    }
};

struct DS{
    int sus[N], blk[MAX_BLOCK];

    DS(){
        memset(sus, 0, sizeof sus);
        memset(blk, 0, sizeof blk);
    }    

    void upd(int k, int x){
        int block_k = k / S;
        sus[k]+=x;
        blk[block_k]+=x;
    }

    int get(int k){
        int block_k = k / S, res = 0;
        for(int i=k; (block_k+1)*S>i; i++) res += sus[i];
        for(int i=block_k+1; MAX_BLOCK>i; i++) res += blk[i];
        return res;
    }
};  

void dfs(int u, int par){
    in[u] = order.size();
    order.eb(u);
    sz[u] = 1;

    for(int v : G[u]) if(v != par){
        dfs(v, u);
        sz[u] += sz[v];
    }
}

void sol(){
    cin >> n >> q;

    for(int i=1; n>=i; i++) cin >> a[i];

    for(int i=1; n>i; i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        G[v].eb(u);
    }

    dfs(1, 0);

    vect<query> que(q);
    vect<int> ans(q), cnt(N);

    for(int i=0; q>i; i++){
        int u;
        cin >> u >> que[i].k;
        que[i].l = in[u];
        que[i].r = in[u] + sz[u] - 1;
        que[i].id = i;
    }

    sort(bend(que));

    int lo=0, hi=-1;
    DS ds;
    for(auto [l, r, k, id] : que){
        while(hi < r){
            hi++;
            int i = order[hi];
            ds.upd(cnt[a[i]], -1);
            cnt[a[i]]++;
            ds.upd(cnt[a[i]], 1);

        }
        while(lo > l){
            lo--;
            int i = order[lo];
            ds.upd(cnt[a[i]], -1);
            cnt[a[i]]++;
            ds.upd(cnt[a[i]], 1);
        }
        while(hi > r){
            int i = order[hi];
            ds.upd(cnt[a[i]], -1);
            cnt[a[i]]--;
            ds.upd(cnt[a[i]], 1);
            hi--;
        }
        while(lo < l){
            int i = order[lo];
            ds.upd(cnt[a[i]], -1);
            cnt[a[i]]--;
            ds.upd(cnt[a[i]], 1);
            lo++;
        }

        ans[id] = ds.get(k);
    }

    for(int i : ans) cout << i << el;
}

