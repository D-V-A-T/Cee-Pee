ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
	int n;
	cin >> n;
	
	int a[n];
	vect<int> left(n), right(n), stk, len(n+5);
	
	for(int &i : a) cin >> i;
	
	for(int i=0; n>i; i++){
		while(stk.size() && a[stk.back()] >= a[i]) stk.pob();
		
		if(stk.size()) left[i] = stk.back() + 1;
		else left[i] = 0;
		
		stk.eb(i);
	}
	
	stk.clear();
	for(int i=n-1; i>=0; i--){
		while(stk.size() && a[stk.back()] >= a[i]) stk.pob();
		
		if(stk.size()) right[i] = stk.back() - 1;
		else right[i] = n-1;
		
		stk.eb(i);
	}
	
	for(int i=0; n>i; i++){
		len[a[i]] = max(len[a[i]], right[i] - left[i] + 1);
	}
	
	vect<int> pfm(n+5, 1e9);
	
	for(int i=1; n>=i; i++) {
		pfm[i] = min(pfm[i-1], len[i]);
	}
	
	
	for(int i=1; n>=i; i++){
		if(pfm[n - i + 1] < i) cout << 0;
		else cout << 1;
	}
	nextl;
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    cin >> t;
    while(t--) sol();
}
