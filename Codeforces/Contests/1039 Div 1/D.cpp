ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}   

int n, dep[N], P[N];
vect<int> G[N],  order;
pii dp[N];
int memo[N];

void dfs(int u, int par){
    P[u] = par;
    order.eb(u);
    for(int v : G[u]) if(v != par) {
        dep[v] = dep[u] + 1;
        dfs(v, u);
    }
}

inline int diddyparty (int k){
    if(memo[k] != -1) return memo[k];

    for(int u : order){
        int b1 = 0, b2 = 0;
        dp[u] = {0, 0};
        for(int v : G[u]) if(v != P[u]) {
            dp[u].fi += dp[v].fi;
            b2 = max(b2, dp[v].se);
            if(b1 < b2) swap(b1, b2);
        }

        if(b1 + b2 + 1 >= k) dp[u].fi++, dp[u].se = 0;
        else dp[u].se = b1 + 1;
    }
    return memo[k] = dp[1].fi;
}

void sol(){
    cin >> n;
    for(int i=1; n>i; i++){
        int u, v;
        cin >> u >> v;
        G[u].eb(v);
        G[v].eb(u);
    }

    const int S = min(n, 1267);

    dfs(1, 0);
    reverse(bend(order));

    memset(memo, -1, sizeof memo);
    
    int l, r, ans[n+5], s;

    for(s=1; s <= S; s++) ans[s] = diddyparty(s);

    pii curr = {s, diddyparty(s)};

    while(true){
        l = curr.fi+1, r = n;
        int sus = curr.fi;
        while(l <= r){
            int mid = (l + r) / 2;            
            if(diddyparty(mid) == curr.se){
                sus = mid;
                l = mid + 1;
            }else r = mid - 1;
        }
        
        for(int i=curr.fi; sus>=i; i++) ans[i] = curr.se;
        
        if(sus >= n) break;
        sus++;
        curr = {sus, diddyparty(sus)};
    }
    
    for(int i=1; n>=i; i++) cout << ans[i] << el;
    
    
}

