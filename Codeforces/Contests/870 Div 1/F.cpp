ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

ll n;
int spf[N], phi[N], sus[N];
vect<signed> prime;


void PREP(){
    iota(spf, spf+n+1, 0);
    iota(phi, phi+n+1, 0);


    for(int i=2; n >= i*i; i++){
        if(spf[i] != i) continue;

        for(int j=i*i; n>=j; j+=i) spf[j] = min(spf[j], i);
    }

    for(int i=2; n>=i; i++){
        if(phi[i] != i) continue;
        for(int j=i; n>=j; j+=i){
            phi[j] -= phi[j] / i;
        }
    }
    
    for(int i=2; n>=i; i++){
        if(spf[i] == i) prime.eb(i);
        sus[spf[i]]++;
    }
}

void sol(){
    cin >> n;

    PREP();

    ll coprime_pairs = accumulate(phi+2, phi+n+1, 0ll), big_pp = 0;

    ll total_pairs = n * (n-1) / 2, zero = n-1, one = total_pairs - coprime_pairs, two = coprime_pairs - n + 1;
    ll epstein = 1;

    vect<int> pfs(1e5);

    for(int i : prime){
        epstein += i > n/2;
        if(i * i <= n) pfs[i] = sus[i];
        if(i > n/2) zero += n - epstein;
    }

    partial_sum(bend(pfs), pfs.begin());

    auto get_suffix = [&](int i){
        return pfs.back() - pfs[i-1];
    };

    for(int i : prime){
        if(i * i <= n) continue;
        big_pp++;

        ll negga = 0, sum = 0;

        for(int mul=2; i * mul <= n; mul++){
            if(i * spf[mul] > n) negga ++;
        }

        int lb = n / i;
        sum = get_suffix(lb + 1);
        sum -= negga; // this should be the number of numbers that coprime with i and spf[i] * spf[x] > n (x <= sqrt)

        two -= sum;
    }

    two -= big_pp * (big_pp - 1) / 2;

    ll three = total_pairs - zero - one - two;

    cerr << one << ' ' << two << ' ' << three << ' ' << zero << el;

    cout << one + two * 2  + three * 3 << el;

}

