int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
int __lcm(int a,int b){return a*b/__gcd(a,b);}

int F[N];

int bp(int a, int b, const int c=MOD){
    int res=1;
    while(b){
        if(b%2)res=(res*a)%c;
        a=sq(a)%MOD;
        b>>=1;
    }return res;
}

int inv(int x){
    return bp(x%MOD, MOD-2);
}

int comb(int n, int k){
    if(k > n)return 0;
    return (F[n] * inv(F[k]*F[n-k]))%MOD;
}

void sol(){
    int n, k;
    cin >> n >> k;
    int a[n], ans=0;
    vect<int> cnt(2);
    for(int& i : a){
        cin >> i;
        cnt[i]++;
    }for(int i=1;min(cnt[1], k)>=i;i++){
        if(i){
            int fumo=k-i;
            if(fumo >= i || fumo > cnt[0])continue;
            ans += comb(cnt[1], i)*comb(cnt[0], fumo);
            ans %= MOD;
        }
    }cout << ans << el;
}

