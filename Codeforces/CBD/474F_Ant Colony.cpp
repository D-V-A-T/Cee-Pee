int n;
vect<int> a;

struct Seggs{
    vect<umap<int ,int>> t;
    Seggs(){
        t.rs(4*n+1);
        build();
    }void build(int l=0, int r=n-1, int id=1){
        if(l == r){
            t[id][a[l]]++;
            return;
        }int mid=(l+r)/2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);
        for(auto& i : t[id*2])t[id][i.fi] += i.se;
        for(auto& i : t[id*2+1])t[id][i.fi] += i.se;
    }int get(int i, int j, int x, int l=0, int r=n-1, int id=1){
        if(l > j || r < i)return 0;
        if(l >= i && r <= j)return t[id][x];
        int mid=(l+r)/2;
        return get(i, j, x, l, mid, id*2) + get(i, j, x, mid+1, r, id*2+1);
    }
};

int lg2(int i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}

int getmin(vect<vect<int>>& spt, int l, int r){
    int i=lg2(r-l+1);
    return min(spt[i][l], spt[i][r+1-(1<<i)]);
}

int getgcd(vect<vect<int>>& spt, int l, int r){
    int i=lg2(r-l+1);
    return __gcd(spt[i][l], spt[i][r+1-(1<<i)]);
}

