int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int len[N], par[N];

int findpar(int u){
    if(par[u] == u)return u;
    return par[u] = findpar(par[u]);
}

void join(int u, int v){
    u=findpar(u);
    v=findpar(v);
    if(u != v){
        if(len[u] < len[v])swap(u, v);
        len[u] += len[v];
        par[v]=u;
    }
}

void sol(){
    int n, m, w;
    cin >> n >> m >> w;
    iota(par, par+N, 0);
    int b[n], a[n];
    umap<int, vect<int>> fr;
    umap<int, int> tw, tb;
    unordered_set<int> us;
    for(int& i : a)cin >> i;
    for(int& i : b)cin >> i;
    while(m--){
        int u,v;cin >> u >> v;
        join(u-1, v-1);
    }for(int i=0;n>i;i++){
        int pi=findpar(i);
        us.insert(pi);
        fr[pi].eb(i);
        tw[pi] += a[i];
        tb[pi] += b[i];
    }
    n=us.size();
    vect<vect<int>> dp(n+5, vect<int>(w+5));
    int i=0;
    for(int pi : us){
        for(int j=0;w>=j;j++){
            if(j)dp[i][j]=max(dp[i][j], dp[i][j-1]);
            if(i)dp[i][j]=max(dp[i][j], dp[i-1][j]);
            if(j-tw[pi] >= 0){
                if(!i) dp[i][j]=max(dp[i][j], tb[pi]);
                else dp[i][j]=max(dp[i][j],  dp[i-1][j-tw[pi]] + tb[pi]);
            }
            for(int k : fr[pi]){
                if(j - a[k] < 0)continue;
                if(!i) dp[i][j] = max(dp[i][j], b[k]);
                else dp[i][j] = max(dp[i][j], dp[i-1][j-a[k]] + b[k]);
            }
            
        }i++;
    }cout << dp[n-1][w];
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    //cin >> t;
    while(t--)sol();
}
