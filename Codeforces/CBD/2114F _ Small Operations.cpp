int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

vect<int> ds[N];
int dx[N], dy[N];

void sol(){
    int x, y, k;
    cin >> x >> y >> k;
    int lim = max(x, y);
    fill(dx, dx+lim+1, INF);
    fill(dy, dy+lim+1, INF);

    bool visited[lim+1];
    memset(visited, 0, sizeof visited);

    queue<int> que;
    dx[x] = 0;
    que.push(x);
    while(que.size()){
        int u = que.front();
        visited[u] = 1;
        que.pop();
        for(int d : ds[u]){
            if(d > k)break;
            int v = u/d;
            if(!visited[v]){
                que.push(v);
                dx[v] = min(dx[v], dx[u]+1);
            }
        }
    }

    memset(visited, 0, sizeof visited);
    que.push(y);
    dy[y] = 0;
    while(que.size()){
        int u = que.front();
        visited[u] = 1;
        que.pop();
        for(int d : ds[u]){
            if(d > k)break;
            int v = u/d;
            if(!visited[v]){
                que.push(v);
                dy[v] = min(dy[v], dy[u]+1);
            }
        }
    }

    int ans = 1e9;
    for(int i=1;lim>= i;i++){
        ans = min(ans, dx[i] + dy[i]);
    }
    cout << (ans < 1e9 ? ans : -1) << el;
}

