ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, m;
    cin >> n >> m;

    vect<pii> G[n+1], P[n+1];
    int a[n+1];
    vect<int> wei;

    for(int i=1;n>=i;i++)cin >> a[i];
    for(int i=0;m>i;i++){
        int u,v,w;
        cin >> u >> v >> w;
        wei.eb(w);
        G[u].eb(v, w);
        P[v].eb(u, w);
    }

    if(wei.empty()){
        cout << "-1\n";return;
    }

    sort(bend(wei));
    wei.rs(unique(bend(wei)) - wei.begin());
    int l=0, r=wei.size()-1, ans=-1;
    while(l <= r){
        int mid = (l+r)/2;
        ll bound = wei[mid];

        ll sum[n+1];
        memset(sum, -5, sizeof sum);
        sum[1] = 0;

        for(int i=1;n>=i;i++){
            for(auto [p, w] : P[i]){
                if(w > bound)continue;
                if(sum[p] + a[p] >= w){
                    sum[i] = max(sum[i], sum[p] + a[p]);
                }
            }
        }

        if(sum[n] > 0){
            ans = bound;
            r = mid-1;
        }else l = mid+1;
    }
    cout << ans << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen();
    int t=1;
    cin >> t;
    while(t--)sol();
}
