int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct fumo{
    int u, v, w;
    bool operator < (const fumo& o) const{
        return w < o.w;
    }
};

vect<int> par, len, mn;

int findpar(int u){
    if(par[u] == u)return u;
    return par[u] = findpar(par[u]);
}

void join(int u, int v, int w){
    u = findpar(u);
    v = findpar(v);
    if(u != v){
        if(len[u] > len[v])swap(u, v);
        len[v] += len[u];
        mn[v] = min({mn[v], mn[u], w});
        par[u] = v;
    }
}

void sol(){
    int n, m;
    cin >> n >> m;
    vect<fumo> e(m);
    for(fumo& i : e)cin >> i.u >> i.v >> i.w;
    sort(bend(e));
    
    par.assign(n+1, 0);
    len.assign(n+1, 1);
    mn.assign(n+1, 1e9);
    iota(bend(par), 0);
    int ans = 2e9;
    for(fumo i : e){
        join(i.u, i.v, i.w);
        int pu = findpar(i.u), pv = findpar(i.v);
        if(pu == findpar(1) && findpar(1) == findpar(n)){
            ans = min(ans, i.w + mn[pu]);
        }
    }
    cout << ans << el;

}

