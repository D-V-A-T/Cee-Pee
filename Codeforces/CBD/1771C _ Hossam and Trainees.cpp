int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}


bool isprime(int n){
    if(n < 2)return false;
    for(int i=2;i*i<=n;i++)if(n%i == 0)return false;
    return true;
}


vect<int> pr;

void sol(){
    int n;
    cin >> n;
    int a[n];
    string ans = "NO";
    umap<int, bool> fumo;
    auto funky = [&](int x){
        int pp=x;
        if(ans == "YES")return;
        vect<int> pd;
        for(int i : pr){
            if(i*i > x)break;
            while(x%i == 0){
                if(fumo[i]){
                    ans="YES";return;
                }
                pd.eb(i);
                x/=i;
            }
        }
        auto it = unique(bend(pd));
        pd.rs(it - pd.begin());
        if(x>1){
            if(fumo[x]){
                ans="YES";
                
                cout << x << ' ' << isprime(x) << ' ' << fumo[x] << ' ' << pp << el;
                return;
            }pd.eb(x);
        }
        for(int i : pd)fumo[i]=1;
    };
    for(int& i : a){
        cin >> i;
        funky(i);
    }
    
    
    cout << ans << el;
}

