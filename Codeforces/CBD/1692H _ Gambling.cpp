int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    int a[n];
    for(int& i : a)cin >> i;
    // we should aim to maximize w-l with w being the wining rounds and l being the losing rounds
    // the w_i - l_i can be calculated as 2 * (pfs[a[i]][i] - pfs[a[i]][j-1]) - (i-j+1)
    //                                <=> 2pfs[a[i]][i] - i - 1 + (j - 2pfs[a[i]][j-1])
    map<int, int> cnt;
    map<int, pii> fumo;
    vect<int> ans(3);
    int wl=1;
    for(int i=0;n>i;i++){
        cnt[a[i]]++;
        if(2*(cnt[a[i]]) - i + 1 + fumo[a[i]].fi > wl){
            wl = 2*(cnt[a[i]]) - i + 1 + fumo[a[i]].fi;
            ans = {a[i], fumo[a[i]].se, i};
        }
        if(i - 2*(cnt[a[i]]-1) > fumo[a[i]].fi){
            fumo[a[i]] = {i - 2*(cnt[a[i]]-1), i};
        }
    }cout << ans[0] << ' ' << ans[1]+1 << ' ' << ans[2]+1 << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    cin >> t;
    while(t--)sol();
}
