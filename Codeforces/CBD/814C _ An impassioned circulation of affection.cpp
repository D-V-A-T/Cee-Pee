int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    string s;
    cin >> n >> s;
    vect<vect<int>> ans(200, vect<int>(n+5));

    for(int i=0;n>i;i++){
        for(char c = 'a'; 'z'>=c;c++){
            int same = 0;
            for(int j=i;j>=0;j--){
                same += s[j] == c;
                ans[c][abs(j-i)+1 - same] = max(ans[c][abs(j-i)+1 - same], abs(i-j)+1);
            }same = 0;
            for(int j=i;n>j;j++){
                same += s[j] == c;
                ans[c][abs(j-i)+1 - same] = max(ans[c][abs(j-i)+1 - same], abs(i-j)+1);
            }
        }
    }

    for(char c = 'a';'z'>=c;c++){
        for(int i=1;n>=i;i++){
            ans[c][i] = max(ans[c][i], ans[c][i-1]);
        }
    }
    
    int q;cin >> q;
    while(q--){
        int m ;char c;cin >> m >> c;
        cout << (ans[c][m] ? ans[c][m] : m) << el;
    }
}

