ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    int a[n+1];
    vect<int> ans(n+1), fumo(n+1);
    vect<vect<int>> mp(n+1);
    unordered_set<int> no_gift, gift;
    // no_gift : people with no gifts
    // gift : people who can gift
    for(int i=1;n>=i;i++){
        no_gift.insert(i);
        gift.insert(i);
        cin >> a[i];
        fumo[a[i]]++;
        mp[a[i]].eb(i);
    }
    for(int i=1;n>=i;i++){
        if(fumo[a[i]] == 1){
            // a[i] is only wanted to be gifted by i
            ans[i] = a[i];
            no_gift.erase(a[i]);
            gift.erase(i);
        }
    }

    for(int i=1;n>=i;i++){
        if(no_gift.count(a[i]) && gift.count(i)){
            ans[i] = a[i];
            no_gift.erase(a[i]);
            gift.erase(i);
        }
    }
    if(no_gift.size()){
        vect<int> g, ng;
        for(int i : gift)g.eb(i);
        for(int i : no_gift)ng.eb(i);
        for(int i=0;g.size()>i;i++){
            ans[g[i]] = ng[i];
        }
    }
    for(int i=1;n>=i;i++){
        if(ans[i] == i){ 
            // if someone is assigned to himself,
            // switch with someone who also wishes to gift a[i]
            if(i == mp[a[i]].back()){
                swap(ans[i], ans[mp[a[i]][0]]);
            }else{
                swap(ans[i], ans[mp[a[i]].back()]);
            }
        }
    }
    int k = 0;
    for(int i=1;n>=i;i++)if(ans[i] == a[i])k++;
    cout << el << k << el;
    for(int i=1;n>=i;i++)cout << ans[i] << ' ';
    nextl;

}

