int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int res[N];

void sol(){
    ll c,d,x;
    cin >> c >> d >> x;

    

    ll ans = 0;
    auto calc = [&](const int G){
        ll uv = (x + d*G);

        if(uv % c)return;
        uv /= c;
        if(uv % G)return;
        uv /= G;

        ans += (1ll<<res[uv]);
    };

    for(int i=1;i*i<=x;i++){
        if(x%i==0){
            calc(i);
            if(i*i<x)calc(x/i);
        }
    }

    cout << ans << el;
}

