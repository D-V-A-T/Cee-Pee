int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int lg(int x, int y){
    int res = 0;
    while(y%x == 0){
        res ++;
        y /= x;
    } 
    return res;
}

int dp[2][201][27*200+5];

void sol(){
    int n, k;
    cin >> n >> k;
    pii a[n+1];
    for(int i=1;n>=i;i++){
        int x;cin >> x;
        a[i].fi = lg(2, x);
        a[i].se = lg(5, x);
    }
    memset(dp[0], -5, sizeof dp[0]);
    dp[0][0][0]=0;
    int lim = 0;
    for(int i=1;n>=i;i++){
        int c=i&1, p=!c;
        lim += a[i].se;
        memset(dp[c], -5, sizeof dp[c]);
        for(int j=0;i>=j;j++){
            for(int f5=0;lim>=f5;f5++){
                int x = (j && f5>=a[i].se && dp[p][j-1][f5-a[i].se]+a[i].fi > -1)
                     ? dp[p][j-1][f5-a[i].se]+a[i].fi : -1e9;
                dp[c][j][f5] = max({dp[c][j][f5], dp[p][j][f5], x});
            }
        }
    }
    int ans = 0;
    for(int i=0;200*27>=i;i++){
        ans = max(ans, min(i, dp[n%2][k][i]));
    }
    cout << ans;
}

