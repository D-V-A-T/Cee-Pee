int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int bp(int a, int b = MOD-2){
    a %= MOD;
    int res=1;
    while(b){
        if(b&1)res = (res * a) % MOD;
        a=(a*a)%MOD;
        b/=2;
    }return res;
}
    
void sol(){
    int x, k;
    cin >> x >> k;
    if(k==0 || x==0){
        cout << (x*2%MOD);return;
    }
    int inv = bp(bp(2, k)), losses = (bp(2,k+1)-2) * (bp(2, k-1));
    losses %= MOD;
    int ans = pm((x%MOD) * pm(bp(2, k+1) * bp(2, k)) - losses);
    ans = (ans * inv) % MOD;
    cout << ans;    
}

