void sol(){
    // numbers from i can go to j (i < j) <=> there are no k (i <= k < j) that
    // s[k] = L && s[k+1] = R
    int n,q;
    cin >> n >> q;
    vect<int> a(n+1), diff(n+5);
    for(int i=1;n>=i;i++){
        cin >> a[i];
        if(i != a[i]){
            int l=min(i, a[i]), r=max(i, a[i]);
            diff[l]++;
            diff[r]--;
        }
    }for(int i=1;n>=i;i++)diff[i]+=diff[i-1];
    string s;
    cin >> s;
    s = "$"+s+"$";
    unordered_set<int> reimu;
    for(int i=1;n>i;i++){
        if(s[i] == 'L' && s[i+1] == 'R' && diff[i]){
            reimu.insert(i);
        }
    }while(q--){
        int p;cin >> p;
        if(s[p] == 'L')s[p]='R';
        else s[p]='L';
        if(s[p-1] == 'L' && s[p] == 'R' && diff[p-1])reimu.insert(p-1);
        else if(reimu.find(p-1) != reimu.end())reimu.erase(p-1);
        if(s[p] == 'L' && s[p+1] == 'R' && diff[p])reimu.insert(p);
        else if(reimu.find(p) != reimu.end())reimu.erase(p);
        if(reimu.empty())cout << "YES\n";
        else cout << "NO\n";
    }
}

