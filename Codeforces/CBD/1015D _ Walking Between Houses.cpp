int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n, k, s;
    cin >> n >> k >> s;
    int p=1, l=1, r, fumo=0;
    vect<int> ans;
    bool check=0;
    if(p-l < r-p){
        if(fumo + r-p >= s){
            check=1;
            ans.eb(p + s-fumo);
            return;
        }else{
            fumo += r-p;
            ans.eb(r);
            r--;

        }
    }else{
        if(fumo + p-l >= s){
            check=1;
            ans.eb(p - s+fumo);
            return;
        }else{
            fumo += p-l;
            ans.eb(l);
            l++;
        }
    }if(check){
        cout << "YES\n";
        for(auto i:ans)cout << i << ' ';
    }else cout << "NO\n";
}

