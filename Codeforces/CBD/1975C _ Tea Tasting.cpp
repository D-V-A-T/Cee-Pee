int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    int a[n], b[n], pfs[n];
    for(int& i : b)cin >> i;
    for(int& i : a)cin >> i;
    vect<int> ans(n), diff(n);
    partial_sum(a, a+n, pfs);

    auto sum = [&](int l, int r){
        if(r<l)return 0ll;
        return pfs[r] - (l ? pfs[l-1] : 0);
    };

    for(int i=0;n>i;i++){
        int l=i, r=n-1, p=-1;
        while(l <= r){
            int mid=(l+r)/2;
            if(sum(i, mid) >= b[i]){
                p=mid;
                r=mid-1;
            }else l=mid+1;
        }if(p==-1){
            diff[i]++;
        }else{
            ans[p] += b[i] - sum(i, p-1);
            diff[i]++;
            diff[p]--;
        }
    }
    for(int i=0;n>i;i++){
        diff[i] += (i ? diff[i-1] : 0);
        ans[i] += diff[i]*a[i];
    }
    for(int i : ans)cout << i << ' ';
    nextl;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    cin >> t;
    while(t--)sol();
}
