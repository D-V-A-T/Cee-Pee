int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

bool visited[N];
vect<vect<int>> g(N);
int sumlen=0, sumpaths=0;
dbl dp[N];

void DFS(int start, int step){
    visited[start]=1;
    int fumo=0;
    for(int i : g[start]){
        if(!visited[i]){
            DFS(i,step+1);
            dp[start] += dp[i]+1;
            fumo++;
        }
    }
    if(fumo){
        dp[start]/=1.0*fumo;
    }
    else dp[start]=0;
}

