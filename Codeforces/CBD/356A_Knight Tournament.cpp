int n, q;

struct Seggs{
    vect<int> t, lz;
    Seggs(){
        t.rs(4*n+1);
        lz.rs(4*n+1);
    }void push(int l, int r, int id){
        if(!lz[id])return;
        if(!t[id])t[id]=lz[id];
        if(l < r){
            if(!lz[id*2])lz[id*2]=lz[id];
            if(!lz[id*2+1])lz[id*2+1]=lz[id];
        }lz[id]=0;
    }
    void upd(int i, int j, int x, int l=0, int r=n-1, int id=1){
        if(i > j)return;
        push(l, r, id);  
        if(l > j || r< i)return;
        if(l >= i && r <= j){
            lz[id]=x;
            push(l, r, id);
            return;
        }int mid=(l+r)/2;
        upd(i, j, x, l, mid, id*2);
        upd(i, j, x, mid+1, r, id*2+1);
    }int get(int p, int l=0, int r=n-1, int id=1){
        push(l, r, id);
        if(l > p || r < p)return -INF;
        if(l == r)return t[id];
        int mid=(l+r)/2;
        return max(get(p, l, mid, id*2), get(p, mid+1, r, id*2+1));
    }
};

void sol(){
    cin >> n >> q;
    Seggs sgt;
    while(q--){
        int l, r, x;
        cin >> l >> r >> x;
        l--;r--;x--;
        sgt.upd(l, x-1, x+1);
        sgt.upd(x+1, r, x+1);
    }for(int i=0;n>i;i++)cout << sgt.get(i) << ' ';
}

