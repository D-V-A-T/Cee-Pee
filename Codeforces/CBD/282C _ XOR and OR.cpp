ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    string a, b;
    cin >> a >> b;
    // 00 -> 00
    // 01 -> 11
    // 11 -> 10 or 01
    // -> we can change the position of 1s and increase/decrease the number of 1s 
    //     but can't delete all the 1s in a
    // -> if there're 1s in a and there're no 1s in b or vice versa 
    //    then the answer is NO
    if(a.size() != b.size() || !!count(bend(a), '1') != !!count(bend(b), '1')){
        cout << "NO";
        return;
    }cout << "YES";

}

