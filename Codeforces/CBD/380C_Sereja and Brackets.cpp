int n, q;
string s;

struct Seggs{
    vect<vect<int>> t;
    Seggs(){
        t.rs(4*n+1, vect<int>(3));
        // [0] : RBS cnt
        // [1] : spare ')'
        // [2] : spare '('
        build();
    }void build(int l=0, int r=n-1, int id=1){
        if(l == r){
            if(s[l] == ')')t[id][1]=1;
            else t[id][2]=1;
            return;
        }int mid=(l+r)/2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);
        t[id][0] = t[id*2][0] + t[id*2+1][0] + min(t[id*2][2], t[id*2+1][1]);
        t[id][1] = t[id*2][1] + max(0ll, t[id*2+1][1] - t[id*2][2]);
        t[id][2] = t[id*2+1][2] + max(0ll, t[id*2][2] - t[id*2+1][1]);
        //cout << t[id][0] << el;
    }void get(int i, int j, vect<int>& v, int l=0, int r=n-1, int id=1){
        if(l > j || r < i)return;
        if(l >= i && r <= j){
            v.eb(id);
            return;
        }int mid=(l+r)/2;
        get(i, j, v, l, mid, id*2);
        get(i, j, v, mid+1, r, id*2+1);
    }int askreimu(int l, int r){
        vect<int> v;
        get(l, r, v);
        int ans=0, fumo=0;
        for(int id : v){
            ans += t[id][0] + min(fumo, t[id][1]);
            fumo -= min(fumo, t[id][1]);
            fumo += t[id][2];
        }return ans*2;
    }
};

void sol(){
    cin >> s >> q;
    n=s.size();
    Seggs sgt;
    while(q--){
        int l, r;
        cin >> l >> r;
        cout << sgt.askreimu(l-1, r-1) << el;
    }
}

