int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}


int D[2][N], mn[2][N], visited[N];
vect<int> graph[N], a(N), dep(N);

void DFS(int u, int depth){
    visited[u] = 1;
    dep[u] = depth;

    for(int v : graph[u]){
        if(visited[v])continue;
        if(dep[u]%2 != 1){ // dep[v] % 2 = 1
            D[1][v] = D[1][u] + a[v];
            D[0][v] = D[0][u] - a[v];
        }else{
            D[1][v] = D[1][u] - a[v];
            D[0][v] = D[0][u] + a[v];
        }
        mn[1][v] = min(mn[1][u], D[1][v]);
        mn[0][v] = min(mn[0][u], D[0][v]);
        DFS(v, depth+1);
    }
}

