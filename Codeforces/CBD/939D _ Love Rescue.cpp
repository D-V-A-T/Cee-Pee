ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int par[269], len[269];
vect<pair<char, char>> ans;

int findpar(int u){
    if(par[u] == u)return u;
    return par[u] = findpar(par[u]);
}

int join(int u, int v){
    u=findpar(u);
    v=findpar(v);
    if(u != v){
        ans.eb(u, v);
        if(len[u] < len[v])swap(u,v);
        par[v] = u;
        len[u] += len[v];
    }
}

