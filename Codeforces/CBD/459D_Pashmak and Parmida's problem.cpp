struct FenwickTree{
    vect<int> bit;
    FenwickTree(){bit.rs(N);}
    void upd(int x){
        for(;x>=0;x=(x&(x+1))-1)
            bit[x]++;
    }int get(int x){
        int res=0;
        for(;x<N;x|=x+1)
            res += bit[x];
        return res;
    }
};

void sol(){
    int n, ans=0;
    cin >> n;
    vect<int> a(n), cnt(n), rcnt(n);
    umap<int, int> fumo;
    for(int i=0;n>i;i++){
        cin >> a[i];
        fumo[a[i]]++;
        cnt[i]=fumo[a[i]];
    }fumo.clear();
    for(int i=n-1;i>=0;i--){
        fumo[a[i]]++;
        rcnt[i]=fumo[a[i]];
    }FenwickTree fwt;
    fwt.upd(cnt[0]);
    for(int i=1;n>i;i++){
        ans += fwt.get(rcnt[i]+1);
        fwt.upd(cnt[i]);
    }cout << ans;
}

