class DSU{
public:
    vector<int> par, len;
    DSU(const int& n){
        par.rs(n+69);
        len.rs(n+69, 1);
        iota(bend(par), 0);
    }int findpar(int u){
        if(u == par[u])
            return u;
        return u=findpar(par[u]);
    }void join(int u, int v){
        u=findpar(u);
        v=findpar(v);
        if(u != v){
            if(len[u] < len[v])swap(u, v);
            par[v]=u;
            len[u]+=len[v];
        }
    }
};

