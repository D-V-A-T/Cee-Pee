ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    vect<int> ap(n+1), bp(n+1), a(n), b(n);
    for(int& i : a)cin >> i;
    for(int& i : b)cin >> i;
    for(int i=0;n>i;i++){
        ap[a[i]] = i;
        bp[b[i]] = i;
    }
    vect<pii> ans;
    for(int j=0;2>j;j++) // sort a few times =]
    for(int i=0;n>i;i++){
        int u = n-1-bp[a[i]];
        if(a[i] == b[i])u = n/2;
        ap[a[i]] = u;
        ap[a[u]] = i;
        bp[b[i]] = u;
        bp[b[u]] = i;
        if(i != u){
            ans.eb(i, u);
            swap(a[i], a[u]);
            swap(b[i], b[u]);
        }        
    }
    reverse(bend(a));
    if(a == b){
        cout << ans.size() << el;
        for(pii i : ans)cout << i.fi+1 << ' ' << i.se+1 << el;
    }else cout << "-1\n";
}

