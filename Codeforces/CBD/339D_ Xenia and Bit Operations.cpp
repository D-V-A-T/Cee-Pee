int n;
vect<int> v;

int lg2(int i) {
    return i ? __builtin_clzll(1) - __builtin_clzll(i) : -1;
}

struct Seggs{
    vect<int> t, bit;
    Seggs(){
        t.rs(4*n+1);
        build();
    }void build(int l=0, int r=n-1, int id=1, int depth=1){
        if(l == r){
            t[id]=v[l];
            return;
        }int mid=(l+r)/2;
        build(l, mid, id*2, depth+1);
        build(mid+1, r, id*2+1, depth+1);
        if((lg2(n)-depth)&1)t[id]=t[id*2]^t[id*2+1];
        else t[id]=t[id*2]|t[id*2+1];
    }void upd(int p, int x, int l=0, int r=n-1, int id=1, int depth=1){
        if(l > p || r < p)return;
        if(l == r){
            t[id]=x;
            return;
        }int mid=(l+r)/2;
        upd(p, x, l, mid, id*2, depth+1);
        upd(p, x, mid+1, r, id*2+1, depth+1);
        if((lg2(n)-depth)&1)t[id]=t[id*2]^t[id*2+1];
        else t[id]=t[id*2]|t[id*2+1];
    }
};

void sol(){
    int q;
    cin >> n >> q;
    n=1<<n;
    v.rs(n);
    for(int& i : v)cin >> i;
    Seggs sgt;
//cout << sgt.t[1] << el;
    while(q--){
        int p, x;
        cin >> p >> x;
        sgt.upd(p-1, x);
        cout << sgt.t[1] << el;
    }
}

