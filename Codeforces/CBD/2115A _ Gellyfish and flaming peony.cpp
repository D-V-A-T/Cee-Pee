int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dist[5001], visited[5001], Gcd[5001][5001];

void sol(){
    int n;
    cin >> n;
    int x = 0, a[n];
    memset(dist, 5, sizeof dist);
    memset(visited, 0, sizeof visited);

    queue<int> que;

    for(int i=0;n>i;i++){
        cin >> a[i];
        dist[a[i]] = 0;
        visited[a[i]] = 1;
        que.push(a[i]);
        x = __gcd(x, a[i]);
    }

    while(que.size()){
        int u = que.front();
        que.pop();

        if(u == x)break;

        for(int i=0;n>i;i++){
            int v = Gcd[min(a[i], u)][max(a[i], u)];
            if(visited[v])continue; 
            visited[v] = 1;
            que.push(v);
            dist[v] = min(dist[v]+0, dist[u]+1);
        }
    }

    cout << dist[x] + n - (!dist[x] ? count(a, a+n, x) : 1) << el;
}

