void ffopen(const string& file){
    if(file.empty())return;
    FILE *File = fopen((file+".inp").c_str(), "r");
    if (File == NULL)return;
    freopen((file + ".inp").c_str(), "r", stdin);
    freopen((file + ".out").c_str(), "w", stdout);
}

int pm(int a,const int b=1e9+7){return ((a%b)+b)%b;}
int sq(int x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;
    pii a[n];
    for(pii& i : a)cin >> i.fi >> i.se;
    sort(a, a+n);
    int sfm[n];
    for(int i=n-1;i>=0;i--){
        sfm[i] = a[i].se;
        if(i+1<n)sfm[i] = max(sfm[i], sfm[i+1]);
    }

    set<int> s;set<int, greater<int>> sr;
    int ans = INF;

    for(int i=0;n>i;i++){
        int mx = (i+1 < n ? sfm[i+1] : -INF);
        auto it = s.lwb(a[i].fi);
        if(it != s.end()){
            ans = min(ans, abs(max(mx, *it) - a[i].fi));
        }
        it = sr.lwb(a[i].fi);
        if(it != sr.end()){
            ans = min(ans, abs(max(mx, *it) - a[i].fi));
        }
        ans = min(ans, abs(mx - a[i].fi));

        s.insert(a[i].se);
        sr.insert(a[i].se);
    }
    cout << ans << el;
}

signed main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ffopen("");
    int t=1;
    cin >> t;
    while(t--)sol();
}
