ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int a[N], b[N], differ[N], ans = 0, n;

bool getbit(int mask, int b){
	return mask & (1ll << b);
}

void diddyparty(const vect<int> &x, const vect<int> &y, int *e){
	int cnt[20][2], add = 0; memset(cnt, 0, sizeof cnt);
	
	for(int i : x){
		for(int b=0; 20>b; b++) cnt[b][getbit(e[i], b)]++;
	}	
	
	for(int i : y){
		for(int b=0; 20>b; b++){
			add += cnt[b][getbit(e[i], b) ^ 1] * (1ll << b);
		}
	}	
	
	if(x == y) add /= 2;
	
	ans += add;
}

void epsteinisland(vect<int> curr, int bigbit){
	
	if(curr.empty()) return;
	
	if(bigbit == -1){
		diddyparty(curr, curr, a);		
		return;
	}
	
	vect<int> Z, O, Zz, Zo, Oz, Oo;
	
	for(int i : curr){
		if(getbit(differ[i], bigbit)) O.eb(i);
		else Z.eb(i);
	}
	
	for(int i : O){
		if(getbit(a[i], bigbit)) Oo.eb(i);
		else Oz.eb(i);
	}
	
	for(int i : Z){
		if(getbit(a[i], bigbit)) Zo.eb(i);
		else Zz.eb(i);
	}
	
	diddyparty(Oz, Zz, a);
	diddyparty(Oo, Zo, a);
	diddyparty(Oz, Zo, b);
	diddyparty(Oo, Zz, b);	
	
	
	epsteinisland(O, bigbit-1);
	epsteinisland(Z, bigbit-1);
	
}

