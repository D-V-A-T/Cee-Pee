ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

void sol(){
    int n;
    cin >> n;

    int dp[n+5][n+5];

    memset(dp, 5, sizeof dp);

    for(int i=0; n>=i; i++) dp[i][0] = 0;

    int a[n+5], p, perm[n+5];

    
    for(int i=1; n>=i; i++) cin >> perm[i];
    for(int i=1; n>=i; i++) cin >> a[i];

    vect<int> stk[n+5], ans(n+5, INF);

    stk[0].eb(0);

    multiset<int> ms[n+5];
    vect<int> del[n+5];

    for(int i=1; n>=i; i++){
        p = 0;
        for(int j=i; j>0; j--) if(perm[j] < perm[i]) {
            p = j;
            break;
        }

        int nxt = n+1;

        for(int j=i; n>=j; j++) if(perm[j] < perm[i]){
            nxt = j-1;
            break;
        }

        del[nxt].eb(i);


        // if(stk[1].size()) cout << i << ' ' << stk[1][0] << el;

        for(int j=1; n>=j; j++){
            int lw = lwb(bend(stk[j-1]), p) - stk[j-1].begin();
            if(lw < stk[j-1].size()) dp[i][j] = a[i] + dp[stk[j-1][lw]][j-1];
            if(ms[j-1].size()) dp[i][j] = min(dp[i][j], a[i] + (*ms[j-1].begin()));


            // cout << dp[i][j] << " \n"[j==n];

            // if(i == 3 && j == 2){
            //     cout << dp[stk[j-1][lw]][1] << el;
            // }
        }

        stk[0].eb(i);
        dp[i][0] = 0;
        for(int j=1; n>=j; j++){
            ans[j] = min(ans[j], dp[i][j]);
            ms[j].insert(dp[i][j]);
            auto &st = stk[j];
            while(st.size() && dp[st.back()][j] >= dp[i][j]) st.pob();
            st.eb(i);
        }

        for(int j : del[i]){
            for(int f=1; n>=f; f++){
                ms[f].erase(ms[f].find(dp[j][f]));
            }
        }
    }

    for(int i=1; n>=i; i++) cout << ans[i] << ' ';
    nextl;
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    cin >> t;
    while(t--) sol();
}
