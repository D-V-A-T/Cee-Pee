const ll N = 3e5 + 1, MOD = 998244353, INF = 1000000000000000069;

mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int candy[505][505], F[N];

int B(int a, int b=MOD-2){
    int res = 1;
    while(b){
        if(b&1) res = res * a % MOD;
        a = a * a % MOD;
        b >>= 1;
    }
    return res;
}

int E(int j, int i){
    if(i < 0 || j < 0) return 0;
    return candy[i][j];
}


void sol(){
    int n, L, R;
    cin >> n >> L >> R;
    int dp[n+5][n+5][n+5];

    memset(dp, 0, sizeof dp);

    vect<int> cnt(n+5);
    int mx = 0;
    for(int i=0; n>i; i++){
        int x; cin >> x;
        cnt[x]++;
        mx = max(mx, x);
    }

    dp[mx][1][1] = 1;

    int positions = cnt[mx] + 1;


    for(int num = mx-1; num>0; num--){
        for(int i=1; L>=i; i++){
            for(int j=1; R>=j; j++){
                dp[num][i][j] = (
                    dp[num+1][i-1][j-1] * E(positions, cnt[num] - 2) % MOD
                +   dp[num+1][i-1][j] * E(positions - 1, cnt[num] - 1) % MOD 
                +   dp[num+1][i][j-1] * E(positions - 1, cnt[num] - 1) % MOD
                +   dp[num+1][i][j] * E(positions - 2, cnt[num]) % MOD 
                ) % MOD;
            }
        }

        positions += cnt[num];
    }

    cout << dp[1][L][R] << el;
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();

    F[0] = 1;
    for(int i=1; N>i; i++) F[i] = F[i-1] * i % MOD;

    for(int i=0; 500>=i; i++){
        for(int j=1; 500>=j; j++){
            candy[i][j] = F[i + j - 1] * B(F[i] * F[j-1] % MOD) % MOD; // i candies for j people
        }
    }

    int t = 1;
    // cin >> t;
    while(t--) sol();
}
