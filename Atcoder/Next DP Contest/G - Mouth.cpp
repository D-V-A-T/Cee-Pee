ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

struct node{
    int left, right, sum, mn;
    node operator + (const node &a){
        node res;
        res.left = min(left, sum + a.left);
        res.right = min(a.right, a.sum + right);
        res.sum = sum + a.sum;
        res.mn = min({mn, a.mn, right + a.left});
        return res;
    }
    void SET(int x){
        left = right = sum = mn = x;
    }
};

int n, q;
vect<int> a;

struct Seggs{
    node t[4 * N];

    Seggs(){
        build();
    }

    void build(int l=0, int r=n-1, int id=1){
        if(l == r){
            t[id].SET(a[l] > 0 ? -a[l] : 1);
            return;
        }

        int mid = (l + r) / 2;
        build(l, mid, id*2);
        build(mid+1, r, id*2+1);

        t[id] = t[id*2] + t[id*2+1];
    }

    void upd(int i, int l=0, int r=n-1, int id=1){
        if(l > i || r < i) return;
        if(l == r){
            t[id].SET(a[l] > 0 ? -a[l] : 1);
            return;
        }

        int mid = (l + r) / 2;
        upd(i, l, mid, id*2);
        upd(i, mid+1, r, id*2+1);

        t[id] = t[id*2] + t[id*2+1];
    }
};

