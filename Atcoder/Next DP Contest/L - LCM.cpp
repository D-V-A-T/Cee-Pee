const ll N = 2e5 + 10, MOD = 998244353, INF = 1000000000000000069;

mt19937_64 rng(chrono::high_resolution_clock::now().time_since_epoch().count());

ll rand(ll l, ll r){
    return uniform_int_distribution<ll>(l, r)(rng);
}
ll pm(ll a,const int b=MOD){return (a%=b) < 0 ? a + b : a;}
ll sq(ll x){return x*x;}
ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

bool is_prime[N];
vect<int> divs[N], pf[N];

int B(int a, int b=MOD-2){
    int res = 1;
    while(b){
        if(b&1) res = res * a % MOD;
        a = a * a % MOD;
        b >>= 1;
    }
    return res;
}

void sol(){
    int n;
    cin >> n;

    vect<int> a(n), sum(N), dp(n);
    // sum[i] : dp values of all j that i | A[j]


    for(int &i : a) cin >> i;

    dp[0] = 1;
    for(int d : divs[a[0]]) sum[d] = 1 * a[0];


    for(int i=1; n>i; i++){
        dp[i] = 0;

        for(int d : divs[a[i]]){
            int res = 0;
            const auto &v = pf[a[i] / d];

            for(int mask=1; 1<<v.size() >mask; mask++){
                int prod = 1;
                for(int j=0; v.size()>j; j++) if(mask & (1<<j)) prod *= v[j];

                res += popcnt(mask) & 1 ? sum[d * prod] : -sum[d * prod];
                res %= MOD;
            }


            // if(i == n-1){
            //     cout << d << ' ' << sum[d] << ' ' << res << el;
            // }

            res = pm(sum[d] - res);

            dp[i] = (dp[i] + a[i] * (res * B(d) % MOD) ) % MOD;
        }

        for(int d : divs[a[i]]) sum[d] = (sum[d] + dp[i] * a[i]) % MOD;        

        cout << dp[i] << el;
    }
}

