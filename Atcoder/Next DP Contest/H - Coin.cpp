ll __lcm(ll a, ll b, const ll lim=LLONG_MAX){
    if(a == -1 || b == -1)return -1;
    ll g = __gcd(a,b);
    if(b/g > lim/a)return -1;
    return (a/g)*b;
}

int dp[4004][4004][2];

void sol(){
    int n;
    cin >> n;

    char c[n+5][n+5];

    for(int i=1; n>=i; i++){
        for(int j=1; n>=j; j++){
            cin >> c[i][j];
        }
    }

    vect<int> ans(2 * n+5);

    for(auto &i : dp) for(auto &j : i){
        j[0] = 1e9;
        j[1] = -1e9;
    }

    for(int i=1; n>=i; i++){
        for(int j=1; n>=j; j++){
            if(i == 1 && j == 1){
                dp[i][j][0] = dp[i][j][1] = 0;
                continue;
            }
            dp[i][j][0] = min(dp[i-1][j][0], dp[i][j-1][0]) + (c[i][j] == '@');
            dp[i][j][1] = max(dp[i-1][j][1], dp[i][j-1][1]) + (c[i][j] == '@');

            // if(dp[i][j][0] == 0) cout << i << ' ' << j << ' ' << dp[i][j][0] << ' ' << dp[i][j][1] << el;

            ans[dp[i][j][0]]++;
            ans[dp[i][j][1] + 1]--;
        }
    }

    ans[0]++;
    ans[1]--;
    for(int i=0; 2*n-2>=i; i++){
        if(i) ans[i] += ans[i-1];
        cout << ans[i] << el;
    }
}

signed main(){
    ios_base::sync_with_stdio(0);
    cin.tie(NULL);cout.tie(NULL);
    IO();
    int t = 1;
    // cin >> t;
    while(t--) sol();
}
